import { readdirSync, readFileSync, statSync } from 'node:fs';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';

type GrammarName = 'rust' | 'typescript' | 'python';

interface Violation {
	file: string;
	kind: 'missing-header' | 'forbidden' | 'yaml-regression';
	detail: string;
}

const GRAMMARS: readonly GrammarName[] = ['rust', 'typescript', 'python'];
const FORBIDDEN_SRC = /\{%-?\s*(extends|macro|include|match|set)\b/;
const HEADER_RE = /^\{#-?\s*@generated\s+by\s+@sittir\/codegen/;
const REPO_ROOT = fileURLToPath(new URL('../../../../', import.meta.url));

function printUsage(): void {
	process.stdout.write('Usage: check-jinja\n');
}

function lineOf(text: string, offset: number): number {
	return text.slice(0, offset).split('\n').length;
}

function grammarTemplateDir(grammar: GrammarName): string {
	return join(REPO_ROOT, 'packages', grammar, 'templates');
}

function countTemplates(): number {
	return GRAMMARS.map((grammar) => {
		try {
			return readdirSync(grammarTemplateDir(grammar)).filter((name) => name.endsWith('.jinja')).length;
		} catch {
			return 0;
		}
	}).reduce((sum, count) => sum + count, 0);
}

function collectViolations(): Violation[] {
	const violations: Violation[] = [];

	for (const grammar of GRAMMARS) {
		const dir = grammarTemplateDir(grammar);
		let entries: string[];
		try {
			entries = readdirSync(dir);
		} catch {
			process.stderr.write(`skip: ${dir} not found\n`);
			continue;
		}
		for (const name of entries) {
			const path = join(dir, name);
			const stats = statSync(path);
			if (!stats.isFile()) continue;
			if (name.endsWith('.yaml') || name.endsWith('.yml')) {
				violations.push({
					file: path,
					kind: 'yaml-regression',
					detail: 'YAML template format was retired in feature 011 — remove this file'
				});
				continue;
			}
			if (!name.endsWith('.jinja')) continue;
			const body = readFileSync(path, 'utf8');
			if (!HEADER_RE.test(body)) {
				violations.push({
					file: path,
					kind: 'missing-header',
					detail: 'file lacks the `{# @generated by @sittir/codegen #}` header'
				});
			}
			const forbidden = new RegExp(FORBIDDEN_SRC.source, 'g');
			let match: RegExpExecArray | null;
			while ((match = forbidden.exec(body)) !== null) {
				violations.push({
					file: path,
					kind: 'forbidden',
					detail: `forbidden construct: ${match[0]} (line ${lineOf(body, match.index)})`
				});
			}
		}
	}

	return violations;
}

export async function run(argv: string[]): Promise<number> {
	if (argv[0] === '--help' || argv[0] === '-h') {
		printUsage();
		return 0;
	}

	const violations = collectViolations();
	if (violations.length === 0) {
		process.stdout.write(
			`✓ ${countTemplates()} .jinja files — all headers present, no forbidden constructs, no YAML regressions\n`
		);
		return 0;
	}

	process.stderr.write(`✗ ${violations.length} violation(s):\n`);
	for (const violation of violations) {
		process.stderr.write(`  ${violation.kind}: ${violation.file}\n`);
		process.stderr.write(`    ${violation.detail}\n`);
	}
	return 1;
}

const _isMain = import.meta.url === `file://${process.argv[1]}`;
if (_isMain) {
	run(process.argv.slice(2))
		.then(process.exit)
		.catch((error: unknown) => {
			process.stderr.write(`check-jinja: ${(error as Error).stack ?? error}\n`);
			process.exit(1);
		});
}

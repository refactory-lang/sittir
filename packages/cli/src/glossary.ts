/**
 * CLI glossary generator — renders docs/cli-command-glossary.md from the
 * live commander program tree so the doc can never rot.
 *
 * Usage:
 *   pnpm exec tsx packages/cli/src/glossary.ts > docs/cli-command-glossary.md
 */
import type { Command, Option, Argument } from 'commander';
import { buildProgram } from './cli.ts';

// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------

/**
 * Recursively collect every (sub)command's full path name from `program`.
 *
 * @example
 *   allCommandNames(program)
 *   // → ['gen', 'validate counts', 'tool probe-kind', ...]
 */
export function allCommandNames(program: Command): string[] {
	const names: string[] = [];
	collectCommandNames(program, [], names);
	return names;
}

/**
 * Render a markdown glossary for `program`, mirroring the structure of
 * docs/compiler-phase-glossary.md. One top-level section per top-level
 * namespace command; each sub-command gets a heading, description, options
 * table, and an example invocation.
 */
export function renderGlossary(program: Command): string {
	const lines: string[] = [];

	lines.push('<!-- @generated by packages/cli/src/glossary.ts — run');
	lines.push('     `pnpm exec tsx packages/cli/src/glossary.ts > docs/cli-command-glossary.md` to refresh -->');
	lines.push('');
	lines.push('# CLI Command Glossary');
	lines.push('');
	lines.push('Per-command reference for every `sittir` sub-command. This document is');
	lines.push('generated from the live commander program tree; run the command above to');
	lines.push('refresh after adding or modifying commands.');
	lines.push('');
	lines.push('---');

	for (const top of program.commands) {
		const topName = top.name();
		lines.push('');
		lines.push(`## \`${topName}\``);
		lines.push('');
		lines.push(top.description() || '_No description._');

		const sub = top.commands;
		if (sub.length > 0) {
			// Namespace with sub-commands
			for (const cmd of sub) {
				appendCommandSection(lines, topName, cmd);
			}
		} else {
			// Top-level leaf command (e.g. gen)
			appendCommandBody(lines, topName, top, /* isTop= */ true);
		}
	}

	lines.push('');
	return lines.join('\n');
}

// ---------------------------------------------------------------------------
// Private helpers
// ---------------------------------------------------------------------------

function collectCommandNames(cmd: Command, path: string[], out: string[]): void {
	for (const sub of cmd.commands) {
		const subPath = [...path, sub.name()];
		out.push(subPath.join(' '));
		collectCommandNames(sub, subPath, out);
	}
}

function appendCommandSection(lines: string[], namespace: string, cmd: Command): void {
	const cmdName = cmd.name();
	lines.push('');
	lines.push(`### \`${namespace} ${cmdName}\``);
	lines.push('');
	lines.push(cmd.description() || '_No description._');
	appendCommandBody(lines, `${namespace} ${cmdName}`, cmd, /* isTop= */ false);
}

function appendCommandBody(lines: string[], fullPath: string, cmd: Command, isTop: boolean): void {
	// Arguments
	const args: Argument[] =
		(cmd as Command & { registeredArguments?: Argument[]; _args?: Argument[] }).registeredArguments ??
		(cmd as Command & { _args?: Argument[] })._args ??
		[];
	if (args.length > 0) {
		lines.push('');
		lines.push('**Arguments**');
		lines.push('');
		for (const arg of args) {
			const required = arg.required ? '(required)' : '(optional)';
			const variad = arg.variadic ? '…' : '';
			const desc = arg.description || '';
			lines.push(`- \`${arg.name()}${variad}\` ${required}${desc ? ` — ${desc}` : ''}`);
		}
	}

	// Options
	const opts: readonly Option[] = cmd.options;
	if (opts.length > 0) {
		lines.push('');
		lines.push('**Options**');
		lines.push('');
		for (const opt of opts) {
			const def = opt.defaultValue !== undefined ? ` (default: \`${String(opt.defaultValue)}\`)` : '';
			const choices = (opt as Option & { argChoices?: string[] }).argChoices?.length
				? ` choices: ${(opt as Option & { argChoices?: string[] }).argChoices!.map((c) => `\`${c}\``).join(' | ')}`
				: '';
			const desc = [opt.description, choices].filter(Boolean).join(' —');
			lines.push(`- \`${opt.flags}\` — ${desc || '_No description._'}${def}`);
		}
	}

	// Example invocation
	const argExample = args.map((a) => (a.required ? `<${a.name()}>` : `[${a.name()}]`)).join(' ');
	lines.push('');
	lines.push('**Example**');
	lines.push('');
	lines.push(`\`\`\`sh`);
	lines.push(`pnpm exec tsx packages/cli/src/cli.ts ${fullPath}${argExample ? ' ' + argExample : ''} [options]`);
	lines.push(`\`\`\``);

	if (isTop) {
		// Separator for top-level leaf commands
		lines.push('');
		lines.push('---');
	}
}

// ---------------------------------------------------------------------------
// Main — write to stdout when executed directly
// ---------------------------------------------------------------------------

const isMain = import.meta.url === `file://${process.argv[1]}`;
if (isMain) {
	const program = buildProgram();
	process.stdout.write(renderGlossary(program));
}

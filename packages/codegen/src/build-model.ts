/**
 * Orchestrator: 13-step pipeline → GrammarModel
 *
 * Runs the full pipeline from grammar name to hydrated, optimized GrammarModel.
 */

import { loadGrammar } from './grammar.ts';
import { loadNodeTypes } from './node-types.ts';
import { classifyRules } from './enriched-grammar.ts';
import {
	initializeModels,
	reconcile,
	applyAllMembers,
	refineAllModelTypes,
	type GrammarModel,
	type NodeModel,
} from './node-model.ts';
import { inferTokenAliases, applyTokenAliases } from './semantic-aliases.ts';
import { applyNaming } from './naming.ts';
import { optimize } from './optimization.ts';
import { hydrate } from './hydration.ts';

function stripChildSignatures(children: any): any {
	if (Array.isArray(children)) {
		return children.map((c: any) => {
			const { childSignature, ...rest } = c;
			return rest;
		});
	}
	const { childSignature, ...rest } = children;
	return rest;
}

// ---------------------------------------------------------------------------
// JSON5 serialization (preserved from grammar-model.ts)
// ---------------------------------------------------------------------------

function toJson5(value: unknown, indent: number = 0): string {
	const pad = '  '.repeat(indent);
	const pad1 = '  '.repeat(indent + 1);

	if (value === null || value === undefined) return 'null';
	if (typeof value === 'boolean' || typeof value === 'number') return String(value);
	if (typeof value === 'string') return JSON.stringify(value);

	if (Array.isArray(value)) {
		if (value.length === 0) return '[]';
		if (value.every(v => typeof v === 'string' || typeof v === 'number' || typeof v === 'boolean')) {
			const inner = value.map(v => JSON.stringify(v)).join(', ');
			if (inner.length < 80) return `[${inner}]`;
		}
		const items = value.map(v => `${pad1}${toJson5(v, indent + 1)},`);
		return `[\n${items.join('\n')}\n${pad}]`;
	}

	if (typeof value === 'object') {
		const obj = value as Record<string, unknown>;
		// Skip Map and other non-plain objects
		if (value instanceof Map || value instanceof Set) return '{}';
		const entries = Object.entries(obj);
		if (entries.length === 0) return '{}';
		const lines = entries.map(([k, v]) => {
			const key = /^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(k) ? k : JSON.stringify(k);
			return `${pad1}${key}: ${toJson5(v, indent + 1)},`;
		});
		return `{\n${lines.join('\n')}\n${pad}}`;
	}

	return String(value);
}

function serializeToJson5(nodes: Map<string, NodeModel>): string {
	const serializable: Record<string, unknown> = {};
	for (const [k, v] of nodes) {
		// Strip runtime-only properties
		const { typeName, factoryName, ...rest } = v as any;
		if (rest.modelType === 'branch') {
			const { members, rule, ...branchRest } = rest;
			// Convert fields to match old format for compatibility
			if (branchRest.fields) {
				branchRest.fields = branchRest.fields.map((f: any) => {
					const { fieldSignature, ...fieldRest } = f;
					return fieldRest;
				});
			}
			if (branchRest.children) {
				branchRest.children = stripChildSignatures(branchRest.children);
			}
			serializable[k] = branchRest;
		} else if (rest.modelType === 'container') {
			const { members, rule, ...containerRest } = rest;
			if (containerRest.children) {
				containerRest.children = stripChildSignatures(containerRest.children);
			}
			serializable[k] = containerRest;
		} else {
			const { rule, ...leafRest } = rest;
			serializable[k] = leafRest;
		}
	}
	return `// Auto-generated by @sittir/codegen — do not edit\n${toJson5(serializable)}\n`;
}

// ---------------------------------------------------------------------------
// buildModel — 13-step orchestrator
// ---------------------------------------------------------------------------

export function buildModel(grammarName: string): { grammarModel: GrammarModel; serialized: string } {
	// Steps 1-2: Load sources
	const grammar = loadGrammar(grammarName);
	const nodeTypes = loadNodeTypes(grammarName);

	// Step 3: Classify grammar rules
	const enrichedRules = classifyRules(grammar);

	// Step 4: Initialize from NodeTypes
	const models = initializeModels(nodeTypes);

	// Step 5: Reconcile
	reconcile(models, enrichedRules, grammarName);

	// Step 6: Apply members
	applyAllMembers(models, grammar);

	// Step 7: Refine model types
	refineAllModelTypes(models);

	// Steps 8-9: Semantic aliases
	const aliases = inferTokenAliases(models, grammar);
	applyTokenAliases(models, aliases);

	// Step 10: Naming
	applyNaming(models);

	// Serialize before optimization (matches current behavior)
	const serialized = serializeToJson5(models);

	// Step 11: Optimize
	const signatures = optimize(models);

	// Step 12: Hydrate
	const hydratedModels = hydrate(models);

	// Step 13: Return
	return {
		grammarModel: {
			name: grammarName,
			models: hydratedModels,
			signatures,
		},
		serialized,
	};
}

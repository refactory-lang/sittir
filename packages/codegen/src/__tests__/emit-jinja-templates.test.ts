import { describe, it, expect } from 'vitest';
import { mkdtempSync, readdirSync, readFileSync, writeFileSync, rmSync, mkdirSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';
import { emitJinjaTemplates, writeJinjaTemplates } from '../emitters/templates.ts';
import {
	AssembledBranch, AssembledLeaf, AssembledKeyword,
} from '../compiler/node-map.ts';
import type { NodeMap } from '../compiler/types.ts';
import type { SeqRule } from '../compiler/rule.ts';

function makeMinimalNodeMap(): NodeMap {
	const nameRule: SeqRule = {
		type: 'seq',
		members: [{ type: 'field', name: 'name', content: { type: 'symbol', name: '_identifier' } }],
	};
	const nodes = new Map<string, any>([
		['function_item', new AssembledBranch('function_item', nameRule, nameRule)],
		['identifier', new AssembledLeaf('identifier', { type: 'pattern', value: '[a-z]+' })],
		['kw_fn', new AssembledKeyword('kw_fn', { type: 'string', value: 'fn' })],
	]);
	return {
		grammar: 'test',
		grammarSha: 'test-sha',
		rules: {},
		nodes,
		externals: new Set(),
		word: undefined,
	} as unknown as NodeMap;
}

describe('emitJinjaTemplates — T021 pure function', () => {
	it('returns a Map of kind → .jinja body, skipping no-file nodes', () => {
		const nm = makeMinimalNodeMap();
		const result = emitJinjaTemplates({ grammar: 'test', nodeMap: nm });
		// branch → one entry; leaf + keyword → skipped.
		expect(result.bodies.has('function_item')).toBe(true);
		expect(result.bodies.has('identifier')).toBe(false);
		expect(result.bodies.has('kw_fn')).toBe(false);
	});

	it('bodies include the @generated comment header', () => {
		const nm = makeMinimalNodeMap();
		const result = emitJinjaTemplates({ grammar: 'test', nodeMap: nm });
		const body = result.bodies.get('function_item');
		expect(body).toBeDefined();
		expect(body!).toMatch(/^\{#\s*@generated by @sittir\/codegen/);
	});

	it('body contains the Jinja translation of the rule template', () => {
		const nm = makeMinimalNodeMap();
		const result = emitJinjaTemplates({ grammar: 'test', nodeMap: nm });
		const body = result.bodies.get('function_item')!;
		expect(body).toContain('{{ name }}');
	});
});

describe('writeJinjaTemplates — T022 stale-file cleanup', () => {
	it('writes one .jinja per map entry', () => {
		const tmp = mkdtempSync(join(tmpdir(), 'sittir-jinja-'));
		try {
			const emitted = {
				bodies: new Map([
					['function_item', '{# @generated #}\n{{ name }}'],
					['block', '{# @generated #}\n{ {{ children }} }'],
				]),
				meta: {},
			};
			writeJinjaTemplates(emitted, tmp);
			const files = readdirSync(tmp).filter(f => f.endsWith('.jinja'));
			expect(files.sort()).toEqual(['block.jinja', 'function_item.jinja']);
			expect(readFileSync(join(tmp, 'function_item.jinja'), 'utf-8')).toContain('{{ name }}');
		} finally {
			rmSync(tmp, { recursive: true, force: true });
		}
	});

	it('deletes stale .jinja files whose kind is not in the new emission', () => {
		const tmp = mkdtempSync(join(tmpdir(), 'sittir-jinja-'));
		try {
			// Seed with an orphan file
			writeFileSync(join(tmp, 'removed_rule.jinja'), '{# stale #}\nbogus');
			writeFileSync(join(tmp, 'function_item.jinja'), '{# old #}\nold');
			// Write with only function_item in emission
			const emitted = {
				bodies: new Map([['function_item', '{# new #}\n{{ name }}']]),
				meta: {},
			};
			writeJinjaTemplates(emitted, tmp);
			const files = readdirSync(tmp).filter(f => f.endsWith('.jinja'));
			expect(files).toEqual(['function_item.jinja']);
			// The surviving file has the new content
			expect(readFileSync(join(tmp, 'function_item.jinja'), 'utf-8')).toContain('new');
		} finally {
			rmSync(tmp, { recursive: true, force: true });
		}
	});

	it('preserves .gitkeep and non-.jinja files when cleaning stale', () => {
		const tmp = mkdtempSync(join(tmpdir(), 'sittir-jinja-'));
		try {
			writeFileSync(join(tmp, '.gitkeep'), '');
			writeFileSync(join(tmp, 'README.md'), '# docs');
			writeFileSync(join(tmp, 'old_rule.jinja'), 'bogus');
			const emitted = {
				bodies: new Map([['kept_rule', '{# new #}\nkept']]),
				meta: {},
			};
			writeJinjaTemplates(emitted, tmp);
			const files = readdirSync(tmp).sort();
			expect(files).toContain('.gitkeep');
			expect(files).toContain('README.md');
			expect(files).toContain('kept_rule.jinja');
			expect(files).not.toContain('old_rule.jinja');
		} finally {
			rmSync(tmp, { recursive: true, force: true });
		}
	});

	it('creates output directory if it does not exist', () => {
		const tmp = mkdtempSync(join(tmpdir(), 'sittir-jinja-'));
		try {
			const nested = join(tmp, 'does', 'not', 'exist');
			const emitted = { bodies: new Map([['r', 'body']]), meta: {} };
			writeJinjaTemplates(emitted, nested);
			expect(readdirSync(nested)).toEqual(['r.jinja']);
		} finally {
			rmSync(tmp, { recursive: true, force: true });
		}
	});
});

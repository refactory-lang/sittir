/**
 * Emits joinby.ts — separator map for list children.
 * Maps node kind → separator string used when joining array fields.
 */

import type { KindMeta } from '../grammar-reader.ts';
import { emitRule } from './rules.ts';

export interface EmitJoinByConfig {
	grammar: string;
	nodes: KindMeta[];
}

/**
 * Emit the joinBy separator map.
 * Detects separators from grammar rules (comma-separated patterns, etc.)
 */
export function emitJoinBy(config: EmitJoinByConfig): string {
	const { grammar, nodes } = config;
	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('');
	lines.push("import type { JoinByMap } from '@sittir/core';");
	lines.push('');
	lines.push('export const joinBy: JoinByMap = {');

	for (const node of nodes) {
		const rule = emitRule({ grammar, node });
		// Check if any field has a separator
		for (const [fieldName, meta] of Object.entries(rule.fields)) {
			if (meta.sep) {
				lines.push(`  '${node.kind}': '${meta.sep}',`);
				break; // one separator per kind
			}
		}
	}

	lines.push('};');
	return lines.join('\n');
}

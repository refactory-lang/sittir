/**
 * Emits joinby.ts — separator map for list children.
 * Maps node kind → separator string used when joining array fields.
 */

import type { NodeModel } from '../grammar-model.ts';
import { structuralNodes, fieldsOf } from './utils.ts';

export interface EmitJoinByConfig {
	grammar: string;
	nodes: NodeModel[];
}

/**
 * Emit the joinBy separator map.
 * Reads separators from pre-computed FieldModel.separator.
 */
export function emitJoinBy(config: EmitJoinByConfig): string {
	const nodes = structuralNodes(config.nodes);
	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('');
	lines.push("import type { JoinByMap } from '@sittir/types';");
	lines.push('');
	lines.push('export const joinBy: JoinByMap = {');

	for (const node of nodes) {
		for (const field of fieldsOf(node)) {
			if (field.separator) {
				lines.push(`  '${node.kind}': '${field.separator}',`);
				break; // one separator per kind
			}
		}
	}

	lines.push('};');
	return lines.join('\n');
}

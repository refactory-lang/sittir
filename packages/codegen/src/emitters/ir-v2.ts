/**
 * Emits ir.ts — developer-facing namespace re-exporting factories with short names.
 *
 * Consumes NodeMap directly. Derives from factory exports — thin namespace wrapper.
 * No imports from node-model.ts or naming.ts.
 */

import type { NodeMap, AssembledNode, AssembledPolymorph } from '../compiler/rule.ts'
import { nameNode } from '../compiler/assemble.ts'

export interface EmitIrFromNodeMapConfig {
    grammar: string
    nodeMap: NodeMap
}

export function emitIrFromNodeMap(config: EmitIrFromNodeMapConfig): string {
    const { nodeMap } = config

    const lines: string[] = [
        '// Auto-generated by @sittir/codegen — do not edit',
        '',
    ]

    // Collect factory imports
    const factoryImports: string[] = []
    const fromImports: string[] = []
    const usedKeys = new Map<string, string>()

    for (const [kind, node] of nodeMap.nodes) {
        if (!node.factoryName) continue // supertype, group, token — no factory

        const rawName = toRawFactoryName(kind)
        factoryImports.push(rawName)

        // Polymorph: import per-form factories
        if (node.modelType === 'polymorph') {
            for (const form of node.forms) {
                factoryImports.push(`${rawName}_${form.name}_`)
            }
        }

        // from() imports
        if (node.modelType === 'branch' || node.modelType === 'container' || node.modelType === 'polymorph') {
            fromImports.push(`${node.factoryName}From`)
            if (node.modelType === 'polymorph') {
                for (const form of node.forms) {
                    const formTypeName = form.name.split('_').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join('')
                    fromImports.push(`${node.factoryName}${formTypeName}From`)
                }
            }
        }
    }

    if (factoryImports.length > 0) {
        lines.push(`import { ${factoryImports.join(', ')} } from './factories.js';`)
    }
    if (fromImports.length > 0) {
        lines.push(`import { ${fromImports.join(', ')} } from './from.js';`)
    }
    lines.push('')

    lines.push('export const ir = {')

    // Branch/container/polymorph factories — combined with .from()
    lines.push('  // Node factories')
    for (const [kind, node] of nodeMap.nodes) {
        if (!node.factoryName) continue
        if (node.modelType !== 'branch' && node.modelType !== 'container' && node.modelType !== 'polymorph') continue

        const irKey = resolveIrKey(kind, usedKeys)
        const rawName = toRawFactoryName(kind)
        const fromFn = `${node.factoryName}From`

        if (node.modelType === 'polymorph' && node.forms.length > 1) {
            const variantEntries = node.forms.map(form => {
                const vFactory = `${rawName}_${form.name}_`
                const formTypeName = form.name.split('_').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join('')
                const vFrom = `${node.factoryName}${formTypeName}From`
                return `${form.name}: Object.assign(${vFactory}, { from: ${vFrom} })`
            })
            lines.push(`  ${irKey}: Object.assign(${rawName}, { from: ${fromFn}, ${variantEntries.join(', ')} }),`)
        } else {
            lines.push(`  ${irKey}: Object.assign(${rawName}, { from: ${fromFn} }),`)
        }
    }

    lines.push('')

    // Keyword factories
    lines.push('  // Keyword factories')
    for (const [kind, node] of nodeMap.nodes) {
        if (node.modelType !== 'keyword') continue
        if (!node.factoryName) continue
        const irKey = resolveIrKey(kind, usedKeys)
        lines.push(`  ${irKey}: ${toRawFactoryName(kind)},`)
    }

    lines.push('')

    // Leaf factories
    lines.push('  // Leaf node factories')
    for (const [kind, node] of nodeMap.nodes) {
        if (node.modelType !== 'leaf' && node.modelType !== 'enum') continue
        if (!node.factoryName) continue
        const irKey = resolveIrKey(kind, usedKeys)
        lines.push(`  ${irKey}: ${toRawFactoryName(kind)},`)
    }

    lines.push('} as const;')
    return lines.join('\n')
}

function resolveIrKey(kind: string, usedKeys: Map<string, string>): string {
    const { irKey } = nameNode(kind)
    // Try short name first (strip common suffixes)
    const short = toShortName(kind)
    if (!usedKeys.has(short)) {
        usedKeys.set(short, kind)
        return short
    }
    // Collision — use full irKey
    if (!usedKeys.has(irKey)) {
        usedKeys.set(irKey, kind)
        return irKey
    }
    return irKey
}

function toShortName(kind: string): string {
    // Strip common suffixes: _item, _expression, _statement, _declaration
    const stripped = kind
        .replace(/_item$/, '')
        .replace(/_expression$/, '')
        .replace(/_statement$/, '')
        .replace(/_declaration$/, '')
        .replace(/_definition$/, '')
    const parts = stripped.split('_')
    return parts.map((w, i) => i === 0 ? w : w.charAt(0).toUpperCase() + w.slice(1)).join('')
}

function toRawFactoryName(kind: string): string {
    const { factoryName } = nameNode(kind)
    // JS reserved words get a trailing underscore
    const reserved = new Set(['break', 'case', 'catch', 'class', 'const', 'continue',
        'debugger', 'default', 'delete', 'do', 'else', 'enum', 'export', 'extends',
        'false', 'finally', 'for', 'function', 'if', 'import', 'in', 'instanceof',
        'let', 'new', 'null', 'return', 'static', 'super', 'switch', 'this',
        'throw', 'true', 'try', 'typeof', 'var', 'void', 'while', 'with', 'yield',
        'type', 'interface', 'module', 'namespace', 'async', 'await'])
    return reserved.has(factoryName) ? `${factoryName}_` : factoryName
}

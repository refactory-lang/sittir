interface TreeSitterIdConstConfig {
	readonly kindIds?: GeneratedIdTable;
	readonly fieldIds?: GeneratedIdTable;
	readonly kinds: readonly string[];
	readonly fields: readonly string[];
	readonly sourceArtifact?: string;
}

interface IdEnumEntry {
	readonly key: string;
	readonly id: number;
	readonly cName?: string;
	readonly memberName: string;
}

function emitTreeSitterIdConsts(
	lines: string[],
	config: TreeSitterIdConstConfig
): void {
	const kindEntries = collectIdEntries(config.kinds, config.kindIds);
	const fieldEntries = collectIdEntries(config.fields, config.fieldIds);
	if (kindEntries.length === 0 && fieldEntries.length === 0) return;

	lines.push(
		'/** Tree-sitter numeric IDs from the generated parser artifact. */'
	);
	if (config.sourceArtifact) {
		lines.push(
			`export const TREE_SITTER_ID_SOURCE = ${JSON.stringify(config.sourceArtifact)};`
		);
	}
	lines.push('');

	emitIdMaps(lines, {
		forwardName: 'TREE_SITTER_KIND_ID_BY_KIND',
		reverseName: 'TREE_SITTER_KIND_BY_KIND_ID',
		jsonName: 'TREE_SITTER_KIND_ID_JSON',
		entries: kindEntries
	});
	lines.push('');

	emitIdEnumBlock(lines, {
		enumName: 'TSFieldId',
		forwardName: 'TREE_SITTER_FIELD_ID_BY_NAME',
		reverseName: 'TREE_SITTER_FIELD_NAME_BY_ID',
		jsonName: 'TREE_SITTER_FIELD_ID_JSON',
		recordType: 'Record<string, TSFieldId>',
		entries: fieldEntries
	});
}

function emitIdEnumBlock(
	lines: string[],
	config: {
		readonly enumName: string;
		readonly forwardName: string;
		readonly reverseName: string;
		readonly jsonName: string;
		readonly recordType: string;
		readonly entries: readonly IdEnumEntry[];
	}
): void {
	if (config.entries.length === 0) return;

	lines.push(`export const enum ${config.enumName} {`);
	for (const entry of config.entries) {
		lines.push(`  ${entry.memberName} = ${entry.id},`);
	}
	lines.push('}');
	lines.push('');

	lines.push(`export const ${config.forwardName} = {`);
	for (const entry of config.entries) {
		lines.push(
			`  ${JSON.stringify(entry.key)}: ${config.enumName}.${entry.memberName},`
		);
	}
	lines.push(`} as const satisfies ${config.recordType};`);
	lines.push('');

	lines.push(`export const ${config.reverseName} = {`);
	for (const entry of config.entries) {
		lines.push(
			`  [${config.enumName}.${entry.memberName}]: ${JSON.stringify(entry.key)},`
		);
	}
	lines.push('} as const;');
	lines.push('');

	lines.push(`export const ${config.jsonName} = [`);
	for (const entry of config.entries) {
		lines.push(
			`  { name: ${JSON.stringify(entry.key)}, id: ${entry.id}, enumName: ${JSON.stringify(entry.memberName)}, cName: ${JSON.stringify(entry.cName ?? null)} },`
		);
	}
	lines.push('] as const;');
	lines.push('');
}

function emitIdMaps(
	lines: string[],
	config: {
		readonly forwardName: string;
		readonly reverseName: string;
		readonly jsonName: string;
		readonly entries: readonly IdEnumEntry[];
	}
): void {
	if (config.entries.length === 0) return;

	lines.push(`export const ${config.forwardName} = {`);
	for (const entry of config.entries) {
		lines.push(`  ${JSON.stringify(entry.key)}: ${entry.id},`);
	}
	lines.push('} as const satisfies Record<string, number>;');
	lines.push('');

	lines.push(`export const ${config.reverseName} = {`);
	for (const entry of config.entries) {
		lines.push(`  [${entry.id}]: ${JSON.stringify(entry.key)},`);
	}
	lines.push('} as const;');
	lines.push('');

	lines.push(`export const ${config.jsonName} = [`);
	for (const entry of config.entries) {
		lines.push(
			`  { name: ${JSON.stringify(entry.key)}, id: ${entry.id}, enumName: ${JSON.stringify(entry.memberName)}, cName: ${JSON.stringify(entry.cName ?? null)} },`
		);
	}
	lines.push('] as const;');
}

function collectIdEntries(
	keys: readonly string[],
	ids: GeneratedIdTable | undefined
): IdEnumEntry[] {
	const idMap = toIdMap(ids);
	const usedNames = new Map<string, string>();
	const result: IdEnumEntry[] = [];

	const keyedEntries = [...new Set(keys)]
		.map((key) => ({ key, entry: idMap.get(key) }))
		.filter(
			(item): item is { key: string; entry: GeneratedIdEntry } =>
				item.entry !== undefined
		)
		.sort(
			(a, b) =>
				(a.entry.id ?? -1) - (b.entry.id ?? -1) ||
				a.key.localeCompare(b.key) ||
				(a.entry.parser?.cSymbol ?? '').localeCompare(b.entry.parser?.cSymbol ?? '')
		);

	for (const { key, entry } of keyedEntries) {
		const cSymbol = entry.parser?.cSymbol;
		const baseName =
			cSymbol && /^[A-Za-z_]\w*$/.test(cSymbol)
				? treeSitterCNameMemberName(cSymbol)
				: treeSitterIdMemberName(key);
		const existingKey = usedNames.get(baseName);
		const memberName =
			existingKey === undefined || existingKey === key
				? baseName
				: `${baseName}_${entry.id ?? ''}`;
		usedNames.set(memberName, key);
		if (existingKey === undefined) usedNames.set(baseName, key);
		result.push({ key, id: entry.id ?? -1, cName: cSymbol, memberName });
	}

	return result;
}

function toIdMap(
	ids: GeneratedIdTable | undefined
): Map<string, GeneratedIdEntry> {
	if (!ids) return new Map();
	const entries = ids instanceof Map ? [...ids.entries()] : Object.entries(ids);
	return new Map(
		entries.map(([name, entry]) => [
			name,
			typeof entry === 'number' ? { id: entry } : entry
		])
	);
}

function collectFieldNames(nodeMap: NodeMap): string[] {
	const result = new Set<string>();
	for (const [, node] of nodeMap.nodes) {
		for (const field of fieldsOfNode(node)) result.add(field.name);
	}
	return [...result];
}

function treeSitterIdMemberName(key: string): string {
	if (key.startsWith('_')) {
		const name = pascalCaseFromSnake(key);
		return name ? `Hidden${name}` : 'Hidden';
	}
	if (/^[A-Za-z_]\w*$/.test(key)) return pascalCaseFromSnake(key);
	return bitflagMemberName(key);
}

function treeSitterCNameMemberName(cName: string): string {
	return treeSitterPascalCase(cName.replace('sym_', ''));
}

function treeSitterPascalCase(s: string): string {
	return s
		.replace(/^_+/, '')
		.split('_')
		.filter(Boolean)
		.map((w) => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase())
		.join('');
}

// ---------------------------------------------------------------------------
// Bitflag const enum emission (ADR-0012)
// ---------------------------------------------------------------------------

interface BitflagBinding {
	readonly kind: string;
	readonly field: AssembledField;
	readonly constName: string;
	readonly values: readonly string[];
	readonly nonEmptyRepeat: boolean;
}

/**
 * Walk the NodeMap and emit a `const enum` declaration per bitflag-
 * classified field. Deduplicates by `constName`: when two fields
 * collapse to the same name and carry the same keyword set, a single
 * declaration serves both.
 */
function emitBitflagConstEnums(lines: string[], nodeMap: NodeMap): void {
	const bindings = collectBitflagBindings(nodeMap);
	if (bindings.length === 0) return;
	// Sort alphabetically by constName for deterministic diffs.
	bindings.sort((a, b) => a.constName.localeCompare(b.constName));

	lines.push(
		'// Bitflag const enums — ordered-unique literal sets per bitflag field (ADR-0012)'
	);
	const seen = new Set<string>();
	for (const b of bindings) {
		if (seen.has(b.constName)) continue;
		seen.add(b.constName);
		lines.push(`/** Bitflag set for \`${b.kind}.${b.field.name}\`. */`);
		lines.push(`export const enum ${b.constName} {`);
		// Zero-flag member only when the repeat allows zero (plain repeat,
		// not repeat1). For repeat1-backed bitflags, None would be a
		// type-system lie — at least one flag must be present.
		if (!b.nonEmptyRepeat) {
			lines.push('  None = 0,');
		}
		b.values.forEach((v, i) => {
			lines.push(`  ${bitflagMemberName(v)} = 1 << ${i},`);
		});
		lines.push('}');
		lines.push('');
	}
}

/**
 * Walk the NodeMap and collect one BitflagBinding per bitflag-
 * classified field across all structural and group kinds. Resolves
 * the const name with collision disambiguation — fields whose name
 * collapses to the same PascalCase identifier across kinds get a
 * kind-prefixed name instead of the bare field-name form.
 */
function collectBitflagBindings(nodeMap: NodeMap): BitflagBinding[] {
	const candidates: {
		kind: string;
		field: AssembledField;
		bare: string;
		prefixed: string;
		values: readonly string[];
		nonEmptyRepeat: boolean;
	}[] = [];
	for (const [kind, node] of nodeMap.nodes) {
		for (const f of fieldsOfNode(node)) {
			if (keywordPresenceKind(f, nodeMap) !== 'bitflag') continue;
			const values = keywordPresenceValues(f, nodeMap);
			if (values.length < 2) continue;
			candidates.push({
				kind,
				field: f,
				bare: bitflagBareConstName(f.propertyName),
				prefixed: bitflagPrefixedConstName(kind, f.propertyName),
				values,
				nonEmptyRepeat: keywordPresenceIsNonEmptyRepeat(f)
			});
		}
	}
	// Disambiguate collisions: a bare name used by more than one kind
	// gets the prefixed form for every occurrence.
	const bareCounts = new Map<string, number>();
	for (const c of candidates)
		bareCounts.set(c.bare, (bareCounts.get(c.bare) ?? 0) + 1);
	return candidates.map((c) => ({
		kind: c.kind,
		field: c.field,
		constName: (bareCounts.get(c.bare) ?? 0) > 1 ? c.prefixed : c.bare,
		values: c.values,
		nonEmptyRepeat: c.nonEmptyRepeat
	}));
}

/**
 * Compute the const enum name for a bitflag field from its property
 * name alone (collision-free form).
 *
 * Examples: `modifiers` → `Modifiers`, `functionModifiers` → `FunctionModifiers`.
 */
export function bitflagBareConstName(propertyName: string): string {
	return pascalCaseFromCamel(propertyName);
}

/**
 * Compute the disambiguated const enum name by prefixing the parent
 * kind.
 *
 * Example: `class_declaration.modifiers` → `ClassDeclarationModifiers`.
 */
export function bitflagPrefixedConstName(
	kind: string,
	propertyName: string
): string {
	return pascalCaseFromSnake(kind) + pascalCaseFromCamel(propertyName);
}

/**
 * Resolve the bitflag const name for a given kind + field pair.
 *
 * This must agree with {@link collectBitflagBindings} — callers in
 * other emitters (types / factories / from) use this to reference the
 * emitted name.
 */
export function resolveBitflagConstName(
	kind: string,
	field: AssembledField,
	nodeMap: NodeMap
): string | undefined {
	if (keywordPresenceKind(field, nodeMap) !== 'bitflag') return undefined;
	// Recompute the collision map so callers don't have to thread it.
	const bareCounts = new Map<string, number>();
	for (const [k, n] of nodeMap.nodes) {
		for (const f of fieldsOfNode(n)) {
			if (keywordPresenceKind(f, nodeMap) !== 'bitflag') continue;
			const bare = bitflagBareConstName(f.propertyName);
			bareCounts.set(bare, (bareCounts.get(bare) ?? 0) + 1);
			// Ignore k/n unused warning — bareCounts only cares about collisions.
			void k;
			void n;
		}
	}
	const bare = bitflagBareConstName(field.propertyName);
	return (bareCounts.get(bare) ?? 0) > 1
		? bitflagPrefixedConstName(kind, field.propertyName)
		: bare;
}

/**
 * Convert a keyword string to a valid PascalCase const-enum member.
 * Strips non-word characters and PascalCases each segment.
 *
 * Examples: `async` → `Async`, `pub(crate)` → `PubCrate`.
 *
 * Pure-punctuation literals (e.g. python comparison operators `<`,
 * `>=`, `!=`) all have zero word segments after the non-word strip;
 * routing them all through the `Unknown` fallback produced duplicate
 * enum members that `tsgo` / TS-native rejects with `Identifier X has
 * already been declared`. Map the most common operator punctuation
 * to mnemonic names so each literal produces a distinct identifier.
 * The table intentionally covers comparison + bitwise + logical +
 * assignment forms shared across rust / ts / python grammars; any
 * literal not in the table still falls through to the
 * char-code-based fallback below, which generates a unique name per
 * literal (prefixed `Op_<codepoints>`) so duplicates never collide.
 */
const PUNCT_MNEMONIC: Record<string, string> = {
	'<': 'Lt',
	'>': 'Gt',
	'<=': 'Le',
	'>=': 'Ge',
	'==': 'Eq',
	'!=': 'Ne',
	'<>': 'NeAlt',
	'===': 'EqStrict',
	'!==': 'NeStrict',
	'+': 'Plus',
	'-': 'Minus',
	'*': 'Star',
	'/': 'Slash',
	'%': 'Percent',
	'**': 'StarStar',
	'//': 'SlashSlash',
	'&': 'Amp',
	'|': 'Pipe',
	'^': 'Caret',
	'~': 'Tilde',
	'&&': 'AmpAmp',
	'||': 'PipePipe',
	'??': 'Nullish',
	'<<': 'Shl',
	'>>': 'Shr',
	'>>>': 'Ushr',
	'=': 'Assign',
	'+=': 'PlusAssign',
	'-=': 'MinusAssign',
	'*=': 'StarAssign',
	'/=': 'SlashAssign',
	'%=': 'PercentAssign',
	'**=': 'StarStarAssign',
	'//=': 'SlashSlashAssign',
	'&=': 'AmpAssign',
	'|=': 'PipeAssign',
	'^=': 'CaretAssign',
	'<<=': 'ShlAssign',
	'>>=': 'ShrAssign',
	'>>>=': 'UshrAssign',
	'!': 'Bang',
	'?': 'Question',
	'@': 'At',
	'#': 'Hash',
	$: 'Dollar',
	'.': 'Dot',
	',': 'Comma',
	':': 'Colon',
	';': 'Semi',
	'(': 'LParen',
	')': 'RParen',
	'[': 'LBracket',
	']': 'RBracket',
	'{': 'LBrace',
	'}': 'RBrace',
	'=>': 'FatArrow',
	'->': 'ThinArrow',
	'::': 'DoubleColon'
};

function bitflagMemberName(keyword: string): string {
	// Pure-punctuation mnemonic fast path — produces distinct names
	// for common operators without depending on word segmentation.
	const punctName = PUNCT_MNEMONIC[keyword];
	if (punctName) return punctName;

	const segments = keyword
		.replace(/[^\w]+/g, '_')
		.split('_')
		.filter(Boolean);
	if (segments.length === 0) {
		// Unknown punctuation — hash the literal's char codes into a
		// stable suffix so repeated fallbacks don't collide. Prefixed
		// `Op_` to make it readable + obviously-a-fallback at a glance.
		const codepoints = Array.from(keyword)
			.map((c) => c.codePointAt(0)!.toString(16))
			.join('');
		return `Op_${codepoints || 'empty'}`;
	}
	const name = segments
		.map((s) => s.charAt(0).toUpperCase() + s.slice(1))
		.join('');
	// Prefix a leading digit so the name is a valid identifier.
	return /^\d/.test(name) ? `K${name}` : name;
}

function pascalCaseFromSnake(s: string): string {
	return s
		.replace(/^_+/, '')
		.split('_')
		.filter(Boolean)
		.map((w) => w.charAt(0).toUpperCase() + w.slice(1))
		.join('');
}

function pascalCaseFromCamel(s: string): string {
	if (s.length === 0) return s;
	return s.charAt(0).toUpperCase() + s.slice(1);
}

/** Yield the fields of a node — branch, polymorph (via forms), group. */
function fieldsOfNode(node: AssembledNode): readonly AssembledField[] {
	switch (node.modelType) {
		case 'branch':
			return node.fields;
		case 'group':
			return node.fields;
		case 'polymorph': {
			const seen = new Set<string>();
			const out: AssembledField[] = [];
			for (const form of node.forms) {
				for (const f of form.fields) {
					if (!seen.has(f.name)) {
						seen.add(f.name);
						out.push(f);
					}
				}
			}
			return out;
		}
		default:
			return [];
	}
}

function getFields(node: AssembledNode) {
	switch (node.modelType) {
		case 'branch':
			return node.fields;
		case 'polymorph': {
			// Union of all form fields
			const seen = new Set<string>();
			const fields: (typeof node.forms)[0]['fields'] = [];
			for (const form of node.forms) {
				for (const f of form.fields) {
					if (!seen.has(f.name)) {
						seen.add(f.name);
						fields.push(f);
					}
				}
			}
			return fields;
		}
		default:
			return [];
	}
}

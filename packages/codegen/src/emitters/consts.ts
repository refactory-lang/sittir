/**
 * Emits a consts module with discoverable arrays and maps from the grammar.
 *
 * Exports:
 *   NODE_KINDS     — all branch node kind strings
 *   LEAF_KINDS     — all leaf/terminal node kind strings
 *   ALL_KINDS      — NODE_KINDS ∪ LEAF_KINDS
 *   KEYWORDS       — alphabetic anonymous tokens (fn, struct, let, etc.)
 *   OPERATORS      — non-alphabetic anonymous tokens (+, -, ::, etc.)
 *   FIELD_MAP      — per-node-kind field metadata
 */

import type { NodeMeta } from '../grammar-reader.ts';
import { listEnumValues } from '../grammar-reader.ts';
import { toFieldName } from '../naming.ts';

export interface EnumKind {
  kind: string;
  values: string[];
}

export interface EmitConstsConfig {
  grammar: string;
  nodeKinds: string[];
  leafKinds: string[];
  keywords: string[];
  operators: string[];
  nodes: NodeMeta[];
  enumKinds?: EnumKind[];
}

export function emitConsts(config: EmitConstsConfig): string {
  const { nodeKinds, leafKinds, keywords, operators, nodes } = config;

  const lines: string[] = [
    '// Auto-generated by ir-codegen — do not edit',
    '',
  ];

  // NODE_KINDS
  lines.push('/** All branch (non-leaf) node kind strings. */');
  lines.push('export const NODE_KINDS = [');
  for (const kind of nodeKinds.sort()) {
    lines.push(`  '${kind}',`);
  }
  lines.push('] as const;');
  lines.push('');

  // LEAF_KINDS
  lines.push('/** All leaf/terminal node kind strings. */');
  lines.push('export const LEAF_KINDS = [');
  for (const kind of leafKinds.sort()) {
    lines.push(`  '${kind}',`);
  }
  lines.push('] as const;');
  lines.push('');

  // ALL_KINDS
  lines.push('/** All node kind strings (branch + leaf). */');
  lines.push('export const ALL_KINDS = [...NODE_KINDS, ...LEAF_KINDS] as const;');
  lines.push('');

  // KEYWORDS
  lines.push('/** Language keywords (alphabetic anonymous tokens). */');
  lines.push('export const KEYWORDS = [');
  for (const kw of keywords) {
    lines.push(`  '${kw}',`);
  }
  lines.push('] as const;');
  lines.push('');

  // OPERATORS
  lines.push('/** Operator/punctuation tokens. */');
  lines.push('export const OPERATORS = [');
  for (const op of operators) {
    lines.push(`  '${op.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}',`);
  }
  lines.push('] as const;');
  lines.push('');

  // Type aliases
  lines.push('export type NodeKind = (typeof NODE_KINDS)[number];');
  lines.push('export type LeafKind = (typeof LEAF_KINDS)[number];');
  lines.push('export type AnyKind = (typeof ALL_KINDS)[number];');
  lines.push('export type Keyword = (typeof KEYWORDS)[number];');
  lines.push('export type Operator = (typeof OPERATORS)[number];');
  lines.push('');

  // FIELD_MAP — per-node field metadata
  lines.push('/** Per-node-kind field metadata. */');
  lines.push('export const FIELD_MAP: Record<NodeKind, ReadonlyArray<{');
  lines.push('  name: string;');
  lines.push('  required: boolean;');
  lines.push('  multiple: boolean;');
  lines.push('}>> = {');

  for (const node of [...nodes].sort((a, b) => a.kind.localeCompare(b.kind))) {
    lines.push(`  '${node.kind}': [`);
    for (const field of node.fields) {
      lines.push(`    { name: '${toFieldName(field.name)}', required: ${field.required}, multiple: ${field.multiple} },`);
    }
    lines.push('  ],');
  }
  lines.push('};');
  lines.push('');

  // Enum-like leaf kinds (e.g., primitive_type → ['bool', 'char', 'i32', ...])
  if (config.enumKinds && config.enumKinds.length > 0) {
    for (const ek of config.enumKinds) {
      // primitive_type → PRIMITIVE_TYPES, predefined_type → PREDEFINED_TYPES
      const constName = ek.kind.toUpperCase() + 'S';
      // primitive_type → PrimitiveType
      const typeName = ek.kind.split('_').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join('');

      lines.push(`/** Valid values for \`${ek.kind}\` nodes. */`);
      lines.push(`export const ${constName} = [`);
      for (const v of ek.values) {
        lines.push(`  '${v.replace(/'/g, "\\'")}',`);
      }
      lines.push('] as const;');
      lines.push(`export type ${typeName} = (typeof ${constName})[number];`);
      lines.push('');
    }
  }

  return lines.join('\n');
}

/**
 * Emits consts.ts — discoverable arrays and maps from the grammar.
 *
 * Consumes NodeMap directly. No imports from node-model.ts or naming.ts.
 */

import type { NodeMap } from '../compiler/types.ts';
import type { AssembledNode, AssembledField } from '../compiler/node-map.ts';
import type {
	GeneratedIdEntry,
	GeneratedIdTable,
	GeneratedIdTables
} from '../compiler/generated-metadata.ts';
import {
	isRequired,
	isMultiple,
	keywordPresenceKind,
	keywordPresenceValues,
	keywordPresenceIsNonEmptyRepeat
} from './shared.ts';

export interface EmitConstsConfig {
	grammar: string;
	nodeMap: NodeMap;
	generatedIdTables?: GeneratedIdTables;
}

export function emitConsts(config: EmitConstsConfig): string {
	const { nodeMap, generatedIdTables } = config;

	const nodeKinds: string[] = []; // branch + container + polymorph
	const leafKinds: string[] = []; // leaf + keyword + enum
	const keywords: string[] = []; // keyword model type (alphabetic tokens)
	const operators: string[] = []; // token model type (non-alphabetic)
	const enumEntries: { kind: string; values: string[] }[] = [];

	for (const [kind, node] of nodeMap.nodes) {
		switch (node.modelType) {
			case 'branch':
			case 'container':
			case 'polymorph':
				nodeKinds.push(kind);
				break;
			case 'leaf':
			case 'enum':
				leafKinds.push(kind);
				if (node.modelType === 'enum') {
					enumEntries.push({ kind, values: node.values });
				}
				break;
			case 'keyword':
				leafKinds.push(kind);
				keywords.push(kind);
				break;
			case 'token':
				if (/^[a-z_]+$/i.test(kind)) {
					keywords.push(kind);
				} else {
					operators.push(kind);
				}
				break;
			// supertype, group — not in any public const array
		}
	}

	const lines: string[] = [
		'// Auto-generated by @sittir/codegen — do not edit',
		''
	];

	// NODE_KINDS
	lines.push('/** All branch (non-leaf) node kind strings. */');
	lines.push('export const NODE_KINDS = [');
	for (const kind of nodeKinds.sort()) {
		lines.push(`  '${kind}',`);
	}
	lines.push('] as const;');
	lines.push('');

	// LEAF_KINDS
	lines.push('/** All leaf/terminal node kind strings. */');
	lines.push('export const LEAF_KINDS = [');
	for (const kind of leafKinds.sort()) {
		lines.push(`  '${kind}',`);
	}
	lines.push('] as const;');
	lines.push('');

	// ALL_KINDS
	lines.push('/** All node kind strings (branch + leaf). */');
	lines.push(
		'export const ALL_KINDS = [...NODE_KINDS, ...LEAF_KINDS] as const;'
	);
	lines.push('');

	// KEYWORDS
	lines.push('/** Language keywords (alphabetic anonymous tokens). */');
	lines.push('export const KEYWORDS = [');
	for (const kw of keywords.sort()) {
		lines.push(`  '${kw}',`);
	}
	lines.push('] as const;');
	lines.push('');

	// OPERATORS — JSON.stringify to safely handle quotes/backslashes/newlines
	lines.push('/** Operator/punctuation tokens. */');
	lines.push('export const OPERATORS = [');
	for (const op of operators.sort()) {
		lines.push(`  ${JSON.stringify(op)},`);
	}
	lines.push('] as const;');
	lines.push('');

	// Type aliases
	// Note: `AnyOperator` (not `Operator`) to avoid collision with concrete
	// grammar terminal types named `_operator` that `types.ts` exports.
	lines.push('export type NodeKind = (typeof NODE_KINDS)[number];');
	lines.push('export type LeafKind = (typeof LEAF_KINDS)[number];');
	lines.push('export type AnyKind = (typeof ALL_KINDS)[number];');
	lines.push('export type Keyword = (typeof KEYWORDS)[number];');
	lines.push('export type AnyOperator = (typeof OPERATORS)[number];');
	lines.push('');

	emitTreeSitterIdConsts(lines, {
		kindIds: generatedIdTables?.kindIds,
		fieldIds: generatedIdTables?.fieldIds,
		kinds: [
			...new Set([...nodeKinds, ...leafKinds, ...keywords, ...operators])
		],
		fields: collectFieldNames(nodeMap),
		sourceArtifact: generatedIdTables?.sourceArtifact
	});

	// FIELD_MAP
	lines.push('/** Per-node-kind field metadata. */');
	lines.push('export const FIELD_MAP: Record<NodeKind, ReadonlyArray<{');
	lines.push('  name: string;');
	lines.push('  required: boolean;');
	lines.push('  multiple: boolean;');
	lines.push('}>> = {');

	for (const kind of nodeKinds.sort()) {
		const node = nodeMap.nodes.get(kind)!;
		lines.push(`  '${kind}': [`);
		const fields = getFields(node);
		for (const f of fields) {
			lines.push(
				`    { name: '${f.propertyName}', required: ${isRequired(f)}, multiple: ${isMultiple(f)} },`
			);
		}
		lines.push('  ],');
	}
	lines.push('};');
	lines.push('');

	// Bitflag const enums — one per bitflag-classified field (ADR-0012).
	emitBitflagConstEnums(lines, nodeMap);

	// Enum values
	const emittedValueTypes = new Set<string>();
	for (const ek of enumEntries.sort((a, b) => a.kind.localeCompare(b.kind))) {
		const constName = ek.kind.toUpperCase() + 'S';
		const typeName =
			ek.kind
				.split('_')
				.filter(Boolean)
				.map((w) => w.charAt(0).toUpperCase() + w.slice(1))
				.join('') + 'Value';

		lines.push(`/** Valid values for \`${ek.kind}\` nodes. */`);
		lines.push(`export const ${constName} = [`);
		for (const v of ek.values) {
			lines.push(`  '${v.replace(/'/g, "\\'")}',`);
		}
		lines.push('] as const;');
		// Emit the type alias only once per unique name — hidden kinds like
		// `_accessibility_modifier` and their visible counterparts like
		// `accessibility_modifier` resolve to the same PascalCase type name.
		// Emitting both would produce a duplicate identifier TS2300 error.
		if (!emittedValueTypes.has(typeName)) {
			emittedValueTypes.add(typeName);
			lines.push(`export type ${typeName} = (typeof ${constName})[number];`);
		}
		lines.push('');
	}

	return lines.join('\n');
}


/**
 * Emits an index.ts barrel file that re-exports all generated modules.
 *
 * Uses the @sittir/typescript IR builder declarative from() API.
 * This is the IR-based replacement for the string-concatenation approach
 * in index-file.ts.
 */

import { ir } from '@sittir/typescript';
import { LeafBuilder } from '@sittir/types';

// ---------------------------------------------------------------------------
// Leaf helpers
// ---------------------------------------------------------------------------

const leaf = <K extends string>(kind: K, text: string) => new LeafBuilder(kind, text);
const str = (text: string) => leaf('string', `'${text}'`);

// ---------------------------------------------------------------------------
// AST construction helpers
// ---------------------------------------------------------------------------

/** `export * from './module.js';` */
function reExportAll(from: string) {
  return ir.exportStatement.from({
    children: leaf('namespace_export', '*'),
    source: str(from),
  });
}

/** `export { a, b } from './module.js';` */
function reExportNamed(names: string[], from: string) {
  return ir.exportStatement.from({
    children: ir.exportClause.from({
      children: names.map(n => ir.exportSpecifier.from({ name: ir.identifier(n) })),
    }),
    source: str(from),
  });
}

/** `export type { A, B } from './module.js';` — uses leaf (type-only export not in CST IR) */
function reExportTypes(names: string[], from: string) {
  return leaf('export_statement', `export type { ${names.join(', ')} } from '${from}'`);
}

// ---------------------------------------------------------------------------
// Public API — same interface as index-file.ts
// ---------------------------------------------------------------------------

export type { EmitIndexConfig } from './index-file.ts';
import type { EmitIndexConfig } from './index-file.ts';

export function emitIndexIr(_config: EmitIndexConfig): string {
  const statements: string[] = [
    '// Auto-generated by ir-codegen — do not edit',
    '',
    '// Types (grammar + projected IR nodes)',
    reExportAll('./types.js').renderImpl() + ';',
    '',
    '// Builders (ir namespace with branch + leaf builders)',
    reExportNamed(['ir', 'fromCST', 'edit'], './builder.js').renderImpl() + ';',
    reExportTypes(['CSTNode'], './builder.js').renderImpl() + ';',
    '',
    '// Constants (node kinds, keywords, operators, field maps)',
    reExportAll('./consts.js').renderImpl() + ';',
    '',
    '// Base builder + leaf builder + edit utilities (re-export from @sittir/types)',
    reExportNamed(['Builder', 'LeafBuilder'], '@sittir/types').renderImpl() + ';',
    reExportTypes(
      ['RenderContext', 'Edit', 'FieldKinds', 'FieldName', 'NodeTransform', 'NodeType'],
      '@sittir/types',
    ).renderImpl() + ';',
    '',
  ];

  return statements.join('\n');
}

type FieldCarryingNode = Extract<AssembledNode, { modelType: 'branch' | 'group' | 'polymorph' }>

/** Resolve a container node's children element type to a concrete TS type expression. */
function childElementType(node: { children: readonly AssembledChild[] }, nodeMap: NodeMap): string {
    const parts = new Set<string>()
    for (const c of node.children) {
        for (const t of slotKindNames(c)) {
            const ref = nodeMap.nodes.get(t)
            if (!ref) { parts.add(JSON.stringify(t)); continue }
            const name = ref.typeName
            parts.add(/^[A-Za-z_$][\w$]*$/.test(name) ? `T.${name}` : JSON.stringify(t))
        }
    }
    if (parts.size === 0) return 'never'
    const union = [...parts].join(' | ')
    return parts.size > 1 ? `(${union})` : union
}

/**
 * Build the TypeScript stamp expression for an auto-stamp-eligible field.
 *
 * @remarks
 * Two cases:
 *
 * - **Source A** (`field.literalValues.length === 1`): the field content is an
 *   inline string literal. Stamp the string directly, e.g. `'pub' as const`.
 *
 * - **Source B** (`field.contentTypes.length === 1` and the referenced kind is
 *   an `AssembledKeyword`): the field content is a hidden-rule terminal with a
 *   single word-like text value (e.g. `_kw_async`). Stamp a minimal leaf
 *   NodeData object whose shape matches `Terminal<kind, text>`:
 *   `{ $type: '_kw_async', $text: 'async', $source: 'factory', $named: true }`.
 *
 * Returns `undefined` when the field is NOT auto-stamp-eligible.
 */
function autoStampExpression(f: AssembledField, nodeMap: NodeMap): string | undefined {
    // Delegates to the shared stampExpressionFor which uses the new values model.
    return stampExpressionFor(f, nodeMap)
}

/**
 * Build the RHS expression for a factory field assignment when the
 * field classifies as keyword-presence (ADR-0012).
 *
 * Scalar boolean:   `_bk(config?.propertyName, '<kind>', '<text>', <named>)`
 * Array  boolean:   `_bkArr(config?.propertyName, '<kind>', '<text>', <named>)`
 *   (degenerate repeat-of-one-literal)
 * Bitflag:          `_bf(config?.propertyName, [<kinds>], [<texts>], <named>)`
 *
 * Returns `undefined` when the field doesn't classify as keyword-presence.
 */
function keywordPresenceAssignmentExpr(
    f: AssembledField,
    opt: string,
    nodeMap: NodeMap,
): string | undefined {
    const kw = keywordPresenceKind(f, nodeMap)
    if (kw === null) return undefined
    const access = `config${opt}.${f.propertyName}`
    if (kw === 'boolean') {
        const lit = keywordPresenceValue(f, nodeMap)
        if (lit === undefined) return undefined
        const triple = resolveKeywordPresenceTriple(f, lit, nodeMap)
        if (!triple) return undefined
        const helper = isMultiple(f) ? '_bkArr' : '_bk'
        return `${helper}(${access}, ${JSON.stringify(triple.kind)}, ${JSON.stringify(triple.text)}, ${triple.named})`
    }
    // bitflag
    const lits = keywordPresenceValues(f, nodeMap)
    if (lits.length === 0) return undefined
    const triples = lits.map(l => resolveKeywordPresenceTriple(f, l, nodeMap)).filter((t): t is { kind: string; text: string; named: boolean } => t !== undefined)
    if (triples.length !== lits.length) return undefined
    const kinds = `[${triples.map(t => JSON.stringify(t.kind)).join(', ')}]`
    const texts = `[${triples.map(t => JSON.stringify(t.text)).join(', ')}]`
    // All bitflag entries have the same `named` flag (they're all hidden
    // `_kw_*` OR all direct-terminal OR all visible-keyword — the
    // grammar won't mix them). Use the first triple's flag.
    const named = triples[0]!.named
    return `_bf(${access}, ${kinds}, ${texts}, ${named})`
}

/**
 * Resolve an AssembledField's element type to a concrete TS type expression
 * for the factory surface — each resolved node kind is prefixed with `T.` so
 * the reference resolves against the `import * as T from './types.js'` import.
 *
 * Delegates to the shared {@link fieldTypeComponents} walker so the node-ref
 * / literal / alias-source / hidden-keyword / missing-kind logic stays in one
 * place (types.ts::fieldTypeExpr is the same walk with no prefix).
 */
function fieldElementType(f: AssembledField, nodeMap: NodeMap): string {
    const literals = slotLiteralValues(f)
    const kindNames = slotKindNames(f)

    if (literals.length > 0 && kindNames.length === 0) {
        return literals.map(v => JSON.stringify(v)).join(' | ')
    }
    if (kindNames.length === 0 && literals.length === 0) return 'string'

    const components = fieldTypeComponents(f, nodeMap)
    const parts: string[] = []
    for (const comp of components) {
        if (comp.kind === 'literal') {
            parts.push(JSON.stringify(comp.value))
        } else if (comp.kind === 'nodeKind') {
            parts.push(isValidIdent(comp.value) ? `T.${comp.value}` : JSON.stringify(comp.rawKind))
        } else {
            // Missing kind — factories can't register for stub emission
            // (types.ts owns that side). Fall back to the `T.` prefix so
            // the reference at least links against whatever stub types.ts
            // emits for its own missing kind.
            parts.push(`T.${comp.value}`)
        }
    }
    return [...new Set(parts)].join(' | ')
}

function emitFieldCarryingFactory(
    node: FieldCarryingNode,
    fields: readonly AssembledField[],
    children: readonly AssembledChild[],
    nodeMap: NodeMap,
    isPolymorphForm = false,
): string {
    const fn = node.rawFactoryName!
    const hasFields = fields.length > 0
    const hasChildren = children.length > 0
    const childrenMultiple = resolveChildrenMultiple(children)
    const opt = resolveConfigOptional(fields, children, nodeMap)
    const typeKind = node.modelType === 'group' ? node.parentKind ?? node.kind : node.kind
    const configType = resolveConfigType(node, isPolymorphForm)
    const lines: string[] = []
    lines.push(`export function ${fn}(config${opt}: ${configType}) {`)

    if (hasFields) {
        lines.push('  const fields = {')
        for (const f of fields) {
            const stamp = autoStampExpression(f, nodeMap)
            if (stamp !== undefined) {
                // Auto-stamp: field has a single fixed literal value — stamp it
                // directly without reading from config. The field is omitted from
                // Config so the caller cannot (and need not) supply it.
                lines.push(`    ${f.name}: ${stamp},`)
                continue
            }
            const kwExpr = keywordPresenceAssignmentExpr(f, opt, nodeMap)
            if (kwExpr !== undefined) {
                lines.push(`    ${f.name}: ${kwExpr},`)
                continue
            }
            lines.push(`    ${f.name}: config${opt}.${f.propertyName},`)
        }
        lines.push('  };')
    }
    if (hasChildren) {
        // If every required child slot is auto-stamp-eligible, pre-stamp them
        // directly rather than reading from config. The stamp expression for
        // each required slot is a factory call or literal value. Optional
        // children contribute nothing (they default to absent in the array).
        const requiredChildren = children.filter(c => isRequired(c))
        const allRequiredAutoStamp = requiredChildren.length > 0
            && requiredChildren.every(c => isAutoStampSlot(c, nodeMap))
        if (allRequiredAutoStamp) {
            // Build the pre-stamped children array literal.
            const stampedItems = requiredChildren.map(c => stampExpressionFor(c, nodeMap) ?? 'undefined')
            lines.push(`  const children = config${opt}.children ?? [${stampedItems.join(', ')}];`)
        } else {
            lines.push(`  const children = config${opt}.children ?? [];`)
        }
    }

    const variantName = node.modelType == 'group' ? resolvePolymorphFormVariantName(node) : undefined
    lines.push('  return {')
    lines.push(`    $type: '${typeKind}' as const,`)
    lines.push(`    $source: 'factory' as const,`)
    lines.push('    $named: true as const,')
    if (variantName) lines.push(`    $variant: '${variantName}' as const,`)
    if (hasFields) lines.push('    $fields: fields,')
    if (hasChildren) lines.push('    $children: children,')

    lines.push(...emitFluentFieldMethods(fn, fields, nodeMap))

    if (hasChildren) {
        const childElem = childElementType({ children }, nodeMap)
        if (childrenMultiple) {
            // Unified fluent `children()` — no-arg getter, varargs setter.
            // `$`-prefix on NodeData metadata (`$children`) freed the bare
            // `children` identifier on the factory surface, matching the
            // existing `value()` / `values()` fluent pattern for fields.
            const childrenNonEmpty = children.some(c => isNonEmpty(c))
            lines.push(`    children(...items: ${childElem}[]) {`)
            lines.push('      if (items.length === 0) return children;')
            if (childrenNonEmpty) {
                lines.push(`      _assertNonEmpty(items, '${node.kind}.children');`)
            }
            lines.push(`      return ${fn}({ ...config, children: items });`)
            lines.push('    },')
        } else {
            lines.push(`    child(value?: ${childElem}) {`)
            lines.push('      if (value === undefined) return children[0];')
            lines.push(`      return ${fn}({ ...config, children: [value] });`)
            lines.push('    },')
        }
    }

    lines.push(...factorySuffix(node.treeTypeName))
    lines.push('  };')
    lines.push('}')
    return lines.join('\n')
}


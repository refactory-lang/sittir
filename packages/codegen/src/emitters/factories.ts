type FieldCarryingNode = Extract<AssembledNode, { modelType: 'branch' | 'group' | 'polymorph' }>

/** Resolve a container node's children element type to a concrete TS type expression. */
function childElementType(node: { children: readonly AssembledChild[] }, nodeMap: NodeMap): string {
    const parts = new Set<string>()
    for (const c of node.children) {
        for (const t of slotKindNames(c)) {
            const ref = nodeMap.nodes.get(t)
            if (!ref) { parts.add(JSON.stringify(t)); continue }
            const name = ref.typeName
            parts.add(/^[A-Za-z_$][\w$]*$/.test(name) ? `T.${name}` : JSON.stringify(t))
        }
    }
    if (parts.size === 0) return 'never'
    const union = [...parts].join(' | ')
    return parts.size > 1 ? `(${union})` : union
}

/**
 * Build the TypeScript stamp expression for an auto-stamp-eligible field.
 *
 * @remarks
 * Two cases:
 *
 * - **Source A** (`field.literalValues.length === 1`): the field content is an
 *   inline string literal. Stamp the string directly, e.g. `'pub' as const`.
 *
 * - **Source B** (`field.contentTypes.length === 1` and the referenced kind is
 *   an `AssembledKeyword`): the field content is a hidden-rule terminal with a
 *   single word-like text value (e.g. `_kw_async`). Stamp a minimal leaf
 *   NodeData object whose shape matches `Terminal<kind, text>`:
 *   `{ $type: '_kw_async', $text: 'async', $source: 'factory', $named: true }`.
 *
 * Returns `undefined` when the field is NOT auto-stamp-eligible.
 */
function autoStampExpression(f: AssembledField, nodeMap: NodeMap): string | undefined {
    // Delegates to the shared stampExpressionFor which uses the new values model.
    return stampExpressionFor(f, nodeMap)
}

/** Resolve an AssembledField's element type to a concrete TS type expression. */
function fieldElementType(f: AssembledField, nodeMap: NodeMap): string {
    const literals = slotLiteralValues(f)
    const kindNames = slotKindNames(f)

    if (literals.length > 0 && kindNames.length === 0) {
        return literals.map(v => JSON.stringify(v)).join(' | ')
    }
    if (kindNames.length === 0 && literals.length === 0) return 'string'

    // Alias-source projection (ADR-0006 extended to the factory surface):
    // when a content type is the TARGET of `alias($.source, $.target)`, the
    // body follows `source`'s shape. Factory config accepts the source type
    // so callers construct nodes with the real structure.
    const resolveAliased = (t: string): string => {
        const source = f.aliasSources?.[t]
        if (!source) return t
        return nodeMap.nodes.get(source) ? source : t
    }
    const nodeParts = kindNames.map(resolveAliased).map(t => {
        const node = nodeMap.nodes.get(t)
        if (!node) {
            const fallback = t.replace(/(?:^|_)([a-z])/g, (_: string, c: string) => c.toUpperCase())
            return `T.${fallback}`
        }
        const name = node.typeName
        return /^[A-Za-z_$][\w$]*$/.test(name) ? `T.${name}` : JSON.stringify(t)
    })
    const litParts = literals.map(v => JSON.stringify(v))
    return [...new Set([...nodeParts, ...litParts])].join(' | ')
}

function emitFieldCarryingFactory(
    node: FieldCarryingNode,
    fields: readonly AssembledField[],
    children: readonly AssembledChild[],
    nodeMap: NodeMap,
    isPolymorphForm = false,
): string {
    const fn = node.rawFactoryName!
    const hasFields = fields.length > 0
    const hasChildren = children.length > 0
    const childrenMultiple = resolveChildrenMultiple(children)
    const opt = resolveConfigOptional(fields, children, nodeMap)
    const typeKind = node.modelType === 'group' ? node.parentKind ?? node.kind : node.kind
    const configType = resolveConfigType(node, isPolymorphForm)
    const lines: string[] = []
    lines.push(`export function ${fn}(config${opt}: ${configType}) {`)

    if (hasFields) {
        lines.push('  const fields = {')
        for (const f of fields) {
            const stamp = autoStampExpression(f, nodeMap)
            if (stamp !== undefined) {
                // Auto-stamp: field has a single fixed literal value — stamp it
                // directly without reading from config. The field is omitted from
                // Config so the caller cannot (and need not) supply it.
                lines.push(`    ${f.name}: ${stamp},`)
            } else {
                lines.push(`    ${f.name}: config${opt}.${f.propertyName},`)
            }
        }
        lines.push('  };')
    }
    if (hasChildren) {
        // If every required child slot is auto-stamp-eligible, pre-stamp them
        // directly rather than reading from config. The stamp expression for
        // each required slot is a factory call or literal value. Optional
        // children contribute nothing (they default to absent in the array).
        const requiredChildren = children.filter(c => isRequired(c))
        const allRequiredAutoStamp = requiredChildren.length > 0
            && requiredChildren.every(c => isAutoStampSlot(c, nodeMap))
        if (allRequiredAutoStamp) {
            // Build the pre-stamped children array literal.
            const stampedItems = requiredChildren.map(c => stampExpressionFor(c, nodeMap) ?? 'undefined')
            lines.push(`  const children = config${opt}.children ?? [${stampedItems.join(', ')}];`)
        } else {
            lines.push(`  const children = config${opt}.children ?? [];`)
        }
    }

    const variantName = node.modelType == 'group' ? resolvePolymorphFormVariantName(node) : undefined
    lines.push('  return {')
    lines.push(`    $type: '${typeKind}' as const,`)
    lines.push(`    $source: 'factory' as const,`)
    lines.push('    $named: true as const,')
    if (variantName) lines.push(`    $variant: '${variantName}' as const,`)
    if (hasFields) lines.push('    $fields: fields,')
    if (hasChildren) lines.push('    $children: children,')

    lines.push(...emitFluentFieldMethods(fn, fields, nodeMap))

    if (hasChildren) {
        const childElem = childElementType({ children }, nodeMap)
        if (childrenMultiple) {
            lines.push('    getChildren() { return children; },')
            const childrenNonEmpty = children.some(c => isNonEmpty(c))
            if (childrenNonEmpty) {
                lines.push(`    setChildren(...items: ${childElem}[]) {`)
                lines.push(`      _assertNonEmpty(items, '${node.kind}.children');`)
                lines.push(`      return ${fn}({ ...config, children: items });`)
                lines.push('    },')
            } else {
                lines.push(`    setChildren(...items: ${childElem}[]) { return ${fn}({ ...config, children: items }); },`)
            }
        } else {
            lines.push(`    getChild() { return children[0]; },`)
            lines.push(`    setChild(child: ${childElem}) { return ${fn}({ ...config, children: [child] }); },`)
        }
    }

    lines.push(...factorySuffix(node.treeTypeName))
    lines.push('  };')
    lines.push('}')
    return lines.join('\n')
}

/**
 * Determine whether a children slot is "multiple-shaped" (i.e., any child entry is repeated).
 *
 * @param children - The assembled child descriptors for the node.
 * @returns `true` when at least one child entry is repeated.
 * @remarks
 *   A single `children[0]` check misses the repeated signal when inlining (T063)
 *   flattens a choice of hidden helpers into a mixed list of single + repeated entries.
 */
function resolveChildrenMultiple(children: readonly AssembledChild[]): boolean {
    return children.some(c => isMultiple(c))
}

/**
 * Determine whether the `config` parameter should be optional (`?`).
 *
 * @param fields - The assembled field descriptors for the node.
 * @param children - The assembled child descriptors for the node.
 * @param nodeMap - The assembled node map (used for auto-stamp detection).
 * @returns The option marker — `'?'` when every non-auto-stamped field and
 *   child is optional, `''` otherwise.
 * @remarks
 *   Auto-stamp-eligible fields are excluded from the "required" check because
 *   they are never present in Config — the factory stamps them directly.
 *   Only fields that remain in Config can make config required.
 */
function resolveConfigOptional(
    fields: readonly AssembledField[],
    children: readonly AssembledChild[],
    nodeMap: NodeMap,
): string {
    // Auto-stamp-eligible fields are excluded from the "required" check —
    // they never appear in Config. Auto-stamp-eligible children are also
    // excluded: when all required children are parameterless, they are
    // stamped directly in the factory body rather than read from config.
    const hasRequired = fields.some(f => isRequired(f) && autoStampExpression(f, nodeMap) === undefined)
        || children.some(c => isRequired(c) && !isAutoStampSlot(c, nodeMap))
    return hasRequired ? '' : '?'
}

/**
 * Resolve the config type reference for a field-carrying factory parameter.
 *
 * @param node - The node descriptor (provides `typeName` and `parentKind`).
 * @param isPolymorphForm - Whether the factory is emitting a polymorph form (not the dispatcher).
 * @returns A TS source string like `T.FunctionItem.Config` or `T.FunctionItemBodyFormConfig`.
 * @remarks
 *   Polymorph forms keep the flat `${typeName}Config` alias (synthetic UForm kinds have
 *   no namespace sugar). Base kinds use `T.${typeName}.Config` via the declaration-merged
 *   namespace.
 */
function resolveConfigType(node: FieldCarryingNode, isPolymorphForm: boolean): string {
    return isPolymorphForm
        ? `T.${node.typeName}Config`
        : `T.${node.typeName}.Config`
}

/**
 * Resolve the `$variant` tag name for a polymorph form factory.
 *
 * @param node - The node descriptor (provides `kind` and `parentKind`).
 * @returns The variant name suffix (e.g. `'body'` from `'struct_item_body'`), or
 *   `undefined` when the node is not a polymorph form.
 * @remarks
 *   Polymorph form factories tag their output with `variant: '<name>'` so the
 *   renderer's variant dispatch (path 1) can discriminate forms whose templates
 *   differ only by literal tokens (e.g. rust `struct_item` body vs semi — same
 *   `$VARS`, differ by trailing `;`). The variant name is the form-kind suffix
 *   after the parent kind.
 */
function resolvePolymorphFormVariantName(node: AssembledGroup): string | undefined {
    return node.parentKind && node.kind.startsWith(node.parentKind + '_')
        ? node.kind.slice(node.parentKind.length + 1)
        : undefined
}

/**
 * Emit fluent getter/setter method source lines for all fields of a factory.
 *
 * @param fn - The factory function name (used in setter body to rebuild the node).
 * @param fields - The assembled field descriptors to generate methods for.
 * @param nodeMap - The assembled node map (used for type resolution).
 * @returns Array of source lines for the getter/setter methods (ready to splice into
 *   the `return { ... }` object literal).
 * @remarks
 *   Bodies delegate to `_fs` / `_fsm` helpers (T042j) so each line is one call
 *   instead of a hand-inlined ternary. The explicit parameter / return type
 *   annotations keep consumer IntelliSense seeing the exact per-field shape.
 *
 *   The fluent getter/setter parameter name is always `value` / `values`:
 *   not field-derived — the field name is already the method name, and a derived
 *   param ends up identical to the method (shadowing) or collides with reserved
 *   words. `value`/`values` is unambiguous and keeps signatures uniform.
 */
function emitFluentFieldMethods(
    fn: string,
    fields: readonly AssembledField[],
    nodeMap: NodeMap,
): string[] {
    const lines: string[] = []
    for (const f of fields) {
        const method = f.propertyName === 'type' ? 'typeField' : f.propertyName
        const stamp = autoStampExpression(f, nodeMap)
        if (stamp !== undefined) {
            // Auto-stamp: field value is a fixed constant — emit a getter-only
            // method that returns the constant. No setter parameter.
            lines.push(`    get ${method}() { return fields.${f.name}; },`)
            continue
        }
        const fMultiple = isMultiple(f)
        const param = fMultiple ? 'values' : 'value'
        if (fMultiple) {
            const elemType = fieldElementType(f, nodeMap)
            // Rest params can't be `readonly T[]` (TS2370) so plain mutable arrays
            // are used. NonEmptyArray<T> is a tuple alias `readonly [T, ...(readonly T[])]`
            // which TS does accept as a valid rest type.
            // When elemType is a union (`A | B`), wrap in parens to avoid
            // `A | B[]` being parsed as `A | (B[])`.
            const elemForArray = elemType.includes(' | ') ? `(${elemType})` : elemType
            const restType = isNonEmpty(f) ? `NonEmptyArray<${elemType}>` : `${elemForArray}[]`
            lines.push(`    ${method}(...${param}: ${restType}) { return _fsm(config, ${fn}, '${f.propertyName}', ${param}, fields.${f.name}); },`)
        } else {
            const elemType = fieldElementType(f, nodeMap)
            const paramType = isRequired(f) ? elemType : `${elemType} | undefined`
            lines.push(`    ${method}(${param}?: ${paramType}) { return _fs(config, ${fn}, '${f.propertyName}', ${param}, fields.${f.name}); },`)
        }
    }
    return lines
}

// ---------------------------------------------------------------------------
// Container factory (children only, no fields)
// ---------------------------------------------------------------------------

interface ContainerNode {
    readonly kind: string
    readonly typeName: string
    readonly treeTypeName: string
    readonly rawFactoryName?: string
    readonly children: readonly AssembledChild[]
}

function emitContainerFactory(node: ContainerNode, nodeMap: NodeMap): string {
    const fn = node.rawFactoryName!
    const lines: string[] = []
    const anyMultiple = resolveContainerMultiple(node)
    const anyNonEmpty = node.children.some(c => isNonEmpty(c))
    const elementType = resolveContainerElementType(node, nodeMap)
    if (anyMultiple) {
        lines.push(`export function ${fn}(...children: ${elementType}[]) {`)
        if (anyNonEmpty) {
            lines.push(`  _assertNonEmpty(children, '${node.kind}.children');`)
        }
    } else {
        const firstChild = node.children[0]
        const required = firstChild ? isRequired(firstChild) : false
        const optMark = required ? '' : '?'
        lines.push(`export function ${fn}(child${optMark}: ${elementType}) {`)
        // Required child: type guarantees non-null, wrap directly.
        // Optional child: null/undefined is valid → wrap only if present.
        lines.push(required
            ? `  const children = [child];`
            : `  const children = child != null ? [child] : [];`
        )
    }
    lines.push('  return {')
    lines.push(`    $type: '${node.kind}' as const,`)
    lines.push(`    $source: 'factory' as const,`)
    lines.push('    $named: true as const,')
    lines.push('    $children: children,')
    lines.push(...factorySuffix(node.treeTypeName))
    lines.push('  };')
    lines.push('}')
    return lines.join('\n')
}

/**
 * Determine whether a container node uses a rest-params (multiple-children) signature.
 *
 * @param node - The container node descriptor.
 * @returns `true` when any child entry is repeated.
 * @remarks
 *   Containers are "multiple-shaped" when ANY child entry is repeated. Inlining
 *   (T063) can flatten a choice of hidden helpers into a mixed list of single +
 *   repeated entries, so checking only `children[0]` misses the repeated signal.
 */
function resolveContainerMultiple(node: ContainerNode): boolean {
    return node.children.some(c => isMultiple(c))
}

/**
 * Resolve the element type for a container node's children parameter.
 *
 * @param node - The container node descriptor.
 * @param nodeMap - The assembled node map for type resolution.
 * @returns A TS source string for the element type (e.g. `T.FunctionItem | T.Block`).
 * @remarks
 *   Uses the concrete element type union (e.g. `FunctionItem | Block`) instead of
 *   the generic `ChildOf<X>` alias so consumers see the actual types in
 *   hover/autocomplete with no indirection.
 */
function resolveContainerElementType(node: ContainerNode, nodeMap: NodeMap): string {
    return childElementType(node, nodeMap)
}

// ---------------------------------------------------------------------------
// Polymorph factory — dispatcher + inline form factories
// ---------------------------------------------------------------------------

interface PolymorphNode {
    readonly kind: string
    readonly typeName: string
    readonly treeTypeName: string
    readonly rawFactoryName?: string
    readonly forms: AssembledGroup[]
    readonly source?: 'promoted' | 'override'
    readonly variantChildKinds?: readonly string[]
}

function emitPolymorphFactory(node: PolymorphNode, nodeMap: NodeMap): string {
    const fn = node.rawFactoryName!
    const forms = node.forms

    if (forms.length === 0) {
        // Defensive stub — shouldn't happen after classifier fix.
        return `export function ${fn}(_config?: unknown) { return { $type: '${node.kind}' as const, $source: 'factory' as const, $named: true as const, render() { return render(this); }, toEdit(s: number | ByteRange, e?: number) { return typeof s === 'number' ? toEdit(this, s, e!) : toEdit(this, s); }, replace(t: T.${node.treeTypeName}) { const r = t.range(); return toEdit(this, r); } }; }`
    }

    const lines: string[] = []
    const configUnion = buildPolymorphConfigUnion(forms)
    const polyOpt = resolvePolymorphConfigOptional(forms)
    lines.push(`export function ${fn}(config${polyOpt}: ${configUnion}) {`)

    lines.push(...emitPolymorphDispatch(node, forms))
    lines.push('}')

    // Emit each form factory inline after the dispatcher.
    // `isPolymorphForm=true` routes the form factory's config parameter
    // through the legacy flat alias instead of namespace sugar — synthetic
    // UForm kinds aren't in `NamespaceMap`.
    const parts = [lines.join('\n')]
    for (const form of forms) {
        parts.push(emitFieldCarryingFactory(form, form.fields, form.children, nodeMap, true))
    }
    return parts.join('\n')
}

/**
 * Build the config parameter union type string for a polymorph dispatcher.
 *
 * @param forms - The polymorph form descriptors.
 * @returns A TS source string like `T.StructItemBodyFormConfig | T.StructItemSemiFormConfig`.
 * @remarks
 *   The inline union (rather than `ConfigOf<T>` over a union T) is what lets
 *   TypeScript narrow via `'field' in config` — distributive `ConfigOf` over union
 *   members doesn't narrow the same way.
 */
function buildPolymorphConfigUnion(forms: AssembledGroup[]): string {
    return forms.map(f => `T.${f.typeName}Config`).join(' | ')
}

/**
 * Determine whether the polymorph dispatcher's `config` parameter should be optional.
 *
 * @param forms - The polymorph form descriptors.
 * @returns `''` when any form has a required field or child, `'?'` otherwise.
 */
function resolvePolymorphConfigOptional(forms: AssembledGroup[]): string {
    const anyFormHasRequired = forms.some(f =>
        f.fields.some(fd => isRequired(fd)) ||
        f.children.some(c => isRequired(c)),
    )
    return anyFormHasRequired ? '' : '?'
}

/**
 * Emit the dispatch body lines for a polymorph factory.
 *
 * @param node - The polymorph node descriptor.
 * @param forms - The polymorph form descriptors.
 * @param fn - The factory function name.
 * @returns Array of source lines for the dispatch body (without the surrounding `{ }`).
 * @remarks
 *   Two dispatch strategies:
 *   - `source='override'`: forms share parent fields, so field-presence doesn't
 *     discriminate. Use the variant child's type from `config.children[0].type`,
 *     OR an explicit `config.variant` discriminator (T032).
 *   - `source='promoted'`: forms have heterogeneous field sets. Discriminate by
 *     field-presence, most-specific first.
 */
function emitPolymorphDispatch(
    node: PolymorphNode,
    forms: AssembledGroup[],
): string[] {
    const lines: string[] = []
    if (forms.length > 1) {
        const isOverride = node.source === 'override'
        if (isOverride) {
            // Variant child kind discrimination first (most reliable —
            // the kind on the variant child is the parse-tree truth).
            for (const form of forms) {
                const childKind = `${node.kind}_${form.name}`
                lines.push(`  if (config && Array.isArray((config as any).children) && (config as any).children[0]?.type === '${childKind}') return ${form.rawFactoryName!}(config as T.${form.typeName}Config);`)
            }
            // Then explicit `variant` discriminator on config.
            for (const form of forms) {
                lines.push(`  if (config && (config as any).variant === '${form.name}') return ${form.rawFactoryName!}(config as T.${form.typeName}Config);`)
            }
            // Fallback to first form when nothing discriminates.
            const fallback = forms[0]!
            lines.push(`  return ${fallback.rawFactoryName!}(config as T.${fallback.typeName}Config);`)
        } else {
            const sorted = [...forms].sort((a, b) => b.fields.length - a.fields.length)
            const fallback = sorted[sorted.length - 1]!
            const seenFieldSets = new Set<string>()
            for (const form of sorted) {
                if (form === fallback) continue
                if (form.fields.length === 0) continue
                const key = [...form.fields.map(f => f.propertyName)].sort().join(',')
                if (seenFieldSets.has(key)) continue
                seenFieldSets.add(key)
                const checks = form.fields
                    .map(f => `'${f.propertyName}' in config`)
                    .join(' && ')
                lines.push(`  if (config && ${checks}) return ${form.rawFactoryName!}(config as T.${form.typeName}Config);`)
            }
            lines.push(`  return ${fallback.rawFactoryName!}(config as T.${fallback.typeName}Config);`)
        }
    } else {
        lines.push(`  return ${forms[0]!.rawFactoryName!}(config);`)
    }
    return lines
}

// ---------------------------------------------------------------------------
// Text factory (leaves, keywords, enums)
// ---------------------------------------------------------------------------

interface TextFactoryNode {
    readonly kind: string
    readonly treeTypeName: string
    readonly rawFactoryName?: string
}

function emitTextFactory(
    node: TextFactoryNode,
    sig: string,
    textExpr: string,
    guard?: string,
): string {
    const fn = node.rawFactoryName!
    const body: string[] = [`export function ${fn}${sig} {`]
    if (guard) body.push(`  ${guard}`)
    body.push(
        '  return {',
        `    $type: '${node.kind}' as const,`,
        `    $source: 'factory' as const,`,
        '    $named: true as const,',
        `    $text: ${textExpr},`,
        `    render: () => ${textExpr},`,
        `    toEdit: (s: number | ByteRange, e?: number) => typeof s === 'number' ? { startPos: s, endPos: e!, insertedText: ${textExpr} } : { startPos: s.start.index, endPos: s.end.index, insertedText: ${textExpr} },`,
        `    replace: (t: T.${node.treeTypeName}) => { const r = t.range(); return { startPos: r.start.index, endPos: r.end.index, insertedText: ${textExpr} }; },`,
        '  };',
        '}',
    )
    return body.join('\n')
}


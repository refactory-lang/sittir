/**
 * Emit a per-form factory for a refined kind.
 *
 * @remarks
 * The per-form factory accepts the form's narrowed Config (base kind's
 * Config minus the fields stamped by this form), stamps the form's
 * selected literals directly into `$fields` alongside user-supplied
 * fields, and returns a NodeData shape structurally identical to the
 * base factory's output (and to what `readNode` produces from a parsed
 * tree). No `$variant` tag — the selected literals live in `$fields`
 * exactly as they do when parsed, so the round-trip contract is
 * preserved.
 *
 * The fluent method suffix (render/toEdit/replace) mirrors the base
 * factory so the output shape is interchangeable; callers switching
 * between `ir.interfaceBody.curly(...)` and `readNode(...)` get the
 * same surface.
 */
function emitRefineFormFactory(
    node: AssembledNode,
    form: RefineFormInfo,
    info: RefineKindInfo,
    nodeMap: NodeMap,
): string | undefined {
    if (node.modelType !== 'branch' && node.modelType !== 'group') return undefined
    const baseFn = node.rawFactoryName
    if (!baseFn) return undefined
    const formFn = refineFormFactoryName(baseFn, form.name)
    const narrowed = new Map<string, string>()
    for (const n of form.narrowedFields) narrowed.set(n.fieldName, n.literal)
    const fields = node.modelType === 'branch' ? node.fields : node.fields
    const children = node.modelType === 'branch' ? (node.children ?? []) : node.children
    const hasFields = fields.length > 0
    const hasChildren = children.length > 0
    const opt = resolveRefineFormConfigOptional(fields, children, nodeMap, narrowed)
    const formTypeName = refineFormTypeName(info.typeName, form.name)
    const lines: string[] = []
    lines.push(`export function ${formFn}(config${opt}: T.${formTypeName}.Config) {`)
    if (hasFields) {
        lines.push('  const fields = {')
        for (const f of fields) {
            const narrowedLit = narrowed.get(f.name)
            if (narrowedLit !== undefined) {
                lines.push(`    ${f.name}: ${JSON.stringify(narrowedLit)} as const,`)
                continue
            }
            const stamp = autoStampExpression(f, nodeMap)
            if (stamp !== undefined) {
                lines.push(`    ${f.name}: ${stamp},`)
                continue
            }
            const kwExpr = keywordPresenceAssignmentExpr(f, opt, nodeMap)
            if (kwExpr !== undefined) {
                lines.push(`    ${f.name}: ${kwExpr},`)
                continue
            }
            lines.push(`    ${f.name}: config${opt}.${f.propertyName},`)
        }
        lines.push('  };')
    }
    if (hasChildren) {
        lines.push(`  const children = config${opt}.children ?? [];`)
    }
    lines.push('  return {')
    lines.push(`    $type: '${node.kind}' as const,`)
    lines.push(`    $source: 'factory' as const,`)
    lines.push('    $named: true as const,')
    if (hasFields) lines.push('    $fields: fields,')
    if (hasChildren) lines.push('    $children: children,')
    lines.push(...factorySuffix(node.treeTypeName))
    lines.push('  };')
    lines.push('}')
    return lines.join('\n')
}

/**
 * Per-form equivalent of `resolveConfigOptional` — factors the narrowed
 * fields out of the "required" check (those are stamped by this form and
 * never come from Config input).
 */
function resolveRefineFormConfigOptional(
    fields: readonly AssembledField[],
    children: readonly AssembledChild[],
    nodeMap: NodeMap,
    narrowed: ReadonlyMap<string, string>,
): string {
    const hasRequired = fields.some(f =>
        isRequired(f)
        && autoStampExpression(f, nodeMap) === undefined
        && !narrowed.has(f.name)
    )
        || children.some(c => isRequired(c) && !isAutoStampSlot(c, nodeMap))
    return hasRequired ? '' : '?'
}

/**
 * Determine whether a children slot is "multiple-shaped" (i.e., any child entry is repeated).
 *
 * @param children - The assembled child descriptors for the node.
 * @returns `true` when at least one child entry is repeated.
 * @remarks
 *   A single `children[0]` check misses the repeated signal when inlining (T063)
 *   flattens a choice of hidden helpers into a mixed list of single + repeated entries.
 */
function resolveChildrenMultiple(children: readonly AssembledChild[]): boolean {
    return children.some(c => isMultiple(c))
}

/**
 * Determine whether the `config` parameter should be optional (`?`).
 *
 * @param fields - The assembled field descriptors for the node.
 * @param children - The assembled child descriptors for the node.
 * @param nodeMap - The assembled node map (used for auto-stamp detection).
 * @returns The option marker — `'?'` when every non-auto-stamped field and
 *   child is optional, `''` otherwise.
 * @remarks
 *   Auto-stamp-eligible fields are excluded from the "required" check because
 *   they are never present in Config — the factory stamps them directly.
 *   Only fields that remain in Config can make config required.
 */
function resolveConfigOptional(
    fields: readonly AssembledField[],
    children: readonly AssembledChild[],
    nodeMap: NodeMap,
): string {
    // Auto-stamp-eligible fields are excluded from the "required" check —
    // they never appear in Config. Auto-stamp-eligible children are also
    // excluded: when all required children are parameterless, they are
    // stamped directly in the factory body rather than read from config.
    const hasRequired = fields.some(f => isRequired(f) && autoStampExpression(f, nodeMap) === undefined)
        || children.some(c => isRequired(c) && !isAutoStampSlot(c, nodeMap))
    return hasRequired ? '' : '?'
}

/**
 * Resolve the config type reference for a field-carrying factory parameter.
 *
 * @param node - The node descriptor (provides `typeName` and `parentKind`).
 * @param isPolymorphForm - Whether the factory is emitting a polymorph form (not the dispatcher).
 * @returns A TS source string like `T.FunctionItem.Config` or `T.FunctionItemBodyFormConfig`.
 * @remarks
 *   Polymorph forms keep the flat `${typeName}Config` alias (synthetic UForm kinds have
 *   no namespace sugar). Base kinds use `T.${typeName}.Config` via the declaration-merged
 *   namespace.
 */
function resolveConfigType(node: FieldCarryingNode, isPolymorphForm: boolean): string {
    return isPolymorphForm
        ? `T.${node.typeName}Config`
        : `T.${node.typeName}.Config`
}

/**
 * Resolve the `$variant` tag name for a polymorph form factory.
 *
 * @param node - The node descriptor (provides `name` and `parentKind`).
 * @returns The form's short name (e.g. `'body'`, `'binary'`, `'form0'`), or
 *   `undefined` when the node is not a polymorph form.
 * @remarks
 *   Polymorph form factories tag their output with `$variant: '<name>'` so the
 *   renderer's variant dispatch (path 1) can discriminate forms whose templates
 *   differ only by literal tokens (e.g. rust `struct_item` body vs semi — same
 *   `$VARS`, differ by trailing `;`).
 *
 *   Single source of truth (DRY): the variant name is `form.name`, assigned at
 *   assembly time in {@link buildAssembledFormGroups}. Reconstructing it from
 *   the kind suffix is fragile — `source='override'` polymorphs use
 *   `${parent}__form_${name}` and slicing by `${parent}_` yields `_form_<name>`
 *   (leading underscore garbage). Use `form.name` directly and let assemble
 *   own the naming decision.
 */
function resolvePolymorphFormVariantName(node: AssembledGroup): string | undefined {
    return node.parentKind ? node.name : undefined
}

/**
 * Emit fluent getter/setter method source lines for all fields of a factory.
 *
 * @param fn - The factory function name (used in setter body to rebuild the node).
 * @param fields - The assembled field descriptors to generate methods for.
 * @param nodeMap - The assembled node map (used for type resolution).
 * @returns Array of source lines for the getter/setter methods (ready to splice into
 *   the `return { ... }` object literal).
 * @remarks
 *   Bodies delegate to `_fs` / `_fsm` helpers (T042j) so each line is one call
 *   instead of a hand-inlined ternary. The explicit parameter / return type
 *   annotations keep consumer IntelliSense seeing the exact per-field shape.
 *
 *   The fluent getter/setter parameter name is always `value` / `values`:
 *   not field-derived — the field name is already the method name, and a derived
 *   param ends up identical to the method (shadowing) or collides with reserved
 *   words. `value`/`values` is unambiguous and keeps signatures uniform.
 */
function emitFluentFieldMethods(
    fn: string,
    fields: readonly AssembledField[],
    nodeMap: NodeMap,
): string[] {
    const lines: string[] = []
    for (const f of fields) {
        const method = f.propertyName === 'type' ? 'typeField' : f.propertyName
        const stamp = autoStampExpression(f, nodeMap)
        if (stamp !== undefined) {
            // Auto-stamp: field value is a fixed constant — emit a getter-only
            // method that returns the constant. No setter parameter.
            lines.push(`    get ${method}() { return fields.${f.name}; },`)
            continue
        }
        const fMultiple = isMultiple(f)
        const param = fMultiple ? 'values' : 'value'
        if (fMultiple) {
            const elemType = fieldElementType(f, nodeMap)
            // Rest params can't be `readonly T[]` (TS2370) so plain mutable arrays
            // are used. NonEmptyArray<T> is a tuple alias `readonly [T, ...(readonly T[])]`
            // which TS does accept as a valid rest type.
            // When elemType is a union (`A | B`), wrap in parens to avoid
            // `A | B[]` being parsed as `A | (B[])`.
            const elemForArray = elemType.includes(' | ') ? `(${elemType})` : elemType
            const restType = isNonEmpty(f) ? `NonEmptyArray<${elemType}>` : `${elemForArray}[]`
            lines.push(`    ${method}(...${param}: ${restType}) { return _fsm(config, ${fn}, '${f.propertyName}', ${param}, fields.${f.name}); },`)
        } else {
            const elemType = fieldElementType(f, nodeMap)
            const paramType = isRequired(f) ? elemType : `${elemType} | undefined`
            lines.push(`    ${method}(${param}?: ${paramType}) { return _fs(config, ${fn}, '${f.propertyName}', ${param}, fields.${f.name}); },`)
        }
    }
    return lines
}

// ---------------------------------------------------------------------------
// Container factory (children only, no fields)
// ---------------------------------------------------------------------------

interface ContainerNode {
    readonly kind: string
    readonly typeName: string
    readonly treeTypeName: string
    readonly rawFactoryName?: string
    readonly children: readonly AssembledChild[]
}

function emitContainerFactory(node: ContainerNode, nodeMap: NodeMap): string {
    const fn = node.rawFactoryName!
    const lines: string[] = []
    const anyMultiple = resolveContainerMultiple(node)
    const anyNonEmpty = node.children.some(c => isNonEmpty(c))
    const elementType = resolveContainerElementType(node, nodeMap)
    if (anyMultiple) {
        lines.push(`export function ${fn}(...children: ${elementType}[]) {`)
        if (anyNonEmpty) {
            lines.push(`  _assertNonEmpty(children, '${node.kind}.children');`)
        }
    } else {
        const firstChild = node.children[0]
        const required = firstChild ? isRequired(firstChild) : false
        const optMark = required ? '' : '?'
        lines.push(`export function ${fn}(child${optMark}: ${elementType}) {`)
        // Required child: type guarantees non-null, wrap directly.
        // Optional child: null/undefined is valid → wrap only if present.
        lines.push(required
            ? `  const children = [child];`
            : `  const children = child != null ? [child] : [];`
        )
    }
    lines.push('  return {')
    lines.push(`    $type: '${node.kind}' as const,`)
    lines.push(`    $source: 'factory' as const,`)
    lines.push('    $named: true as const,')
    lines.push('    $children: children,')
    lines.push(...factorySuffix(node.treeTypeName))
    lines.push('  };')
    lines.push('}')
    return lines.join('\n')
}

/**
 * Determine whether a container node uses a rest-params (multiple-children) signature.
 *
 * @param node - The container node descriptor.
 * @returns `true` when any child entry is repeated.
 * @remarks
 *   Containers are "multiple-shaped" when ANY child entry is repeated. Inlining
 *   (T063) can flatten a choice of hidden helpers into a mixed list of single +
 *   repeated entries, so checking only `children[0]` misses the repeated signal.
 */
function resolveContainerMultiple(node: ContainerNode): boolean {
    return node.children.some(c => isMultiple(c))
}

/**
 * Resolve the element type for a container node's children parameter.
 *
 * @param node - The container node descriptor.
 * @param nodeMap - The assembled node map for type resolution.
 * @returns A TS source string for the element type (e.g. `T.FunctionItem | T.Block`).
 * @remarks
 *   Uses the concrete element type union (e.g. `FunctionItem | Block`) instead of
 *   the generic `ChildOf<X>` alias so consumers see the actual types in
 *   hover/autocomplete with no indirection.
 */
function resolveContainerElementType(node: ContainerNode, nodeMap: NodeMap): string {
    return childElementType(node, nodeMap)
}

// ---------------------------------------------------------------------------
// Polymorph factory — dispatcher + inline form factories
// ---------------------------------------------------------------------------

interface PolymorphNode {
    readonly kind: string
    readonly typeName: string
    readonly treeTypeName: string
    readonly rawFactoryName?: string
    readonly forms: AssembledGroup[]
    readonly source?: 'promoted' | 'override'
    readonly variantChildKinds?: readonly string[]
}

function emitPolymorphFactory(node: PolymorphNode, nodeMap: NodeMap): string {
    const fn = node.rawFactoryName!
    const forms = node.forms

    if (forms.length === 0) {
        // Defensive stub — shouldn't happen after classifier fix.
        return `export function ${fn}(_config?: unknown) { return { $type: '${node.kind}' as const, $source: 'factory' as const, $named: true as const, render() { return render(this); }, toEdit(s: number | ByteRange, e?: number) { return typeof s === 'number' ? toEdit(this, s, e!) : toEdit(this, s); }, replace(t: T.${node.treeTypeName}) { const r = t.range(); return toEdit(this, r); } }; }`
    }

    const parts: string[] = []
    parts.push(emitPolymorphDispatcher(node, forms))

    // Emit each form factory inline after the dispatcher.
    // `isPolymorphForm=true` routes the form factory's config parameter
    // through the legacy flat alias instead of namespace sugar — synthetic
    // UForm kinds aren't in `NamespaceMap`.
    for (const form of forms) {
        const hoist = resolveHoistedForm(form, nodeMap)
        if (hoist) {
            parts.push(emitHoistedPolymorphFormFactory(form, hoist, nodeMap))
        } else {
            parts.push(emitFieldCarryingFactory(form, form.fields, form.children, nodeMap, true))
        }
    }
    return parts.join('\n')
}

/**
 * Build the inline rebuild expression used by a hoisted polymorph form's
 * fluent setter. Projects every hoisted inner field from `inner.$fields`
 * (snake_case raw names) back into a camelCase Config literal, then
 * overlays the patched key at the tail (so it takes precedence).
 *
 * Replaces the previous runtime `_rebuildHoist` helper — the field set is
 * known at emit time, so the whole projection is a static object literal
 * rather than a generic loop in the generated output. Fewer indirections
 * in the factory, and the inner field inventory is visible in the generated
 * source (not reconstructed at runtime from `Object.keys`).
 *
 * @param innerFields - Hoisted inner fields the form exposes at the top level.
 * @param patchKey - The camelCase property name being overridden by the setter.
 * @param patchVar - The setter parameter expression (e.g. `'value'`, `'values'`).
 * @param configType - The form's `*Config` type name, used for the cast.
 * @returns A string like
 *   `{ left: (inner as any).$fields.left, right: (inner as any).$fields.right, operator: value } as T.FooConfig`.
 */
function buildHoistedRebuildExpr(
    innerFields: readonly AssembledField[],
    patchKey: string,
    patchVar: string,
    configType: string,
): string {
    const parts: string[] = []
    for (const f of innerFields) {
        // Skip the field being patched — avoids a duplicate-key in the emitted
        // object literal. The patched key is appended below so the setter's
        // new value is the authoritative one.
        if (f.propertyName === patchKey) continue
        parts.push(`${f.propertyName}: (inner as any).$fields.${f.name}`)
    }
    parts.push(`${patchKey}: ${patchVar}`)
    return `{ ${parts.join(', ')} } as ${configType}`
}

/**
 * Emit a polymorph form factory whose inner child's fields have been
 * hoisted up into the Config surface.
 *
 * @remarks
 * The emitted factory accepts the hoisted Config (flat camelCase fields from
 * the inner child), constructs the inner child via its factory, then returns
 * a NodeData object with `$children: [inner]`. Runtime shape matches the
 * non-hoisted version — this is purely an input-side ergonomic.
 *
 * Fluent getter/setter surface is the inner child's field methods applied
 * at the top level. No `getChild` / `setChild` is emitted — the child slot
 * is hidden from consumers since they never construct it directly.
 *
 * @see {@link resolveHoistedForm}
 */
function emitHoistedPolymorphFormFactory(
    form: AssembledGroup,
    hoist: HoistedForm,
    nodeMap: NodeMap,
): string {
    const fn = form.rawFactoryName!
    const configType = `T.${form.typeName}Config`
    const variantName = form.name
    const parentKind = form.parentKind ?? form.kind

    // Determine optionality — inner child's required, non-auto-stamp fields
    // make the config required.
    const anyRequired = hoist.innerFields.some(f => isRequired(f) && resolveEffectiveLiteral(f, nodeMap) === undefined)
    const opt = anyRequired ? '' : '?'

    const lines: string[] = []
    lines.push(`export function ${fn}(config${opt}: ${configType}) {`)
    // Accept both the hoisted-fields Config AND the legacy
    // `{ children: [Inner] }` shape. The legacy shape reaches this factory
    // when the dispatcher's back-compat branch routes readNode-derived
    // NodeData (`$children: [Inner]`) in via `nodeToConfig` — that path
    // produces `{ children: [...] }` not flat fields. When `children` is
    // present, reuse the first element as the inner child directly; else
    // construct the inner child from the hoisted fields.
    lines.push(`  const _cfg: any = config;`)
    lines.push(`  const inner = _cfg && Array.isArray(_cfg.children) && _cfg.children.length > 0`)
    lines.push(`    ? _cfg.children[0]`)
    lines.push(`    : ${hoist.innerFactoryName}(_cfg);`)
    lines.push(`  const children = [inner] as const;`)
    lines.push('  return {')
    lines.push(`    $type: '${parentKind}' as const,`)
    lines.push(`    $source: 'factory' as const,`)
    lines.push('    $named: true as const,')
    lines.push(`    $variant: '${variantName}' as const,`)
    lines.push('    $children: children,')

    // Fluent getter/setter surface — reuse the inner child's field methods at
    // the top level, rebuilt via this factory so the caller stays on the form
    // factory's result type. Setters project the inner node's current fields
    // into a fresh hoisted Config (camelCase) with the override applied,
    // regardless of whether the original config shape was hoisted or legacy
    // `{ children: [...] }`. That keeps setters stable across both input
    // shapes.
    for (const f of hoist.innerFields) {
        const stamp = autoStampExpression(f, nodeMap)
        const method = f.propertyName === 'type' ? 'typeField' : f.propertyName
        if (stamp !== undefined) {
            // Auto-stamp field — getter-only, reads through the inner node.
            lines.push(`    get ${method}() { return (inner as any).$fields.${f.name}; },`)
            continue
        }
        const fMultiple = isMultiple(f)
        const param = fMultiple ? 'values' : 'value'
        // Inline-rebuild expression: project the inner node's snake_case $fields
        // back into the hoisted camelCase Config, then overlay the patched
        // property. `_rebuildHoist` is no longer emitted — the field set is
        // known at emit time so we build the object literal directly. Overriding
        // the patched key via `[propertyName]: value` keeps it at the tail.
        const rebuild = buildHoistedRebuildExpr(hoist.innerFields, f.propertyName, param, configType)
        if (fMultiple) {
            const elemType = fieldElementType(f, nodeMap)
            const elemForArray = elemType.includes(' | ') ? `(${elemType})` : elemType
            const restType = isNonEmpty(f) ? `NonEmptyArray<${elemType}>` : `${elemForArray}[]`
            lines.push(`    ${method}(...${param}: ${restType}) { return ${fn}(${rebuild}); },`)
        } else {
            const elemType = fieldElementType(f, nodeMap)
            const paramType = isRequired(f) ? elemType : `${elemType} | undefined`
            lines.push(`    ${method}(${param}?: ${paramType}) {`)
            lines.push(`      if (${param} === undefined) return (inner as any).$fields.${f.name};`)
            lines.push(`      return ${fn}(${rebuild});`)
            lines.push(`    },`)
        }
    }

    lines.push(...factorySuffix(form.treeTypeName))
    lines.push('  };')
    lines.push('}')
    return lines.join('\n')
}

/**
 * Emit the polymorph dispatcher function: overloaded signatures (one per
 * variant, return type narrowed via `ReturnType<typeof formFactory>`) followed
 * by an implementation signature accepting the discriminated union.
 *
 * The body switches on `config.$variant` when present and falls back to
 * source-specific discrimination (variant-child kind probing for `'override'`
 * polymorphs, field-presence for `'promoted'` polymorphs) when it is absent —
 * preserving backward compatibility with pre-`$variant` consumers.
 */
function emitPolymorphDispatcher(node: PolymorphNode, forms: AssembledGroup[]): string {
    const fn = node.rawFactoryName!
    const lines: string[] = []

    if (forms.length > 1) {
        // One overload per variant so call-site `config.$variant === 'binary'`
        // narrows the return type to the binary form factory's output.
        for (const form of forms) {
            const formConfig = `T.${form.typeName}Config`
            lines.push(
                `export function ${fn}(config: { readonly $variant: '${form.name}' } & ${formConfig}): ReturnType<typeof ${form.rawFactoryName!}>;`,
            )
        }
        // Back-compat overload: accept the legacy config-union (no `$variant`)
        // so pre-variant callers and internal `from.ts` dispatchers still
        // type-check. Return type is the union of all per-form return types.
        const polyOpt = resolvePolymorphConfigOptional(forms)
        const configUnion = buildPolymorphConfigUnion(forms)
        const returnUnion = forms.map(f => `ReturnType<typeof ${f.rawFactoryName!}>`).join(' | ')
        lines.push(`export function ${fn}(config${polyOpt}: ${configUnion}): ${returnUnion};`)
    }

    const polyOpt = resolvePolymorphConfigOptional(forms)
    const configUnion = buildPolymorphConfigUnion(forms)
    // Implementation signature — the untyped union accepted at the dispatch
    // boundary. The overloads above govern what callers see.
    lines.push(`export function ${fn}(config${polyOpt}: ${configUnion}) {`)
    lines.push(...emitPolymorphDispatch(node, forms))
    lines.push('}')
    return lines.join('\n')
}

/**
 * Build the config parameter union type string for a polymorph dispatcher.
 *
 * @param forms - The polymorph form descriptors.
 * @returns A TS source string like `T.StructItemBodyFormConfig | T.StructItemSemiFormConfig`.
 * @remarks
 *   The inline union (rather than `ConfigOf<T>` over a union T) is what lets
 *   TypeScript narrow via `'field' in config` — distributive `ConfigOf` over union
 *   members doesn't narrow the same way.
 */
function buildPolymorphConfigUnion(forms: AssembledGroup[]): string {
    return forms.map(f => `T.${f.typeName}Config`).join(' | ')
}

/**
 * Determine whether the polymorph dispatcher's `config` parameter should be optional.
 *
 * @param forms - The polymorph form descriptors.
 * @returns `''` when any form has a required field or child, `'?'` otherwise.
 */
function resolvePolymorphConfigOptional(forms: AssembledGroup[]): string {
    const anyFormHasRequired = forms.some(f =>
        f.fields.some(fd => isRequired(fd)) ||
        f.children.some(c => isRequired(c)),
    )
    return anyFormHasRequired ? '' : '?'
}

/**
 * Emit the dispatch body lines for a polymorph factory.
 *
 * @param node - The polymorph node descriptor.
 * @param forms - The polymorph form descriptors.
 * @returns Array of source lines for the dispatch body (without the surrounding `{ }`).
 * @remarks
 *   Dispatch priority:
 *   1. `config.$variant` discriminator — single source of truth. Emitted as a
 *      `switch` on `config.$variant` (exhaustive over the form names).
 *   2. Fallback — only runs when `$variant` is absent:
 *      - `source='override'`: probe `config.children[0].type` (parse-tree truth).
 *      - `source='promoted'`: discriminate by field-presence, most-specific first.
 *      - Otherwise: first form.
 *
 *   Single-form polymorphs (`forms.length === 1`) skip the switch entirely.
 */
function emitPolymorphDispatch(
    node: PolymorphNode,
    forms: AssembledGroup[],
): string[] {
    const lines: string[] = []
    if (forms.length === 1) {
        lines.push(`  return ${forms[0]!.rawFactoryName!}(config);`)
        return lines
    }

    // Path 1: explicit `$variant` discriminator — covers the declared overloads.
    lines.push(`  switch ((config as { $variant?: string }).$variant) {`)
    for (const form of forms) {
        lines.push(`    case '${form.name}': return ${form.rawFactoryName!}(config as T.${form.typeName}Config);`)
    }
    lines.push(`  }`)

    // Path 2: back-compat fallback for callers that omit `$variant` (e.g.,
    // legacy code, `.from()` re-entry, readNode → factory recursion).
    lines.push(...emitPolymorphFallbackDispatch(node, forms))
    return lines
}

/**
 * Emit the pre-`$variant` back-compat dispatch lines (child-kind probing /
 * field-presence) that fire only when `config.$variant` is absent.
 */
function emitPolymorphFallbackDispatch(
    node: PolymorphNode,
    forms: AssembledGroup[],
): string[] {
    const lines: string[] = []
    const isOverride = node.source === 'override'
    if (isOverride) {
        // Variant child kind discrimination — the kind on the variant child is
        // the parse-tree truth.
        for (const form of forms) {
            const childKind = `${node.kind}_${form.name}`
            lines.push(`  if (config && Array.isArray((config as any).children) && (config as any).children[0]?.type === '${childKind}') return ${form.rawFactoryName!}(config as T.${form.typeName}Config);`)
        }
        const fallback = forms[0]!
        lines.push(`  return ${fallback.rawFactoryName!}(config as T.${fallback.typeName}Config);`)
        return lines
    }

    // `source='promoted'`: heterogeneous field sets. Discriminate by
    // field-presence, most-specific first.
    const sorted = [...forms].sort((a, b) => b.fields.length - a.fields.length)
    const fallback = sorted[sorted.length - 1]!
    const seenFieldSets = new Set<string>()
    for (const form of sorted) {
        if (form === fallback) continue
        if (form.fields.length === 0) continue
        const key = [...form.fields.map(f => f.propertyName)].sort().join(',')
        if (seenFieldSets.has(key)) continue
        seenFieldSets.add(key)
        const checks = form.fields
            .map(f => `'${f.propertyName}' in config`)
            .join(' && ')
        lines.push(`  if (config && ${checks}) return ${form.rawFactoryName!}(config as T.${form.typeName}Config);`)
    }
    lines.push(`  return ${fallback.rawFactoryName!}(config as T.${fallback.typeName}Config);`)
    return lines
}


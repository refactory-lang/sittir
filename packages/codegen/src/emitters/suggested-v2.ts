/**
 * Emits overrides.suggested.ts — a human-readable summary of every
 * derivation Link produced. Covers both applied mutations (the rule
 * tree was rewritten) and held-back mutations (included would have,
 * but the `include` filter excluded them).
 *
 * The emitted file is NOT a runnable grammar extension. It's a
 * documentation / copy-paste source: each entry shows the derivation
 * source, confidence, and a ready-to-paste `transform()` snippet you
 * can drop into your own `overrides.ts` to make the inference explicit.
 *
 * Emitter input is the NodeMap (for kind → typeName lookups) plus the
 * DerivationLog (the actual findings). Suggestions are grouped by
 * derivation category so a curator can skim one section at a time.
 */

import type { NodeMap, DerivationLog, InferredFieldEntry, PromotedRuleEntry } from '../compiler/rule.ts'

export interface EmitSuggestedFromNodeMapConfig {
    grammar: string
    nodeMap: NodeMap
}

export function emitSuggestedFromNodeMap(config: EmitSuggestedFromNodeMapConfig): string {
    const { grammar, nodeMap } = config
    const log: DerivationLog = nodeMap.derivations
    const lines: string[] = []

    lines.push('// Auto-generated by @sittir/codegen — DO NOT EDIT')
    lines.push('//')
    lines.push(`// Derivation log for grammar '${grammar}'.`)
    lines.push('//')
    lines.push('// This file is a readable summary of every rule classification')
    lines.push('// and field-name inference Link produced. It is NOT runnable —')
    lines.push('// copy individual entries into your own overrides.ts to make')
    lines.push('// the inference explicit in your grammar extension.')
    lines.push('//')
    lines.push('// Each entry is tagged [applied] (Link mutated the rule tree)')
    lines.push('// or [held] (the `include` filter excluded it from the output).')
    lines.push('')

    // ---------------------------------------------------------------
    // Summary counts
    // ---------------------------------------------------------------
    const inferredApplied = log.inferredFields.filter(e => e.applied).length
    const inferredHeld = log.inferredFields.length - inferredApplied
    const promotedApplied = log.promotedRules.filter(e => e.applied).length
    const promotedHeld = log.promotedRules.length - promotedApplied

    lines.push('// ---------------------------------------------------------------')
    lines.push('// Summary')
    lines.push('// ---------------------------------------------------------------')
    lines.push(`// Field inferences: ${log.inferredFields.length}  (${inferredApplied} applied, ${inferredHeld} held)`)
    lines.push(`// Rule promotions:  ${log.promotedRules.length}  (${promotedApplied} applied, ${promotedHeld} held)`)
    lines.push('')

    // ---------------------------------------------------------------
    // Rule-level promotions — grouped by classification
    // ---------------------------------------------------------------
    lines.push('// ---------------------------------------------------------------')
    lines.push('// Rule promotions')
    lines.push('// ---------------------------------------------------------------')
    lines.push('//')
    lines.push('// Link classified these rules based on their shape. The `promoted`')
    lines.push('// source tag means "derived by Link" rather than "written by the')
    lines.push('// grammar author". To make a promotion explicit, declare the rule')
    lines.push('// with its concrete type in your grammar.js (e.g. add it to the')
    lines.push('// `supertypes:` array).')
    lines.push('')
    const byClassification = new Map<PromotedRuleEntry['classification'], PromotedRuleEntry[]>()
    for (const entry of log.promotedRules) {
        const list = byClassification.get(entry.classification) ?? []
        list.push(entry)
        byClassification.set(entry.classification, list)
    }
    for (const [classification, entries] of byClassification) {
        lines.push(`// ${classification} (${entries.length})`)
        for (const e of entries) {
            const tag = e.applied ? '[applied]' : '[held]   '
            lines.push(`//   ${tag}  ${e.kind}`)
        }
        lines.push('')
    }
    if (log.promotedRules.length === 0) {
        lines.push('// (none)')
        lines.push('')
    }

    // ---------------------------------------------------------------
    // Field name inferences — grouped by target symbol
    // ---------------------------------------------------------------
    lines.push('// ---------------------------------------------------------------')
    lines.push('// Field name inferences')
    lines.push('// ---------------------------------------------------------------')
    lines.push('//')
    lines.push('// Each entry: the symbol being referenced, the inferred field name,')
    lines.push('// the agreement ratio across all named parents, and a copy-paste')
    lines.push('// `transform()` snippet for overrides.ts.')
    lines.push('//')
    lines.push('// To adopt a suggestion: copy the rule block into your overrides.ts')
    lines.push('// rules map and pick a position index for the transform patch.')
    lines.push('')

    // Group by targetSymbol so each symbol's inferences show together.
    const byTarget = new Map<string, InferredFieldEntry[]>()
    for (const entry of log.inferredFields) {
        const list = byTarget.get(entry.targetSymbol) ?? []
        list.push(entry)
        byTarget.set(entry.targetSymbol, list)
    }
    // Sort targets by descending sample size × confidence for easy skimming.
    const sortedTargets = [...byTarget.entries()].sort((a, b) => {
        const aScore = a[1][0]!.sampleSize * a[1][0]!.agreement
        const bScore = b[1][0]!.sampleSize * b[1][0]!.agreement
        return bScore - aScore
    })

    for (const [targetSymbol, entries] of sortedTargets) {
        // Dedupe by kind — a parent can carry multiple bare refs to
        // the same symbol, but the suggestion is the same either way.
        // Track applied-or-held per kind (any applied → applied for
        // display; positional count is the sum).
        const byKind = new Map<string, { applied: boolean; refCount: number }>()
        for (const e of entries) {
            const existing = byKind.get(e.kind)
            if (existing) {
                existing.refCount += 1
                existing.applied = existing.applied || e.applied
            } else {
                byKind.set(e.kind, { applied: e.applied, refCount: 1 })
            }
        }
        const first = entries[0]!
        const pct = (first.agreement * 100).toFixed(0)
        lines.push(`// ${targetSymbol} → field('${first.fieldName}')  ` +
            `[${first.confidence}, ${pct}% agreement, ${first.sampleSize} parents]`)
        for (const [kind, info] of byKind) {
            const tag = info.applied ? '[applied]' : '[held]   '
            const refSuffix = info.refCount > 1 ? ` (${info.refCount} refs)` : ''
            lines.push(`//   ${tag}  ${kind}${refSuffix}`)
        }
        lines.push('//')
        lines.push('//   Override snippet (one per affected parent — pick a position):')
        for (const kind of byKind.keys()) {
            lines.push(`//     ${kind}: ($, original) => transform(original, {`)
            lines.push(`//       N: field('${first.fieldName}'),  // TODO: pick position N for $.${targetSymbol}`)
            lines.push('//     }),')
        }
        lines.push('')
    }
    if (log.inferredFields.length === 0) {
        lines.push('// (none)')
        lines.push('')
    }

    return lines.join('\n')
}

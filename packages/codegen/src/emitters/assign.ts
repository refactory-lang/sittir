/**
 * Emits assign.ts — tree node hydration and in-place editing.
 *
 * Separated from factories.ts to keep factories pure (typed fields in → typed node out).
 * This file contains:
 *   - assignByKind table: kind → assign function
 *   - Per-kind assign functions: recursively hydrate from TreeNode
 *   - edit(): universal entry point for in-place editing
 */

import type { HydratedNodeModel, HydratedFieldModel } from '../node-model.ts';
import { isTupleChildren, eachChildSlot } from '../node-model.ts';
import { toTypeName, toFactoryName, toRawFactoryName, toFieldName, toGrammarTypeName } from '../naming.ts';
import { structuralNodes, fieldsOf, leafKindsOf, keywordKindsOf, childSlotNames } from './utils.ts';
import { buildProjectionContext, projectKinds, type ProjectionContext } from './kind-projections.ts';

export interface EmitAssignConfig {
	grammar: string;
	nodes: HydratedNodeModel[];
}

export function emitAssign(config: EmitAssignConfig): string {
	const { grammar } = config;
	const nodes = structuralNodes(config.nodes);
	const leafKinds = leafKindsOf(config.nodes);
	const keywordKinds = keywordKindsOf(config.nodes);
	const leafSet = new Set(leafKinds);
	const grammarTypeName = toGrammarTypeName(grammar);
	const grammarPrefix = grammarTypeName.slice(0, -5);
	const grammarAlias = `${grammarPrefix}Grammar`;
	const ctx = buildProjectionContext(new Map(config.nodes.map(n => [n.kind, n])));

	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('// Tree node hydration and in-place editing');
	lines.push('');

	// Imports
	lines.push(`import type { ${grammarAlias} } from './grammar.js';`);
	lines.push("import type { NodeKind, Edit, Simplify } from '@sittir/types';");

	// Import TreeNode for edit() generic + named *Tree interfaces for assign functions
	const treeImports = new Set<string>();
	treeImports.add('NodeData'); // for edit() return type
	treeImports.add('TreeNode'); // for edit() generic
	for (const node of nodes) treeImports.add(toTypeName(node.kind) + 'Tree');
	lines.push(`import type { ${[...treeImports].sort().join(', ')} } from './types.js';`);
	lines.push("import { createRenderer, bindRange } from '@sittir/core';");
	lines.push("import { join, dirname } from 'node:path';");
	lines.push("import { fileURLToPath } from 'node:url';");
	lines.push('');
	lines.push('const __dirname = dirname(fileURLToPath(import.meta.url));');
	lines.push("const { render } = createRenderer(join(__dirname, '..', 'templates.yaml'));");

	// Import all factory functions (raw kind names, _prefix for reserved words)
	const factoryImports: string[] = [];
	for (const node of nodes) factoryImports.push(toRawFactoryName(node.kind));
	for (const kind of leafKinds) factoryImports.push(toRawFactoryName(kind));
	lines.push(`import { ${factoryImports.join(', ')} } from './factories.js';`);

	// Import config types for the cast in assign functions
	const typeImports = new Set<string>();
	for (const node of nodes) {
		typeImports.add(toTypeName(node.kind) + 'Config');
	}
	lines.push(`import type { ${[...typeImports].sort().join(', ')} } from './types.js';`);
	lines.push('');

	// ---------------------------------------------------------------------------
	// assignByKind table
	// ---------------------------------------------------------------------------
	lines.push('/** Kind → assign function. Branches call .assign() recursively, leaves read text(). */');
	lines.push('const _assignTable: Record<string, (target: any) => unknown> = {');

	for (const node of nodes) {
		lines.push(`  '${node.kind}': (t) => assign${toTypeName(node.kind)}(t),`);
	}
	for (const kind of leafKinds) {
		const rawFn = toRawFactoryName(kind);
		const fixedText = keywordKinds.get(kind);
		if (fixedText !== undefined) {
			lines.push(`  '${kind}': () => ${rawFn}(),`);
		} else {
			lines.push(`  '${kind}': (t) => ${rawFn}(t.text()),`);
		}
	}

	lines.push('};');
	lines.push('');
	lines.push('/** Lookup assign function for a kind. Throws if kind is unknown. */');
	lines.push('export function assignByKind(kind: string, target: any): unknown {');
	lines.push('  const fn = _assignTable[kind];');
	lines.push(`  if (!fn) throw new Error(\`No assign function for kind '\${kind}' in ${grammarAlias} grammar\`);`);
	lines.push('  return fn(target);');
	lines.push('}');
	lines.push('');

	// ---------------------------------------------------------------------------
	// Per-kind assign functions
	// ---------------------------------------------------------------------------
	for (const node of nodes) {
		const typeName = toTypeName(node.kind);
		const rawFn = toRawFactoryName(node.kind);
		const configTypeName = typeName + 'Config';
		const fields = fieldsOf(node);

		lines.push(`export function assign${typeName}(target: ${typeName}Tree) {`);
		lines.push(`  const config: Record<string, unknown> = {};`);

		// Separate tree-sitter fields from override fields
		const tsFields = fields.filter(f => !f.override);
		const overrideFields = fields.filter(f => f.override);

		for (const f of tsFields) {
			const camel = f.propertyName ?? toFieldName(f.name);
			const proj = projectKinds(f.kinds, ctx);
			const named = expandForRuntime(proj.expandedAll, ctx);
			if (named.length === 0) {
				// Anonymous-only field (operator tokens etc.) — read text value directly
				if (f.required) {
					lines.push(`  config['${camel}'] = target.field('${f.name}')!.text();`);
				} else {
					lines.push(`  config['${camel}'] = target.field('${f.name}')?.text();`);
				}
				continue;
			}
			emitAssignField(lines, f, camel, named, leafSet, node.kind);
		}

		// Override fields: promote from children array (wrap heuristics)
		if (overrideFields.length > 0) {
			emitOverrideFieldPromotion(lines, overrideFields, ctx, leafSet, node.kind);
		}

		// Hydrate children field if present
		if (node.children != null) {
			const slotNames = childSlotNames(node.children, ctx);
			eachChildSlot(node.children, (slot, i) => {
				const slotProj = projectKinds(slot.kinds, ctx);
				emitAssignChildren(lines, slot, expandForRuntime(slotProj.expandedAll, ctx), node.kind, slotNames[i]!);
			});
		}

		lines.push(`  return bindRange(target, ${rawFn}(config as ${configTypeName}));`);
		lines.push(`}`);
		lines.push('');
	}

	// ---------------------------------------------------------------------------
	// edit() — universal entry point
	// ---------------------------------------------------------------------------
	lines.push('/**');
	lines.push(' * Create an in-place editor for a parsed tree node.');
	lines.push(' * Recursively hydrates via assignByKind, attaches range for .toEdit().');
	lines.push(' */');
	lines.push(`export function edit<K extends NodeKind<${grammarAlias}>>(target: TreeNode<K>): Simplify<NodeData<K> & { toEdit(): Edit; replace(): Edit; render(): string }> {`);
	lines.push(`  const fn = _assignTable[target.type];`);
	lines.push(`  if (!fn) throw new Error(\`No assign function for kind '\${target.type}' in ${grammarAlias} grammar\`);`);
	lines.push('  return fn(target) as Simplify<NodeData<K> & { toEdit(): Edit; replace(): Edit; render(): string }>;');
	lines.push('}');
	lines.push('');

	return lines.join('\n');
}

/** Emit a single field assignment line inside an assign function. */
function emitAssignField(lines: string[], f: { name: string; required: boolean; multiple: boolean }, configKey: string, fieldNamedTypes: string[], leafSet: Set<string>, nodeKind: string): void {
	const fieldAccess = `target.field('${f.name}')`;
	const kindSet = JSON.stringify(fieldNamedTypes);

	if (f.required) {
		if (f.multiple) {
			// Multiple required: collect all children matching this field's accepted kinds
			lines.push(`  config['${configKey}'] = (() => {`);
			lines.push(`    const _kinds = new Set(${kindSet});`);
			lines.push(`    const _items = target.children().filter((c) => _kinds.has(c.type));`);
			lines.push(`    if (_items.length === 0) throw new Error(\`Required field '${f.name}' has no children on '${nodeKind}' tree node\`);`);
			lines.push(`    return _items.map((c) => assignByKind(c.type, c));`);
			lines.push(`  })();`);
		} else if (fieldNamedTypes.length === 1) {
			const childKind = fieldNamedTypes[0]!;
			if (leafSet.has(childKind)) {
				lines.push(`  config['${configKey}'] = assignByKind('${childKind}', ${fieldAccess}!);`);
			} else {
				lines.push(`  config['${configKey}'] = assign${toTypeName(childKind)}(${fieldAccess}! as ${toTypeName(childKind)}Tree);`);
			}
		} else {
			lines.push(`  config['${configKey}'] = assignByKind(${fieldAccess}!.type, ${fieldAccess}!);`);
		}
	} else {
		if (f.multiple) {
			// Multiple optional: collect matching children, undefined if none
			lines.push(`  config['${configKey}'] = (() => {`);
			lines.push(`    const _kinds = new Set(${kindSet});`);
			lines.push(`    const _items = target.children().filter((c) => _kinds.has(c.type));`);
			lines.push(`    return _items.length > 0 ? _items.map((c) => assignByKind(c.type, c)) : undefined;`);
			lines.push(`  })();`);
		} else if (fieldNamedTypes.length === 1) {
			const childKind = fieldNamedTypes[0]!;
			if (leafSet.has(childKind)) {
				lines.push(`  config['${configKey}'] = ${fieldAccess} ? assignByKind('${childKind}', ${fieldAccess}!) : undefined;`);
			} else {
				lines.push(`  config['${configKey}'] = ${fieldAccess} ? assign${toTypeName(childKind)}(${fieldAccess}! as ${toTypeName(childKind)}Tree) : undefined;`);
			}
		} else {
			lines.push(`  config['${configKey}'] = ${fieldAccess} ? assignByKind(${fieldAccess}!.type, ${fieldAccess}!) : undefined;`);
		}
	}
}

/** Emit children hydration inside an assign function. */
function emitAssignChildren(
	lines: string[],
	ch: { required: boolean; multiple: boolean },
	childNamed: string[],
	nodeKind: string,
	propName: string,
): void {
	const kindSet = JSON.stringify(childNamed);
	if (ch.multiple) {
		lines.push(`  config['${propName}'] = (() => {`);
		lines.push(`    const _kinds = new Set(${kindSet});`);
		lines.push(`    const _items = target.children().filter((c) => _kinds.has(c.type));`);
		if (ch.required) {
			lines.push(`    if (_items.length === 0) throw new Error(\`Required children missing on '${nodeKind}' tree node\`);`);
		}
		lines.push(`    return _items.map((c) => assignByKind(c.type, c));`);
		lines.push(`  })();`);
	} else {
		lines.push(`  config['${propName}'] = (() => {`);
		lines.push(`    const _kinds = new Set(${kindSet});`);
		lines.push(`    const _child = target.children().find((c) => _kinds.has(c.type));`);
		if (ch.required) {
			lines.push(`    if (!_child) throw new Error(\`Required child missing on '${nodeKind}' tree node\`);`);
		} else {
			lines.push(`    if (!_child) return undefined;`);
		}
		lines.push(`    return assignByKind(_child.type, _child);`);
		lines.push(`  })();`);
	}
}

/**
 * Emit override field promotion — wrap heuristics for children→fields promotion.
 *
 * Heuristic 2: Unique kind → find child by kind, promote to fields
 * Heuristic 3: Anonymous token → find unnamed child by text, promote to fields
 * Heuristic 4: Token-positional → split same-kind children at token boundary
 * Heuristic 5: CHOICE branch → token position determines field assignment
 *
 * For simplicity in the initial implementation, we use a general approach:
 * - Anonymous override fields: find non-named child by matching anonymous tokens
 * - Named override fields: find named child by kind (unique) or position
 */
function emitOverrideFieldPromotion(
	lines: string[],
	overrideFields: readonly HydratedFieldModel[],
	ctx: ProjectionContext,
	leafSet: Set<string>,
	nodeKind: string,
): void {
	// Collect all children once for override field promotion
	lines.push(`  // Override field promotion (wrap heuristics)`);
	lines.push(`  const _allChildren = target.children();`);

	// Track which children indices have been consumed
	lines.push(`  const _consumed = new Set<number>();`);

	// First pass: anonymous override fields (heuristic 3 — token as value)
	const anonFields = overrideFields.filter(f => f.overrideAnonymous);
	for (const f of anonFields) {
		const camel = f.propertyName ?? toFieldName(f.name);
		lines.push(`  config['${camel}'] = (() => {`);
		lines.push(`    for (let i = 0; i < _allChildren.length; i++) {`);
		lines.push(`      if (_consumed.has(i)) continue;`);
		lines.push(`      const c = _allChildren[i]!;`);
		lines.push(`      if (!(c as any).isNamed()) { _consumed.add(i); return c.text(); }`);
		lines.push(`    }`);
		lines.push(`    return undefined;`);
		lines.push(`  })();`);
	}

	// Second pass: named override fields (heuristic 2 — unique kind, or heuristic 4 — positional)
	const namedFields = overrideFields.filter(f => !f.overrideAnonymous);
	for (const f of namedFields) {
		const camel = f.propertyName ?? toFieldName(f.name);
		const proj = projectKinds(f.kinds, ctx);
		const named = expandForRuntime(proj.expandedAll, ctx);

		if (named.length === 0) {
			// No kinds known — skip (shouldn't happen for named override fields)
			lines.push(`  config['${camel}'] = undefined;`);
			continue;
		}

		const kindSet = JSON.stringify(named);
		lines.push(`  config['${camel}'] = (() => {`);
		lines.push(`    const _kinds = new Set(${kindSet});`);
		lines.push(`    for (let i = 0; i < _allChildren.length; i++) {`);
		lines.push(`      if (_consumed.has(i)) continue;`);
		lines.push(`      const c = _allChildren[i]!;`);
		lines.push(`      if (_kinds.has(c.type)) { _consumed.add(i); return assignByKind(c.type, c); }`);
		lines.push(`    }`);
		lines.push(`    return undefined;`);
		lines.push(`  })();`);
	}
}

/** Expand supertype names to concrete subtypes for runtime dispatch. */
function expandForRuntime(kinds: string[], ctx: ProjectionContext): string[] {
	const result = new Set<string>();
	for (const k of kinds) {
		const subs = ctx.expandedSupertypes.get(k);
		if (subs) {
			for (const s of subs) result.add(s);
		} else {
			result.add(k);
		}
	}
	return [...result].sort();
}

/**
 * Emits assign.ts — tree node hydration and in-place editing.
 *
 * Separated from factories.ts to keep factories pure (typed fields in → typed node out).
 * This file contains:
 *   - assignByKind table: kind → assign function
 *   - Per-kind assign functions: recursively hydrate from TreeNode
 *   - edit(): universal entry point for in-place editing
 */

import type { KindMeta, FieldMeta } from '../grammar-reader.ts';
import { toTypeName, toFactoryName, toGrammarTypeName } from '../naming.ts';

export interface EmitAssignConfig {
	grammar: string;
	nodes: KindMeta[];
	leafKinds: string[];
	keywordKinds: Map<string, string>;
}

export function emitAssign(config: EmitAssignConfig): string {
	const { grammar, nodes, leafKinds, keywordKinds } = config;
	const leafSet = new Set(leafKinds);
	const grammarTypeName = toGrammarTypeName(grammar);
	const grammarPrefix = grammarTypeName.slice(0, -5);
	const grammarAlias = `${grammarPrefix}Grammar`;

	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('// Tree node hydration and in-place editing');
	lines.push('');

	// Imports
	lines.push(`import type { ${grammarAlias} } from './grammar.js';`);
	lines.push("import type { NodeKind } from '@sittir/types';");
	lines.push("import type { Edit } from '@sittir/types';");

	// Import TreeNode for edit() generic + named *Tree interfaces for assign functions
	const treeImports = new Set<string>();
	treeImports.add('NodeData'); // for edit() return type
	treeImports.add('TreeNode'); // for edit() generic
	for (const node of nodes) treeImports.add(toTypeName(node.kind) + 'Tree');
	lines.push(`import type { ${[...treeImports].sort().join(', ')} } from './types.js';`);
	lines.push("import { render } from '@sittir/core';");
	lines.push("import { rules } from './rules.js';");
	lines.push("import { joinBy } from './joinby.js';");

	// Import all factory functions
	const factoryImports: string[] = [];
	for (const node of nodes) factoryImports.push(toFactoryName(node.kind));
	for (const kind of leafKinds) factoryImports.push(toFactoryName(kind));
	lines.push(`import { ${factoryImports.join(', ')} } from './factories.js';`);

	// Import type names for return types
	const typeImports = new Set<string>();
	for (const node of nodes) {
		typeImports.add(toTypeName(node.kind));
	}
	for (const kind of leafKinds) {
		typeImports.add(toTypeName(kind));
	}
	lines.push(`import type { ${[...typeImports].sort().join(', ')} } from './types.js';`);
	lines.push('');

	// ---------------------------------------------------------------------------
	// assignByKind table
	// ---------------------------------------------------------------------------
	lines.push('/** Kind → assign function. Branches call .assign() recursively, leaves read text(). */');
	lines.push('const _assignTable: Record<string, (target: any) => any> = {');

	for (const node of nodes) {
		lines.push(`  '${node.kind}': (t) => assign${toTypeName(node.kind)}(t),`);
	}
	for (const kind of leafKinds) {
		const factoryName = toFactoryName(kind);
		const fixedText = keywordKinds.get(kind);
		if (fixedText !== undefined) {
			lines.push(`  '${kind}': () => ${factoryName}(),`);
		} else {
			lines.push(`  '${kind}': (t) => ${factoryName}(t.text()),`);
		}
	}

	lines.push('};');
	lines.push('');
	lines.push('/** Lookup assign function for a kind. Throws if kind is unknown. */');
	lines.push('export function assignByKind(kind: string, target: any): any {');
	lines.push('  const fn = _assignTable[kind];');
	lines.push("  if (!fn) throw new Error(`No assign function for kind '${kind}'`);");
	lines.push('  return fn(target);');
	lines.push('}');
	lines.push('');

	// ---------------------------------------------------------------------------
	// Per-kind assign functions
	// ---------------------------------------------------------------------------
	for (const node of nodes) {
		const typeName = toTypeName(node.kind);
		const factoryName = toFactoryName(node.kind);

		lines.push(`export function assign${typeName}(target: ${typeName}Tree): ${typeName} & { toEdit(): Edit; replace(): Edit; render(): string } {`);
		lines.push(`  const result = ${factoryName}({`);

		for (const f of node.fields) {
			if (f.namedTypes.length === 0) continue;
			emitAssignField(lines, f, leafSet);
		}

		lines.push(`  } as any);`);

		// Attach toEdit with target's range
		// Override toEdit: no-arg uses target's range, with args forwards to original
		lines.push(`  const _origToEdit = result.toEdit.bind(result);`);
		lines.push(`  const boundToEdit = () => {`);
		lines.push(`    const r = target.range();`);
		lines.push(`    return { startPos: r.start.index, endPos: r.end.index, insertedText: result.render() } as Edit;`);
		lines.push(`  };`);
		lines.push(`  (result as any).toEdit = boundToEdit;`);
		lines.push(`  (result as any).replace = boundToEdit;`);
		lines.push(`  return result as any;`);
		lines.push(`}`);
		lines.push('');
	}

	// ---------------------------------------------------------------------------
	// Simplify — flattens intersection for clean tooltips (shallow, no recursion)
	// ---------------------------------------------------------------------------
	lines.push('/** Flatten an intersection into a single object type (shallow). */');
	lines.push('type Simplify<T> = { [K in keyof T]: T[K] } & {};');
	lines.push('');

	// ---------------------------------------------------------------------------
	// edit() — universal entry point
	// ---------------------------------------------------------------------------
	lines.push('/**');
	lines.push(' * Create an in-place editor for a parsed tree node.');
	lines.push(' * Recursively hydrates via assignByKind, attaches range for .toEdit().');
	lines.push(' */');
	lines.push(`export function edit<K extends NodeKind<${grammarAlias}>>(target: TreeNode<K>): Simplify<NodeData<K> & { toEdit(): Edit; replace(): Edit; render(): string }> {`);
	lines.push('  return assignByKind(target.type, target) as any;');
	lines.push('}');
	lines.push('');

	return lines.join('\n');
}

/** Emit a single field assignment line inside an assign function. */
function emitAssignField(lines: string[], f: FieldMeta, leafSet: Set<string>): void {
	const fieldAccess = `target.field('${f.name}')`;

	if (f.required) {
		if (f.multiple) {
			lines.push(`    ${f.name}: ${fieldAccess} ? [assignByKind(${fieldAccess}!.type, ${fieldAccess}!)] : [],`);
		} else if (f.namedTypes.length === 1) {
			const childKind = f.namedTypes[0]!;
			if (leafSet.has(childKind)) {
				lines.push(`    ${f.name}: assignByKind('${childKind}', ${fieldAccess}!),`);
			} else {
				lines.push(`    ${f.name}: assign${toTypeName(childKind)}(${fieldAccess}!),`);
			}
		} else {
			lines.push(`    ${f.name}: assignByKind(${fieldAccess}!.type, ${fieldAccess}!),`);
		}
	} else {
		if (f.multiple) {
			lines.push(`    ${f.name}: ${fieldAccess} ? [assignByKind(${fieldAccess}!.type, ${fieldAccess}!)] : undefined,`);
		} else if (f.namedTypes.length === 1) {
			const childKind = f.namedTypes[0]!;
			if (leafSet.has(childKind)) {
				lines.push(`    ${f.name}: ${fieldAccess} ? assignByKind('${childKind}', ${fieldAccess}!) : undefined,`);
			} else {
				lines.push(`    ${f.name}: ${fieldAccess} ? assign${toTypeName(childKind)}(${fieldAccess}!) : undefined,`);
			}
		} else {
			lines.push(`    ${f.name}: ${fieldAccess} ? assignByKind(${fieldAccess}!.type, ${fieldAccess}!) : undefined,`);
		}
	}
}

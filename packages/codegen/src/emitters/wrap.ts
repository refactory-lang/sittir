/**
 * Emits wrap.ts — lazy tree node hydration via readNode + promote/drillIn.
 *
 * The generated wrap functions do NOT recurse at wrap time. Instead they:
 * 1. Promote override-named children from `children[]` into `fields{}`
 * 2. Return an object with lazy getter methods that call readNode → wrapNode on demand
 *
 * This means if you never drill into `parameters`, its subtree is never read.
 */

import type { HydratedNodeModel, HydratedFieldModel } from '../node-model.ts';
import { isTupleChildren, eachChildSlot } from '../node-model.ts';
import { toTypeName, toFactoryName, toRawFactoryName, toFieldName, toGrammarTypeName } from '../naming.ts';
import { structuralNodes, fieldsOf, leafKindsOf, keywordKindsOf, childSlotNames } from './utils.ts';
import { buildProjectionContext, projectKinds, type ProjectionContext } from './kind-projections.ts';

export interface EmitWrapConfig {
	grammar: string;
	nodes: HydratedNodeModel[];
}

export function emitWrap(config: EmitWrapConfig): string {
	const { grammar } = config;
	const nodes = structuralNodes(config.nodes);
	const leafKinds = leafKindsOf(config.nodes);
	const keywordKinds = keywordKindsOf(config.nodes);
	const leafSet = new Set(leafKinds);
	const grammarTypeName = toGrammarTypeName(grammar);
	const grammarPrefix = grammarTypeName.slice(0, -5);
	const grammarAlias = `${grammarPrefix}Grammar`;
	const ctx = buildProjectionContext(new Map(config.nodes.map(n => [n.kind, n])));

	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('// Lazy tree node hydration via readNode + promote/drillIn');
	lines.push('');

	// Imports
	lines.push("import type { AnyNodeData } from '@sittir/types';");
	lines.push("import { readNode, type TreeHandle } from '@sittir/core';");
	lines.push('');

	// ---------------------------------------------------------------------------
	// Runtime helpers: promote, promoteAnon, drillIn, wrapNode
	// ---------------------------------------------------------------------------
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('// Runtime helpers');
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('');
	lines.push('/** Promote a named child from children[] into fields{} by kind. */');
	lines.push('function promote(data: AnyNodeData, kind: string): void {');
	lines.push('  if (!data.children) return;');
	lines.push('  const arr = data.children as AnyNodeData[];');
	lines.push('  const idx = arr.findIndex(c => c.named && c.type === kind);');
	lines.push('  if (idx >= 0) {');
	lines.push('    data.fields = data.fields ?? {};');
	lines.push('    (data.fields as Record<string, unknown>)[kind] = arr.splice(idx, 1)[0];');
	lines.push('  }');
	lines.push('}');
	lines.push('');
	lines.push('/** Promote an anonymous token from children[] into fields{} by text match. */');
	lines.push('function promoteAnon(data: AnyNodeData, name: string, values: string[]): void {');
	lines.push('  if (!data.children) return;');
	lines.push('  const arr = data.children as AnyNodeData[];');
	lines.push('  const idx = arr.findIndex(c => !c.named && c.text !== undefined && values.includes(c.text));');
	lines.push('  if (idx >= 0) {');
	lines.push('    data.fields = data.fields ?? {};');
	lines.push('    (data.fields as Record<string, unknown>)[name] = arr.splice(idx, 1)[0];');
	lines.push('  }');
	lines.push('}');
	lines.push('');
	lines.push('/** Promote the first remaining named child into a field (positional). */');
	lines.push('function promoteFirst(data: AnyNodeData, fieldName: string): void {');
	lines.push('  if (!data.children) return;');
	lines.push('  const arr = data.children as AnyNodeData[];');
	lines.push('  const idx = arr.findIndex(c => c.named);');
	lines.push('  if (idx >= 0) {');
	lines.push('    data.fields = data.fields ?? {};');
	lines.push('    (data.fields as Record<string, unknown>)[fieldName] = arr.splice(idx, 1)[0];');
	lines.push('  }');
	lines.push('}');
	lines.push('');
	lines.push('/** Promote all remaining named children into a field array (positional). */');
	lines.push('function promoteAll(data: AnyNodeData, fieldName: string): void {');
	lines.push('  if (!data.children) return;');
	lines.push('  const arr = data.children as AnyNodeData[];');
	lines.push('  const matching: AnyNodeData[] = [];');
	lines.push('  for (let i = arr.length - 1; i >= 0; i--) {');
	lines.push('    if (arr[i]!.named) matching.unshift(arr.splice(i, 1)[0]!);');
	lines.push('  }');
	lines.push('  if (matching.length > 0) {');
	lines.push('    data.fields = data.fields ?? {};');
	lines.push('    (data.fields as Record<string, unknown>)[fieldName] = matching;');
	lines.push('  }');
	lines.push('}');
	lines.push('');
	lines.push('/** Promote a named child by kind into a specific field name (different from kind). */');
	lines.push('function promoteNamed(data: AnyNodeData, fieldName: string, kinds: string[]): void {');
	lines.push('  if (!data.children) return;');
	lines.push('  const arr = data.children as AnyNodeData[];');
	lines.push('  const idx = arr.findIndex(c => c.named && kinds.includes(c.type));');
	lines.push('  if (idx >= 0) {');
	lines.push('    data.fields = data.fields ?? {};');
	lines.push('    (data.fields as Record<string, unknown>)[fieldName] = arr.splice(idx, 1)[0];');
	lines.push('  }');
	lines.push('}');
	lines.push('');
	lines.push('/** Promote the first anonymous child into a field (positional, for hidden grammar tokens). */');
	lines.push('function promoteFirstAnon(data: AnyNodeData, fieldName: string): void {');
	lines.push('  if (!data.children) return;');
	lines.push('  const arr = data.children as AnyNodeData[];');
	lines.push('  const idx = arr.findIndex(c => !c.named);');
	lines.push('  if (idx >= 0) {');
	lines.push('    data.fields = data.fields ?? {};');
	lines.push('    (data.fields as Record<string, unknown>)[fieldName] = arr.splice(idx, 1)[0];');
	lines.push('  }');
	lines.push('}');
	lines.push('');
	lines.push('/** Drill into a child entry: read its subtree lazily via readNode + wrapNode. */');
	lines.push('function drillIn(entry: unknown, tree: TreeHandle): unknown {');
	lines.push('  if (!entry) return undefined;');
	lines.push('  const e = entry as AnyNodeData;');
	lines.push('  if (e.nodeId != null) {');
	lines.push('    return wrapNode(readNode(tree, e.nodeId), tree);');
	lines.push('  }');
	lines.push('  return entry;');
	lines.push('}');
	lines.push('');
	lines.push('/** Drill into all matching entries in a field (for multiple/list fields). */');
	lines.push('function drillInAll(entries: unknown, tree: TreeHandle): unknown[] {');
	lines.push('  if (!entries) return [];');
	lines.push('  const arr = Array.isArray(entries) ? entries : [entries];');
	lines.push('  return arr.map(e => drillIn(e, tree));');
	lines.push('}');
	lines.push('');

	// ---------------------------------------------------------------------------
	// _wrapTable: kind → wrap function dispatch
	// ---------------------------------------------------------------------------
	lines.push('/** Kind → wrap function. */');
	lines.push('const _wrapTable: Record<string, (data: AnyNodeData, tree: TreeHandle) => unknown> = {');

	for (const node of nodes) {
		lines.push(`  '${node.kind}': (d, t) => wrap${toTypeName(node.kind)}(d, t),`);
	}
	// Leaves just pass through
	for (const kind of leafKinds) {
		lines.push(`  '${kind}': (d) => d,`);
	}

	lines.push('};');
	lines.push('');
	lines.push('/** Wrap a node by dispatching to its kind-specific wrapper. */');
	lines.push('export function wrapNode(data: AnyNodeData, tree: TreeHandle): unknown {');
	lines.push('  const fn = _wrapTable[data.type];');
	lines.push('  if (!fn) return data; // Unknown kind — return as-is');
	lines.push('  return fn(data, tree);');
	lines.push('}');
	lines.push('');

	// ---------------------------------------------------------------------------
	// Per-kind wrap functions: promote overrides, return lazy getters
	// ---------------------------------------------------------------------------
	for (const node of nodes) {
		const typeName = toTypeName(node.kind);
		const fields = fieldsOf(node);

		const tsFields = fields.filter(f => !f.override);
		const overrideFields = fields.filter(f => f.override)
			.sort((a, b) => (a.position ?? 0) - (b.position ?? 0));

		lines.push(`export function wrap${typeName}(data: AnyNodeData, tree: TreeHandle): unknown {`);

		// Promote override fields from children into fields
		for (const f of overrideFields) {
			if (f.overrideAnonymous) {
				const values = f.overrideValues;
				if (values && values.length > 0) {
					lines.push(`  promoteAnon(data, '${f.name}', ${JSON.stringify(values)});`);
				}
			} else {
				const proj = projectKinds(f.kinds, ctx);
				const named = expandForRuntime(proj.expandedAll, ctx);
				if (named.length === 0) {
					// No kinds — hidden grammar token, promote by position
					lines.push(`  promoteFirstAnon(data, '${f.name}');`);
				} else if (named.length === 1 && named[0] === f.name) {
					// Kind matches field name — use simple promote
					lines.push(`  promote(data, '${f.name}');`);
				} else if (named.length > 5) {
					// Large supertype expansion — use positional promote
					// (safe because overrides are sorted by position)
					if (f.multiple) {
						lines.push(`  promoteAll(data, '${f.name}');`);
					} else {
						lines.push(`  promoteFirst(data, '${f.name}');`);
					}
				} else if (named.length > 0) {
					lines.push(`  promoteNamed(data, '${f.name}', ${JSON.stringify(named)});`);
				}
			}
		}

		// Build the returned object with lazy getters
		lines.push('  return {');
		lines.push('    ...data,');

		// Getters for tree-sitter fields
		for (const f of tsFields) {
			const camel = f.propertyName ?? toFieldName(f.name);
			if (f.multiple) {
				lines.push(`    get ${camel}() { return drillInAll(data.fields?.['${f.name}'], tree); },`);
			} else {
				const proj = projectKinds(f.kinds, ctx);
				const named = expandForRuntime(proj.expandedAll, ctx);
				if (named.length === 0) {
					// Anonymous-only field — return text directly
					lines.push(`    get ${camel}() { return (data.fields?.['${f.name}'] as AnyNodeData | undefined)?.text; },`);
				} else {
					lines.push(`    get ${camel}() { return drillIn(data.fields?.['${f.name}'], tree); },`);
				}
			}
		}

		// Getters for override fields
		for (const f of overrideFields) {
			const camel = f.propertyName ?? toFieldName(f.name);
			if (f.overrideAnonymous) {
				lines.push(`    get ${camel}() { return (data.fields?.['${f.name}'] as AnyNodeData | undefined)?.text; },`);
			} else if (f.multiple) {
				lines.push(`    get ${camel}() { return drillInAll(data.fields?.['${f.name}'], tree); },`);
			} else {
				lines.push(`    get ${camel}() { return drillIn(data.fields?.['${f.name}'], tree); },`);
			}
		}

		// Children getter (skip slots already handled as override fields)
		if (node.children != null) {
			const fieldKeys = new Set(fields.map(f => f.propertyName ?? toFieldName(f.name)));
			const slotNames = childSlotNames(node.children, ctx);
			eachChildSlot(node.children, (slot, i) => {
				const name = slotNames[i]!;
				if (fieldKeys.has(name)) return;
				if (name === 'children') {
					// Default children slot
					if (slot.multiple) {
						lines.push(`    get children() { return (data.children ?? []).map(c => drillIn(c, tree)); },`);
					} else {
						lines.push(`    get child() { return drillIn(data.children?.[0], tree); },`);
					}
				} else if (slot.multiple) {
					lines.push(`    get ${name}() { return drillInAll(data.fields?.['${name}'], tree); },`);
				} else {
					lines.push(`    get ${name}() { return drillIn(data.fields?.['${name}'], tree); },`);
				}
			});
		}

		lines.push('  };');
		lines.push('}');
		lines.push('');
	}

	// ---------------------------------------------------------------------------
	// readTreeNode — typed public entry point
	// ---------------------------------------------------------------------------
	lines.push('/**');
	lines.push(' * Read a parsed tree node into a lazily-wrapped NodeData.');
	lines.push(' * One level deep — getters drill into subtrees on demand.');
	lines.push(' */');
	lines.push('export function readTreeNode(tree: TreeHandle, nodeId?: number): unknown {');
	lines.push('  const data = readNode(tree, nodeId);');
	lines.push('  return wrapNode(data, tree);');
	lines.push('}');
	lines.push('');

	return lines.join('\n');
}

/** Expand supertype names to concrete subtypes for runtime dispatch. */
function expandForRuntime(kinds: string[], ctx: ProjectionContext): string[] {
	const result = new Set<string>();
	for (const k of kinds) {
		const subs = ctx.expandedSupertypes.get(k);
		if (subs) {
			for (const s of subs) result.add(s);
		} else {
			result.add(k);
		}
	}
	return [...result].sort();
}

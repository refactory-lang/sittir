/**
 * Emits from.ts — the .from() API with inlined resolution per factory.
 *
 * Resolution logic is generated per-field based on known accepted types,
 * so there is no generic resolver or FromContext round-trip. Each .from()
 * directly calls sibling factories, and validation happens automatically
 * through the leaf factory functions.
 *
 * Separated from factories.ts for tree-shaking: users who only use the
 * regular API never load .from() resolution code.
 */

import type { KindMeta, FieldMeta } from '../grammar-reader.ts';
import { toTypeName, toFactoryName, toFieldName } from '../naming.ts';

export interface EmitFromConfig {
	grammar: string;
	nodes: KindMeta[];
	leafKinds: string[];
	keywordKinds: Map<string, string>;
	/** leaf kind → list of valid string values (enum-like) */
	leafValues?: Map<string, string[]>;
}

/**
 * Emit the from.ts file containing .from() functions for all branch node kinds.
 */
export function emitFrom(config: EmitFromConfig): string {
	const { nodes, leafKinds, keywordKinds, leafValues } = config;
	const leafSet = new Set(leafKinds);
	const leafValueMap = leafValues ?? new Map<string, string[]>();

	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('// .from() API — ergonomic resolution with inlined per-field logic');
	lines.push('');

	// Import types from core (only types, no runtime resolution)
	lines.push("import type { NodeData, AssignableNode } from '@sittir/core';");
	lines.push('');

	// Import factory functions — only the ones actually referenced by .from() resolution
	const referencedFactories = collectReferencedFactories(nodes, leafSet, leafValueMap, keywordKinds);
	if (referencedFactories.size > 0) {
		const imports = [...referencedFactories].sort().join(', ');
		lines.push(`import { ${imports} } from './factories.js';`);
	}

	// Import types from factories.ts (Config, Node, FromInput, FromNode types)
	const typeImports: string[] = [];
	for (const node of nodes) {
		const typeName = toTypeName(node.kind);
		typeImports.push(`${typeName}FromInput`);
		typeImports.push(`${typeName}FromNode`);
	}
	if (typeImports.length > 0) {
		lines.push(`import type { ${typeImports.join(', ')} } from './factories.js';`);
	}

	// No rules/joinby/render imports needed — .from() creates nodes via
	// the regular factories, which already close over rules + render methods.
	lines.push('');

	// Grammar-specific FromValue type (for array compression)
	const branchKinds = nodes.map(n => n.kind);
	const allKinds = [...branchKinds, ...leafKinds];
	lines.push(`type FromKind = ${allKinds.map(k => `'${k}'`).join(' | ')};`);
	lines.push('interface FromObject { kind?: FromKind; [key: string]: FromValue | undefined; }');
	lines.push('type FromValue = string | number | boolean | NodeData | FromValue[] | FromObject;');
	lines.push('');

	// isNodeData helper
	lines.push('function isNodeData(v: any): v is NodeData {');
	lines.push("  return v !== null && typeof v === 'object' && 'type' in v && 'fields' in v;");
	lines.push('}');
	lines.push('');

	// _resolveByKind — dispatch table for explicit { kind: '...' } objects
	lines.push('function _resolveByKind(kind: string, rest: any): any {');
	lines.push('  switch (kind) {');
	for (const node of nodes) {
		const fromFn = `${toFactoryName(node.kind)}From`;
		lines.push(`    case '${node.kind}': return ${fromFn}(rest);`);
	}
	lines.push(`    default: throw new Error(\`Unknown kind for .from(): '\${kind}'\`);`);
	lines.push('  }');
	lines.push('}');
	lines.push('');

	// _inferBranch — score object by field name matching against candidate branch types
	lines.push('const _BRANCH_FIELDS: Record<string, string[]> = {');
	for (const node of nodes) {
		const fieldNames = node.fields.map(f => f.name);
		if (fieldNames.length > 0) {
			lines.push(`  '${node.kind}': ${JSON.stringify(fieldNames)},`);
		}
	}
	lines.push('};');
	lines.push('');
	lines.push('function _inferBranch(obj: any, candidates: string[]): any {');
	lines.push("  const keys = Object.keys(obj).filter(k => k !== 'children');");
	lines.push('  let best: string | null = null;');
	lines.push('  let bestScore = -1;');
	lines.push('  for (const c of candidates) {');
	lines.push('    const fields = _BRANCH_FIELDS[c];');
	lines.push('    if (!fields) continue;');
	lines.push('    const fieldSet = new Set(fields);');
	lines.push('    let matches = 0, misses = 0;');
	lines.push('    for (const k of keys) { if (fieldSet.has(k)) matches++; else misses++; }');
	lines.push('    const score = matches * 2 - misses;');
	lines.push('    if (score > bestScore) { bestScore = score; best = c; }');
	lines.push('  }');
	lines.push('  if (best) return _resolveByKind(best, obj);');
	lines.push("  throw new Error(`Cannot infer kind for object with keys: ${keys.join(', ')}`);");
	lines.push('}');
	lines.push('');

	// Per-kind .from() functions
	for (const node of nodes) {
		lines.push(emitFromFunction(node, leafSet, leafValueMap, keywordKinds, nodes));
		lines.push('');
	}

	return lines.join('\n');
}

/**
 * Generate the ergonomic setter replacement lines for a node kind.
 * Shared between the plain-object resolution path and the SgNode dispatch path.
 */
function emitErgonomicSetters(
	node: KindMeta,
	ctorField: FieldMeta | null,
	baseVar: string,
	leafSet: Set<string>,
	leafValueMap: Map<string, string[]>,
	keywordKinds: Map<string, string>,
	allNodes: KindMeta[],
): string[] {
	const lines: string[] = [];

	for (const field of node.fields) {
		if (field === ctorField) continue;
		const camel = toFieldName(field.name);
		const setterName = camel === 'type' ? 'typeField' : camel;
		if (field.multiple) {
			lines.push(`  ${baseVar}.${setterName} = (...v: any[]) => {`);
			lines.push(`    const arr = v.length === 1 && Array.isArray(v[0]) ? v[0] : v;`);
			lines.push(`    (${baseVar}.fields as any)['${field.name}'] = arr.map((e: any) => ${emitResolveExpr('e', field, leafSet, leafValueMap, keywordKinds, allNodes)});`);
			lines.push(`    return ${baseVar};`);
			lines.push(`  };`);
		} else {
			lines.push(`  ${baseVar}.${setterName} = (v: any) => { (${baseVar}.fields as any)['${field.name}'] = ${emitResolveExpr('v', field, leafSet, leafValueMap, keywordKinds, allNodes)}; return ${baseVar}; };`);
		}
	}

	if (node.hasChildren && node.children) {
		const childField: FieldMeta = {
			name: 'children',
			required: node.children.required,
			multiple: true,
			types: node.children.types,
			namedTypes: node.children.namedTypes,
		};
		lines.push(`  ${baseVar}.children = (...v: any[]) => { (${baseVar}.fields as any).children = v.map((e: any) => ${emitResolveExpr('e', childField, leafSet, leafValueMap, keywordKinds, allNodes)}); return ${baseVar}; };`);
	}

	return lines;
}

/**
 * Emit a single .from() function for a branch node kind.
 *
 * Accepts three input shapes:
 *   1. FromInput object — resolved recursively into NodeData
 *   2. FromValue[] (array compression) — wrapped as children
 *   3. AssignableNode (SgNode) — delegates to .assign(), with ergonomic setters
 */
function emitFromFunction(
	node: KindMeta,
	leafSet: Set<string>,
	leafValueMap: Map<string, string[]>,
	keywordKinds: Map<string, string>,
	allNodes: KindMeta[],
): string {
	const typeName = toTypeName(node.kind);
	const factoryName = toFactoryName(node.kind);
	const ctorField = selectConstructorField(node);

	const lines: string[] = [];

	// Function overloads for strict typing
	const exportName = `${factoryName}From`;
	// Overload 1: AssignableNode → FromNode (SgNode dispatch)
	lines.push(`export function ${exportName}(input: AssignableNode<'${node.kind}'>): ${typeName}FromNode;`);
	// Overload 2: FromInput → FromNode (plain object resolution)
	if (node.hasChildren) {
		lines.push(`export function ${exportName}(input: ${typeName}FromInput | FromValue[]): ${typeName}FromNode;`);
	} else {
		lines.push(`export function ${exportName}(input: ${typeName}FromInput): ${typeName}FromNode;`);
	}
	// Implementation signature
	lines.push(`export function ${exportName}(input: any): ${typeName}FromNode {`);

	// SgNode detection — delegate to .assign() with ergonomic setters
	// .assign() nodes use an overrides mechanism (getters), so we wrap
	// the strict setters: resolve the value, then call the original setter.
	lines.push(`  if (typeof input.field === 'function') {`);
	lines.push(`    const base: any = ${factoryName}.assign(input);`);
	for (const field of node.fields) {
		const camel = toFieldName(field.name);
		const setterName = camel === 'type' ? 'typeField' : camel;
		lines.push(`    const _orig_${field.name} = base.${setterName};`);
		if (field.multiple) {
			lines.push(`    base.${setterName} = (...v: any[]) => {`);
			lines.push(`      const arr = v.length === 1 && Array.isArray(v[0]) ? v[0] : v;`);
			lines.push(`      return _orig_${field.name}(...arr.map((e: any) => ${emitResolveExpr('e', field, leafSet, leafValueMap, keywordKinds, allNodes)}));`);
			lines.push(`    };`);
		} else {
			lines.push(`    base.${setterName} = (v: any) => _orig_${field.name}(${emitResolveExpr('v', field, leafSet, leafValueMap, keywordKinds, allNodes)});`);
		}
	}
	if (node.hasChildren && node.children) {
		const childField: FieldMeta = {
			name: 'children',
			required: node.children.required,
			multiple: true,
			types: node.children.types,
			namedTypes: node.children.namedTypes,
		};
		lines.push(`    const _orig_children = base.children;`);
		lines.push(`    base.children = (...v: any[]) => _orig_children(...v.map((e: any) => ${emitResolveExpr('e', childField, leafSet, leafValueMap, keywordKinds, allNodes)}));`);
	}
	lines.push(`    return base;`);
	lines.push(`  }`);

	// Array compression
	if (node.hasChildren) {
		lines.push(`  const obj: any = Array.isArray(input) ? { children: input } : input;`);
	} else {
		lines.push(`  const obj: any = input;`);
	}

	// Resolve each field inline
	lines.push(`  const resolved: any = {};`);
	for (const field of node.fields) {
		lines.push(`  if (obj['${field.name}'] !== undefined) {`);
		if (field.multiple) {
			lines.push(`    const raw = obj['${field.name}'];`);
			lines.push(`    const arr = Array.isArray(raw) ? raw : [raw];`);
			lines.push(`    resolved['${field.name}'] = arr.map((v: any) => ${emitResolveExpr('v', field, leafSet, leafValueMap, keywordKinds, allNodes)});`);
		} else {
			lines.push(`    resolved['${field.name}'] = ${emitResolveExpr(`obj['${field.name}']`, field, leafSet, leafValueMap, keywordKinds, allNodes)};`);
		}
		lines.push(`  }`);
	}

	// Handle children
	if (node.hasChildren && node.children) {
		lines.push(`  if (obj['children'] !== undefined) {`);
		lines.push(`    const arr = Array.isArray(obj['children']) ? obj['children'] : [obj['children']];`);
		const childField: FieldMeta = {
			name: 'children',
			required: node.children.required,
			multiple: true,
			types: node.children.types,
			namedTypes: node.children.namedTypes,
		};
		lines.push(`    resolved['children'] = arr.map((v: any) => ${emitResolveExpr('v', childField, leafSet, leafValueMap, keywordKinds, allNodes)});`);
		lines.push(`  }`);
	}

	// Create node via regular factory
	lines.push(`  const base: any = ${factoryName}(resolved);`);

	// Replace fluent setters with ergonomic ones
	lines.push(...emitErgonomicSetters(node, ctorField, 'base', leafSet, leafValueMap, keywordKinds, allNodes));

	lines.push(`  return base;`);
	lines.push(`}`);

	return lines.join('\n');
}

/**
 * Emit an inline expression that resolves a value for a specific field.
 * The expression takes a variable name and returns resolved NodeData.
 *
 * This is the core of inlined resolution — no generic dispatcher needed.
 */
function emitResolveExpr(
	varName: string,
	field: FieldMeta,
	leafSet: Set<string>,
	leafValueMap: Map<string, string[]>,
	keywordKinds: Map<string, string>,
	allNodes: KindMeta[],
): string {
	const leafTypes = field.namedTypes.filter(t => leafSet.has(t));
	const branchTypes = field.namedTypes.filter(t => !leafSet.has(t));
	const anonTokens = field.types.filter(t => !field.namedTypes.includes(t) && !t.startsWith('_'));

	// Simple case: single leaf type, no branches, no anon tokens
	if (leafTypes.length === 1 && branchTypes.length === 0 && anonTokens.length === 0) {
		const factory = toFactoryName(leafTypes[0]!);
		return `(isNodeData(${varName}) ? ${varName} : ${factory}(String(${varName}) as any))`;
	}

	// Simple case: single branch type, no leaves
	if (branchTypes.length === 1 && leafTypes.length === 0 && anonTokens.length === 0) {
		const factory = toFactoryName(branchTypes[0]!);
		const fromFn = `${factory}From`;
		return `(isNodeData(${varName}) ? ${varName} : Array.isArray(${varName}) ? ${fromFn}(${varName} as any) : typeof ${varName} === 'object' ? ${fromFn}(${varName} as any) : ${varName})`;
	}

	// Complex case: use inline IIFE for multi-type resolution
	return `((v => {${emitResolveBody('v', leafTypes, branchTypes, anonTokens, leafSet, leafValueMap, keywordKinds, allNodes)}})(${varName}))`;
}

/**
 * Emit the body of a resolve function (inside an IIFE or named function).
 * Handles all value types: NodeData passthrough, boolean, number, string, array, object.
 */
function emitResolveBody(
	v: string,
	leafTypes: string[],
	branchTypes: string[],
	anonTokens: string[],
	leafSet: Set<string>,
	leafValueMap: Map<string, string[]>,
	keywordKinds: Map<string, string>,
	allNodes: KindMeta[],
): string {
	const parts: string[] = [];

	// NodeData passthrough
	parts.push(`if(isNodeData(${v}))return ${v};`);

	// Boolean → boolean_literal (if accepted)
	if (leafTypes.includes('boolean_literal') || leafTypes.length === 0) {
		parts.push(`if(typeof ${v}==='boolean')return booleanLiteral(String(${v}) as any);`);
	}

	// Number → integer_literal or float_literal
	const hasInt = leafTypes.includes('integer_literal') || leafTypes.length === 0;
	const hasFloat = leafTypes.includes('float_literal');
	if (hasInt && hasFloat) {
		parts.push(`if(typeof ${v}==='number')return Number.isInteger(${v})?integerLiteral(String(${v}) as any):floatLiteral(String(${v}) as any);`);
	} else if (hasInt) {
		parts.push(`if(typeof ${v}==='number')return integerLiteral(String(${v}) as any);`);
	} else if (hasFloat) {
		parts.push(`if(typeof ${v}==='number')return floatLiteral(String(${v}) as any);`);
	}

	// String resolution
	parts.push(`if(typeof ${v}==='string'){${emitStringResolve(v, leafTypes, anonTokens, leafSet, leafValueMap)}}`);

	// Array → wrap in single branch type as children
	if (branchTypes.length > 0) {
		if (branchTypes.length === 1) {
			const fromFn = `${toFactoryName(branchTypes[0]!)}From`;
			parts.push(`if(Array.isArray(${v}))return ${fromFn}(${v} as any);`);
		} else {
			// Multiple branch types — can't auto-resolve arrays without kind
			parts.push(`if(Array.isArray(${v}))throw new Error('Array value with ambiguous branch types — use {kind} to disambiguate');`);
		}
	}

	// Object resolution
	parts.push(`if(typeof ${v}==='object'&&${v}!==null){${emitObjectResolve(v, branchTypes, allNodes)}}`);

	// Fallback error
	parts.push(`throw new Error('Cannot resolve .from() value');`);

	return parts.join('');
}

/**
 * Emit string resolution logic for a specific field.
 */
function emitStringResolve(
	v: string,
	leafTypes: string[],
	anonTokens: string[],
	_leafSet: Set<string>,
	leafValueMap: Map<string, string[]>,
): string {
	const parts: string[] = [];

	// Exactly one leaf type → use directly
	if (leafTypes.length === 1) {
		parts.push(`return ${toFactoryName(leafTypes[0]!)}(${v} as any);`);
		return parts.join('');
	}

	// Multiple leaf types → check enum values first
	if (leafTypes.length > 1) {
		for (const lt of leafTypes) {
			const vals = leafValueMap.get(lt);
			if (vals && vals.length > 0) {
				const valSet = vals.map(val => `'${escapeString(val)}'`).join(',');
				parts.push(`if([${valSet}].includes(${v}))return ${toFactoryName(lt)}(${v} as any);`);
			}
		}
	}

	// Anonymous tokens (operators)
	if (anonTokens.length > 0) {
		const tokenSet = anonTokens.map(t => `'${escapeString(t)}'`).join(',');
		parts.push(`if([${tokenSet}].includes(${v}))return{type:${v},fields:{},text:${v}} as any;`);
	}

	// Prefer identifier-like types
	const hasTypeIdent = leafTypes.includes('type_identifier');
	const hasIdent = leafTypes.includes('identifier');
	if (hasTypeIdent && hasIdent) {
		parts.push(`return typeIdentifier(${v});`);
		return parts.join('');
	}

	const identPriority = ['identifier', 'type_identifier', 'field_identifier', 'shorthand_field_identifier'];
	for (const ik of identPriority) {
		if (leafTypes.includes(ik)) {
			parts.push(`return ${toFactoryName(ik)}(${v} as any);`);
			return parts.join('');
		}
	}

	// Fallback to first leaf type
	if (leafTypes.length > 0) {
		parts.push(`return ${toFactoryName(leafTypes[0]!)}(${v} as any);`);
		return parts.join('');
	}

	// No leaf types — plain text node
	parts.push(`return{type:'string',fields:{},text:${v}} as any;`);
	return parts.join('');
}

/**
 * Emit object resolution logic for a specific field.
 */
function emitObjectResolve(
	v: string,
	branchTypes: string[],
	allNodes: KindMeta[],
): string {
	const parts: string[] = [];

	// Explicit kind → dispatch to the right .from()
	parts.push(`if('kind' in ${v}&&typeof ${v}.kind==='string'){`);
	// Build dispatch table for known branch types
	parts.push(`const{kind:k,...rest}=${v};`);
	if (branchTypes.length > 0) {
		parts.push(`switch(k){`);
		for (const bt of branchTypes) {
			parts.push(`case '${bt}':return ${toFactoryName(bt)}From(rest as any);`);
		}
		// Also allow any valid kind for flexibility
		parts.push(`}`);
	}
	// General dispatch for any kind (handles kinds not in branchTypes)
	parts.push(`return _resolveByKind(k,rest);`);
	parts.push(`}`);

	// Single branch type → use directly
	if (branchTypes.length === 1) {
		parts.push(`return ${toFactoryName(branchTypes[0]!)}From(${v} as any);`);
	} else if (branchTypes.length > 1) {
		// Multiple → score by field matching
		parts.push(`return _inferBranch(${v},${JSON.stringify(branchTypes)});`);
	} else {
		parts.push(`throw new Error('No branch types accepted for object value');`);
	}

	return parts.join('');
}

/**
 * Collect all factory function names referenced by .from() resolution.
 */
function collectReferencedFactories(
	nodes: KindMeta[],
	leafSet: Set<string>,
	leafValueMap: Map<string, string[]>,
	keywordKinds: Map<string, string>,
): Set<string> {
	const refs = new Set<string>();

	// All branch factories (for regular factory calls and .from() dispatch)
	for (const node of nodes) {
		refs.add(toFactoryName(node.kind));
	}

	// Leaf factories referenced by field resolution
	for (const node of nodes) {
		for (const field of [...node.fields, ...(node.children ? [node.children as FieldMeta] : [])]) {
			for (const t of field.namedTypes) {
				if (leafSet.has(t)) {
					refs.add(toFactoryName(t));
				}
			}
		}
		// Boolean/number always potentially needed
		if (leafSet.has('boolean_literal')) refs.add(toFactoryName('boolean_literal'));
		if (leafSet.has('integer_literal')) refs.add(toFactoryName('integer_literal'));
		if (leafSet.has('float_literal')) refs.add(toFactoryName('float_literal'));
	}

	return refs;
}

function selectConstructorField(node: KindMeta): FieldMeta | null {
	const required = node.fields.filter(f => f.required && !f.multiple);
	if (required.length === 1) return required[0]!;
	const name = required.find(f => f.name === 'name');
	if (name) return name;
	return required[0] ?? null;
}

function escapeString(s: string): string {
	return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

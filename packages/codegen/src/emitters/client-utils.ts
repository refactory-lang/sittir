export interface EmitClientUtilsConfig {
	nodeMap: NodeMap;
	generatedIdTables?: GeneratedIdTables;
	/** Trivia kind names (e.g. `['line_comment', 'block_comment']`), extracted
	 *  from the grammar's `highlights.scm`. When non-empty the emitted
	 *  `$trivia()` signature uses a typed union instead of `unknown[]`. */
	triviaKinds?: string[];
}

export function emitClientUtils(config: EmitClientUtilsConfig): string {
	const { nodeMap, generatedIdTables } = config;

	// Resolve trivia kind names to their TypeScript type names via nodeMap.
	const triviaTypeNames = resolveTriviaTypeNames(config.triviaKinds, nodeMap);

	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('// Shared client-side resolution utilities for .from() and factories');
	lines.push('');
	lines.push("import type { AnyNodeData, AnyTreeNodeOf, ByteRange, Edit } from '@sittir/types';");
	if (triviaTypeNames.length > 0) {
		lines.push(`import type { ${triviaTypeNames.join(', ')}, AnyTransport, NamespaceMap } from './types.js';`);
	} else {
		lines.push("import type { AnyTransport, NamespaceMap } from './types.js';");
	}
	lines.push("import { KIND_NAMES, kindIdFromName } from './types.js';");
	lines.push("import { render, toEdit } from './boundary.ts';");
	lines.push('');

	// Emit grammar-agnostic NodeData helpers directly into this per-grammar
	// utils.ts. Generated factories.ts / wrap.ts only ever import from
	// `./utils.js`. DRY at the codegen-source layer (one emitter, one set
	// of bodies); per-grammar duplication of generated text is intentional
	// locality.
	lines.push('/**');
	lines.push(' * Wrap a factory-built node literal with the four `$`-prefixed shared');
	lines.push(' * methods (`$render`, `$toEdit`, `$replace`, `$trivia`).');
	lines.push(' *');
	lines.push(' * Generic on `T` so the literal type flows through unchanged — the');
	lines.push(' * return type is `T & { ... methods ... }` so callers retain narrow');
	lines.push(' * per-kind property types. No spread (rule 6), no `defineProperty`');
	lines.push(' * (rule 1) — methods are merged via `Object.assign` and become');
	lines.push(' * enumerable members of the returned object.');
	lines.push(' */');
	lines.push('export function withMethods<T extends object>(');
	lines.push('  node: T');
	lines.push('): T & {');
	lines.push('  $render(): string;');
	lines.push('  $toEdit(startOrRange: number | ByteRange, endPos?: number): Edit;');
	lines.push('  $replace(target: { range(): ByteRange }): Edit;');
	lines.push(`  $trivia(...args: ${buildTriviaParamType(triviaTypeNames)}[]): AnyNodeData;`);
	lines.push('} {');
	lines.push('  return Object.assign(node, {');
	lines.push('    $render(this: AnyNodeData): string { return render(this); },');
	lines.push('    $toEdit(this: AnyNodeData, startOrRange: number | ByteRange, endPos?: number): Edit {');
	lines.push('      return toEdit(this, startOrRange, endPos);');
	lines.push('    },');
	lines.push('    $replace(this: AnyNodeData, target: { range(): ByteRange }): Edit {');
	lines.push('      return toEdit(this, target.range());');
	lines.push('    },');
	lines.push(`    $trivia(this: AnyNodeData, ...items: ${buildTriviaParamType(triviaTypeNames)}[]): AnyNodeData {`);
	lines.push(
		"      if (items.length === 1 && typeof items[0] === 'object' && items[0] !== null && ('leading' in items[0] || 'trailing' in items[0])) {"
	);
	lines.push('        (this as unknown as Record<string, unknown>).$triviaData = items[0];');
	lines.push('      } else {');
	lines.push('        (this as unknown as Record<string, unknown>).$triviaData = { leading: items as AnyNodeData[] };');
	lines.push('      }');
	lines.push('      return this;');
	lines.push('    },');
	lines.push('  });');
	lines.push('}');
	lines.push('');

	// isNodeData
	lines.push('/**');
	lines.push(' * Type guard: returns true if `v` is a NodeData.');
	lines.push(' *');
	lines.push(' * Accepts any node produced by `readNode`, a factory, or `.from()` — distinguished');
	lines.push(' * from loose config bags by the presence of any of:');
	lines.push(' *   - `_*` storage keys (branch nodes with named fields, de-hoisted),');
	lines.push(' *   - `$text` (leaf nodes, or branch nodes with `SITTIR_DEBUG_TEXT=1`),');
	lines.push(' *   - `$children` (container nodes whose children arrive without field names),');
	lines.push(' *   - `$source` (provenance tag stamped by `readNode` and every factory).');
	lines.push(' */');
	lines.push('export function isNodeData<K extends keyof NamespaceMap>(');
	lines.push("  v: NamespaceMap[K]['Node'] | NamespaceMap[K]['Loose'] | NamespaceMap[K]['Tree']");
	lines.push("): v is NamespaceMap[K]['Node'];");
	lines.push('export function isNodeData(v: unknown): v is AnyNodeData;');
	lines.push('export function isNodeData(v: unknown): v is AnyNodeData {');
	lines.push("  if (v === null || typeof v !== 'object') return false;");
	lines.push('  const o = v as Record<string, unknown>;');
	lines.push("  if (typeof o['$type'] !== 'number') return false;");
	// Factory/wrap nodes use `_<name>` storage keys (de-hoisted surface).
	// Any top-level `_*` key indicates a branch node with named fields.
	lines.push("  const hasDehoistedFields = Object.keys(o).some((k) => k.startsWith('_'));");
	lines.push('  return hasDehoistedFields');
	lines.push("    || typeof o['$text'] === 'string'");
	lines.push("    || Array.isArray(o['$children'])");
	lines.push("    || o['$source'] === 0 || o['$source'] === 1 || o['$source'] === 2;");
	lines.push('}');
	lines.push('');

	// isTreeNode
	lines.push('/**');
	lines.push(' * Type guard: returns true if `v` is a TreeNode (SgNode-compatible).');
	lines.push(' */');
	lines.push('export function isTreeNode<K extends keyof NamespaceMap>(');
	lines.push("  v: NamespaceMap[K]['Tree'] | NamespaceMap[K]['Node']");
	lines.push("): v is NamespaceMap[K]['Tree'];");
	lines.push('export function isTreeNode(v: unknown): v is AnyTreeNodeOf;');
	lines.push('export function isTreeNode(v: unknown): v is AnyTreeNodeOf {');
	lines.push("  if (v === null || typeof v !== 'object') return false;");
	lines.push('  const o = v as Record<string, unknown>;');
	lines.push(
		"  return typeof o['type'] === 'string' && typeof o['field'] === 'function' && typeof o['text'] === 'function';"
	);
	lines.push('}');
	lines.push('');

	// hasKind
	lines.push('export function hasKind(v: object): v is { kind: string } & Record<string, unknown> {');
	lines.push("  return 'kind' in v && typeof (v as Record<string, unknown>).kind === 'string';");
	lines.push('}');
	lines.push('');

	// isNodeOfKind
	lines.push('export function isNodeOfKind<K extends keyof NamespaceMap>(');
	lines.push('  v: unknown,');
	lines.push('  kind: K,');
	lines.push("): v is NamespaceMap[K]['Node'] {");
	lines.push('  return isNodeData(v) && v.$type === kindIdFromName(kind);');
	lines.push('}');
	lines.push('');

	// hasKindOf
	lines.push('export function hasKindOf<K extends keyof NamespaceMap>(');
	lines.push('  v: object,');
	lines.push('  kind: K,');
	lines.push("): v is { kind: K } & NamespaceMap[K]['Loose'] {");
	lines.push("  return 'kind' in v && (v as Record<string, unknown>).kind === kind;");
	lines.push('}');
	lines.push('');

	// Build the kind→id lookup for numeric $type emission at the wire boundary.
	// Source from the catalog superset (children-only kinds + anon tokens) so
	// projectTransportValue can resolve every parser-symbol-bearing kind without
	// hitting the try/catch fallback that keeps a string $type on the wire.
	const kindEntries = generatedIdTables
		? collectKindEntries(collectCatalogKinds(generatedIdTables), nodeMap, generatedIdTables)
		: undefined;

	emitNativeTransportProjection(lines, nodeMap, kindEntries);

	return lines.join('\n');
}

function emitNativeTransportProjection(
	lines: string[],
	nodeMap: NodeMap,
	kindEntries?: readonly KindEnumEntry[]
): void {
	lines.push('/**');
	lines.push(' * Convert NodeData/factory output into the data-only native transport shape.');
	lines.push(' */');
	lines.push('export function toNativeRenderTransport(node: unknown): AnyTransport {');
	lines.push("  const projected = projectTransportValue(node, 'node');");
	lines.push('  return projected as AnyTransport;');
	lines.push('}');
	lines.push('');
	lines.push(
		"const transportMetadataKeys = new Set(['$type', '$variant', '$source', '$named', '$text', '$span', '$nodeHandle', '$childIndex']);"
	);
	lines.push('');
	emitNativeTransportProjectionRules(lines, nodeMap);
	lines.push('');
	lines.push('function projectTransportValue(value: unknown, path: string): unknown {');
	lines.push('  if (Array.isArray(value)) {');
	lines.push('    return value.map((item, index) => projectTransportValue(item, `${path}[${index}]`));');
	lines.push('  }');
	lines.push("  if (typeof value === 'string') {");
	lines.push('    return { $type: value, $text: value };');
	lines.push('  }');
	lines.push('  if (!isRecord(value)) return value;');
	lines.push('  // $type must be numeric (TSKindId). However, factory-constructed');
	lines.push('  // children can carry a string kind name when kindIdFromName was not invoked');
	lines.push('  // (e.g. generated nodes.test.ts fixtures with `as any`). Attempt resolution');
	lines.push('  // via kindIdFromName so the full projection pipeline runs.');
	lines.push('  let numericType: number | undefined;');
	lines.push('  if (typeof value.$type === "number") {');
	lines.push('    numericType = value.$type;');
	lines.push('  } else if (typeof value.$type === "string") {');
	lines.push('    try {');
	lines.push('      const kindStr = nativeTransportType(value.$type);');
	lines.push('      numericType = kindIdFromName(kindStr);');
	lines.push('    } catch {');
	lines.push('      // Unknown kind name — cannot project, return as-is.');
	lines.push('    }');
	lines.push('  }');
	lines.push('  if (numericType === undefined) return value;');
	lines.push('');
	lines.push('  // Resolve the wire kind name (including alias rewriting) for internal');
	lines.push('  // routing. $type is always numeric after projection.');
	lines.push('  const resolvedKind = nativeTransportType(KIND_NAMES.get(numericType) ?? String(numericType));');
	lines.push('');
	lines.push('  const projected: Record<string, unknown> = {};');
	lines.push('  const isFactory = value.$source === 2;');
	lines.push('  for (const key of transportMetadataKeys) {');
	lines.push("    if (isFactory && (key === '$span' || key === '$nodeHandle' || key === '$childIndex')) continue;");
	lines.push('    if (key in value) projected[key] = value[key];');
	lines.push('  }');
	lines.push('  // $source is numeric (0=ts, 1=sg, 2=factory) — passes through unchanged.');
	lines.push('  // Temporarily store string kind for routing; converted to numeric below.');
	lines.push('  projected.$type = resolvedKind;');
	lines.push('');
	// Factory/wrap nodes store fields under `_<name>` keys (de-hoisted surface).
	// Iterate all enumerable keys; strip the leading `_`
	// when projecting to native transport so the transport receives the plain
	// snake_case field name (e.g. `_name` → `name` field in Rust struct).
	lines.push('  for (const [key, child] of Object.entries(value)) {');
	lines.push('    if (transportMetadataKeys.has(key)) continue;');
	lines.push("    if (key === 'render' || key === 'toEdit' || key === 'replace') continue;");
	lines.push('    if (typeof child === "function") continue;');
	lines.push("    const projKey = key.startsWith('_') ? key.slice(1) : key;");
	lines.push('    if (projKey in projected) continue;');
	lines.push('    projected[projKey] = projectTransportValue(child, `${path}.${projKey}`);');
	lines.push('  }');
	lines.push('');
	lines.push('  projectRawChildrenIntoFields(projected, resolvedKind);');
	lines.push('  inferNativeTransportVariant(projected, resolvedKind);');
	lines.push('  coerceNativeTransportChildren(projected, resolvedKind);');
	lines.push('  coerceKeywordPresenceFields(projected, resolvedKind);');
	lines.push('');
	lines.push('  projected.$type = kindIdFromName(resolvedKind);');
	lines.push('');
	lines.push('  return projected;');
	lines.push('}');
	lines.push('');
	lines.push('function nativeTransportType(kind: string): string {');
	lines.push('  return nativeTransportAliasTargetToSource[kind] ?? kind;');
	lines.push('}');
	lines.push('');
	lines.push('function projectRawChildrenIntoFields(projected: Record<string, unknown>, kind: string): void {');
	lines.push('  const rules = nativeTransportRawChildFieldRules[kind];');
	lines.push('  const children = projected.$children;');
	lines.push('  if (!rules || !Array.isArray(children)) return;');
	lines.push('  const keep: unknown[] = [];');
	lines.push('  const usedForField = new Set<number>();');
	lines.push('  for (const field of rules.fields) {');
	lines.push('    if (projected[field.name] !== undefined) continue;');
	lines.push(
		'    const matches = children.map((child, index) => ({ child, index })).filter(({ child }) => transportValueMatches(child, field.alternatives));'
	);
	lines.push('    if (field.multiple) {');
	lines.push('      if (matches.length > 0 || field.required) {');
	lines.push('        projected[field.name] = matches.map(({ child }) => child);');
	lines.push('        for (const { index } of matches) usedForField.add(index);');
	lines.push('      }');
	lines.push('    } else if (matches.length === 1) {');
	lines.push('      projected[field.name] = matches[0]!.child;');
	lines.push('      usedForField.add(matches[0]!.index);');
	lines.push('    }');
	lines.push('  }');
	lines.push('  for (let index = 0; index < children.length; index++) {');
	lines.push('    const child = children[index];');
	lines.push(
		'    if (!usedForField.has(index) || transportValueMatches(child, rules.childAlternatives)) keep.push(child);'
	);
	lines.push('  }');
	lines.push('  if (keep.length > 0 || rules.childrenRequired) {');
	lines.push('    projected.$children = keep;');
	lines.push('  } else {');
	lines.push('    delete projected.$children;');
	lines.push('  }');
	lines.push('}');
	lines.push('');
	lines.push('function inferNativeTransportVariant(projected: Record<string, unknown>, kind: string): void {');
	lines.push('  if (typeof projected.$variant === "string") return;');
	lines.push('  const rules = nativeTransportVariantRules[kind];');
	lines.push('  if (!rules) return;');
	lines.push('  const matches = rules.filter((rule) => transportVariantMatches(projected, rule));');
	lines.push('  if (matches.length === 1) projected.$variant = matches[0]!.variant;');
	lines.push('}');
	lines.push('');
	lines.push(
		'function transportVariantMatches(node: Record<string, unknown>, rule: NativeTransportVariantRule): boolean {'
	);
	lines.push('  for (const field of rule.fields) {');
	lines.push('    const value = node[field.name];');
	lines.push('    if (value === undefined) {');
	lines.push('      if (field.required) return false;');
	lines.push('      continue;');
	lines.push('    }');
	lines.push(
		'    const ok = field.multiple ? transportArrayMatches(value, field.alternatives) : transportValueMatches(value, field.alternatives);'
	);
	lines.push('    if (!ok) return false;');
	lines.push('  }');
	lines.push('  if (rule.children) {');
	lines.push('    const children = node.$children;');
	lines.push('    if (children === undefined) return !rule.children.required;');
	lines.push('    return transportArrayMatches(children, rule.children.alternatives);');
	lines.push('  }');
	lines.push('  return true;');
	lines.push('}');
	lines.push('');
	lines.push(
		'function transportArrayMatches(value: unknown, alternatives: readonly NativeTransportAlternative[]): boolean {'
	);
	lines.push('  return Array.isArray(value) && value.every((item) => transportValueMatches(item, alternatives));');
	lines.push('}');
	lines.push('');
	lines.push(
		'function transportValueMatches(value: unknown, alternatives: readonly NativeTransportAlternative[]): boolean {'
	);
	// Collapsed terminal strings: match against alternatives by text.
	lines.push("  if (typeof value === 'string') {");
	lines.push('    return alternatives.some((candidate) => candidate.text !== undefined && candidate.text === value);');
	lines.push('  }');
	lines.push('  if (!isRecord(value)) return false;');
	lines.push('  // $type may be a numeric KindId (projected structured node) or a string');
	lines.push('  // (terminal node converted from a literal string via the string fast-path).');
	lines.push('  const vt = value.$type;');
	lines.push('  if (typeof vt !== "string" && typeof vt !== "number") return false;');
	lines.push('  return alternatives.some((candidate) => {');
	lines.push('    // Compare numeric vs numeric (kindIdFromName converts candidate string kind);');
	lines.push('    // compare string vs string for literal terminals.');
	lines.push('    const typeMatch = typeof vt === "number"');
	lines.push('      ? vt === kindIdFromName(candidate.type)');
	lines.push('      : vt === candidate.type;');
	lines.push('    if (!typeMatch) return false;');
	lines.push('    return candidate.text === undefined || value.$text === candidate.text;');
	lines.push('  });');
	lines.push('}');
	lines.push('');
}

function emitNativeTransportProjectionRules(lines: string[], nodeMap: NodeMap): void {
	lines.push('type NativeTransportAlternative = { readonly type: string; readonly text?: string };');
	lines.push(
		'type NativeTransportFieldRule = { readonly name: string; readonly multiple: boolean; readonly required: boolean; readonly alternatives: readonly NativeTransportAlternative[] };'
	);
	lines.push(
		'type NativeTransportRawChildRule = { readonly childrenRequired: boolean; readonly childrenMultiple: boolean; readonly childAlternatives: readonly NativeTransportAlternative[]; readonly fields: readonly NativeTransportFieldRule[] };'
	);
	lines.push(
		'type NativeTransportVariantRule = { readonly variant: string; readonly fields: readonly NativeTransportFieldRule[]; readonly children?: { readonly required: boolean; readonly multiple: boolean; readonly alternatives: readonly NativeTransportAlternative[] } };'
	);
	lines.push('');

	const aliasMap = collectAliasTargetToSourceMap(nodeMap);
	lines.push('const nativeTransportAliasTargetToSource: Record<string, string> = {');
	for (const [target, source] of [...aliasMap.entries()].sort()) {
		lines.push(`  ${JSON.stringify(target)}: ${JSON.stringify(source)},`);
	}
	lines.push('};');
	lines.push('');

	const rawChildRuleEntries = nativeTransportRawChildRuleEntries(nodeMap);
	lines.push('const nativeTransportRawChildFieldRules: Record<string, NativeTransportRawChildRule> = {');
	for (const entry of rawChildRuleEntries) {
		lines.push(`  ${JSON.stringify(entry.kind)}: {`);
		lines.push(`    childrenRequired: ${entry.childrenRequired},`);
		lines.push(`    childrenMultiple: ${entry.childrenMultiple},`);
		lines.push(`    childAlternatives: ${transportAlternativesExpr(entry.childAlternatives)},`);
		lines.push('    fields: [');
		for (const field of entry.fields) {
			lines.push(
				`      { name: ${JSON.stringify(field.name)}, multiple: ${field.multiple}, required: ${field.required}, alternatives: ${transportAlternativesExpr(field.alternatives)} },`
			);
		}
		lines.push('    ],');
		lines.push('  },');
	}
	lines.push('};');
	lines.push('');

	const variantRuleEntries = nativeTransportVariantRuleEntries(nodeMap);
	lines.push('const nativeTransportVariantRules: Record<string, readonly NativeTransportVariantRule[]> = {');
	for (const entry of variantRuleEntries) {
		lines.push(`  ${JSON.stringify(entry.kind)}: [`);
		for (const rule of entry.rules) {
			lines.push('    {');
			lines.push(`      variant: ${JSON.stringify(rule.variant)},`);
			lines.push('      fields: [');
			for (const field of rule.fields) {
				lines.push(
					`        { name: ${JSON.stringify(field.name)}, multiple: ${field.multiple}, required: ${field.required}, alternatives: ${transportAlternativesExpr(field.alternatives)} },`
				);
			}
			lines.push('      ],');
			if (rule.children !== undefined) {
				lines.push(
					`      children: { required: ${rule.children.required}, multiple: ${rule.children.multiple}, alternatives: ${transportAlternativesExpr(rule.children.alternatives)} },`
				);
			}
			lines.push('    },');
		}
		lines.push('  ],');
	}
	lines.push('};');
	lines.push('');

	const booleanKeywordFields = collectBooleanTransportFields(nodeMap);
	lines.push('const nativeTransportBooleanKeywordFieldsByKind: Record<string, ReadonlySet<string>> = {');
	for (const [kind, fields] of [...booleanKeywordFields.entries()].sort((a, b) => a[0].localeCompare(b[0]))) {
		lines.push(`  ${JSON.stringify(kind)}: new Set(${JSON.stringify(fields.sort())}),`);
	}
	lines.push('};');
	lines.push('');

	lines.push('function isRecord(value: unknown): value is Record<string, unknown> {');
	lines.push("  return value !== null && typeof value === 'object' && !Array.isArray(value);");
	lines.push('}');
	lines.push('');
	lines.push('function coerceKeywordPresenceFields(projected: Record<string, unknown>, kind: string): void {');
	lines.push('  const fields = nativeTransportBooleanKeywordFieldsByKind[kind];');
	lines.push('  if (!fields) return;');
	lines.push('  for (const fieldName of fields) {');
	lines.push('    const value = projected[fieldName];');
	lines.push('    if (value === undefined) continue;');
	lines.push("    if (typeof value === 'boolean') continue;");
	lines.push('    if (Array.isArray(value)) {');
	lines.push('      projected[fieldName] = value.length > 0;');
	lines.push('      continue;');
	lines.push('    }');
	lines.push('    projected[fieldName] = true;');
	lines.push('  }');
	lines.push('}');
	lines.push('');
	lines.push('function coerceNativeTransportChildren(projected: Record<string, unknown>, kind: string): void {');
	lines.push('  const children = projected.$children;');
	lines.push('  if (!Array.isArray(children)) return;');
	lines.push('  const variant = typeof projected.$variant === "string" ? projected.$variant : undefined;');
	lines.push('  if (variant !== undefined) {');
	lines.push('    const variantRule = nativeTransportVariantRules[kind]?.find((candidate) => candidate.variant === variant);');
	lines.push('    if (variantRule?.children && !variantRule.children.multiple) {');
	lines.push('      if (children.length === 0 && !variantRule.children.required) { delete projected.$children; return; }');
	lines.push('      projected.$children = children[0];');
	lines.push('      return;');
	lines.push('    }');
	lines.push('  }');
	lines.push('  const rawRule = nativeTransportRawChildFieldRules[kind];');
	lines.push('  if (rawRule && !rawRule.childrenMultiple) {');
	lines.push('    if (children.length === 0 && !rawRule.childrenRequired) { delete projected.$children; return; }');
	lines.push('    projected.$children = children[0];');
	lines.push('  }');
	lines.push('}');
}

function collectBooleanTransportFields(nodeMap: NodeMap): Map<string, string[]> {
	const result = new Map<string, string[]>();
	for (const [, node] of nodeMap.nodes) {
		switch (node.modelType) {
			case 'branch':
			case 'group': {
				const fields = node.fields.filter((field) => fieldUsesBooleanTransport(field, nodeMap)).map((field) => field.name);
				if (fields.length > 0) result.set(node.kind, fields);
				break;
			}
			case 'polymorph': {
				const fields = node.forms.flatMap((form) =>
					form.fields.filter((field) => fieldUsesBooleanTransport(field, nodeMap)).map((field) => field.name)
				);
				if (fields.length > 0) result.set(node.kind, [...new Set(fields)]);
				break;
			}
			default:
				break;
		}
	}
	return result;
}

function fieldUsesBooleanTransport(field: AssembledNonterminal, nodeMap: NodeMap): boolean {
	const components = fieldTypeComponents(field, nodeMap);
	if (components.length === 0) return false;
	for (const component of components) {
		if (component.kind !== 'nodeKind') return false;
		const resolvedKind = resolveTransportReferenceKind(component.rawKind, nodeMap);
		const node = nodeMap.nodes.get(resolvedKind);
		if (!(node instanceof AssembledEnum) || node.values.length !== 1) return false;
	}
	return true;
}

interface NativeTransportRawChildRuleEntry {
	readonly kind: string;
	readonly childrenRequired: boolean;
	readonly childrenMultiple: boolean;
	readonly childAlternatives: readonly TransportAlternative[];
	readonly fields: readonly {
		readonly name: string;
		readonly multiple: boolean;
		readonly required: boolean;
		readonly alternatives: readonly TransportAlternative[];
	}[];
}

function nativeTransportRawChildRuleEntries(nodeMap: NodeMap): NativeTransportRawChildRuleEntry[] {
	const entries: NativeTransportRawChildRuleEntry[] = [];
	for (const [, node] of nodeMap.nodes) {
		const fields = structuralFieldsOf(node).filter((field) => isMultiple(field));
		const children = structuralChildrenOf(node);
		if (fields.length === 0 && children.length === 0) continue;
		const cardinality = combineSlotCardinality(children);
		entries.push({
			kind: node.kind,
			childrenRequired: cardinality.required,
			childrenMultiple: cardinality.multiple,
			childAlternatives: children.flatMap((child) =>
				child.values.flatMap((value) => transportAlternativesForSlotValue(value, nodeMap))
			),
			fields: fields.map((field) => ({
				name: field.name,
				multiple: true,
				required: isRequired(field),
				alternatives: fieldTransportAlternatives(field, nodeMap)
			}))
		});
	}
	return entries.sort((a, b) => a.kind.localeCompare(b.kind));
}

interface NativeTransportVariantRuleEntry {
	readonly kind: string;
	readonly rules: readonly {
		readonly variant: string;
		readonly fields: readonly {
			readonly name: string;
			readonly multiple: boolean;
			readonly required: boolean;
			readonly alternatives: readonly TransportAlternative[];
		}[];
		readonly children?: {
			readonly required: boolean;
			readonly multiple: boolean;
			readonly alternatives: readonly TransportAlternative[];
		};
	}[];
}

function nativeTransportVariantRuleEntries(nodeMap: NodeMap): NativeTransportVariantRuleEntry[] {
	const entries: NativeTransportVariantRuleEntry[] = [];
	for (const [, node] of nodeMap.nodes) {
		if (node.modelType !== 'polymorph') continue;
		entries.push({
			kind: node.kind,
			rules: node.forms.map((form) => {
				const children = form.children;
				return {
					variant: form.name,
					fields: form.fields.map((field) => ({
						name: field.name,
						multiple: isMultiple(field),
						required: isRequired(field),
						alternatives: fieldTransportAlternatives(field, nodeMap)
					})),
					children:
						children.length > 0
							? {
									required: children.some((child) => isRequired(child)),
									multiple: children.some((child) => isMultiple(child)),
									alternatives: children.flatMap((child) =>
										child.values.flatMap((value) => transportAlternativesForSlotValue(value, nodeMap))
									)
								}
							: undefined
				};
			})
		});
	}
	return entries.sort((a, b) => a.kind.localeCompare(b.kind));
}

interface TransportAlternative {
	readonly type: string;
	readonly text?: string;
}

function fieldTransportAlternatives(field: AssembledNonterminal, nodeMap: NodeMap): TransportAlternative[] {
	return fieldTypeComponents(field, nodeMap).flatMap((component) => {
		if (component.kind === 'literal') {
			return [{ type: component.value, text: component.value }];
		}
		return transportAlternativesForKind(component.rawKind, nodeMap);
	});
}

function transportAlternativesForSlotValue(value: NodeOrTerminal, nodeMap: NodeMap): TransportAlternative[] {
	if (isTerminalValue(value)) {
		return [{ type: value.value, text: value.value }];
	}
	if (!isNodeRef(value)) return [];
	const kind = isUnresolvedRef(value.node) ? value.node.name : value.node.kind;
	return transportAlternativesForKind(kind, nodeMap);
}

function transportAlternativesForKind(kind: string, nodeMap: NodeMap): TransportAlternative[] {
	const resolvedKind = resolveTransportReferenceKind(kind, nodeMap);
	if (resolvedKind !== kind) {
		return transportAlternativesForKind(resolvedKind, nodeMap);
	}
	const literal = resolveHiddenKeywordLiteral(kind, nodeMap);
	if (literal !== undefined) return [{ type: kind, text: literal }];
	const node = nodeMap.nodes.get(kind);
	if (node?.modelType === 'keyword') return [{ type: kind, text: node.text }];
	if (node?.modelType === 'token' && node.text !== undefined) {
		return [{ type: kind, text: node.text }];
	}
	if (node?.modelType === 'enum') {
		return node.values.map((text) => ({ type: kind, text }));
	}
	if (node?.modelType === 'supertype') {
		return node.subtypes.flatMap((subtype) => transportAlternativesForKind(subtype, nodeMap));
	}
	return [{ type: kind }];
}

function transportAlternativesExpr(alternatives: readonly TransportAlternative[]): string {
	const seen = new Set<string>();
	const deduped: TransportAlternative[] = [];
	for (const alternative of alternatives) {
		const key = `${alternative.type}\0${alternative.text ?? ''}`;
		if (seen.has(key)) continue;
		seen.add(key);
		deduped.push(alternative);
	}
	return `${JSON.stringify(deduped)} as const`;
}

/**
 * Resolve trivia kind names to their TypeScript type names via nodeMap.
 *
 * @param triviaKinds - Raw kind names from `extractTriviaRoles` (e.g. `['line_comment', 'block_comment']`).
 * @param nodeMap - The assembled node map with `typeName` on each node.
 * @returns Sorted, deduplicated array of TypeScript type names (e.g. `['BlockComment', 'LineComment']`).
 */
function resolveTriviaTypeNames(triviaKinds: string[] | undefined, nodeMap: NodeMap): string[] {
	if (!triviaKinds || triviaKinds.length === 0) return [];
	const typeNames: string[] = [];
	for (const kind of triviaKinds) {
		const node = nodeMap.nodes.get(kind);
		if (node) {
			typeNames.push(node.typeName);
		}
	}
	return [...new Set(typeNames)].sort();
}

/**
 * Build the TypeScript parameter type for `$trivia()`.
 *
 * When typed trivia names are available, emits a union of concrete types
 * plus the `{ leading?: ...; trailing?: ... }` object form. Falls back
 * to `unknown` when no trivia kinds are known.
 *
 * @param triviaTypeNames - Resolved TypeScript type names (e.g. `['BlockComment', 'LineComment']`).
 * @returns The type expression string for the rest parameter (without the trailing `[]`).
 */
function buildTriviaParamType(triviaTypeNames: string[]): string {
	if (triviaTypeNames.length === 0) return 'unknown';
	const union = triviaTypeNames.join(' | ');
	return `(${union} | { leading?: (${union})[]; trailing?: (${union})[] })`;
}

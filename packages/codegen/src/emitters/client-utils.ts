/**
 * Emits utils.ts — typed facade over `@sittir/common/utils` with grammar-local narrowing.
 * Runtime behavior lives in `@sittir/common/utils`; this module only projects local types.
 */

import type { NodeMap } from '../compiler/types.ts';
import type { GeneratedIdTables } from '../compiler/generated-metadata.ts';

export interface EmitClientUtilsConfig {
nodeMap: NodeMap;
generatedIdTables?: GeneratedIdTables;
/** Trivia kind names (e.g. `['line_comment', 'block_comment']`). */
triviaKinds?: string[];
}

export function emitClientUtils(config: EmitClientUtilsConfig): string {
const triviaTypeNames = resolveTriviaTypeNames(config.triviaKinds ?? [], config.nodeMap);
const lines: string[] = [];
lines.push('// Auto-generated by @sittir/codegen — do not edit');
lines.push('// Typed facade over @sittir/common/utils with grammar-local narrowing helpers');
lines.push('');
lines.push("import type { AnyNodeData, AnyTreeNodeOf, ByteRange, Edit } from '@sittir/types';");
if (triviaTypeNames.length > 0) {
lines.push(`import type { ${triviaTypeNames.join(', ')}, NamespaceMap } from './types.js';`);
} else {
lines.push("import type { NamespaceMap } from './types.js';");
}
lines.push("import { kindIdFromName } from './types.js';");
lines.push("import { render, toEdit } from './boundary.ts';");
lines.push("import { withMethods as withCommonMethods, isNodeData as _isNodeData, isTreeNode as _isTreeNode, hasKind, coerceBooleanKeywordStorage, coerceBitflagStorage } from '@sittir/common/utils';");
lines.push("import type { WithMethodsEngine } from '@sittir/common/utils';");
lines.push('');
lines.push('export { hasKind, coerceBooleanKeywordStorage, coerceBitflagStorage };');
lines.push('');
lines.push(...emitIsNodeData());
lines.push('');
lines.push(...emitIsTreeNode());
lines.push('');
lines.push(...emitMethodsEngine());
lines.push('');
lines.push(...emitWithMethods(triviaTypeNames));
lines.push('');
lines.push(...emitNodeGuards());
lines.push('');
lines.push(...emitTransportHelpers());
return lines.join('\n');
}

function emitMethodsEngine(): string[] {
return [
'export const methodsEngine = {',
'  render(node: AnyNodeData) { return render(node); },',
'  toEdit(node: AnyNodeData, startOrRange: number | ByteRange, endPos?: number) { return toEdit(node, startOrRange, endPos); },',
'} satisfies WithMethodsEngine;',
];
}

function emitWithMethods(triviaTypeNames: readonly string[]): string[] {
const triviaType = buildTriviaParamType(triviaTypeNames);
return [
'export function withMethods<T extends object>(',
'  node: T,',
'  engine: typeof methodsEngine',
'): T & {',
'  $render(): string;',
'  $toEdit(startOrRange: number | ByteRange, endPos?: number): Edit;',
'  $replace(target: { range(): ByteRange }): Edit;',
`  $trivia(...args: ${triviaType}[]): AnyNodeData;`,
'} {',
'  // Grammar-local facade: T extends object to accept wrap.ts union-spread literals.',
'  // Only factory/wrap output — which always satisfies AnyNodeData structurally — calls this.',
'  return withCommonMethods(node as unknown as T & AnyNodeData, engine) as T & {',
'    $render(): string;',
'    $toEdit(startOrRange: number | ByteRange, endPos?: number): Edit;',
'    $replace(target: { range(): ByteRange }): Edit;',
`    $trivia(...args: ${triviaType}[]): AnyNodeData;`,
'  };',
'}'
];
}

function emitIsNodeData(): string[] {
  return [
    'export function isNodeData<K extends keyof NamespaceMap>(',
    "  v: NamespaceMap[K]['Node'] | NamespaceMap[K]['Loose'] | NamespaceMap[K]['Tree']",
    "): v is NamespaceMap[K]['Node'];",
    'export function isNodeData(v: unknown): v is AnyNodeData;',
    'export function isNodeData(v: unknown): v is AnyNodeData {',
    '  return _isNodeData(v);',
    '}',
  ];
}

function emitIsTreeNode(): string[] {
  return [
    'export function isTreeNode<K extends keyof NamespaceMap>(',
    "  v: NamespaceMap[K]['Tree'] | NamespaceMap[K]['Node']",
    "): v is NamespaceMap[K]['Tree'];",
    'export function isTreeNode(v: unknown): v is AnyTreeNodeOf;',
    'export function isTreeNode(v: unknown): v is AnyTreeNodeOf {',
    '  return _isTreeNode(v);',
    '}',
  ];
}

function emitNodeGuards(): string[] {
return [
'export function isNodeOfKind<K extends keyof NamespaceMap>(',
'  v: unknown,',
'  kind: K,',
"): v is NamespaceMap[K]['Node'] {",
'  return isNodeData(v) && v.$type === kindIdFromName(kind);',
'}',
'',
'export function hasKindOf<K extends keyof NamespaceMap>(',
'  v: object,',
'  kind: K,',
"): v is { kind: K } & NamespaceMap[K]['Loose'] {",
"  return 'kind' in v && (v as Record<string, unknown>).kind === kind;",
'}'
];
}

function emitTransportHelpers(): string[] {
return [
'export function toNativeRenderTransport(node: unknown): unknown {',
'  return node;',
'}',
'',
		'export function coerceKindEnumStorage<T = unknown>(',
		'  value: unknown,',
		'  byText: readonly (readonly [string, number])[] = []',
		'): T {',
		'  if (value === undefined || value === null) return undefined as T;',
		"  if (typeof value === 'number') return value as T;",
		'  if (Array.isArray(value)) {',
		'    return value',
		'      .map((item) => coerceKindEnumStorage(item, byText))',
		'      .filter((item) => item !== undefined) as T;',
		'  }',
		'  const text = extractNodeText(value);',
		'  if (text !== undefined) {',
		'    const mapped = byText.find(([candidate]) => candidate === text);',
		'    if (mapped) return mapped[1] as T;',
		'  }',
		'  if (isRecord(value) && typeof value.$type === "number") return value.$type as T;',
		"  if (typeof value === 'string') {",
		'    const mapped = byText.find(([candidate]) => candidate === value);',
		'    return (mapped ? mapped[1] : kindIdFromName(value)) as T;',
		'  }',
		'  return value as T;',
		'}',
'',
'function extractNodeText(value: unknown): string | undefined {',
"  if (typeof value === 'string') return value;",
'  if (isNodeData(value)) {',
"    return typeof value.$text === 'string' ? value.$text : undefined;",
'  }',
'  if (isRecord(value) && typeof value.$text === "string") return value.$text;',
'  return undefined;',
'}',
'',
'function isRecord(value: unknown): value is Record<string, unknown> {',
"  return value !== null && typeof value === 'object' && !Array.isArray(value);",
'}'
];
}

function resolveTriviaTypeNames(triviaKinds: readonly string[], nodeMap: NodeMap): string[] {
const names = triviaKinds
.map((kind) => nodeMap.nodes.get(kind)?.typeName)
.filter((name): name is string => name !== undefined);
return [...new Set(names)].sort();
}

function buildTriviaParamType(triviaTypeNames: readonly string[]): string {
const triviaType = triviaTypeNames.length > 0 ? triviaTypeNames.join(' | ') : 'AnyNodeData';
return `(${triviaType} | { leading?: (${triviaType})[]; trailing?: (${triviaType})[] })`;
}

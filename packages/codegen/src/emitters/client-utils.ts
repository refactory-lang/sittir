/**
 * Emits utils.ts — shared client-side resolution utilities.
 *
 * Contains the runtime helpers shared between factories.ts and from.ts:
 * type guards (isNodeData, isTreeNode), branch field metadata,
 * and _inferBranch scoring algorithm.
 */

import type { HydratedNodeModel } from '../node-model.ts';
import { toFieldName, toGrammarTypeName } from '../naming.ts';
import { structuralNodes, fieldsOf } from './utils.ts';

export interface EmitClientUtilsConfig {
	grammar?: string;
	nodes: HydratedNodeModel[];
}

export function emitClientUtils(config: EmitClientUtilsConfig): string {
	const nodes = structuralNodes(config.nodes);

	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('// Shared client-side resolution utilities for .from() and factories');
	lines.push('');

	lines.push("import type { AnyNodeData, AnyTreeNodeOf } from '@sittir/types';");
	lines.push('');

	// Type guards
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('// Type guards');
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('');

	// isNodeData
	lines.push('/** Returns true if `v` is a NodeData — has `type` string and `fields` object or `text` string. */');
	lines.push('export function isNodeData(v: unknown): v is AnyNodeData {');
	lines.push("  if (v === null || typeof v !== 'object') return false;");
	lines.push("  const o = v as Record<string, unknown>;");
	lines.push("  if (typeof o['type'] !== 'string') return false;");
	lines.push("  return (o['fields'] !== null && typeof o['fields'] === 'object') || typeof o['text'] === 'string';");
	lines.push('}');
	lines.push('');

	// isTreeNode
	lines.push('/** Returns true if `v` is a TreeNode (SgNode-compatible) — has `type`, `field()`, and `text()`. */');
	lines.push('export function isTreeNode(v: unknown): v is AnyTreeNodeOf {');
	lines.push("  if (v === null || typeof v !== 'object') return false;");
	lines.push("  const o = v as Record<string, unknown>;");
	lines.push("  return typeof o['type'] === 'string' && typeof o['field'] === 'function' && typeof o['text'] === 'function';");
	lines.push('}');
	lines.push('');

	// Branch field metadata for inference
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('// Branch inference');
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('');
	lines.push('/** Field names per branch kind — used by _inferBranch for scoring. */');
	lines.push('export const _BRANCH_FIELDS: Record<string, string[]> = {');
	for (const node of nodes) {
		const fieldNames = fieldsOf(node).map(f => f.propertyName ?? toFieldName(f.name));
		if (fieldNames.length > 0) {
			lines.push(`  '${node.kind}': ${JSON.stringify(fieldNames)},`);
		}
	}
	lines.push('};');
	lines.push('');

	lines.push('/**');
	lines.push(' * Infer the best matching branch kind for an untyped object.');
	lines.push(' *');
	lines.push(' * Scoring: matches count double, misses subtract once. This ensures');
	lines.push(' * a candidate with all fields matching but one extra key still wins');
	lines.push(' * over a candidate with fewer matches. Requires at least one match');
	lines.push(' * to prevent empty/wrong-shaped objects from resolving silently.');
	lines.push(' *');
	lines.push(' * Returns the inferred kind string, or null if no candidate matches.');
	lines.push(' */');
	lines.push('export function _inferBranch(obj: object, candidates: string[]): string | null {');
	lines.push("  const keys = Object.keys(obj).filter(k => k !== 'children');");
	lines.push('  let best: string | null = null;');
	lines.push('  let bestScore = 0;');
	lines.push('  let bestMatches = 0;');
	lines.push('  for (const c of candidates) {');
	lines.push('    const fields = _BRANCH_FIELDS[c];');
	lines.push('    if (!fields) continue;');
	lines.push('    const fieldSet = new Set(fields);');
	lines.push('    let matches = 0, misses = 0;');
	lines.push('    for (const k of keys) { if (fieldSet.has(k)) matches++; else misses++; }');
	lines.push('    if (matches === 0) continue;');
	lines.push('    const score = matches * 2 - misses;');
	lines.push('    if (score > bestScore || (score === bestScore && matches > bestMatches)) { bestScore = score; bestMatches = matches; best = c; }');
	lines.push('  }');
	lines.push('  return best;');
	lines.push('}');
	lines.push('');

	// hasKind — checks for { kind: string } discriminant
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('// Kind discrimination');
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('');
	lines.push('/** Returns true if `v` has a `kind` string property for branch discrimination. */');
	lines.push("export function hasKind(v: object): v is { kind: string } & Record<string, unknown> {");
	lines.push("  return 'kind' in v && typeof (v as Record<string, unknown>).kind === 'string';");
	lines.push('}');
	lines.push('');

	// resolveField — shared field resolution utility
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('// Field resolution');
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('');
	lines.push('/**');
	lines.push(' * Resolve a single field value for .from() input.');
	lines.push(' * Passes through NodeData, delegates scalars and objects to the resolver.');
	lines.push(' * Arrays are mapped element-wise.');
	lines.push(' */');
	lines.push('export function resolveField<T>(');
	lines.push('  value: unknown,');
	lines.push('  resolver: (input: unknown) => T,');
	lines.push('): T {');
	lines.push('  if (value == null) return value as T;');
	lines.push('  if (isNodeData(value)) return value as T;');
	lines.push('  if (Array.isArray(value)) return value.map(v => resolveField(v, resolver)) as T;');
	lines.push('  return resolver(value);');
	lines.push('}');
	lines.push('');

	return lines.join('\n');
}

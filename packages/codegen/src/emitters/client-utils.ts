	lines.push(' */');
	lines.push('export function isNodeData<K extends keyof NamespaceMap>(');
	lines.push(
		"  v: NamespaceMap[K]['Node'] | NamespaceMap[K]['Loose'] | NamespaceMap[K]['Tree']"
	);
	lines.push("): v is NamespaceMap[K]['Node'];");
	lines.push('export function isNodeData(v: unknown): v is AnyNodeData;');

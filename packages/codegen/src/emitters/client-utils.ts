/**
 * Emits utils.ts — shared client-side resolution utilities.
 *
 * Contains type guards, kind discrimination, and field resolution helpers
 * used by factories.ts and from.ts.
 */

import type { HydratedNodeModel } from '../node-model.ts';
import { structuralNodes, fieldsOf } from './utils.ts';

export interface EmitClientUtilsConfig {
	grammar?: string;
	nodes: HydratedNodeModel[];
}

export function emitClientUtils(config: EmitClientUtilsConfig): string {
	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('// Shared client-side resolution utilities for .from() and factories');
	lines.push('');

	lines.push("import type { AnyNodeData, AnyTreeNodeOf, RuntimeNodeOf, FluentNodeOf, TreeNodeOf } from '@sittir/types';");
	lines.push("import type { KindMap, ConfigMap, FromInputMap } from './types.js';");
	lines.push('');

	// Type guards
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('// Type guards');
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('');

	// isNodeData — narrows to RuntimeNodeOf<KindMap[K]>
	lines.push('/**');
	lines.push(' * Type guard: returns true if `v` is a NodeData (has `type` + `fields` or `text`).');
	lines.push(' * Generic overload narrows from a union of RuntimeNodeOf, FromInput, or TreeNode');
	lines.push(' * to RuntimeNodeOf<KindMap[K]>.');
	lines.push(' */');
	lines.push('export function isNodeData<K extends keyof KindMap & keyof FromInputMap>(');
	lines.push('  v: RuntimeNodeOf<KindMap[K]> | FromInputMap[K] | TreeNodeOf<KindMap[K]>');
	lines.push('): v is RuntimeNodeOf<KindMap[K]>;');
	lines.push('export function isNodeData(v: unknown): v is AnyNodeData;');
	lines.push('export function isNodeData(v: unknown): v is AnyNodeData {');
	lines.push("  if (v === null || typeof v !== 'object') return false;");
	lines.push("  const o = v as Record<string, unknown>;");
	lines.push("  if (typeof o['type'] !== 'string') return false;");
	lines.push("  return (o['fields'] !== null && typeof o['fields'] === 'object') || typeof o['text'] === 'string';");
	lines.push('}');
	lines.push('');

	// isTreeNode — narrows to TreeNodeOf<KindMap[K]>
	lines.push('/**');
	lines.push(' * Type guard: returns true if `v` is a TreeNode (SgNode-compatible).');
	lines.push(' * Generic overload narrows from a union to TreeNodeOf<KindMap[K]>.');
	lines.push(' */');
	lines.push('export function isTreeNode<K extends keyof KindMap & keyof FromInputMap>(');
	lines.push('  v: TreeNodeOf<KindMap[K]> | RuntimeNodeOf<KindMap[K]>');
	lines.push('): v is TreeNodeOf<KindMap[K]>;');
	lines.push('export function isTreeNode(v: unknown): v is AnyTreeNodeOf;');
	lines.push('export function isTreeNode(v: unknown): v is AnyTreeNodeOf {');
	lines.push("  if (v === null || typeof v !== 'object') return false;");
	lines.push("  const o = v as Record<string, unknown>;");
	lines.push("  return typeof o['type'] === 'string' && typeof o['field'] === 'function' && typeof o['text'] === 'function';");
	lines.push('}');
	lines.push('');

	// Kind discrimination
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('// Kind discrimination');
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('');
	lines.push('/** Returns true if `v` has a `kind` string property for branch discrimination. */');
	lines.push("export function hasKind(v: object): v is { kind: string } & Record<string, unknown> {");
	lines.push("  return 'kind' in v && typeof (v as Record<string, unknown>).kind === 'string';");
	lines.push('}');
	lines.push('');

	// Field resolution
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('// Field resolution');
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('');
	lines.push('/**');
	lines.push(' * Resolve a field value for .from() input.');
	lines.push(' * Passes through NodeData, delegates scalars and objects to the resolver.');
	lines.push(' * Arrays are mapped element-wise, returning T[].');
	lines.push(' */');
	lines.push('export function resolveField<T>(value: readonly unknown[], resolver: (input: unknown) => T): T[];');
	lines.push('export function resolveField<T>(value: unknown, resolver: (input: unknown) => T): T;');
	lines.push('export function resolveField<T>(value: unknown, resolver: (input: unknown) => T): T | T[] {');
	lines.push('  if (value == null) return value as T;');
	lines.push('  if (isNodeData(value)) return value as T;');
	lines.push('  if (Array.isArray(value)) return value.map(v => resolveField(v, resolver));');
	lines.push('  return resolver(value);');
	lines.push('}');
	lines.push('');

	// Grammar-typed type guards
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('// Grammar-typed type guards');
	lines.push('// ---------------------------------------------------------------------------');
	lines.push('');

	// isNodeOfKind
	lines.push('/**');
	lines.push(' * Type guard: returns true if `v` is a NodeData of the given kind.');
	lines.push(' * Narrows `v` to `KindMap[K]`.');
	lines.push(' */');
	lines.push('export function isNodeOfKind<K extends keyof KindMap>(');
	lines.push('  v: unknown,');
	lines.push('  kind: K,');
	lines.push('): v is KindMap[K] {');
	lines.push("  return isNodeData(v) && v.type === kind;");
	lines.push('}');
	lines.push('');

	// hasKindOf
	lines.push('/**');
	lines.push(' * Type guard: returns true if `v` has `kind === k`.');
	lines.push(' * Narrows `v` to `{ kind: K } & FromInputMap[K]`.');
	lines.push(' */');
	lines.push('export function hasKindOf<K extends keyof FromInputMap>(');
	lines.push('  v: object,');
	lines.push('  kind: K,');
	lines.push('): v is { kind: K } & FromInputMap[K] {');
	lines.push("  return 'kind' in v && (v as Record<string, unknown>).kind === kind;");
	lines.push('}');
	lines.push('');

	// resolveFieldAs
	lines.push('/**');
	lines.push(' * Resolve a field value and narrow the result to `KindMap[K]`.');
	lines.push(' */');
	lines.push('export function resolveFieldAs<K extends keyof KindMap>(');
	lines.push('  value: unknown,');
	lines.push('  resolver: (input: unknown) => KindMap[K],');
	lines.push('): KindMap[K] {');
	lines.push('  return resolveField(value, resolver) as KindMap[K];');
	lines.push('}');
	lines.push('');

	return lines.join('\n');
}

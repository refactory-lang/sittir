/**
 * Emits utils.ts — shared client-side resolution utilities.
 * Consumes NodeMap directly. Output is mostly static.
 */

import type { NodeMap } from '../compiler/rule.ts'

export interface EmitClientUtilsConfig {
    nodeMap: NodeMap
}

export function emitClientUtils(_config: EmitClientUtilsConfig): string {
    const lines: string[] = []
    lines.push('// Auto-generated by @sittir/codegen — do not edit')
    lines.push('// Shared client-side resolution utilities for .from() and factories')
    lines.push('')
    lines.push("import type { AnyNodeData, AnyTreeNodeOf, RuntimeNodeOf, TreeNodeOf } from '@sittir/types';")
    lines.push("import type { KindMap, LooseMap } from './types.js';")
    lines.push('')

    // isNodeData
    lines.push('/**')
    lines.push(' * Type guard: returns true if `v` is a NodeData (has `type` + `fields` or `text`).')
    lines.push(' */')
    lines.push('export function isNodeData<K extends keyof KindMap & keyof LooseMap>(')
    lines.push('  v: RuntimeNodeOf<KindMap[K]> | LooseMap[K] | TreeNodeOf<KindMap[K]>')
    lines.push('): v is RuntimeNodeOf<KindMap[K]>;')
    lines.push('export function isNodeData(v: unknown): v is AnyNodeData;')
    lines.push('export function isNodeData(v: unknown): v is AnyNodeData {')
    lines.push("  if (v === null || typeof v !== 'object') return false;")
    lines.push("  const o = v as Record<string, unknown>;")
    lines.push("  if (typeof o['type'] !== 'string') return false;")
    lines.push("  return (o['fields'] !== null && typeof o['fields'] === 'object') || typeof o['text'] === 'string';")
    lines.push('}')
    lines.push('')

    // isTreeNode
    lines.push('/**')
    lines.push(' * Type guard: returns true if `v` is a TreeNode (SgNode-compatible).')
    lines.push(' */')
    lines.push('export function isTreeNode<K extends keyof KindMap & keyof LooseMap>(')
    lines.push('  v: TreeNodeOf<KindMap[K]> | RuntimeNodeOf<KindMap[K]>')
    lines.push('): v is TreeNodeOf<KindMap[K]>;')
    lines.push('export function isTreeNode(v: unknown): v is AnyTreeNodeOf;')
    lines.push('export function isTreeNode(v: unknown): v is AnyTreeNodeOf {')
    lines.push("  if (v === null || typeof v !== 'object') return false;")
    lines.push("  const o = v as Record<string, unknown>;")
    lines.push("  return typeof o['type'] === 'string' && typeof o['field'] === 'function' && typeof o['text'] === 'function';")
    lines.push('}')
    lines.push('')

    // hasKind
    lines.push("export function hasKind(v: object): v is { kind: string } & Record<string, unknown> {")
    lines.push("  return 'kind' in v && typeof (v as Record<string, unknown>).kind === 'string';")
    lines.push('}')
    lines.push('')

    // isNodeOfKind
    lines.push('export function isNodeOfKind<K extends keyof KindMap>(')
    lines.push('  v: unknown,')
    lines.push('  kind: K,')
    lines.push('): v is KindMap[K] {')
    lines.push("  return isNodeData(v) && v.type === kind;")
    lines.push('}')
    lines.push('')

    // hasKindOf
    lines.push('export function hasKindOf<K extends keyof LooseMap>(')
    lines.push('  v: object,')
    lines.push('  kind: K,')
    lines.push('): v is { kind: K } & LooseMap[K] {')
    lines.push("  return 'kind' in v && (v as Record<string, unknown>).kind === kind;")
    lines.push('}')
    lines.push('')

    return lines.join('\n')
}

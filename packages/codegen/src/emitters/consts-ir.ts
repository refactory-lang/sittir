/**
 * Emits a consts module with discoverable arrays and maps from the grammar.
 *
 * Uses the @sittir/typescript IR builder declarative from() API to construct
 * TypeScript AST nodes, mirroring the tree-sitter CST structure.
 *
 * This is the IR-based replacement for the string-concatenation approach in
 * consts.ts. Toggle via EmitConstsConfig.useIr (or import directly).
 *
 * Exports:
 *   NODE_KINDS     — all branch node kind strings
 *   LEAF_KINDS     — all leaf/terminal node kind strings
 *   ALL_KINDS      — NODE_KINDS ∪ LEAF_KINDS
 *   KEYWORDS       — alphabetic anonymous tokens (fn, struct, let, etc.)
 *   OPERATORS      — non-alphabetic anonymous tokens (+, -, ::, etc.)
 *   FIELD_MAP      — per-node-kind field metadata
 */

import type { NodeMeta } from '../grammar-reader.ts';
import { toFieldName } from '../naming.ts';
import { ir } from '@sittir/typescript';
import { LeafBuilder } from '@sittir/types';

// ---------------------------------------------------------------------------
// Leaf helpers — no explicit return types so TypeScript infers the specific
// builder type (e.g. LeafBuilder<'string'> rather than Builder<{kind:string}>)
// ---------------------------------------------------------------------------

const leaf = <K extends string>(kind: K, text: string) => new LeafBuilder(kind, text);
const str = (text: string) => leaf('string', `'${text.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`);
const ident = (name: string) => ir.identifier(name);
const typeIdent = (name: string) => ir.typeIdentifier(name);

// ---------------------------------------------------------------------------
// AST construction helpers — declarative from() API mirroring CST structure
// ---------------------------------------------------------------------------

/** `export const NAME = [...items] as const;` */
function constArray(name: string, items: ReturnType<typeof str>[]) {
  return ir.exportStatement.from({
    declaration: ir.lexicalDeclaration.from({
      kind: leaf('const', 'const'),
      children: ir.variableDeclarator.from({
        name: ident(name),
        value: ir.asExpression.from({
          children: [
            ir.array.from({ children: items }),
            leaf('type_identifier', 'const'),
          ],
        }),
      }),
    }),
  });
}

/** `export type NAME = (typeof SOURCE)[number];` */
function typeofIndex(name: string, source: string) {
  return ir.exportStatement.from({
    declaration: ir.typeAliasDeclaration.from({
      name: typeIdent(name),
      value: ir.lookupType.from({
        children: [
          ir.parenthesizedType.from({
            children: ir.typeQuery.from({ children: ident(source) }),
          }),
          leaf('predefined_type', 'number'),
        ],
      }),
    }),
  });
}

/** `export const NAME = [...A, ...B] as const;` */
function spreadArray(name: string, sources: string[]) {
  return ir.exportStatement.from({
    declaration: ir.lexicalDeclaration.from({
      kind: leaf('const', 'const'),
      children: ir.variableDeclarator.from({
        name: ident(name),
        value: ir.asExpression.from({
          children: [
            ir.array.from({
              children: sources.map(s => ir.spreadElement.from({ children: ident(s) })),
            }),
            leaf('type_identifier', 'const'),
          ],
        }),
      }),
    }),
  });
}

/** `{ name: 'value', required: true, multiple: false }` */
const bool = (v: boolean) => v ? leaf('true', 'true') : leaf('false', 'false');

function fieldEntry(name: string, required: boolean, multiple: boolean) {
  return ir.object.from({
    children: [
      ir.pair.from({ key: ir.propertyIdentifier('name'), value: str(name) }),
      ir.pair.from({ key: ir.propertyIdentifier('required'), value: bool(required) }),
      ir.pair.from({ key: ir.propertyIdentifier('multiple'), value: bool(multiple) }),
    ],
  });
}

// ---------------------------------------------------------------------------
// Public API — same interface as consts.ts
// ---------------------------------------------------------------------------

export type { EmitConstsConfig, EnumKind } from './consts.ts';

import type { EmitConstsConfig } from './consts.ts';

export function emitConstsIr(config: EmitConstsConfig): string {
  const { nodeKinds, leafKinds, keywords, operators, nodes } = config;

  const statements: string[] = [
    '// Auto-generated by ir-codegen — do not edit',
    '',
  ];

  // NODE_KINDS
  statements.push('/** All branch (non-leaf) node kind strings. */');
  statements.push(constArray('NODE_KINDS', nodeKinds.sort().map(str)).renderImpl() + ';');
  statements.push('');

  // LEAF_KINDS
  statements.push('/** All leaf/terminal node kind strings. */');
  statements.push(constArray('LEAF_KINDS', leafKinds.sort().map(str)).renderImpl() + ';');
  statements.push('');

  // ALL_KINDS
  statements.push('/** All node kind strings (branch + leaf). */');
  statements.push(spreadArray('ALL_KINDS', ['NODE_KINDS', 'LEAF_KINDS']).renderImpl() + ';');
  statements.push('');

  // KEYWORDS
  statements.push('/** Language keywords (alphabetic anonymous tokens). */');
  statements.push(constArray('KEYWORDS', keywords.map(str)).renderImpl() + ';');
  statements.push('');

  // OPERATORS
  statements.push('/** Operator/punctuation tokens. */');
  statements.push(constArray('OPERATORS', operators.map(str)).renderImpl() + ';');
  statements.push('');

  // Type aliases
  statements.push(typeofIndex('NodeKind', 'NODE_KINDS').renderImpl() + ';');
  statements.push(typeofIndex('LeafKind', 'LEAF_KINDS').renderImpl() + ';');
  statements.push(typeofIndex('AnyKind', 'ALL_KINDS').renderImpl() + ';');
  statements.push(typeofIndex('Keyword', 'KEYWORDS').renderImpl() + ';');
  statements.push(typeofIndex('Operator', 'OPERATORS').renderImpl() + ';');
  statements.push('');

  // FIELD_MAP — per-node field metadata
  const fieldMapPairs = [...nodes]
    .sort((a, b) => a.kind.localeCompare(b.kind))
    .map(node =>
      ir.pair.from({
        key: str(node.kind),
        value: ir.array.from({
          children: node.fields.map(f => fieldEntry(toFieldName(f.name), f.required, f.multiple)),
        }),
      }),
    );

  statements.push('/** Per-node-kind field metadata. */');
  statements.push(
    ir.exportStatement.from({
      declaration: ir.lexicalDeclaration.from({
        kind: leaf('const', 'const'),
        children: ir.variableDeclarator.from({
          name: ident('FIELD_MAP'),
          type: ir.typeAnnotation.from({
            children: ir.genericType.from({
              name: typeIdent('Record'),
              typeArguments: ir.typeArguments.from({
                children: [
                  typeIdent('NodeKind'),
                  ir.genericType.from({
                    name: typeIdent('ReadonlyArray'),
                    typeArguments: ir.typeArguments.from({
                      children: ir.objectType.from({
                        children: [
                          ir.propertySignature.from({ name: ir.propertyIdentifier('name'), type: ir.typeAnnotation.from({ children: leaf('predefined_type', 'string') }) }),
                          ir.propertySignature.from({ name: ir.propertyIdentifier('required'), type: ir.typeAnnotation.from({ children: leaf('predefined_type', 'boolean') }) }),
                          ir.propertySignature.from({ name: ir.propertyIdentifier('multiple'), type: ir.typeAnnotation.from({ children: leaf('predefined_type', 'boolean') }) }),
                        ],
                      }),
                    }),
                  }),
                ],
              }),
            }),
          }),
          value: ir.object.from({ children: fieldMapPairs }),
        }),
      }),
    }).renderImpl() + ';',
  );
  statements.push('');

  // Enum-like leaf kinds
  if (config.enumKinds && config.enumKinds.length > 0) {
    for (const ek of config.enumKinds) {
      const constName = ek.kind.toUpperCase() + 'S';
      const typeName = ek.kind.split('_').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join('') + 'Value';

      statements.push(`/** Valid values for \`${ek.kind}\` nodes. */`);
      statements.push(constArray(constName, ek.values.map(str)).renderImpl() + ';');
      statements.push(typeofIndex(typeName, constName).renderImpl() + ';');
      statements.push('');
    }
  }

  return statements.join('\n');
}

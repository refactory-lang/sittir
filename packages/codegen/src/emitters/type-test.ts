/**
 * Type-level test emitter.
 *
 * Generates a .ts file that verifies:
 *   1. Each concrete interface has a `type` literal
 *   2. ConfigOf<T> produces the expected config shape
 *   3. TreeNode<K> types are well-formed
 *
 * If types diverge, `tsc` / `tsgo` will report errors.
 */

import type { HydratedNodeModel } from '../node-model.ts';
import { toTypeName, toGrammarTypeName } from '../naming.ts';
import { structuralNodes, leafKindsOf } from './utils.ts';

export interface EmitTypeTestsConfig {
	grammar?: string;
	nodes: HydratedNodeModel[];
}

export function emitTypeTests(config: EmitTypeTestsConfig): string {
	const nodes = structuralNodes(config.nodes);
	const leafKinds = leafKindsOf(config.nodes);

	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('// Type-level test: verifies generated types are well-formed');
	lines.push('');

	// Import concrete types and derived types
	const typeImports = new Set<string>();
	for (const n of nodes) {
		const tn = toTypeName(n.kind);
		typeImports.add(tn);
		typeImports.add(`${tn}Config`);
		typeImports.add(`${tn}Tree`);
		typeImports.add(`${tn}FromInput`);
	}
	for (const k of leafKinds) {
		const tn = toTypeName(k);
		typeImports.add(tn);
		typeImports.add(`${tn}Tree`);
	}
	lines.push(`import type { ${[...typeImports].sort().join(', ')} } from './types.js';`);
	lines.push("import type { ConfigOf } from '@sittir/types';");
	lines.push('');

	// Assignability helpers
	lines.push('// Compile-time assignability — errors here mean the types diverged');
	lines.push('type Extends<A, B> = A extends B ? true : false;');
	lines.push('type Assert<T extends true> = T;');
	lines.push('');

	// 1. Concrete interfaces have correct `type` literal
	lines.push('// --- Concrete interface `type` literal ---');
	for (const node of nodes) {
		const tn = toTypeName(node.kind);
		lines.push(`export type _Type_${tn} = Assert<Extends<${tn}['type'], '${node.kind}'>>;`);
	}
	for (const kind of leafKinds) {
		const tn = toTypeName(kind);
		lines.push(`export type _Type_${tn} = Assert<Extends<${tn}['type'], '${kind}'>>;`);
	}
	lines.push('');

	// 2. ConfigOf<T> produces the *Config type
	lines.push('// --- ConfigOf<T> assignable to *Config ---');
	for (const node of nodes) {
		const tn = toTypeName(node.kind);
		lines.push(`export type _Config_${tn} = Assert<Extends<ConfigOf<${tn}>, ${tn}Config>>;`);
	}
	lines.push('');

	// 3. TreeNode types are well-formed (have `type` literal)
	lines.push('// --- TreeNode types have correct `type` ---');
	for (const node of nodes) {
		const tn = toTypeName(node.kind);
		lines.push(`export type _Tree_${tn} = Assert<Extends<${tn}Tree['type'], '${node.kind}'>>;`);
	}
	for (const kind of leafKinds) {
		const tn = toTypeName(kind);
		lines.push(`export type _Tree_${tn} = Assert<Extends<${tn}Tree['type'], '${kind}'>>;`);
	}
	lines.push('');

	return lines.join('\n');
}

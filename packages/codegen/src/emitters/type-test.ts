/**
 * Type-level test emitter.
 *
 * Generates a .ts file that verifies:
 *   1. Each *Fields interface ≡ NodeFields<K>
 *   2. Each *Node type = N & FluentSetters<Fields, Excluded, Self> & NodeMethods<K>
 *   3. Each *Config type = *Fields
 *   4. Each *FromInput interface ≡ NodeFromInput<K>
 *
 * If types diverge, `tsc` / `tsgo` will report errors.
 */

import type { KindMeta } from '../grammar-reader.ts';
import { toTypeName } from '../naming.ts';
import { selectConstructorField } from './utils.ts';

export interface EmitTypeTestsConfig {
	nodes: KindMeta[];
}

export function emitTypeTests(config: EmitTypeTestsConfig): string {
	const { nodes } = config;

	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('// Type-level test: verifies generated types match recursive generics');
	lines.push('');
	lines.push("import type { NodeFields, NodeFromInput } from './types.js';");
	lines.push("import type { FluentSetters, NodeMethods } from '@sittir/types';");

	// Import all needed types from types.ts
	const typeImports: string[] = [];
	for (const n of nodes) {
		const tn = toTypeName(n.kind);
		typeImports.push(tn, `${tn}Fields`, `${tn}FromInput`);
	}
	lines.push(`import type { ${typeImports.sort().join(', ')} } from './types.js';`);

	// Import generated types from factories.ts
	const factoryImports: string[] = [];
	for (const n of nodes) {
		const tn = toTypeName(n.kind);
		factoryImports.push(`${tn}Config`, `${tn}Node`);
	}
	lines.push(`import type { ${factoryImports.sort().join(', ')} } from './factories.js';`);
	lines.push('');

	// Assignability helpers
	lines.push('// Compile-time assignability — errors here mean the types diverged');
	lines.push('type IsExact<A, B> = [A] extends [B] ? [B] extends [A] ? true : false : false;');
	lines.push('type Assert<T extends true> = T;');
	lines.push('');

	// 1. Fields ≡ NodeFields<K>
	lines.push('// --- *Fields ≡ NodeFields<K> ---');
	for (const node of nodes) {
		const tn = toTypeName(node.kind);
		lines.push(`export type _Fields_${tn} = Assert<IsExact<${tn}Fields, NodeFields<'${node.kind}'>>>;`);
	}
	lines.push('');

	// 2. Config = Fields
	lines.push('// --- *Config = *Fields ---');
	for (const node of nodes) {
		const tn = toTypeName(node.kind);
		lines.push(`export type _Config_${tn} = Assert<IsExact<${tn}Config, ${tn}Fields>>;`);
	}
	lines.push('');

	// 3. Node = N & FluentSetters<Fields, Excluded, Self> & NodeMethods<K>
	lines.push('// --- *Node = N & FluentSetters<Fields, Excluded, Node> & NodeMethods<K> ---');
	for (const node of nodes) {
		const tn = toTypeName(node.kind);
		const ctorField = selectConstructorField(node);
		const excluded = ctorField ? `'${ctorField.name}'` : 'never';
		lines.push(`export type _Node_${tn} = Assert<IsExact<${tn}Node, ${tn} & FluentSetters<${tn}Fields, ${excluded}, ${tn}Node> & NodeMethods<'${node.kind}'>>>;`);
	}
	lines.push('');

	// 4. FromInput ≡ NodeFromInput<K>
	lines.push('// --- *FromInput ≡ NodeFromInput<K> ---');
	for (const node of nodes) {
		const tn = toTypeName(node.kind);
		lines.push(`export type _FromInput_${tn} = Assert<IsExact<${tn}FromInput, NodeFromInput<'${node.kind}'>>>;`);
	}
	lines.push('');

	return lines.join('\n');
}

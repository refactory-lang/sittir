function grammarExtensions(grammar: string): string[] {
	switch (grammar) {
		case 'rust': return ['rs'];
		case 'typescript': return ['ts', 'tsx'];
		case 'python': return ['py'];
		case 'javascript': return ['js', 'jsx'];
		case 'go': return ['go'];
		case 'java': return ['java'];
		case 'c': return ['c', 'h'];
		case 'cpp': return ['cpp', 'cc', 'cxx', 'hpp', 'hxx', 'h'];
		default: return [];
	}
}

/**
 * Render template emitter.
 *
 * Walks NodeModel.elements to produce S-expression render templates
 * + field metadata for the @sittir/core render engine.
 *
 * The template uses tree-sitter query syntax:
 *   (function_item "fn" name: (_) "(" parameters: (_)* ")" return_type: (_)? body: (_))
 */

import type { NodeModel, NodeElement } from '../grammar-model.ts';
import { type StructuralNode, structuralNodes, fieldsOf } from './utils.ts';

export interface EmitRulesConfig {
	grammar: string;
	node: StructuralNode;
}

export interface EmittedRule {
	template: string;
	fields: Record<string, { required: boolean; multiple?: boolean; sep?: string }>;
}

/**
 * Emit a render rule (S-expression template + field metadata) for a single node kind.
 * Consumes the pre-computed NodeModel.elements instead of walking raw GrammarRule.
 */
export function emitRule(config: EmitRulesConfig): EmittedRule {
	const { node } = config;
	const nodeFields = fieldsOf(node);

	const fields: Record<string, { required: boolean; multiple?: boolean; sep?: string }> = {};
	for (const f of nodeFields) {
		fields[f.name] = {
			required: f.required,
			...(f.multiple ? { multiple: true } : {}),
			...(f.separator ? { sep: f.separator } : {}),
		};
	}

	const hasChildren = node.children != null;
	const sexprs = elementsToSExpr(node.elements, false);

	// Deduplicate: keep first occurrence of each field
	const seen = new Set<string>();
	const deduped = sexprs.filter(el => {
		if (el === '(_)*') return true;
		const fieldMatch = el.match(/^(\w+): /);
		if (fieldMatch) {
			if (seen.has(fieldMatch[1]!)) return false;
			seen.add(fieldMatch[1]!);
		}
		return true;
	});

	// If node has children but none were captured, append at end
	if (hasChildren && !deduped.some(e => e === '(_)*')) {
		deduped.push('(_)*');
	}

	// Deduplicate consecutive (_)* entries
	const final = deduped.filter((el, i) => !(el === '(_)*' && deduped[i - 1] === '(_)*'));

	const template = cleanTemplate(`(${node.kind} ${final.join(' ')})`);
	return { template, fields };
}

/**
 * Convert NodeElements to S-expression parts.
 */
function elementsToSExpr(elements: NodeElement[], optional: boolean): string[] {
	const parts: string[] = [];
	for (const elem of elements) {
		switch (elem.element) {
			case 'field': {
				const q = !elem.field.required ? '?' : elem.field.multiple ? '*' : '';
				parts.push(`${elem.field.name}: (_)${q}`);
				break;
			}
			case 'token':
				// Skip tokens inside optional branches — they're contextual
				if (!optional && !elem.optional) {
					parts.push(`"${escapeQuotes(elem.value)}"`);
				}
				break;
			case 'child':
				parts.push('(_)*');
				break;
			case 'choice': {
				// Pick the branch with the most fields
				let best: string[] = [];
				let bestCount = -1;
				for (const branch of elem.branches) {
					const branchParts = elementsToSExpr(branch, optional);
					const fieldCount = branchParts.filter(e => e.includes(': (_)')).length;
					if (fieldCount > bestCount) {
						bestCount = fieldCount;
						best = branchParts;
					}
				}
				parts.push(...best);
				break;
			}
		}
	}
	return parts;
}

function cleanTemplate(template: string): string {
	return template.replace(/\s+/g, ' ').trim();
}

function escapeQuotes(s: string): string {
	return s.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

/**
 * Emit the full rules registry source for all node kinds.
 */
export function emitRules(config: {
	grammar: string;
	nodes: NodeModel[];
}): string {
	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('');
	lines.push("import type { RulesRegistry } from '@sittir/types';");
	lines.push('');
	lines.push('export const rules: RulesRegistry = {');

	for (const node of structuralNodes(config.nodes)) {
		const rule = emitRule({ grammar: config.grammar, node });
		// Use single quotes and escape internal single quotes
		const escaped = rule.template.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
		lines.push(`  '${node.kind}': '${escaped}',`);
	}

	lines.push('};');
	return lines.join('\n');
}

/**
 * Test scaffold emitter — consumes NodeMap directly.
 * Generates per-kind tests: factory produces correct type, render returns non-empty.
 */

import type { NodeMap } from '../compiler/types.ts'
import type { AssembledNode, AssembledField } from '../compiler/node-map.ts'
import { isValidIdent, isAutoStampField, isAutoStampSlot, isRequired, isMultiple, isNonEmpty, slotKindNames, resolveHoistedForm } from './shared.ts'

export interface EmitTestsConfig {
    grammar: string
    nodeMap: NodeMap
}

export function emitTests(config: EmitTestsConfig): string {
    const { grammar, nodeMap } = config

    const lines: string[] = [
        '// Auto-generated by @sittir/codegen — do not edit',
        "import { describe, it, expect } from 'vitest';",
        "import { ir } from '../src/ir.js';",
        '',
    ]

    // Branch/container/polymorph tests
    for (const [kind, node] of nodeMap.nodes) {
        if (kind.startsWith('_')) continue
        if (!node.factoryName) continue
        const key = node.irKey
        if (!key) continue // synthesised group or skipped kind
        // Skip kinds whose irKey isn't a valid JS identifier — those are
        // anonymous tokens that surface as leaves but can't be accessed
        // via `ir.<key>(...)` syntax. The external externals-inheritance
        // pass surfaces new such kinds for grammars that declare them.
        if (!isValidIdent(key)) continue
        if (nodeMap.polymorphFormKinds.has(kind)) continue

        // Text-template branches (e.g. rust `raw_string_literal`) surface
        // through `factory-map.json5`'s `factoryShapes: "text"` and the
        // generated factory signature is `fn(text: string)`, not a Config
        // object. A node-model branch model with an all-external-token
        // content structure is the trigger — invoke it with a string.
        if (node.modelType === 'branch' && node.isTextTemplate(nodeMap.externals)) {
            emitTextTemplateBranchTest(lines, kind, key)
            continue
        }

        switch (node.modelType) {
            case 'branch':
                emitBranchTest(lines, node, kind, key, nodeMap)
                break
            case 'container':
                emitContainerTest(lines, node, kind, key)
                break
            case 'polymorph':
                emitPolymorphTest(lines, node, kind, key, nodeMap)
                break
            case 'leaf':
                emitLeafTest(lines, node, kind, key)
                break
            case 'keyword':
                emitKeywordTest(lines, node, kind, key)
                break
            case 'enum':
                emitEnumTest(lines, node, kind, key)
                break
        }
    }

    return lines.join('\n')
}

function emitBranchTest(lines: string[], node: AssembledNode, kind: string, key: string, nodeMap: NodeMap): void {
    if (node.modelType !== 'branch') return

    lines.push(`describe('${kind}', () => {`)
    lines.push(`  it('factory produces correct type', () => {`)

    // Build two configs. The type-check test uses the minimal config — only
    // required, non-auto-stamp fields/children. Auto-stamp slots are excluded
    // (the factory stamps them directly; supplying would be a type error).
    //
    // The render test needs NON-EMPTY output, which the minimal config can
    // fail to produce for kinds whose children are all optional (repeat(...)
    // with every alt optional — calling ir.k({}) produces an empty repeat
    // that renders to ""). So the render test unconditionally injects a dummy
    // children element when the kind has a children slot at all, escaping
    // the type via `as any`.
    const typeConfigParts: string[] = []
    for (const f of node.fields) {
        if (isRequired(f) && !isAutoStampField(f, nodeMap)) {
            typeConfigParts.push(`${f.propertyName}: ${dummyValue(f)}`)
        }
    }
    if (node.children && node.children.length > 0) {
        const hasNonAutoStampRequired = node.children.some(
            c => isRequired(c) && !isAutoStampSlot(c, nodeMap),
        )
        if (hasNonAutoStampRequired) {
            const firstKind = slotKindNames(node.children[0]!)[0]
            const dummy = firstKind
                ? `{ $type: '${firstKind}', $text: 'test' } as any`
                : `'test' as any`
            typeConfigParts.push(`children: [${dummy}] as any`)
        }
    }
    const renderConfigParts = [...typeConfigParts]
    if (node.children && node.children.length > 0 && !renderConfigParts.some(p => p.startsWith('children'))) {
        const firstKind = slotKindNames(node.children[0]!)[0]
        const dummy = firstKind
            ? `{ $type: '${firstKind}', $text: 'test' } as any`
            : `'test' as any`
        renderConfigParts.push(`children: [${dummy}] as any`)
    }

    const typeConfigArg = typeConfigParts.length > 0 ? `{ ${typeConfigParts.join(', ')} }` : '{}'
    const renderConfigArg = renderConfigParts.length > 0 ? `{ ${renderConfigParts.join(', ')} }` : '{}'
    lines.push(`    const node = ir.${key}(${typeConfigArg});`)
    lines.push(`    expect(node.$type).toBe('${kind}');`)
    lines.push(`    expect(node.$source).toBe('factory');`)
    lines.push('  });')

    // Render test. Two variants depending on whether the minimal config
    // produces renderable content:
    //
    // - If renderConfig has any injected content (required fields,
    //   required children, or a dummy child for kinds with a children
    //   slot), the render output is expected to be non-empty.
    //
    // - If renderConfig is `{}` (no required content and no children
    //   slot — kinds whose fields are ALL optional, like self_parameter
    //   or field_pattern_shorthand), rendering with no input legitimately
    //   produces an empty string. We still invoke render() to catch
    //   template-walker crashes, but don't assert non-empty — the empty
    //   output is the correct behavior.
    const hasRenderContent = renderConfigArg !== '{}'
    if (hasRenderContent) {
        lines.push(`  it('render produces non-empty string', () => {`)
        lines.push(`    const node = ir.${key}(${renderConfigArg});`)
        lines.push(`    expect(node.render().length).toBeGreaterThan(0);`)
        lines.push('  });')
    } else {
        lines.push(`  it('render does not throw on minimal config', () => {`)
        lines.push(`    const node = ir.${key}(${renderConfigArg});`)
        lines.push(`    expect(() => node.render()).not.toThrow();`)
        lines.push('  });')
    }

    lines.push('});')
    lines.push('')
}

function emitContainerTest(lines: string[], node: AssembledNode, kind: string, key: string): void {
    if (node.modelType !== 'container') return

    // Container factories take positional args: singular-child
    // containers require one `child?` and repeated containers take
    // `...children` rest args. We need a placeholder element when:
    //   - the singular child is required, OR
    //   - any multi children slot is `nonEmpty` (repeat1-sourced)
    //     — the factory's `_assertNonEmpty` helper throws on empty
    //     input, so the no-arg form `ir.kind()` would fail at
    //     runtime even though it type-checks.
    const first = node.children[0]
    const requiredSingular = first && !isMultiple(first) && isRequired(first)
    const anyNonEmpty = node.children.some(c => isNonEmpty(c))
    const firstKindName = first ? slotKindNames(first)[0] : undefined
    const placeholder = (requiredSingular || anyNonEmpty) && firstKindName
        ? `{ type: ${JSON.stringify(firstKindName)} } as never`
        : ''

    lines.push(`describe('${kind}', () => {`)
    lines.push(`  it('factory produces correct type', () => {`)
    lines.push(`    const node = ir.${key}(${placeholder});`)
    lines.push(`    expect(node.$type).toBe('${kind}');`)
    lines.push(`    expect(node.$source).toBe('factory');`)
    lines.push('  });')
    lines.push('});')
    lines.push('')
}

function emitPolymorphTest(lines: string[], node: AssembledNode, kind: string, key: string, nodeMap: NodeMap): void {
    if (node.modelType !== 'polymorph') return

    // Single-form polymorphs emit ir.<key> as a direct function (no
    // `.formName` namespace) — the ir bundleExpr only attaches variant
    // accessors when forms.length > 1. Generate a single direct test
    // instead of per-form variant tests.
    const useDirectCall = node.forms.length <= 1
    lines.push(`describe('${kind}', () => {`)
    for (const form of node.forms) {
        const itLabel = useDirectCall ? 'factory produces correct type' : `${form.name} form produces correct type`
        lines.push(`  it('${itLabel}', () => {`)
        // Hoisted forms surface the inner child's fields at the top level
        // — combine those with the form's own fields. A form can have BOTH
        // direct fields (e.g. `name` on `MacroDefinitionUFormParen`) AND
        // hoisted inner fields (e.g. `body` from the inner container). The
        // earlier "either-or" branch dropped form-level fields whenever
        // hoisting fired and produced `ir.macro.paren({})` even though the
        // factory required `name`.
        const hoist = resolveHoistedForm(form, nodeMap)
        const allFields: AssembledField[] = [...form.fields]
        if (hoist) allFields.push(...hoist.innerFields)
        const configParts = allFields
            .filter(f => isRequired(f) && !isAutoStampField(f, nodeMap))
            .map(f => `${f.propertyName}: ${dummyValue(f)}`)
        // Container-shaped hoist targets: inner factory accepts `...children`
        // via the form's `children` surface. The inner container may assert
        // non-empty so supply a single dummy child of the slot's first kind.
        if (hoist && hoist.innerFields.length === 0) {
            const innerChildNonEmpty = resolveInnerContainerNonEmptyChild(hoist.innerNode)
            if (innerChildNonEmpty) configParts.push(`children: [${innerChildNonEmpty}]`)
        }
        const configArg = configParts.length > 0 ? `{ ${configParts.join(', ')} }` : '{}'
        const callExpr = useDirectCall
            ? `ir.${key}(${configArg})`
            : `ir.${key}.${form.name}(${configArg})`
        lines.push(`    const node = ${callExpr};`)
        lines.push(`    expect(node.$type).toBe('${kind}');`)
    lines.push(`    expect(node.$source).toBe('factory');`)
        lines.push('  });')
    }
    lines.push('});')
    lines.push('')
}

function emitTextTemplateBranchTest(lines: string[], kind: string, key: string): void {
    lines.push(`describe('${kind}', () => {`)
    lines.push(`  it('factory produces correct type', () => {`)
    lines.push(`    const node = ir.${key}('test');`)
    lines.push(`    expect(node.$type).toBe('${kind}');`)
    lines.push(`    expect(node.$source).toBe('factory');`)
    lines.push('  });')
    lines.push(`  it('render produces non-empty string', () => {`)
    lines.push(`    const node = ir.${key}('test');`)
    lines.push(`    expect(node.render().length).toBeGreaterThan(0);`)
    lines.push('  });')
    lines.push('});')
    lines.push('')
}

function emitLeafTest(lines: string[], node: AssembledNode, kind: string, key: string): void {
    // Find a sample text that satisfies the leaf's regex pattern (if
    // any). The factory enforces patterns at runtime now — passing
    // `'test'` to a shebang or metavariable factory would throw at
    // construction time. Try a list of common shapes against the
    // pattern and pick the first match; if none match, the leaf has
    // an exotic shape and we skip the construction test (the regex
    // check itself is the test).
    const pattern = node.modelType === 'leaf' ? node.pattern : undefined
    const sample = pickSampleForPattern(pattern)
    if (sample === null) {
        // No working sample found — skip this leaf's construction
        // test rather than emit a known-failing assertion. The
        // pattern guard is exercised by other tests anyway.
        return
    }
    lines.push(`describe('${kind}', () => {`)
    lines.push(`  it('factory produces correct type', () => {`)
    lines.push(`    const node = ir.${key}(${JSON.stringify(sample)});`)
    lines.push(`    expect(node.$type).toBe('${kind}');`)
    lines.push(`    expect(node.$source).toBe('factory');`)
    lines.push(`    expect(node.$text).toBe(${JSON.stringify(sample)});`)
    lines.push('  });')
    lines.push('});')
    lines.push('')
}

/**
 * Pick a sample string that satisfies a tree-sitter leaf pattern.
 * Tries a handful of common shapes, returning the first that matches
 * (anchored full-string). When `pattern` is undefined the leaf accepts
 * arbitrary text and `'test'` is fine. Returns `null` when no
 * candidate matches and the test should be skipped.
 */
function pickSampleForPattern(pattern: string | undefined): string | null {
    if (!pattern) return 'test'
    // Common candidates ordered loosely from "most likely to match
    // an identifier-ish leaf" to "specific token shapes".
    const candidates = [
        'test', 'a', 'x', '1', '0', '_a',
        '$x', '#!/bin/sh', '/* */', '"hello"', "'h'",
        '0x1', '1.0', '1e0', '1u32', '\n', ' ',
    ]
    let re: RegExp | null = null
    try { re = new RegExp(`^(?:${pattern})$`, 'u') } catch {
        try { re = new RegExp(`^(?:${pattern})$`) } catch { return null }
    }
    for (const c of candidates) {
        if (re.test(c)) return c
    }
    return null
}

function emitKeywordTest(lines: string[], node: AssembledNode, kind: string, key: string): void {
    if (node.modelType !== 'keyword') return
    lines.push(`describe(${JSON.stringify(kind)}, () => {`)
    lines.push(`  it('factory produces keyword', () => {`)
    lines.push(`    const node = ir.${key}();`)
    lines.push(`    expect(node.$type).toBe(${JSON.stringify(kind)});`)
    lines.push(`    expect(node.$source).toBe('factory');`)
    lines.push(`    expect(node.$text).toBe(${JSON.stringify(node.text)});`)
    lines.push('  });')
    lines.push('});')
    lines.push('')
}

function emitEnumTest(lines: string[], node: AssembledNode, kind: string, key: string): void {
    if (node.modelType !== 'enum') return
    const first = node.values[0]
    if (!first) return
    lines.push(`describe('${kind}', () => {`)
    lines.push(`  it('factory accepts valid value', () => {`)
    lines.push(`    const node = ir.${key}('${first.replace(/'/g, "\\'")}');`)
    lines.push(`    expect(node.$type).toBe('${kind}');`)
    lines.push(`    expect(node.$source).toBe('factory');`)
    lines.push('  });')
    lines.push('});')
    lines.push('')
}

/**
 * Synthesize a dummy-child expression for a container-shaped hoisted
 * polymorph-form inner node whose first child slot is required and
 * non-empty, so the auto-generated polymorph form test doesn't fail
 * the inner factory's `_assertNonEmpty` check.
 *
 * @remarks
 * Containers have no fields so `innerFields` in the hoist target is
 * empty — the test emitter would otherwise pass `{}` to the form
 * factory, spread zero children into the inner factory, and trip the
 * assertion. Returns `null` when the container has no required
 * non-empty child slot (one dummy isn't needed) or when no content
 * kind is available.
 */
function resolveInnerContainerNonEmptyChild(innerNode: AssembledNode): string | null {
    // Only container-shaped inners go through this path — others surface
    // their data via fields.
    if (innerNode.modelType !== 'container') return null
    const childSlots = innerNode.children
    if (!childSlots || childSlots.length === 0) return null
    const firstRequired = childSlots.find(
        c => isRequired(c) && isNonEmpty(c)
    )
    if (!firstRequired) return null
    const kinds = slotKindNames(firstRequired)
    if (kinds.length === 0) return null
    return `{ $type: '${kinds[0]}', $text: 'test' } as any`
}

function dummyValue(field: AssembledField): string {
    // Multiple fields need a non-empty dummy array so templates with
    // `$FIELD`/`joinBy` produce non-empty output; otherwise the generated
    // `render produces non-empty string` test fails for kinds where
    // every required field is multiple.
    const kinds = slotKindNames(field)
    if (isMultiple(field)) {
        if (kinds.length > 0) {
            return `[{ $type: '${kinds[0]}', $text: 'test' } as any]`
        }
        return `['test' as any]`
    }
    if (kinds.length > 0) {
        // Use first content type to generate a dummy
        return `{ $type: '${kinds[0]}', $text: 'test' } as any`
    }
    return "'test' as any"
}


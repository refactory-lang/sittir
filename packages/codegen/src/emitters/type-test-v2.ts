/**
 * Type-level test emitter — consumes NodeMap directly.
 * Generates compile-time assertions verifying generated types are well-formed.
 */

import type { NodeMap } from '../compiler/rule.ts'

export interface EmitTypeTestsFromNodeMapConfig {
    nodeMap: NodeMap
}

export function emitTypeTestsFromNodeMap(config: EmitTypeTestsFromNodeMapConfig): string {
    const { nodeMap } = config

    const structuralKinds: { kind: string; typeName: string; hasVariants: boolean }[] = []
    const leafKinds: { kind: string; typeName: string }[] = []

    for (const [kind, node] of nodeMap.nodes) {
        switch (node.modelType) {
            case 'branch':
            case 'container':
                structuralKinds.push({ kind, typeName: node.typeName, hasVariants: false })
                break
            case 'polymorph':
                structuralKinds.push({ kind, typeName: node.typeName, hasVariants: node.forms.length > 1 })
                break
            case 'leaf':
            case 'keyword':
            case 'enum':
                leafKinds.push({ kind, typeName: node.typeName })
                break
        }
    }

    const lines: string[] = [
        '// Auto-generated by @sittir/codegen — do not edit',
        '// Type-level test: verifies generated types are well-formed',
        '',
    ]

    // Imports
    const typeImports = new Set<string>()
    for (const s of structuralKinds) {
        typeImports.add(s.typeName)
        typeImports.add(`${s.typeName}Config`)
        typeImports.add(`${s.typeName}Tree`)
        typeImports.add(`${s.typeName}FromInput`)
    }
    for (const l of leafKinds) {
        typeImports.add(l.typeName)
        typeImports.add(`${l.typeName}Tree`)
    }
    lines.push(`import type { ${[...typeImports].sort().join(', ')} } from './types.js';`)
    lines.push("import type { ConfigOf } from '@sittir/types';")
    lines.push('')

    // Helpers
    lines.push('type Extends<A, B> = A extends B ? true : false;')
    lines.push('type Assert<T extends true> = T;')
    lines.push('')

    // 1. type literal checks
    lines.push('// --- Concrete interface `type` literal ---')
    for (const s of structuralKinds) {
        lines.push(`export type _Type_${s.typeName} = Assert<Extends<${s.typeName}['type'], '${s.kind}'>>;`)
    }
    for (const l of leafKinds) {
        lines.push(`export type _Type_${l.typeName} = Assert<Extends<${l.typeName}['type'], '${l.kind}'>>;`)
    }
    lines.push('')

    // 2. ConfigOf checks (skip polymorphs with variants)
    lines.push('// --- ConfigOf<T> assignable to *Config ---')
    for (const s of structuralKinds) {
        if (s.hasVariants) continue
        lines.push(`export type _Config_${s.typeName} = Assert<Extends<ConfigOf<${s.typeName}>, ${s.typeName}Config>>;`)
    }
    lines.push('')

    // 3. TreeNode type literal checks
    lines.push('// --- TreeNode types have correct `type` ---')
    for (const s of structuralKinds) {
        lines.push(`export type _Tree_${s.typeName} = Assert<Extends<${s.typeName}Tree['type'], '${s.kind}'>>;`)
    }
    for (const l of leafKinds) {
        lines.push(`export type _Tree_${l.typeName} = Assert<Extends<${l.typeName}Tree['type'], '${l.kind}'>>;`)
    }
    lines.push('')

    return lines.join('\n')
}

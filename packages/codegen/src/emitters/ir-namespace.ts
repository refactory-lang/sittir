/**
 * Emits ir.ts — the developer-facing namespace that re-exports
 * all factories with short names, plus keyword and operator aliases.
 *
 * Branch factories are combined with their .from() counterparts via
 * Object.assign, so ir.function.from(...) works.
 *
 * Naming: toShortName strips suffixes (function_item → function),
 * toIrKey converts to camelCase (if_expression → ifExpression).
 * Trailing underscores dropped in the ir namespace (function_ → function).
 */

import type { HydratedNodeModel } from '../node-model.ts';
import { isTupleChildren } from '../node-model.ts';
import { toIrKey, toFactoryName, toRawFactoryName, toShortName, toTypeName } from '../naming.ts';
import { structuralNodes, leafKindsOf, keywordKindsOf, fieldsOf, escapeString } from './utils.ts';
import { buildProjectionContext, projectKinds, type ProjectionContext } from './kind-projections.ts';

export interface EmitIrNamespaceConfig {
	grammar: string;
	nodes: HydratedNodeModel[];
}

export function emitIrNamespace(config: EmitIrNamespaceConfig): string {
	const allNodes = config.nodes;
	const branchKinds = structuralNodes(allNodes).map(n => n.kind);
	const leafKinds = leafKindsOf(allNodes);
	const keywordKinds = keywordKindsOf(allNodes);
	const ctx = buildProjectionContext(new Map(allNodes.map(n => [n.kind, n])));
	const operatorContexts = deriveOperatorContexts(allNodes, ctx);

	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('');

	// Collect all factory imports from factories.ts
	// Reserved words use _prefix internal name from factories.ts
	const factoryImports: string[] = [];
	const importName = toRawFactoryName;
	const modelMap = new Map(allNodes.map(n => [n.kind, n]));
	for (const kind of branchKinds) {
		factoryImports.push(importName(kind));
		// Add variant factory imports
		const node = modelMap.get(kind)!;
		const variants = (node.modelType === 'branch' || node.modelType === 'container') ? node.variants : undefined;
		if (variants && variants.length > 1) {
			for (const v of variants) {
				factoryImports.push(`${importName(kind)}_${v.name}_`);
			}
		}
	}
	for (const kind of leafKinds) {
		if (!keywordKinds.has(kind)) {
			factoryImports.push(importName(kind));
		}
	}
	for (const kind of keywordKinds.keys()) {
		factoryImports.push(importName(kind));
	}

	lines.push(`import { ${factoryImports.join(', ')} } from './factories.js';`);

	// Detect children-only nodes (skip variant from functions for these)
	const childrenOnlyKinds = new Set<string>();
	for (const kind of branchKinds) {
		const n = modelMap.get(kind)!;
		if (n.modelType !== 'branch' && n.modelType !== 'container') continue;
		const nFields = fieldsOf(n);
		if (nFields.length === 0 && n.children != null && !isTupleChildren(n.children)) {
			childrenOnlyKinds.add(kind);
		}
	}

	// Import .from() functions from from.ts (including variant from functions)
	const fromImports: string[] = [];
	for (const kind of branchKinds) {
		fromImports.push(`${toFactoryName(kind)}From`);
		if (childrenOnlyKinds.has(kind)) continue;
		const node = modelMap.get(kind)!;
		const variants = (node.modelType === 'branch' || node.modelType === 'container') ? node.variants : undefined;
		if (variants && variants.length > 1) {
			for (const v of variants) {
				fromImports.push(`${toFactoryName(kind)}${toTypeName(v.name)}From`);
			}
		}
	}
	lines.push(`import { ${fromImports.join(', ')} } from './from.js';`);
	lines.push('');

	// Build ir namespace entries
	// Track used keys for collision detection
	const usedKeys = new Map<string, string>(); // irKey → kind

	lines.push('export const ir = {');

	// Branch factories — combined with .from() and variant factories via Object.assign
	lines.push('  // Branch node factories');
	for (const kind of branchKinds) {
		const irKey = resolveIrKey(kind, usedKeys);
		const fromFn = `${toFactoryName(kind)}From`;
		const node = modelMap.get(kind)!;
		const variants = (node.modelType === 'branch' || node.modelType === 'container') ? node.variants : undefined;
		if (variants && variants.length > 1 && !childrenOnlyKinds.has(kind)) {
			// Each variant gets its own factory + from, attached as properties on the base factory
			const variantEntries = variants
				.filter(v => v.name !== 'from')
				.map(v => {
					const vFactory = `${importName(kind)}_${v.name}_`;
					const vFrom = `${toFactoryName(kind)}${toTypeName(v.name)}From`;
					return `${v.name}: Object.assign(${vFactory}, { from: ${vFrom} })`;
				});
			const extras = variantEntries.length > 0 ? `, ${variantEntries.join(', ')}` : '';
			lines.push(`  ${irKey}: Object.assign(${importName(kind)}, { from: ${fromFn}${extras} }),`);
		} else {
			lines.push(`  ${irKey}: Object.assign(${importName(kind)}, { from: ${fromFn} }),`);
		}
	}

	lines.push('');

	// Keyword factories (zero-arg, fixed text)
	lines.push('  // Keyword factories');
	for (const [kind] of keywordKinds) {
		const irKey = resolveIrKey(kind, usedKeys);
		lines.push(`  ${irKey}: ${importName(kind)},`);
	}

	lines.push('');

	// Leaf factories (text-arg)
	lines.push('  // Leaf node factories');
	for (const kind of leafKinds) {
		if (keywordKinds.has(kind)) continue; // already in keywords
		const irKey = resolveIrKey(kind, usedKeys);
		lines.push(`  ${irKey}: ${importName(kind)},`);
	}

	// Semantic operator aliases
	if (operatorContexts.length > 0) {
		lines.push('');
		lines.push('  // Semantic operator aliases');
		for (const ctx of operatorContexts) {
			for (const token of ctx.tokens) {
				const alias = operatorAlias(token);
				if (alias && !usedKeys.has(alias)) {
					usedKeys.set(alias, `operator:${token}`);
					lines.push(`  ${alias}: () => ({ type: '${ctx.field}', fields: {}, text: '${escapeString(token)}' }),`);
				}
			}
		}
	}

	lines.push('} as const;');
	return lines.join('\n');
}

/**
 * Resolve an ir key, handling collisions by falling back to the full factory name.
 */
function resolveIrKey(kind: string, usedKeys: Map<string, string>): string {
	const short = toIrKey(kind);
	if (!usedKeys.has(short)) {
		usedKeys.set(short, kind);
		return short;
	}
	// Collision — use full factory name
	const full = toFactoryName(kind);
	usedKeys.set(full, kind);
	return full;
}

/** Map common operator tokens to semantic aliases. */
function operatorAlias(token: string): string | undefined {
	const aliases: Record<string, string> = {
		'&&': 'and', '||': 'or',
		'&': 'bitAnd', '|': 'bitOr', '^': 'bitXor',
		'==': 'eq', '!=': 'neq',
		'<': 'lt', '<=': 'lte', '>': 'gt', '>=': 'gte',
		'<<': 'shl', '>>': 'shr',
		'+': 'add', '-': 'sub', '*': 'mul', '/': 'div', '%': 'mod',
		'+=': 'addAssign', '-=': 'subAssign', '*=': 'mulAssign', '/=': 'divAssign',
		'%=': 'modAssign', '&=': 'bitAndAssign', '|=': 'bitOrAssign', '^=': 'bitXorAssign',
		'<<=': 'shlAssign', '>>=': 'shrAssign',
	};
	return aliases[token];
}

/** Derive operator contexts from the model — fields with anonTokens. */
function deriveOperatorContexts(nodes: HydratedNodeModel[], ctx: ProjectionContext): { field: string; tokens: string[] }[] {
	const results: { field: string; tokens: string[] }[] = [];
	for (const node of nodes) {
		if (node.modelType !== 'branch' && node.modelType !== 'container') continue;
		for (const field of fieldsOf(node)) {
			const proj = projectKinds(field.kinds, ctx);
			if (proj.anonTokens.length > 0) {
				results.push({ field: field.name, tokens: proj.anonTokens });
			}
		}
	}
	return results;
}


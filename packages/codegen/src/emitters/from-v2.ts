/**
 * Emits from.ts — consumes NodeMap directly.
 *
 * Owns ALL `from()` resolver string generation. Rule.ts exposes the
 * IR; this file dispatches on `node.modelType` and emits the per-kind
 * resolver bodies plus the module-scoped helpers (_resolveOne,
 * _resolveMany, _resolveLeafString, _resolveByKind, _resolveScalar).
 */

import type {
    NodeMap, AssembledNode, AssembledField, AssembledGroup,
} from '../compiler/rule.ts'

/** Escape a string for safe inclusion inside a single-quoted TS string literal. */
function escForSource(s: string): string {
    return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'")
}

export interface EmitFromNodeMapConfig {
    grammar: string
    nodeMap: NodeMap
}

export function emitFromNodeMap(config: EmitFromNodeMapConfig): string {
    const { nodeMap } = config

    // T042i — dedup state. Each unique resolver kind list gets a
    // single module-scoped constant declaration; resolver call sites
    // reference that constant instead of repeating the literal array
    // inline. Supertypes get *named* constants (`_super_expression`)
    // — when a field's content exactly matches a supertype's subtype
    // set we reuse the supertype's name as the dedup identifier,
    // making the generated code readable and aligning the physical
    // constant with the grammar's own supertype declarations. Any
    // other list falls through to numbered `_K0`, `_K1`, …
    //
    // Reverse lookup: sorted-subtypes key → supertype constant name.
    const supertypeByKey = new Map<string, string>()
    for (const [kind, node] of nodeMap.nodes) {
        if (node.modelType !== 'supertype') continue
        if (node.subtypes.length === 0) continue
        const key = [...node.subtypes].sort().join('\n')
        // First occurrence wins — two supertypes sharing an exact
        // subtype set is rare and the first name is as good as any.
        if (!supertypeByKey.has(key)) {
            const safe = kind.replace(/^_+/, '').replace(/[^\w]/g, '_')
            supertypeByKey.set(key, `_super_${safe}`)
        }
    }
    const kindTableIndex = new Map<string, number>()
    const kindTableLiterals: string[] = []
    const namedEntries = new Map<string, string>()  // name → JSON literal
    const internKinds = (kinds: readonly string[]): string => {
        // Look up by sorted supertype signature first — gives readable
        // names for the common case.
        const superKey = [...kinds].sort().join('\n')
        const superName = supertypeByKey.get(superKey)
        if (superName !== undefined) {
            if (!namedEntries.has(superName)) {
                namedEntries.set(superName, JSON.stringify(kinds))
            }
            return superName
        }
        // Otherwise fall back to numbered dedup.
        const key = JSON.stringify(kinds)
        let idx = kindTableIndex.get(key)
        if (idx === undefined) {
            idx = kindTableLiterals.length
            kindTableIndex.set(key, idx)
            kindTableLiterals.push(key)
        }
        return `_K${idx}`
    }

    const lines: string[] = [
        '// Auto-generated by @sittir/codegen — do not edit',
        '',
    ]

    // Collect factory imports — types come in through a single
    // namespace-type import (`import type * as T from './types.js'`)
    // below, so per-name type listing isn't needed. Every type
    // reference in the emitted body is prefixed with `T.`.
    const factoryImports = new Set<string>()

    for (const [kind, node] of nodeMap.nodes) {
        if (kind.startsWith('_')) continue
        if (!node.factoryName) continue
        if (node.modelType === 'branch' || node.modelType === 'container' || node.modelType === 'polymorph') {
            factoryImports.add(node.rawFactoryName!)
            if (node.modelType === 'polymorph') {
                for (const form of node.forms) {
                    factoryImports.add(form.rawFactoryName!)
                }
            }
        }
        if (node.modelType === 'leaf' || node.modelType === 'keyword' || node.modelType === 'enum') {
            factoryImports.add(node.rawFactoryName!)
        }
    }

    lines.push(`import { ${[...factoryImports].sort().join(', ')} } from './factories.js';`)
    // Namespace import collapses hundreds of per-type lines into one.
    // Every type reference in the emitted body uses the `T.` prefix.
    lines.push(`import type * as T from './types.js';`)
    lines.push("import type { AnyNodeData, FromInputOf, NodeFieldValue } from '@sittir/types';")
    lines.push('')

    // ------------------------------------------------------------------
    // Shared input type + type guard for the resolver layer
    // ------------------------------------------------------------------
    // Every loose-from() caller can hand us:
    //   - a fully-built NodeData     (passthrough path)
    //   - a primitive                (leaf-factory dispatch)
    //   - a { kind, ...rest } object (kind-tagged dispatch)
    //   - an array of any of above   (multi-field slot)
    //   - undefined / null           (absent optional field)
    //
    // `_FromFieldInput` is the minimum closed union that captures every
    // one of those shapes without leaking `unknown`. The `{ kind, ... }`
    // arm is modeled as an index signature whose values recursively match
    // the same union so `{kind, ...rest}` destructuring preserves types.
    lines.push("/** Closed union of every shape a loose-from() field value can hold. */")
    lines.push("type _FromFieldInput =")
    lines.push("  | AnyNodeData")
    lines.push("  | string")
    lines.push("  | number")
    lines.push("  | boolean")
    lines.push("  | null")
    lines.push("  | undefined")
    lines.push("  | { readonly [key: string]: _FromFieldInput }")
    lines.push("  | readonly _FromFieldInput[];")
    lines.push("")
    // Type guard — parameter is `unknown` by convention for a narrowing
    // predicate. This isn't a cast *through* unknown (the rule we care
    // about); it's the legitimate entry point for classifying arbitrary
    // runtime values, same as every other `v is T` guard in the codebase.
    lines.push("function isNodeData(v: unknown): v is AnyNodeData {")
    lines.push("  if (v === null || v === undefined || typeof v !== 'object') return false;")
    lines.push("  if (Array.isArray(v)) return false;")
    lines.push("  const o = v as { readonly type?: unknown; readonly fields?: unknown; readonly text?: unknown };")
    lines.push("  return typeof o.type === 'string'")
    lines.push("    && (typeof o.fields === 'object' || typeof o.text === 'string');")
    lines.push('}')
    lines.push('')

    // Per-node from emission via local dispatch. The interner is
    // passed down so every field resolver call registers its kind
    // list through the same dedup table.
    const perNodeBlocks: string[] = []
    for (const [kind, node] of nodeMap.nodes) {
        if (kind.startsWith('_')) continue
        const source = renderFromForNode(node, nodeMap, internKinds)
        if (source === undefined) continue
        perNodeBlocks.push(source)
    }

    // _fromMap — runtime dispatch table. Same pattern as `_factoryMap`
    // in factories.ts: declared as a plain `as const` object so every
    // entry's type is inferred from the per-kind `fromX` signature.
    // `_FromMap = typeof _fromMap` gives consumers the precise per-slot
    // type without duplicating the kind→function mapping.
    //
    // Declared BEFORE the resolver helpers so `_resolveByKind<K>` can
    // reference `_FromMap[K]` / `_fromMap[kind]` in its signature — the
    // per-kind function declarations it points at are hoisted at both
    // the TS type level and the runtime level, so forward references
    // across the per-node blocks below resolve cleanly.
    lines.push('export const _fromMap = {')
    for (const [kind, node] of nodeMap.nodes) {
        if (kind.startsWith('_')) continue
        if (!node.factoryName) continue
        if (node.modelType === 'token' || node.modelType === 'supertype' || node.modelType === 'group') continue
        if (!node.fromFunctionName) continue
        lines.push(`  ${JSON.stringify(kind)}: ${node.fromFunctionName},`)
    }
    lines.push('} as const;')
    lines.push('export type _FromMap = typeof _fromMap;')
    lines.push('')

    // Loose-input resolver scaffolding — references `_fromMap` /
    // `_FromMap` above and every per-kind `fromX` defined below.
    emitResolverHelpers(lines, nodeMap)
    lines.push('')

    // Emit the dedup table BEFORE the per-node blocks so every
    // `_KN` / `_super_X` identifier is declared by the time it's
    // referenced.
    if (kindTableLiterals.length > 0 || namedEntries.size > 0) {
        lines.push('// Interned resolver kind lists (T042i dedup)')
        for (const [name, literal] of namedEntries) {
            lines.push(`const ${name}: readonly string[] = ${literal};`)
        }
        for (let i = 0; i < kindTableLiterals.length; i++) {
            lines.push(`const _K${i}: readonly string[] = ${kindTableLiterals[i]};`)
        }
        lines.push('')
    }
    for (const block of perNodeBlocks) {
        lines.push(block)
        lines.push('')
    }

    return lines.join('\n')
}

// ---------------------------------------------------------------------------
// Dispatch
// ---------------------------------------------------------------------------

function renderFromForNode(node: AssembledNode, nodeMap: NodeMap, intern: KindInterner): string | undefined {
    if (!node.rawFactoryName || !node.fromFunctionName) return undefined
    switch (node.modelType) {
        case 'branch':
            return emitBranchFrom(node, nodeMap, intern)
        case 'container':
            return emitContainerFrom(node)
        case 'polymorph':
            return emitPolymorphFrom(node, nodeMap, intern)
        case 'leaf':
            return emitStringLikeFrom(node)
        case 'enum':
            return emitStringLikeFrom({
                typeName: node.typeName,
                rawFactoryName: node.rawFactoryName,
                fromFunctionName: node.fromFunctionName,
                enumValues: node.values,
            })
        case 'keyword':
            return emitKeywordFrom(node)
        default:
            return undefined
    }
}

// ---------------------------------------------------------------------------
// Branch from() — loose input, field-level resolution
// ---------------------------------------------------------------------------

interface BranchLikeNode {
    readonly kind: string
    readonly typeName: string
    readonly fromInputTypeName: string
    readonly rawFactoryName?: string
    readonly fromFunctionName?: string
    readonly fields: readonly AssembledField[]
    readonly children?: readonly { readonly required: boolean }[]
}

function emitBranchFrom(node: BranchLikeNode, nodeMap: NodeMap, intern: KindInterner): string {
    const fn = node.fromFunctionName!
    const factory = node.rawFactoryName!
    const fields = node.fields
    const childSlots = node.children ?? []
    const opt =
        fields.some(f => f.required) || childSlots.some(c => c.required) ? '' : '?'
    const typeName = node.typeName
    const lines: string[] = []
    // Return type is inferred from the factory call so the fluent
    // accessor methods (render, toEdit, replace, per-field getters/
    // setters) flow through. Annotating with the bare interface name
    // `${typeName}` loses the methods and fails assignability.
    const inputOptional = opt === '?'
    lines.push(`export function ${fn}(input${opt}: T.${node.fromInputTypeName}) {`)
    if (fields.length > 0) {
        // Fluent-node passthrough: if the caller already holds a
        // factory-built node (fluent accessors + `render`/`toEdit`/
        // `replace` methods), return it unchanged. The `'render' in
        // input` check uniquely identifies fluent outputs — bare
        // readNode NodeData has no method, so validators and
        // parser-driven callers fall through to the factory
        // reconstruction path below.
        const renderGuard = inputOptional ? 'input !== undefined && ' : ''
        lines.push(`  if (${renderGuard}'render' in input) return input;`)
        // After the fluent passthrough, `input` is the FromInputOf
        // camelCase bag arm. Read field properties directly — typed,
        // no widening cast needed. Bare readNode NodeData must be
        // wrapped (via `wrapNode`) before reaching here; wrapped nodes
        // expose camelCase getters that structurally satisfy this bag.
        const neName = (f: AssembledField) => `_ne_${f.propertyName}`
        for (const f of fields) {
            if (f.nonEmpty && f.multiple) {
                const call = resolveFieldFromTypedInput(f, nodeMap, typeName, intern, 'input', inputOptional)
                lines.push(`  const ${neName(f)} = ${call};`)
                lines.push(`  _assertNonEmpty(${neName(f)}, '${node.kind}.${f.propertyName}');`)
            }
        }
        lines.push(`  return ${factory}({`)
        for (const f of fields) {
            if (f.nonEmpty && f.multiple) {
                lines.push(`    ${f.propertyName}: ${neName(f)},`)
            } else {
                lines.push(`    ${f.propertyName}: ${resolveFieldFromTypedInput(f, nodeMap, typeName, intern, 'input', inputOptional)},`)
            }
        }
        if (childSlots.length > 0) {
            const childType = `NonNullable<T.${typeName}Config['children']>`
            const childAccess = inputOptional ? 'input?.children' : 'input.children'
            lines.push(`    children: (${childAccess} ?? []) as ${childType},`)
        }
        lines.push('  });')
    } else {
        lines.push(`  return ${factory}(input);`)
    }
    lines.push('}')
    return lines.join('\n')
}

// ---------------------------------------------------------------------------
// Container from() — accepts element args OR a self NodeData
// ---------------------------------------------------------------------------

interface ContainerFromNode {
    readonly kind: string
    readonly typeName: string
    readonly rawFactoryName?: string
    readonly fromFunctionName?: string
    readonly children: readonly { readonly multiple: boolean }[]
}

function emitContainerFrom(node: ContainerFromNode): string {
    const fn = node.fromFunctionName!
    const factory = node.rawFactoryName!
    const tName = `T.${node.typeName}`
    const childType = `NonNullable<T.${node.typeName}Config['children']>`
    const elementType = `${childType}[number]`
    // Singular-child containers take one positional arg (`child?: T`);
    // repeated-child containers take `...children: T[]`. The from
    // function has to match the factory's signature at the call sites
    // it forwards to — pick the right call shape here.
    const childrenMultiple = node.children.some(c => c.multiple)
    if (childrenMultiple) {
        return [
            `export function ${fn}(...input: readonly (${elementType} | ${tName})[]) {`,
            `  if (input.length === 1 && isNodeData(input[0]) && input[0].type === '${node.kind}') {`,
            `    const data = input[0] as ${tName};`,
            `    return ${factory}(...(data.children as ${childType}));`,
            `  }`,
            `  return ${factory}(...(input as ${childType}));`,
            '}',
        ].join('\n')
    }
    // Cast the extracted single child all the way to the element type —
    // the container factory requires a non-nullable element when the
    // grammar says the child is required, and we can't express
    // "indexed access on a non-null tuple" through ConfigOf without
    // pushing casts downstream.
    return [
        `export function ${fn}(input?: ${elementType} | ${tName}) {`,
        `  if (isNodeData(input) && input.type === '${node.kind}') {`,
        `    const data = input as ${tName};`,
        `    return ${factory}((data.children as ${childType})[0] as ${elementType});`,
        `  }`,
        `  return ${factory}(input as ${elementType});`,
        '}',
    ].join('\n')
}

// ---------------------------------------------------------------------------
// Polymorph from() — dispatcher + inline per-form resolvers
// ---------------------------------------------------------------------------

interface PolymorphFromNode {
    readonly kind: string
    readonly fromInputTypeName: string
    readonly rawFactoryName?: string
    readonly fromFunctionName?: string
    readonly forms: AssembledGroup[]
}

function emitPolymorphFrom(node: PolymorphFromNode, nodeMap: NodeMap, intern: KindInterner): string {
    const fn = node.fromFunctionName!
    const factory = node.rawFactoryName!
    // The polymorph factory takes the strict config-union but `input`
    // is a FromInputOf (loose widening). The two types are structurally
    // incompatible at the type level, so the emitter declares an
    // explicit overload — `fromX` takes the loose input externally,
    // delegates to the factory via a single direct `as` (no `unknown`
    // bridge), and lets the factory's runtime field-presence check
    // handle the dispatch. We spell the target as the literal
    // `ConfigOf<FormN> | ...` union rather than `Parameters<typeof fn>[0]`
    // because grammar kinds named `Parameters` would shadow TypeScript's
    // built-in `Parameters<T>`.
    const configUnion = node.forms.map(f => `T.${f.typeName}Config`).join(' | ')
    // The polymorph factory takes the strict config-union but `input` is
    // a FromInputOf (loose widening). A runtime field-presence check
    // inside the factory handles the dispatch. The config-union cast is
    // a single direct `as` — no `unknown` bridge required. We spell the
    // target as the literal `ConfigOf<FormN> | ...` union rather than
    // `Parameters<typeof fn>[0]` because grammar kinds named `Parameters`
    // would shadow TypeScript's built-in `Parameters<T>`.
    const dispatcher = [
        `export function ${fn}(input?: T.${node.fromInputTypeName}) {`,
        `  return ${factory}(input as ${configUnion});`,
        '}',
    ].join('\n')
    const parts = [dispatcher]
    for (const form of node.forms) {
        const formFn = `${form.typeName.charAt(0).toLowerCase()}${form.typeName.slice(1)}From`
        const formFactory = form.rawFactoryName!
        // Match the form factory's parameter optionality. The
        // factory declares `config:` when any field OR child slot
        // is required (T063 inlining can push a required child onto
        // a form that previously had only optional fields). Passing
        // an optional `input?` where the factory wants a required
        // arg fails the type check.
        const formOpt =
            form.fields.some(fd => fd.required) ||
            form.children.some(c => c.required) ? '' : '?'
        const fLines: string[] = []
        const formInputOptional = formOpt === '?'
        fLines.push(`export function ${formFn}(input${formOpt}: T.${form.typeName}Config) {`)
        if (form.fields.length > 0) {
            // Typed direct reads — `input` is the strict form-config
            // alias, every field property is already typed correctly.
            fLines.push(`  return ${formFactory}({`)
            for (const f of form.fields) {
                fLines.push(`    ${f.propertyName}: ${resolveFieldFromTypedInput(f, nodeMap, form.typeName, intern, 'input', formInputOptional)},`)
            }
            fLines.push('  });')
        } else {
            fLines.push(`  return ${formFactory}(input);`)
        }
        fLines.push('}')
        parts.push(fLines.join('\n'))
    }
    return parts.join('\n\n')
}

// ---------------------------------------------------------------------------
// Leaf / enum from() — `string | NodeData` passthrough
// ---------------------------------------------------------------------------

interface LeafFromNode {
    readonly typeName: string
    readonly rawFactoryName?: string
    readonly fromFunctionName?: string
    /** Enum value list when the underlying node is an enum. */
    readonly enumValues?: readonly string[]
}

function emitStringLikeFrom(node: LeafFromNode): string {
    const fn = node.fromFunctionName!
    const factory = node.rawFactoryName!
    // Enum factories declare their parameter as a literal union;
    // plain leaves take `string`. Cast `input` to the right shape
    // before calling. The literal union is inlined here because
    // `Parameters<typeof X>` would collide with grammar kinds named
    // `parameters` (rust has one) — TypeScript's built-in
    // `Parameters<T>` gets shadowed and emits TS2315.
    const cast = node.enumValues && node.enumValues.length > 0
        ? `input as ${node.enumValues.map(v => `'${escForSource(v)}'`).join(' | ')}`
        : `input as string`
    return [
        `export function ${fn}(input: string | T.${node.typeName}) {`,
        `  if (isNodeData(input)) return input;`,
        `  return ${factory}(${cast});`,
        '}',
    ].join('\n')
}

// ---------------------------------------------------------------------------
// Keyword from() — NodeData passthrough or zero-arg factory
// ---------------------------------------------------------------------------

function emitKeywordFrom(node: LeafFromNode): string {
    const fn = node.fromFunctionName!
    const factory = node.rawFactoryName!
    return [
        `export function ${fn}(input?: T.${node.typeName}) {`,
        `  if (isNodeData(input)) return input;`,
        `  return ${factory}();`,
        '}',
    ].join('\n')
}

// ---------------------------------------------------------------------------
// Field-level resolver call generation
// ---------------------------------------------------------------------------

/** Interner signature passed through the resolver emitter calls. */
type KindInterner = (kinds: readonly string[]) => string

/**
 * Build a field-resolver call that reads a single camelCase property
 * directly off a typed FromInput bag (`input?.fieldName`). Typed
 * access flows the FromInput's per-field type into the resolver's
 * generic slot — no `_f` normalize, no index-signature widening. Used
 * by branch / polymorph-form `fromX` bodies after the top-level kind
 * discriminator has already handed back any pre-built node.
 */
function resolveFieldFromTypedInput(
    field: AssembledField,
    nodeMap: NodeMap,
    parentTypeName: string,
    intern: KindInterner,
    sourceVar: string,
    inputOptional: boolean,
): string {
    const slotType = `NonNullable<T.${parentTypeName}Config['${field.propertyName}']>`
    const typeParam = field.multiple ? `${slotType}[number]` : slotType
    // Optional-chain the read only when the param itself is optional —
    // a required input is never undefined at runtime and TS narrows it
    // to the non-undefined arm, so `input.name` is the right access.
    const access = inputOptional
        ? `${sourceVar}?.${field.propertyName}`
        : `${sourceVar}.${field.propertyName}`
    return resolveFieldCall(access, field, nodeMap, typeParam, intern)
}

function resolveFieldCall(
    prop: string,
    field: AssembledField,
    nodeMap: NodeMap,
    typeParam: string | undefined,
    intern: KindInterner,
): string {
    // Expand supertype references before classifying. A content
    // entry whose kind is a supertype in the NodeMap expands to
    // that supertype's declared subtypes — the resolver works at
    // the concrete kind layer, so dispatching through a supertype
    // literal would never match anything. Expansion also lets
    // T042i reach for the named `_super_<name>` dedup entry since
    // the interner keys on the full subtype set.
    //
    // Dedupe after expansion: contentTypes may legitimately contain
    // a supertype AND one of its concrete subtypes (e.g. `_expression`
    // and `range_expression` can both appear on the same field), and
    // the expansion would otherwise surface the concrete kind twice.
    const seen = new Set<string>()
    const expanded: string[] = []
    for (const t of field.contentTypes) {
        const n = nodeMap.nodes.get(t)
        const items = n?.modelType === 'supertype' ? n.subtypes : [t]
        for (const item of items) {
            if (seen.has(item)) continue
            seen.add(item)
            expanded.push(item)
        }
    }

    // Classify content types into leaves vs branches.
    const leafKinds: string[] = []
    const branchKinds: string[] = []
    for (const t of expanded) {
        const n = nodeMap.nodes.get(t)
        if (!n) {
            // Unknown kind — treat as branch so it goes through _resolveByKind
            branchKinds.push(t)
            continue
        }
        switch (n.modelType) {
            case 'leaf':
            case 'enum':
            case 'keyword':
                leafKinds.push(t)
                break
            case 'token':
                // Anonymous tokens have no factory binding — skip.
                break
            case 'supertype':
            case 'branch':
            case 'container':
            case 'polymorph':
            case 'group':
                branchKinds.push(t)
                break
        }
    }

    // Single-kind fast path: when dispatch reduces to exactly one
    // possible target, skip the generic `_resolveOne`/`_resolveMany`
    // entry point (which iterates the leafKinds / branchKinds arrays)
    // and emit a direct specialized call. Removes one function-call
    // layer + array-iteration dispatch per field read at runtime.
    const total = leafKinds.length + branchKinds.length
    const generic = typeParam ? `<${typeParam}>` : ''
    if (total === 1) {
        const kindName = leafKinds[0] ?? branchKinds[0]!
        const isLeaf = leafKinds.length === 1
        const specialized = field.multiple
            ? (isLeaf ? '_resolveManyLeaf' : '_resolveManyBranch')
            : (isLeaf ? '_resolveOneLeaf' : '_resolveOneBranch')
        return `${specialized}${generic}(${prop}, ${JSON.stringify(kindName)})`
    }
    // T042i: refer to interned kind-list constants instead of
    // repeating the literal arrays at every call site. Duplicated
    // entries collapse to a single module-scoped `_KN = [...]` decl
    // or `_super_<name>` when the list matches a supertype exactly.
    const leafArr = intern(leafKinds)
    const branchArr = intern(branchKinds)
    const helper = field.multiple ? '_resolveMany' : '_resolveOne'
    return `${helper}${generic}(${prop}, ${leafArr}, ${branchArr})`
}

// ---------------------------------------------------------------------------
// Module-scoped resolver helpers (emitted into generated from.ts)
// ---------------------------------------------------------------------------

function emitResolverHelpers(lines: string[], nodeMap: NodeMap): void {
    // Build the leaf registry from NodeMap leaves/keywords/enums.
    // Enum factories now declare their parameter as a literal union
    // (compile-time validation) — the registry slot wants
    // `(text: string) => unknown` so the narrower function isn't
    // assignable directly. Wrap the call in a thin closure that
    // accepts a wide string and casts to the enum's literal union
    // (inlined here because grammar kinds named `parameters` would
    // shadow TypeScript's built-in `Parameters<T>` utility). The
    // factory's runtime guard still catches invalid values.
    const registryEntries: string[] = []
    for (const [kind, node] of nodeMap.nodes) {
        if (kind.startsWith('_')) continue
        if (!node.rawFactoryName) continue
        const factory = node.rawFactoryName
        if (node.modelType === 'enum') {
            const values = node.values.map(v => JSON.stringify(v)).join(', ')
            const literalUnion = node.values.map(v => `'${escForSource(v)}'`).join(' | ')
            registryEntries.push(`  ${JSON.stringify(kind)}: { values: [${values}], factory: (text: string) => ${factory}(text as ${literalUnion}) },`)
        } else if (node.modelType === 'keyword') {
            registryEntries.push(`  ${JSON.stringify(kind)}: { values: [${JSON.stringify(node.text)}], factory: () => ${factory}() },`)
        } else if (node.modelType === 'leaf') {
            registryEntries.push(`  ${JSON.stringify(kind)}: { factory: ${factory} },`)
        }
    }

    lines.push('// --- Loose-input resolver helpers (see C6-prereq) ---')
    lines.push('interface _LeafEntry {')
    lines.push('  readonly values?: readonly string[];')
    lines.push('  readonly pattern?: RegExp;')
    lines.push('  readonly factory: (text: string) => AnyNodeData;')
    lines.push('}')
    lines.push('const _leafRegistry: { readonly [kind: string]: _LeafEntry } = {')
    for (const entry of registryEntries) lines.push(entry)
    lines.push('};')
    lines.push('')

    lines.push('function _resolveLeafString(v: string, kinds: readonly string[]): AnyNodeData | undefined {')
    lines.push('  for (const kind of kinds) {')
    lines.push('    const entry = _leafRegistry[kind];')
    lines.push('    if (!entry) continue;')
    lines.push('    if (entry.values && entry.values.includes(v)) return entry.factory(v);')
    lines.push('    if (entry.pattern && entry.pattern.test(v)) return entry.factory(v);')
    lines.push('  }')
    lines.push('  for (const kind of kinds) {')
    lines.push('    const entry = _leafRegistry[kind];')
    lines.push('    if (entry && !entry.values && !entry.pattern) return entry.factory(v);')
    lines.push('  }')
    lines.push('  return undefined;')
    lines.push('}')
    lines.push('')

    // Generic over the kind literal so the return type is the precise
    // `ReturnType<_FromMap[K]>` — each per-kind factory's output flows
    // through, not a widened `AnyNodeData` union. Callers pass a narrow
    // kind (string-literal from the field's content types or narrowed
    // via an `in`-check against `_fromMap`) to get the specific return
    // shape back. The internal sideways cast routes around per-slot
    // parameter variance without going through `unknown`/`any`.
    lines.push('function _resolveByKind<K extends keyof _FromMap>(')
    lines.push('  kind: K,')
    lines.push('  rest: _FromFieldInput,')
    lines.push('): ReturnType<_FromMap[K]> {')
    lines.push('  const fn = _fromMap[kind];')
    lines.push('  return (fn as (input?: _FromFieldInput) => ReturnType<_FromMap[K]>)(rest);')
    lines.push('}')
    lines.push('')

    const hasBool = nodeMap.nodes.has('boolean_literal')
    const hasInt = nodeMap.nodes.has('integer_literal') || nodeMap.nodes.has('integer')
    const hasFloat = nodeMap.nodes.has('float_literal') || nodeMap.nodes.has('float')
    lines.push('function _resolveScalar(v: boolean | number): AnyNodeData | undefined {')
    if (hasBool) {
        lines.push('  if (typeof v === "boolean") {')
        lines.push('    const e = _leafRegistry["boolean_literal"];')
        lines.push('    return e ? e.factory(v ? "true" : "false") : undefined;')
        lines.push('  }')
    }
    if (hasInt || hasFloat) {
        lines.push('  if (typeof v === "number") {')
        if (hasInt) {
            const intKind = nodeMap.nodes.has('integer_literal') ? 'integer_literal' : 'integer'
            lines.push(`    if (Number.isInteger(v)) {`)
            lines.push(`      const e = _leafRegistry[${JSON.stringify(intKind)}];`)
            lines.push(`      return e ? e.factory(String(v)) : undefined;`)
            lines.push(`    }`)
        }
        if (hasFloat) {
            const floatKind = nodeMap.nodes.has('float_literal') ? 'float_literal' : 'float'
            lines.push(`    const e = _leafRegistry[${JSON.stringify(floatKind)}];`)
            lines.push(`    return e ? e.factory(String(v)) : undefined;`)
        }
        lines.push('  }')
    }
    lines.push('  return undefined;')
    lines.push('}')
    lines.push('')

    // Resolvers are emitted with a `<T>` type parameter so the call site
    // can name the expected slot shape (`_resolveOne<FunctionItem>`);
    // no `extends` constraint because the factory-emitted node interfaces
    // don't all structurally satisfy `AnyNodeData` (they omit the `named`
    // property), and adding such a constraint would force every call site
    // to re-widen. The input is the closed `_FromFieldInput` union so no
    // caller has to cast anything loose.
    lines.push('function _resolveOne<T>(')
    lines.push('  v: _FromFieldInput,')
    lines.push('  leafKinds: readonly string[],')
    lines.push('  branchKinds: readonly string[],')
    lines.push('): T {')
    lines.push('  if (v === undefined || v === null) return v as T;')
    lines.push('  if (isNodeData(v)) return v as T;')
    lines.push('  if (typeof v === "boolean" || typeof v === "number") {')
    lines.push('    const scalar = _resolveScalar(v);')
    lines.push('    if (scalar !== undefined) return scalar as T;')
    lines.push('  }')
    lines.push('  if (typeof v === "string" && leafKinds.length > 0) {')
    lines.push('    const leaf = _resolveLeafString(v, leafKinds);')
    lines.push('    if (leaf !== undefined) return leaf as T;')
    lines.push('  }')
    lines.push('  if (typeof v === "object" && !Array.isArray(v) && "kind" in v) {')
    lines.push('    const { kind, ...rest } = v;')
    lines.push('    if (typeof kind === "string" && kind in _fromMap) return _resolveByKind(kind as keyof _FromMap, rest) as T;')
    lines.push('  }')
    lines.push('  if (branchKinds.length === 1 && typeof v === "object" && !Array.isArray(v)) {')
    lines.push('    const bk = branchKinds[0]!;')
    lines.push('    if (bk in _fromMap) return _resolveByKind(bk as keyof _FromMap, v) as T;')
    lines.push('  }')
    lines.push('  return v as T;')
    lines.push('}')
    lines.push('')

    lines.push('function _resolveMany<T>(')
    lines.push('  v: _FromFieldInput,')
    lines.push('  leafKinds: readonly string[],')
    lines.push('  branchKinds: readonly string[],')
    lines.push('): readonly T[] {')
    lines.push('  if (v === undefined || v === null) return [];')
    lines.push('  const arr: readonly _FromFieldInput[] = Array.isArray(v) ? v : [v];')
    lines.push('  return arr.map(e => _resolveOne<T>(e, leafKinds, branchKinds));')
    lines.push('}')
    lines.push('')

    // Single-kind fast paths — resolver call sites with only one
    // possible target dispatch here directly, skipping the leafKinds
    // / branchKinds iteration in _resolveOne.
    lines.push('function _resolveOneLeaf<T>(v: _FromFieldInput, kind: string): T {')
    lines.push('  if (v === undefined || v === null) return v as T;')
    lines.push('  if (isNodeData(v)) return v as T;')
    lines.push('  if (typeof v === "boolean" || typeof v === "number") {')
    lines.push('    const scalar = _resolveScalar(v);')
    lines.push('    if (scalar !== undefined) return scalar as T;')
    lines.push('  }')
    lines.push('  if (typeof v === "string") {')
    lines.push('    const e = _leafRegistry[kind];')
    lines.push('    if (e !== undefined) return e.factory(v) as T;')
    lines.push('  }')
    lines.push('  if (typeof v === "object" && !Array.isArray(v) && "kind" in v) {')
    lines.push('    const { kind: k, ...rest } = v;')
    lines.push('    if (typeof k === "string" && k in _fromMap) return _resolveByKind(k as keyof _FromMap, rest) as T;')
    lines.push('  }')
    lines.push('  return v as T;')
    lines.push('}')
    lines.push('')

    lines.push('function _resolveOneBranch<T>(v: _FromFieldInput, kind: string): T {')
    lines.push('  if (v === undefined || v === null) return v as T;')
    lines.push('  if (isNodeData(v)) return v as T;')
    lines.push('  if (typeof v === "object" && !Array.isArray(v)) {')
    lines.push('    if ("kind" in v) {')
    lines.push('      const { kind: k, ...rest } = v;')
    lines.push('      if (typeof k === "string" && k in _fromMap) return _resolveByKind(k as keyof _FromMap, rest) as T;')
    lines.push('    }')
    lines.push('    if (kind in _fromMap) return _resolveByKind(kind as keyof _FromMap, v) as T;')
    lines.push('  }')
    lines.push('  return v as T;')
    lines.push('}')
    lines.push('')

    lines.push('function _resolveManyLeaf<T>(v: _FromFieldInput, kind: string): readonly T[] {')
    lines.push('  if (v === undefined || v === null) return [];')
    lines.push('  const arr: readonly _FromFieldInput[] = Array.isArray(v) ? v : [v];')
    lines.push('  return arr.map(e => _resolveOneLeaf<T>(e, kind));')
    lines.push('}')
    lines.push('')

    lines.push('function _resolveManyBranch<T>(v: _FromFieldInput, kind: string): readonly T[] {')
    lines.push('  if (v === undefined || v === null) return [];')
    lines.push('  const arr: readonly _FromFieldInput[] = Array.isArray(v) ? v : [v];')
    lines.push('  return arr.map(e => _resolveOneBranch<T>(e, kind));')
    lines.push('}')
    lines.push('')
    // _assertNonEmpty — runtime guard + static narrowing helper for
    // repeat1-sourced list fields. from() resolves a loose input to a
    // `readonly T[]` via _resolveMany*, but the factory's config slot
    // is the non-empty tuple `readonly [T, ...T[]]`. Calling this
    // assertion on the resolver result narrows the static type to
    // the tuple shape AND throws at runtime if the input was empty.
    lines.push('function _assertNonEmpty<T>(')
    lines.push('  arr: readonly T[],')
    lines.push('  label: string,')
    lines.push('): asserts arr is readonly [T, ...(readonly T[])] {')
    lines.push('  if (arr.length === 0) {')
    lines.push('    throw new Error(`${label}: requires at least one element`);')
    lines.push('  }')
    lines.push('}')
}

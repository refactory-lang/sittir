/**
 * Emits ir.ts — developer-facing namespace re-exporting factories with short names.
 *
 * Consumes NodeMap directly. Derives from factory exports — thin namespace wrapper.
 *
 * Spec 008 US5:
 *   - Namespace imports (`import * as F` / `import * as FR`) replace the
 *     per-entry import walls. Single short line per source module.
 *   - Supertype-grouped sub-namespaces (`ir.expression`, `ir.pattern`, …)
 *     emitted alongside the flat `ir.*` namespace. Members keyed by
 *     supertype-stripped short names; JS reserved words get a `_` suffix.
 */

import type { NodeMap } from '../compiler/types.ts';
import type { GeneratedIdTables } from '../compiler/generated-metadata.ts';
import type { AssembledNode, AssembledSupertype } from '../compiler/node-map.ts';
import { isValidIdent } from './shared.ts';
import { collectKindEntries, collectCatalogKinds, hasCatalogEntry } from './kind-discriminant.ts';
import { camelCase, collectRefineKindInfos, refineFormFactoryName } from './refine-emit.ts';
import type { RefineKindInfo } from './refine-emit.ts';
import type { GrammarRoles, Role } from '../scm/extract-roles.ts';

export interface EmitIrConfig {
	grammar: string;
	nodeMap: NodeMap;
	generatedIdTables?: GeneratedIdTables;
	grammarRoles?: GrammarRoles;
}

export function emitIr(config: EmitIrConfig): string {
	const { nodeMap, generatedIdTables, grammarRoles } = config;

	const kindEntries = generatedIdTables
		? collectKindEntries(collectCatalogKinds(generatedIdTables), nodeMap, generatedIdTables)
		: undefined;

	const refineInfos = collectRefineKindInfos(nodeMap);
	const refineByKind = new Map<string, RefineKindInfo>();
	for (const info of refineInfos ?? []) refineByKind.set(info.kind, info);

	const lines: string[] = [
		'// Auto-generated by @sittir/codegen — do not edit',
		'//',
		'// `ir.*` — flat namespace: every grammar kind exposed as `ir.<camelCaseKind>`.',
		'// `ir.<supertype>.*` — grouped namespaces: each member re-keyed by its',
		'//   supertype-stripped short name (e.g. `ir.expression.binary` === `ir.binaryExpression`).',
		'//',
		'// Both surfaces resolve to the same callable bundle: branch / polymorph',
		'// entries default to `from()` and expose the strict factory as `.strict`.',
		'//',
		'// Edge case: `readNode()` output has no `$source` provenance. If you want',
		'// to feed it directly into `.from()`, use the typed wrapper (`readTreeNode`)',
		'// so `.from()` sees a wrapped node and takes the identity quick-return path.',
		'',
		"import * as F from './factories.js';",
		"import * as FR from './from.js';",
		''
	];

	// _attach helper — uses defineProperty with writable/configurable/enumerable
	// descriptors so Function's reserved read-only properties (name, length,
	// arguments, caller) don't throw. This matters for polymorphs whose form
	// name is a reserved word (e.g. typescript's importSpecifier has a
	// variant form named `name`). Object.assign uses [[Set]] which respects
	// the target's writable attribute, so it fails on these cases.
	// Spec 008 US4 / FR-025 considered replacing this with Object.assign —
	// rejected after discovering the Function-reserved-name collision at
	// TypeScript regen time. `_attach` stays.
	lines.push(
		'function _attach<T extends (...args: never[]) => unknown, P extends Record<string, unknown>>(fn: T, props: P): T & P {'
	);
	lines.push('  for (const key of Object.keys(props)) {');
	lines.push(
		'    Object.defineProperty(fn, key, { value: props[key], writable: true, configurable: true, enumerable: true });'
	);
	lines.push('  }');
	lines.push('  return fn as T & P;');
	lines.push('}');
	lines.push('');

	// ----------------------------------------------------------------------
	// Supertype-grouped sub-namespaces — collected first so they can be
	// both exported as tree-shakeable top-level consts AND attached to
	// the flat `ir` namespace for nested access (`ir.expression.binary`).
	// ----------------------------------------------------------------------
	const groupBlocks: string[] = [];
	const groupNames: string[] = [];
	const usedGroupNames = new Set<string>();
	for (const [kind, node] of nodeMap.nodes) {
		if (node.modelType !== 'supertype') continue;
		const sup = node as AssembledSupertype;
		const groupName = groupNameFor(kind);
		if (!isValidIdent(groupName) || usedGroupNames.has(groupName)) continue;

		const memberEntries: string[] = [];
		const usedMemberKeys = new Set<string>();
		for (const subKind of sup.subtypes) {
			if (subKind.startsWith('_')) continue;
			const sub = nodeMap.nodes.get(subKind);
			if (!sub) continue;
			if (!sub.rawFactoryName) continue;
			if (sub.modelType === 'supertype' || sub.modelType === 'group' || sub.modelType === 'token') continue;
			// TSGrammar-only kinds (no parser symbol — tree-sitter inlined) can
			// never appear at runtime; no factory was emitted for them.
			if (kindEntries && !hasCatalogEntry(kindEntries, subKind)) continue;
			const memberKey = memberKeyFor(subKind, kind);
			if (!isValidIdent(memberKey) || usedMemberKeys.has(memberKey)) continue;
			usedMemberKeys.add(memberKey);

			if (sub.modelType === 'branch') {
				if (!sub.fromFunctionName) continue;
				memberEntries.push(`  ${memberKey}: ${bundleExpr(sub)},`);
			} else if (sub.modelType === 'keyword' || sub.modelType === 'pattern' || sub.modelType === 'enum') {
				memberEntries.push(`  ${memberKey}: F.${sub.rawFactoryName},`);
			}
		}
		if (memberEntries.length === 0) continue;
		usedGroupNames.add(groupName);
		groupNames.push(groupName);

		groupBlocks.push(`export const ${groupName} = {`);
		groupBlocks.push(...memberEntries);
		groupBlocks.push('} as const;');
		groupBlocks.push('');
	}
	if (groupBlocks.length > 0) {
		lines.push('// Supertype-grouped sub-namespaces — tree-shakeable top-level consts.');
		lines.push('// Also attached to `ir.*` below for nested access (e.g. `ir.expression.binary`).');
		lines.push(...groupBlocks);
	}

	const flatKeys = new Set<string>();
	for (const [kind, node] of nodeMap.nodes) {
		if (kind.startsWith('_')) continue;
		if (!node.irKey || !node.rawFactoryName) continue;
		if (!isValidIdent(node.irKey)) continue;
		if (
			node.modelType !== 'branch' &&
			node.modelType !== 'keyword' &&
			node.modelType !== 'pattern' &&
			node.modelType !== 'enum'
		) {
			continue;
		}
		if (node.modelType === 'branch' && !node.fromFunctionName) continue;
		if (kindEntries && !hasCatalogEntry(kindEntries, kind)) continue;
		flatKeys.add(node.irKey);
	}
	const shortAliasBundles = new Map<string, string>();
	for (const [kind, node] of nodeMap.nodes) {
		if (node.modelType !== 'supertype') continue;
		const sup = node as AssembledSupertype;
		for (const subKind of sup.subtypes) {
			if (subKind.startsWith('_')) continue;
			const sub = nodeMap.nodes.get(subKind);
			if (!sub?.rawFactoryName) continue;
			if (kindEntries && !hasCatalogEntry(kindEntries, subKind)) continue;
			const alias = memberKeyFor(subKind, kind);
			if (!isValidIdent(alias) || flatKeys.has(alias) || usedGroupNames.has(alias)) continue;
			let bundle: string | undefined;
			if (sub.modelType === 'branch') {
				if (!sub.fromFunctionName) continue;
				bundle = bundleExpr(sub, refineByKind.get(subKind));
			} else if (sub.modelType === 'keyword' || sub.modelType === 'pattern' || sub.modelType === 'enum') {
				bundle = `F.${sub.rawFactoryName}`;
			} else {
				continue;
			}
			if (shortAliasBundles.has(alias)) continue;
			shortAliasBundles.set(alias, bundle);
		}
	}

	// ------------------------------------------------------------------
	// `from` — canonical factories for native-value → NodeData.
	// Spec 023 US6: grammar-agnostic AST construction from boolean,
	// number, string, comment, type, identifier values.
	// Emitted BEFORE `ir` so it can be referenced as `ir.from`.
	// Also exported standalone for tree-shakeable `from.boolean(...)`.
	// ------------------------------------------------------------------
	let hasFrom = false;
	if (grammarRoles) {
		const fromLines = emitFromNamespace(grammarRoles, nodeMap);
		if (fromLines.length > 0) {
			hasFrom = true;
			lines.push(...fromLines);
			lines.push('');
		}
	}

	// ----------------------------------------------------------------------
	// Flat `ir.*` namespace — every grammar kind by camelCase short name.
	// ----------------------------------------------------------------------
	lines.push('export const ir = {');
	lines.push('  // Node factories');
	for (const [kind, node] of nodeMap.nodes) {
		if (kind.startsWith('_')) continue;
		if (!node.irKey || !node.rawFactoryName || !node.fromFunctionName) continue;
		if (!isValidIdent(node.irKey)) continue;
		if (usedGroupNames.has(node.irKey)) continue;
		if (node.modelType !== 'branch') continue;
		// TSGrammar-only kinds (no parser symbol — tree-sitter inlined) can
		// never appear at runtime; no factory was emitted for them.
		if (kindEntries && !hasCatalogEntry(kindEntries, kind)) continue;
		lines.push(`  ${node.irKey}: ${bundleExpr(node, refineByKind.get(kind))},`);
	}
	lines.push('');

	lines.push('  // Keyword factories');
	for (const [kind, node] of nodeMap.nodes) {
		if (kind.startsWith('_')) continue;
		if (node.modelType !== 'keyword') continue;
		if (!node.irKey || !node.rawFactoryName) continue;
		if (!isValidIdent(node.irKey)) continue;
		if (usedGroupNames.has(node.irKey)) continue;
		// TSGrammar-only kinds (no parser symbol — tree-sitter inlined) can
		// never appear at runtime; no factory was emitted for them.
		if (kindEntries && !hasCatalogEntry(kindEntries, kind)) continue;
		lines.push(`  ${node.irKey}: F.${node.rawFactoryName},`);
	}
	lines.push('');

	lines.push('  // Leaf node factories');
	for (const [kind, node] of nodeMap.nodes) {
		if (kind.startsWith('_')) continue;
		if (node.modelType !== 'pattern' && node.modelType !== 'enum') continue;
		if (!node.irKey || !node.rawFactoryName) continue;
		if (!isValidIdent(node.irKey)) continue;
		if (usedGroupNames.has(node.irKey)) continue;
		// TSGrammar-only kinds (no parser symbol — tree-sitter inlined) can
		// never appear at runtime; no factory was emitted for them.
		if (kindEntries && !hasCatalogEntry(kindEntries, kind)) continue;
		lines.push(`  ${node.irKey}: F.${node.rawFactoryName},`);
	}
	if (shortAliasBundles.size > 0) {
		lines.push('');
		lines.push('  // Supertype-stripped short aliases');
		for (const [alias, bundle] of [...shortAliasBundles.entries()].sort(([a], [b]) => a.localeCompare(b))) {
			lines.push(`  ${alias}: ${bundle},`);
		}
	}
	if (groupNames.length > 0 || hasFrom) {
		lines.push('');
		lines.push('  // Supertype-grouped sub-namespaces (also exported standalone above)');
		for (const g of groupNames) {
			lines.push(`  ${g},`);
		}
		if (hasFrom) {
			lines.push('  from,');
		}
	}
	lines.push('} as const;');

	return lines.join('\n');
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/**
 * Factory+from bundle expression, shared by flat and grouped emission.
 * Polymorphs with >1 form also carry per-variant form bundles. Kinds
 * with refine() metadata carry per-form bundles keyed by the form's
 * camelCase short name (e.g. `ir.interfaceBody.curly`). Branches and
 * polymorphs call the loose `from()` path by default and expose the raw
 * factory as `.strict`.
 */
function bundleExpr(node: AssembledNode, refineInfo?: RefineKindInfo): string {
	if (node.modelType === 'branch') {
		if (!node.rawFactoryName) {
			return `_attach(FR.${node.fromFunctionName}, { from: FR.${node.fromFunctionName} })`;
		}
		const baseFactoryName = node.rawFactoryName;
		const refineEntries =
			refineInfo?.forms.flatMap((form) => {
				const factoryName = refineFormFactoryName(baseFactoryName, form.name);
				const camelKey = JSON.stringify(camelCase(form.name));
				const keys = [camelKey];
				if (camelCase(form.name) !== form.name) keys.push(JSON.stringify(form.name));
				return keys.map((key) => `${key}: F.${factoryName}`);
			}) ?? [];
		const extras = refineEntries.length > 0 ? `, ${refineEntries.join(', ')}` : '';
		return `_attach(FR.${node.fromFunctionName}, { from: FR.${node.fromFunctionName}, strict: F.${baseFactoryName}${extras} })`;
	}
	return `_attach(F.${node.rawFactoryName}, { from: FR.${node.fromFunctionName} })`;
}

/**
 * Supertype kind → group namespace name.
 *   `_expression`            → `expression`
 *   `_declaration_statement` → `declarationStatement`
 *   `_literal_pattern`       → `literalPattern`
 */
function groupNameFor(supertypeKind: string): string {
	const bare = supertypeKind.replace(/^_+/, '');
	return toCamel(bare);
}

/**
 * Member kind → short key within its supertype group.
 * Strip the last underscored segment (e.g. `_expression`, `_item`, `_pattern`).
 * If that collides with a JS reserved word, suffix with `_` per FR-029.
 *
 * Falls back to the full camelCased kind if stripping would leave nothing.
 */
function memberKeyFor(memberKind: string, supertypeKind: string): string {
	const bareMember = memberKind.replace(/^_+/, '');
	const parts = bareMember.split('_');
	let short: string;
	if (parts.length >= 2) {
		short = parts.slice(0, -1).join('_');
	} else {
		short = bareMember;
	}
	const camel = toCamel(short);
	// Avoid collision with the group name itself (e.g. if a subtype happens
	// to strip down to the same key as the group). Fall back to full name.
	if (camel === groupNameFor(supertypeKind)) return toCamel(bareMember);
	return camel;
}

function toCamel(snake: string): string {
	const parts = snake.split('_').filter((p) => p.length > 0);
	if (parts.length === 0) return snake;
	return (
		parts[0] +
		parts
			.slice(1)
			.map((p) => p.charAt(0).toUpperCase() + p.slice(1))
			.join('')
	);
}


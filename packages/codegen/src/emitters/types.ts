/**
 * Emits types.ts — all type aliases derived from the grammar.
 *
 * Generates concrete interfaces as the single source of truth.
 * Config/Tree/FromInput are type transformations applied to them.
 *
 *   1. const enum SyntaxKind — all named node kinds
 *   2. Scoped const enums per supertype
 *   3. Concrete node interfaces (branches, containers, leaves, keywords, enums)
 *   4. Config/Tree/FromInput type aliases via transformations
 *   5. Supertype unions
 *   6. Discriminated union of all node types
 */

import type { HydratedNodeModel } from '../node-model.ts';
import { isTupleChildren, eachChildSlot } from '../node-model.ts';
import { toTypeName, toFieldName, toGrammarTypeName } from '../naming.ts';
import { structuralNodes, fieldsOf, leafKindsOf, supertypeEntriesOf, hiddenEntriesOf, childSlotNames } from './utils.ts';
import { buildProjectionContext, projectKinds, type ProjectionContext, type KindProjection } from './kind-projections.ts';

export interface EmitTypesConfig {
	grammar: string;
	nodes: HydratedNodeModel[];
}

export function emitTypes(config: EmitTypesConfig): string {
	const { grammar } = config;
	const structNodes = structuralNodes(config.nodes);
	const leafKinds = leafKindsOf(config.nodes);
	const supertypes = supertypeEntriesOf(config.nodes);
	const grammarTypeName = toGrammarTypeName(grammar);
	const grammarPrefix = grammarTypeName.slice(0, -5);
	const grammarAlias = `${grammarPrefix}Grammar`;
	const modelMap = new Map(config.nodes.map(n => [n.kind, n]));
	const ctx = buildProjectionContext(modelMap);
	const leafSet = new Set(leafKinds);

	const nodeKinds = structNodes.map(n => n.kind);
	const allKinds = [...nodeKinds, ...leafKinds];
	const generatedTypes = new Set<string>();

	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('');
	lines.push(`import type { ${grammarAlias} } from './grammar.js';`);
	lines.push("import type { NodeData as BaseNodeData, NodeConfig as BaseNodeConfig, TreeNode as BaseTreeNode, ConfigOf, FromInputOf, NodeKind } from '@sittir/types';");
	lines.push('');
	lines.push(`export type { ${grammarAlias} };`);
	lines.push('');
	lines.push('// Grammar-bound type aliases — kept for backward compatibility with generated emitters');
	lines.push(`export type NodeData<K extends NodeKind<${grammarAlias}>> = BaseNodeData<${grammarAlias}, K>;`);
	lines.push(`export type NodeConfig<K extends NodeKind<${grammarAlias}>> = BaseNodeConfig<${grammarAlias}, K>;`);
	lines.push(`export type TreeNode<K extends NodeKind<${grammarAlias}>> = BaseTreeNode<${grammarAlias}, K>;`);
	lines.push('');

	// LeafScalarMap — grammar-specific scalar widenings for .from() input
	const scalarEntries: string[] = [];
	for (const kind of leafKinds) {
		if (kind === 'integer_literal' || kind === 'float_literal') {
			scalarEntries.push(`  ${kind}: number;`);
		} else if (kind === 'boolean_literal') {
			scalarEntries.push(`  ${kind}: boolean;`);
		}
	}
	lines.push('/** Scalar widenings for leaf kinds used by FromInputOf. */');
	lines.push('export type LeafScalarMap = {');
	if (scalarEntries.length > 0) {
		for (const entry of scalarEntries) lines.push(entry);
	}
	lines.push('};');
	// NodeFromInput removed — FromInput types now use FromInputOf<T> (interface-derived)
	lines.push('');

	// -----------------------------------------------------------------------
	// 1. const enum SyntaxKind — all named node kinds
	// -----------------------------------------------------------------------
	lines.push('/** All named node kinds in this grammar. */');
	lines.push('export const enum SyntaxKind {');
	for (const kind of allKinds) {
		const enumKey = toTypeName(kind);
		lines.push(`  ${enumKey} = '${kind}',`);
	}
	lines.push('}');
	lines.push('');

	// -----------------------------------------------------------------------
	// 2. Scoped const enums per supertype
	// -----------------------------------------------------------------------
	if (supertypes.length > 0) {
		lines.push('// Scoped enums per supertype');
		for (const st of supertypes) {
			const cleanName = st.name.replace(/^_/, '');
			const enumName = toTypeName(cleanName) + 'Kind';
			lines.push(`export const enum ${enumName} {`);
			for (const sub of st.subtypes) {
				const enumKey = toTypeName(sub);
				lines.push(`  ${enumKey} = '${sub}',`);
			}
			lines.push('}');
			lines.push('');
		}
	}

	// -----------------------------------------------------------------------
	// 3. Concrete node interfaces
	// -----------------------------------------------------------------------

	// Branch + container nodes
	lines.push('// Node types — concrete interfaces');
	for (const node of structNodes) {
		const typeName = toTypeName(node.kind);
		generatedTypes.add(typeName);
		emitConcreteInterface(lines, node, typeName, ctx, leafSet);
	}
	lines.push('');

	// Leaf nodes
	lines.push('// Leaf node types');
	for (const kind of leafKinds) {
		const typeName = toTypeName(kind);
		if (generatedTypes.has(typeName)) continue;
		generatedTypes.add(typeName);
		const model = modelMap.get(kind)!;
		emitLeafInterface(lines, model, typeName);
	}
	lines.push('');

	// -----------------------------------------------------------------------
	// 4. Config/Tree/FromInput — derived from concrete interfaces
	// -----------------------------------------------------------------------
	lines.push('// Config types — derived from concrete interfaces');
	for (const kind of nodeKinds) {
		lines.push(`export type ${toTypeName(kind)}Config = ConfigOf<${toTypeName(kind)}>;`);
	}
	lines.push('');

	lines.push('// Tree types — grammar-derived (raw field names for tree-sitter compatibility)');
	for (const kind of nodeKinds) {
		lines.push(`export interface ${toTypeName(kind)}Tree extends TreeNode<'${kind}'> {}`);
	}
	lines.push('');

	lines.push('// Leaf tree types');
	for (const kind of leafKinds) {
		lines.push(`export interface ${toTypeName(kind)}Tree extends TreeNode<'${kind}'> {}`);
	}
	lines.push('');

	lines.push('// FromInput types — derived from concrete interfaces');
	for (const kind of nodeKinds) {
		lines.push(`export type ${toTypeName(kind)}FromInput = FromInputOf<${toTypeName(kind)}>;`);
	}
	lines.push('');

	// -----------------------------------------------------------------------
	// 5. Supertype unions
	// -----------------------------------------------------------------------
	if (supertypes.length > 0) {
		lines.push('// Supertype unions (node)');
		for (const st of supertypes) {
			const cleanName = st.name.replace(/^_/, '');
			const typeName = toTypeName(cleanName);
			if (generatedTypes.has(typeName)) continue;
			generatedTypes.add(typeName);

			const members = st.subtypes
				.map(sub => toTypeName(sub))
				.filter(t => generatedTypes.has(t));

			if (members.length > 0) {
				lines.push(`export type ${typeName} =`);
				for (const m of members) lines.push(`  | ${m}`);
				lines.push(';');
				lines.push('');
			}
		}

		const branchSet = new Set(nodeKinds);

		lines.push('// Supertype unions (config — branch kinds only)');
		for (const st of supertypes) {
			const cleanName = st.name.replace(/^_/, '');
			const configName = toTypeName(cleanName) + 'Config';
			const branchMembers = st.subtypes
				.filter(sub => branchSet.has(sub))
				.map(sub => toTypeName(sub) + 'Config');

			if (branchMembers.length > 0) {
				lines.push(`export type ${configName} =`);
				for (const m of branchMembers) lines.push(`  | ${m}`);
				lines.push(';');
				lines.push('');
			}
		}

		lines.push('// Supertype unions (tree)');
		for (const st of supertypes) {
			const cleanName = st.name.replace(/^_/, '');
			const treeName = toTypeName(cleanName) + 'Tree';
			const members = st.subtypes.map(sub => toTypeName(sub) + 'Tree');

			if (members.length > 0) {
				lines.push(`export type ${treeName} =`);
				for (const m of members) lines.push(`  | ${m}`);
				lines.push(';');
				lines.push('');
			}
		}

		lines.push('// Supertype unions (FromInput — branch kinds only)');
		for (const st of supertypes) {
			const cleanName = st.name.replace(/^_/, '');
			const fromInputName = toTypeName(cleanName) + 'FromInput';
			const branchMembers = st.subtypes
				.filter(sub => branchSet.has(sub))
				.map(sub => toTypeName(sub) + 'FromInput');

			if (branchMembers.length > 0) {
				lines.push(`export type ${fromInputName} =`);
				for (const m of branchMembers) lines.push(`  | ${m}`);
				lines.push(';');
				lines.push('');
			}
		}
	}

	// -----------------------------------------------------------------------
	// 5b. Hidden rule unions (non-supertype hidden rules used as child slot kinds)
	// -----------------------------------------------------------------------
	const hiddenEntries = hiddenEntriesOf(config.nodes);
	if (hiddenEntries.length > 0) {
		lines.push('// Hidden rule unions (grammar-internal groupings)');
		for (const h of hiddenEntries) {
			const cleanName = h.name.replace(/^_/, '');
			const typeName = toTypeName(cleanName);
			if (generatedTypes.has(typeName)) continue;
			generatedTypes.add(typeName);

			const members = h.subtypes
				.map(sub => toTypeName(sub))
				.filter(t => generatedTypes.has(t));

			if (members.length > 0) {
				lines.push(`export type ${typeName} =`);
				for (const m of members) lines.push(`  | ${m}`);
				lines.push(';');
				lines.push('');
			}
		}
	}

	// -----------------------------------------------------------------------
	// 6. Discriminated union of all node types
	// -----------------------------------------------------------------------
	lines.push(`export type ${grammarPrefix}Node =`);
	for (const kind of nodeKinds) {
		lines.push(`  | ${toTypeName(kind)}`);
	}
	lines.push(';');
	lines.push('');

	// -----------------------------------------------------------------------
	// 7. KindMap — kind string → concrete interface
	// -----------------------------------------------------------------------
	lines.push('/** Maps every kind string to its concrete interface. */');
	lines.push('export interface KindMap {');
	for (const kind of allKinds) {
		const typeName = toTypeName(kind);
		if (generatedTypes.has(typeName)) {
			lines.push(`  '${kind}': ${typeName};`);
		}
	}
	lines.push('}');
	lines.push('');

	return lines.join('\n');
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function fieldTypeExpr(proj: KindProjection, leafSet: Set<string>): string {
	if (proj.expandedAll.length === 0) {
		const anon = proj.anonTokens.filter(t => t !== '_');
		if (anon.length > 0) return anon.map(t => `'${escapeString(t)}'`).join(' | ');
		return 'string';
	}
	return proj.collapsedTypes.join(' | ');
}

function escapeString(s: string): string {
	return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

function emitConcreteInterface(
	lines: string[],
	node: ReturnType<typeof structuralNodes>[number],
	typeName: string,
	ctx: ProjectionContext,
	leafSet: Set<string>,
): void {
	const fields = fieldsOf(node);
	const hasFields = fields.length > 0;
	const hasChildren = node.children != null;

	lines.push(`export interface ${typeName} {`);
	lines.push(`  readonly type: '${node.kind}';`);

	// Fields block — use raw grammar field names to match factory config keys
	if (hasFields) {
		lines.push('  readonly fields: {');
		for (const f of fields) {
			const proj = projectKinds(f.kinds, ctx);
			let typeExpr: string;
			// Override fields with anonymous token values → string literal union
			if (f.overrideAnonymous && f.overrideValues && f.overrideValues.length > 0 && proj.expandedAll.length === 0) {
				typeExpr = f.overrideValues.map(v => `'${escapeString(v)}'`).join(' | ');
			} else {
				typeExpr = fieldTypeExpr(proj, leafSet);
			}
			const opt = f.required ? '' : '?';
			const key = f.propertyName ?? toFieldName(f.name);
			if (f.multiple) {
				lines.push(`    readonly ${key}${opt}: readonly (${typeExpr})[];`);
			} else {
				lines.push(`    readonly ${key}${opt}: ${typeExpr};`);
			}
		}
		lines.push('  };');
	}

	// Hoisted child slots (skip slots already covered by override fields)
	if (hasChildren) {
		const fieldKeys = new Set(fields.map(f => f.propertyName ?? toFieldName(f.name)));
		const slotNames = childSlotNames(node.children!, ctx);
		eachChildSlot(node.children!, (slot, i) => {
			const name = slotNames[i]!;
			if (fieldKeys.has(name)) return; // already emitted as field
			const slotProj = projectKinds(slot.kinds, ctx);
			const slotType = slotProj.collapsedTypes.join(' | ');
			if (slotType === '') return; // skip empty projections
			const opt = slot.required ? '' : '?';
			if (slot.multiple) {
				lines.push(`  readonly ${name}${opt}: readonly (${slotType})[];`);
			} else {
				lines.push(`  readonly ${name}${opt}: ${slotType};`);
			}
		});
	}

	lines.push('}');
}

function emitLeafInterface(
	lines: string[],
	model: HydratedNodeModel,
	typeName: string,
): void {
	if (model.modelType === 'keyword') {
		lines.push(`export interface ${typeName} {`);
		lines.push(`  readonly type: '${model.kind}';`);
		lines.push(`  readonly text: '${escapeString((model as any).text)}';`);
		lines.push('}');
	} else if (model.modelType === 'enum') {
		const values = (model as any).values as string[];
		const union = values.map(v => `'${escapeString(v)}'`).join(' | ');
		lines.push(`export interface ${typeName} {`);
		lines.push(`  readonly type: '${model.kind}';`);
		lines.push(`  readonly text: ${union};`);
		lines.push('}');
	} else {
		lines.push(`export interface ${typeName} {`);
		lines.push(`  readonly type: '${model.kind}';`);
		lines.push('  readonly text: string;');
		lines.push('}');
	}
}

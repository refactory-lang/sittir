// `fieldsOf` → `node.structuralFields` (getter on AssembledNodeBase +
// subclass overrides). One source: each class owns the semantics for
// its own interface surface.

type LookupUnion = (parts: readonly string[]) => string | undefined

function emitInterface(
    lines: string[],
    node: StructuralNode,
    nodeMap: NodeMap,
    lookupUnion?: LookupUnion,
): void {
    const fields = node.structuralFields
    const children = node.structuralChildren
    lines.push(`export interface ${node.typeName} {`)
    // Hidden-source kinds (`_foo`) are exposed at parse time via
    // `alias(_foo, 'foo')` — tree-sitter emits `$type: 'foo'`, NEVER
    // the underscore form. Strip the leading underscore so the
    // interface's declared `$type` matches what readTreeNode produces
    // AND what the factory stamps (factories always stamp the
    // unprefixed kind). Commit 8b5e67d0 punted a follow-up to remap
    // CST `$type: X` → `$type: _X` at readTreeNode time; stripping
    // here is the cheaper direction.
    lines.push(`  readonly $type: '${node.kind.replace(/^_+/, '')}';`)

    if (fields.length > 0) {
        lines.push('  readonly $fields: {')
        for (const f of fields) {
            const typeExpr = fieldTypeExpr(f, nodeMap, lookupUnion)
            const opt = isRequired(f) ? '' : '?'
            // ADR-0012: boolean-keyword / bitflag brand takes precedence over
            // AutoStamp (auto-stamp applies to required-single fields, these
            // brands apply to optional / repeat slots — non-overlapping).
            const wrappedType = wrapFieldTypeForBrand(f, node.kind, nodeMap, typeExpr)
            if (isMultiple(f)) {
                emitFieldArrayDeclaration(lines, f.name, opt, wrappedType, isNonEmpty(f))
            } else {
                lines.push(`    readonly ${f.name}${opt}: ${wrappedType};`)
            }
        }
        lines.push('  };')
    }

    if (children && children.length > 0) {
        const perChildParts = children.map(c => childContentParts(c, nodeMap))
        const allPartsFlat = perChildParts.flat()
        const aliased = lookupUnion?.(allPartsFlat)
        const childTypes = perChildParts.map(parts => parts.join(' | ')).filter(Boolean)
        if (childTypes.length > 0) {
            const union = aliased ?? childTypes.join(' | ')
            const anyMultiple = children.some(c => isMultiple(c))
            const anyNonEmpty = children.some(c => isNonEmpty(c))
            if (anyMultiple) {
                // Same `repeat1` treatment for children slots — a
                // non-empty children list gets a NonEmptyArray head.
                if (anyNonEmpty) {
                    lines.push(`  readonly $children: NonEmptyArray<${union}>;`)
                } else {
                    lines.push(`  readonly $children: readonly (${union})[];`)
                }
            } else {
                lines.push(`  readonly $children: readonly [${union}];`)
            }
        }
    }

    lines.push('}')
    lines.push('')
}

// ---------------------------------------------------------------------------
// Field array declaration emission
// ---------------------------------------------------------------------------

/**
 * Emit the `readonly <name><opt>: <arrayType>` declaration for a repeated
 * (`multiple`) field inside a `$fields` block.
 *
 * @remarks
 * `repeat1` fields carry a grammar-enforced `length >= 1` guarantee. They
 * are emitted as `NonEmptyArray<T>` — the alias is inherently `readonly`
 * (TS1354 forbids prefixing a type-alias reference with `readonly`, so the
 * `readonly` lives inside the alias definition). Plain `repeat` fields stay
 * `readonly T[]`.
 *
 * @param lines - Output line buffer to append to.
 * @param name - The raw field name (snake_case).
 * @param opt - Optionality suffix: `""` for required, `"?"` for optional.
 * @param typeExpr - The resolved TypeScript type expression for the element type.
 * @param nonEmpty - Whether the field is `repeat1` (non-empty array guaranteed).
 *   `undefined` is treated as `false`.
 */
function emitFieldArrayDeclaration(
    lines: string[],
    name: string,
    opt: string,
    typeExpr: string,
    nonEmpty: boolean | undefined,
): void {
    if (nonEmpty) {
        lines.push(`    readonly ${name}${opt}: NonEmptyArray<${typeExpr}>;`)
    } else {
        lines.push(`    readonly ${name}${opt}: readonly (${typeExpr})[];`)
    }
}

/**
 * Expand a field's content types into the identifier parts that
 * would form its type union. Used by both the dedup pre-pass and
 * the emission pass. Literal-value enums and empty unions return
 * `[]` — they don't get aliased because they don't produce a
 * multi-type union.
 */
function fieldTypeParts(field: AssembledField, nodeMap?: NodeMap): string[] {
    const litVals = slotLiteralValues(field)
    if (litVals.length > 0) return []
    const kinds = slotKindNames(field)
    if (kinds.length === 0) return []
    return kinds.map(t => {
        const node = nodeMap?.nodes.get(t)
        if (!node) return JSON.stringify(t)
        const name = node.typeName
        return /^[A-Za-z_$][\w$]*$/.test(name) ? name : JSON.stringify(t)
    })
}

function childContentParts(child: AssembledChild, nodeMap: NodeMap): string[] {
    // Inline pure-literal values as string-literal types, mirroring
    // `fieldTypeComponents`. A child slot that carries terminal values
    // alongside node refs (e.g. `children` on a union) would surface the
    // literal text as a string-literal union member.
    const parts: string[] = []
    for (const v of child.values) {
        if (isTerminalValue(v)) {
            parts.push(JSON.stringify(v.value))
            continue
        }
        if (!isNodeRef(v)) continue
        const t = isUnresolvedRef(v.node) ? v.node.name : v.node.kind
        // Hidden-keyword kinds (`_not_escape_sequence` → `'\\'`) inline
        // their literal text — same rule as `fieldTypeComponents`. Avoids
        // the dangling `NotEscapeSequence` reference that would otherwise
        // require an extra stub alias.
        const lit = resolveHiddenKeywordLiteral(t, nodeMap)
        if (lit !== undefined) {
            parts.push(JSON.stringify(lit))
            continue
        }
        const n = nodeMap.nodes.get(t)
        if (!n) {
            throw new Error(
                `types: child references kind '${t}' which is not in NodeMap.`,
            )
        }
        const name = n.typeName
        parts.push(/^[A-Za-z_$][\w$]*$/.test(name) ? name : JSON.stringify(t))
    }
    return parts
}

// `childrenOf` → `node.structuralChildren` (getter on AssembledNodeBase +
// subclass overrides). Each class owns its own children semantics.

function emitFormInterface(
    lines: string[],
    node: AssembledNode,
    form: AssembledForm,
    typeName: string,
    nodeMap: NodeMap,
    lookupUnion?: LookupUnion,
): void {
    lines.push(`export interface ${typeName} {`)
    // Same underscore strip as `emitInterface` — UForm parents alias
    // through to a visible kind at parse time.
    lines.push(`  readonly $type: '${node.kind.replace(/^_+/, '')}';`)
    // `$variant` literal discriminant — matches what the form factory stamps
    // on its output (`$variant: '<form.name>' as const`). Makes the parent
    // polymorph union a discriminated union so consumers can narrow via
    // `if (expr.$variant === 'binary') { … }` without structural probing.
    lines.push(`  readonly $variant: '${form.name}';`)

    if (form.fields.length > 0) {
        lines.push('  readonly $fields: {')
        for (const f of form.fields) {
            const typeExpr = fieldTypeExpr(f, nodeMap, lookupUnion)
            const opt = isRequired(f) ? '' : '?'
            const wrappedType = wrapFieldTypeForBrand(f, node.kind, nodeMap, typeExpr)
            if (isMultiple(f)) {
                lines.push(`    readonly ${f.name}${opt}: readonly (${wrappedType})[];`)
            } else {
                lines.push(`    readonly ${f.name}${opt}: ${wrappedType};`)
            }
        }
        lines.push('  };')
    }

    emitFormChildrenSlot(lines, form, nodeMap, lookupUnion)

    lines.push('}')
    lines.push('')
}

// ---------------------------------------------------------------------------
// Polymorph form children slot emission
// ---------------------------------------------------------------------------

/**
 * Emit the `readonly $children` slot for a polymorph form interface, if the
 * form has any children.
 *
 * @remarks
 * T063 inlining can introduce children slots on polymorph forms that were not
 * present in the original grammar rule. This mirrors the branch emitter so
 * that factories can reach `config?.children` without tripping the type
 * checker.
 *
 * @param lines - Output line buffer to append to.
 * @param form - The polymorph form being emitted.
 * @param nodeMap - The assembled node map, used to look up child type names.
 * @param lookupUnion - Optional union alias resolver.
 */
function emitFormChildrenSlot(
    lines: string[],
    form: AssembledForm,
    nodeMap: NodeMap,
    lookupUnion?: LookupUnion,
): void {
    if (!form.children || form.children.length === 0) return
    // Filter slots whose refs ALL resolve to non-constructible kinds.
    // A kind is non-constructible when there's nothing for a caller to
    // supply — the UForm's `$variant` discriminator already captures
    // the only semantic distinction. Three flavours:
    //
    //   1. Tokens / non-userFacing hidden helpers (e.g. `_impl_item_semi`
    //      — a bare `;` marker; token modelType, userFacing=false).
    //   2. Hidden single-literal keywords (e.g. `_pointer_type_const` —
    //      inlined at every reference via `resolveHiddenKeywordLiteral`).
    //   3. Empty branches / groups — interface exists but has no
    //      `$fields` and no `$children` (e.g. `_range_expression_postfix`
    //      — a pure marker for the `postfix` variant).
    //
    // Leaving any of these in `$children` emits a dangling reference
    // (the interface is skipped by `emitLeafTerminalAliases` / still
    // produced as empty) and forces the factory to accept a caller
    // value that carries no information beyond its kind.
    const isEmittableRef = (t: string): boolean => {
        const n = nodeMap.nodes.get(t)
        if (!n || !n.userFacing) return false
        if (resolveHiddenKeywordLiteral(t, nodeMap) !== undefined) return false
        // Empty branch / group — no fields and no children to construct.
        if (n.modelType === 'branch' || n.modelType === 'group') {
            const fs = (n as unknown as { fields: readonly unknown[] }).fields
            const cs = (n as unknown as { children?: readonly unknown[] }).children
            if ((fs?.length ?? 0) === 0 && (cs?.length ?? 0) === 0) return false
        }
        return true
    }
    const emittableChildren = form.children.filter(c => slotKindNames(c).some(isEmittableRef))
    if (emittableChildren.length === 0) return
    const parts = emittableChildren
        .map(c => slotKindNames(c).filter(isEmittableRef).map(t => {
            const n = nodeMap.nodes.get(t)!
            const name = n.typeName
            return /^[A-Za-z_$][\w$]*$/.test(name) ? name : JSON.stringify(t)
        }))
    const flatParts = parts.flat()
    const aliased = lookupUnion?.(flatParts)
    const union = aliased ?? parts.map(p => p.join(' | ')).filter(Boolean).join(' | ')
    if (union) {
        const anyMultiple = emittableChildren.some(c => isMultiple(c))
        if (anyMultiple) {
            lines.push(`  readonly $children: readonly (${union})[];`)
        } else {
            lines.push(`  readonly $children: readonly [${union}];`)
        }
    }
}

/**
 * Format a field's type expression for the types.ts surface — bare
 * identifiers (no `T.` prefix) and missing kinds registered via
 * {@link missingKindTypes} for stub emission.
 *
 * Delegates to the shared {@link fieldTypeComponents} walker so the node-ref /
 * literal / alias-source / hidden-keyword logic lives in one place
 * (factories.ts::fieldElementType is the same walk with a `T.` prefix).
 */
function fieldTypeExpr(
    field: AssembledField,
    nodeMap?: NodeMap,
    lookupUnion?: LookupUnion,
): string {
    const litVals = slotLiteralValues(field)
    const kinds = slotKindNames(field)

    // Pure-literal slot (no node refs) — emit as a string-literal union.
    if (kinds.length === 0 && litVals.length > 0) {
        return [...new Set(litVals.map(v => JSON.stringify(v)))].join(' | ')
    }
    if (kinds.length === 0) return 'string'
    if (!nodeMap) return 'string' // defensive; current callers always pass nodeMap

    const components = fieldTypeComponents(field, nodeMap)
    const parts: string[] = []
    for (const comp of components) {
        if (comp.kind === 'literal') {
            parts.push(JSON.stringify(comp.value))
        } else if (comp.kind === 'nodeKind') {
            parts.push(isValidIdent(comp.value) ? comp.value : JSON.stringify(comp.rawKind))
        } else {
            // missing kind — register for stub emission and use the
            // PascalCase fallback name (bare, no prefix).
            missingKindTypes.set(comp.rawKind, comp.value)
            parts.push(comp.value)
        }
    }
    const deduped = [...new Set(parts)]
    const alias = lookupUnion?.(deduped)
    if (alias) return alias
    return deduped.join(' | ')
}

/**
 * Wrap a field's type expression with the correct ADR-0012 brand when
 * the field classifies as keyword-presence, or fall through to the
 * auto-stamp brand, or no brand otherwise.
 *
 * Precedence:
 *   1. `BooleanKeyword<T>` when `keywordPresenceKind === 'boolean'`.
 *   2. `Bitflag<ConstEnumName, T>` when `keywordPresenceKind === 'bitflag'`.
 *   3. `AutoStamp<T>` when `isAutoStampField`.
 *   4. Bare `T` otherwise.
 *
 * The keyword-presence and auto-stamp domains don't overlap: auto-stamp
 * requires required+non-repeated, boolean requires optional, bitflag
 * requires repeat.
 */
function wrapFieldTypeForBrand(
    f: AssembledField,
    kind: string,
    nodeMap: NodeMap,
    typeExpr: string,
): string {
    const kw = keywordPresenceKind(f, nodeMap)
    if (kw === 'boolean') return `BooleanKeyword<${typeExpr}>`
    if (kw === 'bitflag') {
        const constName = resolveBitflagConstName(kind, f, nodeMap) ?? 'number'
        if (constName !== 'number') referencedBitflagConsts.add(constName)
        return `Bitflag<${constName}, ${typeExpr}>`
    }
    if (isAutoStampField(f, nodeMap)) return `AutoStamp<${typeExpr}>`
    return typeExpr
}

function toPascal(kind: string): string {
    return kind.replace(/^_/, '').split('_').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join('')
}

/** Quote a type/object key if it is not a plain identifier. */
function quoteKey(key: string): string {
    return /^[A-Za-z_$][\w$]*$/.test(key) ? key : JSON.stringify(key)
}

// ---------------------------------------------------------------------------
// refine() per-form type emission (ADR-0010 phase 2)
// ---------------------------------------------------------------------------

/**
 * Emit per-form Tree aliases for every refined kind.
 *
 * @remarks
 * Refine narrows choice selections at the Config/factory surface, not
 * the parse shape — the tree produced by tree-sitter is identical
 * regardless of which form constructed the node. The per-form Tree
 * alias therefore points at the base kind's Tree type; it exists so
 * method return types (`curly().typeField(...)`) can name a form-
 * specific Tree type at compile time without a structural duplicate.
 */
function emitRefineFormTreeAliases(
    lines: string[],
    refineInfos: readonly RefineKindInfo[] | undefined,
): void {
    if (!refineInfos || refineInfos.length === 0) return
    lines.push('// refine() per-form Tree aliases — same shape as the base kind Tree.')
    for (const info of refineInfos) {
        for (const form of info.forms) {
            const formType = refineFormTypeName(info.typeName, form.name)
            lines.push(`export type ${formType}Tree = ${info.typeName}Tree;`)
        }
    }
    lines.push('')
}

/**
 * Emit the namespace sugar block for one kind — the declaration-merged
 * `namespace <TypeName> { Config; Fluent; Loose; Tree; Kind; }` block,
 * plus per-form sub-namespaces when refine() registered forms for this
 * kind.
 *
 * For refined kinds:
 *   - Each form gets its own sub-namespace `<TypeName>.<FormPascal>`
 *     exposing `Config` (base Config minus the form's auto-stamped
 *     fields) and `Tree` (alias to the base kind Tree).
 *   - The top-level `<TypeName>.Config` shadows the generic
 *     `ConfigFor<'kind'>` with the first-declared form's Config — so
 *     bare-call sugar `ir.<kind>({...})` routes to the default form's
 *     Config surface.
 */
function emitNamespaceSugarBlock(
    lines: string[],
    kind: string,
    node: AssembledNode,
    refineInfo: RefineKindInfo | undefined,
): void {
    lines.push(`export namespace ${node.typeName} {`)
    if (refineInfo && refineInfo.forms.length > 0) {
        emitRefineFormSubNamespaces(lines, node.typeName, kind, refineInfo)
        const defaultForm = refineInfo.forms[0]!
        const defaultShortName = refineFormTypeName(node.typeName, defaultForm.name)
            .slice(node.typeName.length)
        lines.push(`  /** Default form: '${defaultForm.name}' (first-declared). */`)
        lines.push(`  export type Config = ${defaultShortName}.Config;`)
    } else {
        lines.push(`  export type Config = ConfigFor<'${kind}'>;`)
    }
    lines.push(`  export type Fluent = FluentFor<'${kind}'>;`)
    lines.push(`  export type Loose = LooseFor<'${kind}'>;`)
    lines.push(`  export type Tree = TreeFor<'${kind}'>;`)
    lines.push(`  export type Kind = '${kind}';`)
    lines.push('}')
}

/**
 * Emit the per-form sub-namespace blocks for a refined kind.
 *
 * Each form gets:
 *   - `Config` — `Omit<ConfigFor<'kind'>, 'field1' | 'field2'>` stripping
 *     the form's narrowed fields (those selections map to a single
 *     string literal, so phase-1 auto-stamp would otherwise need to be
 *     reapplied on top of the main Config).
 *   - `Tree`   — alias to the base Tree type (same parse shape).
 */
function emitRefineFormSubNamespaces(
    lines: string[],
    parentTypeName: string,
    kind: string,
    refineInfo: RefineKindInfo,
): void {
    for (const form of refineInfo.forms) {
        const formType = refineFormTypeName(parentTypeName, form.name)
        const shortName = formType.slice(parentTypeName.length)
        lines.push(`  export namespace ${shortName} {`)
        const narrowed = form.narrowedFields
        if (narrowed.length > 0) {
            const omitKeys = narrowed
                .map(n => JSON.stringify(snakeToCamel(n.fieldName)))
                .join(' | ')
            lines.push(`    export type Config = Omit<ConfigFor<'${kind}'>, ${omitKeys}>;`)
        } else {
            lines.push(`    export type Config = ConfigFor<'${kind}'>;`)
        }
        lines.push(`    export type Tree = ${formType}Tree;`)
        lines.push('  }')
    }
}

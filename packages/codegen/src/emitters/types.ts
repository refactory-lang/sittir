// SyntaxKind enum emission
// ---------------------------------------------------------------------------

/**
 * Emit the `export const enum SyntaxKind { … }` block, deduplicating on
 * member name.
 *
 * @remarks
 * Two kinds can resolve to the same `typeName` (e.g. python's `true` and
 * the string-literal keyword `'True'` both map to `True`). The first
 * occurrence wins; subsequent duplicates are skipped to avoid a
 * `const enum` duplicate-member error.
 *
 * @param lines - Output line buffer to append to.
 * @param allKinds - Ordered list of all kind strings (structural + leaf).
 * @param nodeMap - The assembled node map, used to look up `typeName` per kind.
 */
function emitSyntaxKindEnum(lines: string[], allKinds: readonly string[], nodeMap: NodeMap): void {
	lines.push('export const enum SyntaxKind {');
	const seenEnumMembers = new Set<string>();
	for (const kind of allKinds) {
		const node = nodeMap.nodes.get(kind);
		const member = node?.typeName ?? toPascal(kind);
		if (seenEnumMembers.has(member)) continue;
		seenEnumMembers.add(member);
		lines.push(`  ${member} = '${kind}',`);
	}
	lines.push('}');
	lines.push('');
}

/**
 * Emit the runtime KindID enum and bidirectional lookup helpers.
 *
 * @remarks
 * The generator stays name-first: the lookup helpers are still emitted
 * from kind names, but the runtime discriminant surface is numeric so
 * data/transport interfaces can carry `TSKindId.*` instead of string
 * literals.
 */
function emitKindIdEnumAndLookups(lines: string[], entries: KindEnumEntry[]): void {
	lines.push('export const enum TSKindId {');
	for (const entry of entries) {
		lines.push(`  ${entry.member} = ${entry.id},`);
	}
	lines.push('}');
	lines.push('');

	lines.push('export const KIND_NAMES: ReadonlyMap<number, string> = new Map([');
	for (const entry of entries) {
		lines.push(`  [${entry.id}, ${JSON.stringify(entry.kind)}],`);
	}
	lines.push(']);');
	lines.push('');

	lines.push('export function kindIdFromName(kindName: string): TSKindId {');
	lines.push('  switch (kindName) {');
	// Catalog-key cases (e.g. `"as_pattern"`, `"plus"`).
	for (const entry of entries) {
		lines.push(`    case ${JSON.stringify(entry.kind)}: return TSKindId.${entry.member};`);
	}
	// Symbol-value cases: resolve display names (e.g. `"+"` → Plus,
	// `"is not"` → IsNot) so callers can pass the literal text.
	// Only emitted when symbolName doesn't collide with an existing
	// catalog key — prevents the python `_as_pattern` symbolName
	// `"as_pattern"` shadowing the real `as_pattern` entry.
	const seenCases = new Set(entries.map((e) => e.kind));
	for (const entry of entries) {
		if (!entry.symbolName) continue;
		if (seenCases.has(entry.symbolName)) continue;
		seenCases.add(entry.symbolName);
		lines.push(`    case ${JSON.stringify(entry.symbolName)}: return TSKindId.${entry.member};`);
	}
	lines.push('    default: throw new TypeError(`unknown kind name ${kindName}`);');
	lines.push('  }');
	lines.push('}');
	lines.push('');
}


/**
 * Emit the `readonly $children` slot for a polymorph form interface, if the
 * form contains children.
 *
 * @remarks
 * T063 inlining can introduce children slots on polymorph forms that were not
 * present in the original grammar rule. This mirrors the branch emitter so
 * that factories can reach `config?.children` without tripping the type
 * checker.
 *
 * @param lines - Output line buffer to append to.
 * @param form - The polymorph form being emitted.
 * @param nodeMap - The assembled node map, used to look up child type names.
 * @param lookupUnion - Optional union alias resolver.
 */
function emitFormChildrenSlot(
	lines: string[],
	form: AssembledGroup,
	nodeMap: NodeMap,
	lookupUnion?: LookupUnion
): void {
	if (!form.children || form.children.length === 0) return;
	// Filter slots whose refs ALL resolve to non-constructible kinds.
	// A kind is non-constructible when there's nothing for a caller to
	// supply — the UForm's `$variant` discriminator already captures
	// the only semantic distinction. Three flavours:
	//
	//   1. Tokens / non-userFacing hidden helpers (e.g. `_impl_item_semi`
	//      — a bare `;` marker; token modelType, userFacing=false).
	//   2. Hidden single-literal keywords (e.g. `_pointer_type_const` —
	//      inlined at every reference via `resolveHiddenKeywordLiteral`).
	//   3. Empty branches / groups — interface exists but has no
	//      `$fields` and no `$children` (e.g. `_range_expression_postfix`
	//      — a pure marker for the `postfix` variant).
	//
	// Leaving any of these in `$children` emits a dangling reference
	// (the interface is skipped by `emitLeafTerminalAliases` / still
	// produced as empty) and forces the factory to accept a caller
	// value that carries no information beyond its kind.
	const isEmittableRef = (t: string): boolean => {
		const n = nodeMap.nodes.get(t);
		if (!n) return false;
		// T046: hidden non-token groups have fragment factories and interfaces —
		// include them in child slots. Token modelType hidden kinds stay excluded.
		const isHiddenGroup = t.startsWith('_') && n.modelType !== 'token' && n.modelType !== 'multi';
		if (!n.userFacing && !isHiddenGroup) return false;
		if (resolveHiddenKeywordLiteral(t, nodeMap) !== undefined) return false;
		// Empty branch / group — no fields and no children to construct.
		if (n.modelType === 'branch' || n.modelType === 'group') {
			if (n.fields.length === 0 && n.children.length === 0) return false;
		}
		return true;
	};
	const emittableChildren = form.children.filter((c) => slotKindNames(c).some(isEmittableRef));
	if (emittableChildren.length === 0) return;
	const parts = emittableChildren.map((c) =>
		slotKindNames(c)
			.filter(isEmittableRef)
			.map((t) => {
				const n = nodeMap.nodes.get(t)!;
				const name = n.typeName;
				return /^[A-Za-z_$][\w$]*$/.test(name) ? name : JSON.stringify(t);
			})
	);
	const flatParts = parts.flat();
	const aliased = lookupUnion?.(flatParts);
	const union =
		aliased ??
		parts
			.map((p) => p.join(' | '))
			.filter(Boolean)
			.join(' | ');
	if (union) {
		const anyMultiple = emittableChildren.some((c) => isMultiple(c));
		if (anyMultiple) {
			lines.push(`  readonly $children: readonly (${union})[];`);
		} else {
			lines.push(`  readonly $children: readonly [${union}];`);
		}
	}
}

/**
 * Format a field's type expression for the types.ts surface — bare
 * identifiers (no `T.` prefix) and missing kinds registered via
 * {@link missingKindTypes} for stub emission.
 *
 * Delegates to the shared {@link fieldTypeComponents} walker so the node-ref /
 * literal / alias-source / hidden-keyword logic lives in one place
 * (factories.ts::fieldElementType is the same walk with a `T.` prefix).
 */
function fieldTypeExpr(field: AssembledNonterminal, nodeMap?: NodeMap, lookupUnion?: LookupUnion): string {
	const litVals = slotLiteralValues(field);
	const kinds = slotKindNames(field);

	// Pure-literal slot (no node refs) — emit as a string-literal union.
	if (kinds.length === 0 && litVals.length > 0) {
		return [...new Set(litVals.map((v) => JSON.stringify(v)))].join(' | ');
	}
	if (kinds.length === 0) return 'string';
	if (!nodeMap) return 'string'; // defensive; current callers always pass nodeMap

	const components = fieldTypeComponents(field, nodeMap);
	const parts: string[] = [];
	for (const comp of components) {
		if (comp.kind === 'literal') {
			parts.push(JSON.stringify(comp.value));
		} else if (comp.kind === 'nodeKind') {
			parts.push(isValidIdent(comp.value) ? comp.value : JSON.stringify(comp.rawKind));
		} else {
			// missing kind — register for stub emission and use the
			// PascalCase fallback name (bare, no prefix).
			missingKindTypes.set(comp.rawKind, comp.value);
			parts.push(comp.value);
		}
	}
	const deduped = [...new Set(parts)];
	const alias = lookupUnion?.(deduped);
	if (alias) return alias;
	return deduped.join(' | ');
}

/**
 * Wrap a field's type expression with the correct ADR-0012 brand when
 * the field classifies as keyword-presence, or fall through to the
 * auto-stamp brand, or no brand otherwise.
 *
 * Precedence:
 *   1. `BooleanKeyword<T>` when `keywordPresenceKind === 'boolean'`.
 *   2. `Bitflag<ConstEnumName, T>` when `keywordPresenceKind === 'bitflag'`.
 *   3. `AutoStamp<T>` when `isAutoStampField`.
 *   4. Bare `T` otherwise.
 *
 * The keyword-presence and auto-stamp domains don't overlap: auto-stamp
 * requires required+non-repeated, boolean requires optional, bitflag
 * requires repeat.
 */
function stringUnion(values: readonly string[]): string {
	return values.length === 0 ? 'never' : values.map((value) => JSON.stringify(value)).join(' | ');
}

function enumStorageDiscriminantExpr(
	storageInfo: ReturnType<typeof resolveFieldStorageInfo>,
	nodeMap: NodeMap,
	kindEntries: readonly KindEnumEntry[] | undefined
): string {
	if (!kindEntries) return 'number';
	const members = new Set<string>();
	for (const enumKind of storageInfo.enumKinds) {
		const node = nodeMap.nodes.get(enumKind);
		if (!(node instanceof AssembledEnum)) continue;
		for (const value of node.values) {
			const entry = findKindEntry(kindEntries, value);
			if (entry) members.add(`TSKindId.${entry.member}`);
		}
	}
	return members.size === 0 ? 'number' : [...members].join(' | ');
}

function storageFieldTypeExpr(
	f: AssembledNonterminal,
	nodeMap: NodeMap,
	typeExpr: string
): string {
	const storageInfo = resolveFieldStorageInfo(f, nodeMap);
	if (storageInfo.kind === 'boolean') {
		return 'boolean';
	}
	if (storageInfo.kind === 'bitflag') {
		return 'number';
	}
	if (storageInfo.kind === 'kindEnum') {
		return 'number';
	}
	if (isAutoStampField(f, nodeMap)) return `AutoStamp<${typeExpr}>`;
	return typeExpr;
}

function fieldInputHintTypeExpr(
	f: AssembledNonterminal,
	kind: string,
	nodeMap: NodeMap,
	kindEntries: readonly KindEnumEntry[] | undefined
): string | undefined {
	const storageInfo = resolveFieldStorageInfo(f, nodeMap);
	if (storageInfo.kind === 'boolean') {
		return `BooleanKeyword<${stringUnion(storageInfo.texts)}>`;
	}
	if (storageInfo.kind === 'bitflag') {
		const constName = resolveBitflagConstName(kind, f, nodeMap) ?? 'number';
		if (constName !== 'number') referencedBitflagConsts.add(constName);
		return `Bitflag<${constName}, number>`;
	}
	if (storageInfo.kind === 'kindEnum') {
		return `KindEnum<${stringUnion(storageInfo.texts)}, ${enumStorageDiscriminantExpr(storageInfo, nodeMap, kindEntries)}>`;
	}
	return undefined;
}

function emitFieldInputHints(
	lines: string[],
	fields: readonly AssembledNonterminal[],
	kind: string,
	nodeMap: NodeMap,
	kindEntries: readonly KindEnumEntry[] | undefined
): void {
	const hintLines = fields
		.map((field) => {
			const hintType = fieldInputHintTypeExpr(field, kind, nodeMap, kindEntries);
			if (!hintType) return undefined;
			const opt = isRequired(field) ? '' : '?';
			const storageInfo = resolveFieldStorageInfo(field, nodeMap);
			if (isMultiple(field) && !storageInfo.collapsesMultiplicity) {
				const arrType = isNonEmpty(field) ? `NonEmptyArray<${hintType}>` : `readonly (${hintType})[]`;
				return `    readonly ${quoteKey(field.name)}${opt}: ${arrType};`;
			}
			return `    readonly ${quoteKey(field.name)}${opt}: ${hintType};`;
		})
		.filter((line): line is string => line !== undefined);
	if (hintLines.length === 0) return;
	lines.push('  readonly __inputHints__?: {');
	lines.push(...hintLines);
	lines.push('  };');
}

function toPascal(kind: string): string {
	return kind
		.replace(/^_/, '')
		.split('_')
		.map((w) => w.charAt(0).toUpperCase() + w.slice(1))
		.join('');
}

/** Quote a type/object key if it is not a plain identifier. */
function quoteKey(key: string): string {
	return /^[A-Za-z_$][\w$]*$/.test(key) ? key : JSON.stringify(key);
}


/**
 * Emit a single `export interface <TypeName>Ns extends NodeNs<…> {}` line.
 *
 * @remarks
 * Spec 009 Layer 1: threads `NamespaceMap` through `NodeNs` so that
 * `Loose` → `FromInputOf<T, Scalars, Strings, [], NamespaceMap>` can
 * short-circuit multi-branch union recursions to `NamespaceMap[K]['Loose']`
 * lookups instead of re-projecting per arm.
 *
 * @param lines - Output line buffer to append to.
 * @param typeName - The `TypeName` portion of the interface name.
 */
function emitNamespaceInterfaceLine(lines: string[], typeName: string): void {
    lines.push(`export interface ${typeName}Ns extends NodeNs<${typeName}, LeafScalarMap, LeafStringMap, NamespaceMap> {}`)
}

// ---------------------------------------------------------------------------
// Interface emitters
// ---------------------------------------------------------------------------

function fieldsOf(node: AssembledNode): readonly AssembledField[] {
    if (node.modelType === 'branch') return node.fields
    if (node.modelType === 'polymorph') {
        const seen = new Set<string>()
        const fields: AssembledField[] = []
        for (const form of node.forms) {
            for (const f of form.fields) {
                if (!seen.has(f.name)) { seen.add(f.name); fields.push(f) }
            }
        }
        return fields
    }
    return []
}

type LookupUnion = (parts: readonly string[]) => string | undefined

function emitInterface(
    lines: string[],
    node: StructuralNode,
    nodeMap: NodeMap,
    lookupUnion?: LookupUnion,
): void {
    const fields = fieldsOf(node)
    const children = childrenOf(node)
    lines.push(`export interface ${node.typeName} {`)
    lines.push(`  readonly $type: '${node.kind}';`)

    if (fields.length > 0) {
        lines.push('  readonly $fields: {')
        for (const f of fields) {
            const typeExpr = fieldTypeExpr(f, nodeMap, lookupUnion)
            const opt = isRequired(f) ? '' : '?'
            // Auto-stamp fields are wrapped in AutoStamp<> so ConfigOf/FromInputOf
            // can exclude them from the input surface. The field is still present
            // in $fields so NodeData output shape and readNode round-trips are
            // unchanged.
            const wrappedType = isAutoStampField(f, nodeMap) ? `AutoStamp<${typeExpr}>` : typeExpr
            if (isMultiple(f)) {
                emitFieldArrayDeclaration(lines, f.name, opt, wrappedType, isNonEmpty(f))
            } else {
                lines.push(`    readonly ${f.name}${opt}: ${wrappedType};`)
            }
        }
        lines.push('  };')
    }

    if (children && children.length > 0) {
        const perChildParts = children.map(c => childContentParts(c, nodeMap))
        const allPartsFlat = perChildParts.flat()
        const aliased = lookupUnion?.(allPartsFlat)
        const childTypes = perChildParts.map(parts => parts.join(' | ')).filter(Boolean)
        if (childTypes.length > 0) {
            const union = aliased ?? childTypes.join(' | ')
            const anyMultiple = children.some(c => isMultiple(c))
            const anyNonEmpty = children.some(c => isNonEmpty(c))
            if (anyMultiple) {
                // Same `repeat1` treatment for children slots — a
                // non-empty children list gets a NonEmptyArray head.
                if (anyNonEmpty) {
                    lines.push(`  readonly $children: NonEmptyArray<${union}>;`)
                } else {
                    lines.push(`  readonly $children: readonly (${union})[];`)
                }
            } else {
                lines.push(`  readonly $children: readonly [${union}];`)
            }
        }
    }

    lines.push('}')
    lines.push('')
}

// ---------------------------------------------------------------------------
// Field array declaration emission
// ---------------------------------------------------------------------------

/**
 * Emit the `readonly <name><opt>: <arrayType>` declaration for a repeated
 * (`multiple`) field inside a `$fields` block.
 *
 * @remarks
 * `repeat1` fields carry a grammar-enforced `length >= 1` guarantee. They
 * are emitted as `NonEmptyArray<T>` — the alias is inherently `readonly`
 * (TS1354 forbids prefixing a type-alias reference with `readonly`, so the
 * `readonly` lives inside the alias definition). Plain `repeat` fields stay
 * `readonly T[]`.
 *
 * @param lines - Output line buffer to append to.
 * @param name - The raw field name (snake_case).
 * @param opt - Optionality suffix: `""` for required, `"?"` for optional.
 * @param typeExpr - The resolved TypeScript type expression for the element type.
 * @param nonEmpty - Whether the field is `repeat1` (non-empty array guaranteed).
 *   `undefined` is treated as `false`.
 */
function emitFieldArrayDeclaration(
    lines: string[],
    name: string,
    opt: string,
    typeExpr: string,
    nonEmpty: boolean | undefined,
): void {
    if (nonEmpty) {
        lines.push(`    readonly ${name}${opt}: NonEmptyArray<${typeExpr}>;`)
    } else {
        lines.push(`    readonly ${name}${opt}: readonly (${typeExpr})[];`)
    }
}

/**
 * Expand a field's content types into the identifier parts that
 * would form its type union. Used by both the dedup pre-pass and
 * the emission pass. Literal-value enums and empty unions return
 * `[]` — they don't get aliased because they don't produce a
 * multi-type union.
 */
function fieldTypeParts(field: AssembledField, nodeMap?: NodeMap): string[] {
    const litVals = slotLiteralValues(field)
    if (litVals.length > 0) return []
    const kinds = slotKindNames(field)
    if (kinds.length === 0) return []
    return kinds.map(t => {
        const node = nodeMap?.nodes.get(t)
        if (!node) return JSON.stringify(t)
        const name = node.typeName
        return /^[A-Za-z_$][\w$]*$/.test(name) ? name : JSON.stringify(t)
    })
}

function childContentParts(child: AssembledChild, nodeMap: NodeMap): string[] {
    return slotKindNames(child).map(t => {
        const n = nodeMap.nodes.get(t)
        if (!n) {
            throw new Error(
                `types: child references kind '${t}' which is not in NodeMap.`,
            )
        }
        const name = n.typeName
        return /^[A-Za-z_$][\w$]*$/.test(name) ? name : JSON.stringify(t)
    })
}

function childrenOf(node: AssembledNode): readonly AssembledChild[] {
    if (node instanceof AssembledBranch || node instanceof AssembledContainer || node instanceof AssembledGroup) {
        return node.children ?? []
    }
    return []
}

function emitFormInterface(
    lines: string[],
    node: AssembledNode,
    form: AssembledForm,
    typeName: string,
    nodeMap: NodeMap,
    lookupUnion?: LookupUnion,
): void {
    lines.push(`export interface ${typeName} {`)
    lines.push(`  readonly $type: '${node.kind}';`)
    // `$variant` literal discriminant — matches what the form factory stamps
    // on its output (`$variant: '<form.name>' as const`). Makes the parent
    // polymorph union a discriminated union so consumers can narrow via
    // `if (expr.$variant === 'binary') { … }` without structural probing.
    lines.push(`  readonly $variant: '${form.name}';`)

    if (form.fields.length > 0) {
        lines.push('  readonly $fields: {')
        for (const f of form.fields) {
            const typeExpr = fieldTypeExpr(f, nodeMap, lookupUnion)
            const opt = isRequired(f) ? '' : '?'
            const wrappedType = isAutoStampField(f, nodeMap) ? `AutoStamp<${typeExpr}>` : typeExpr
            if (isMultiple(f)) {
                lines.push(`    readonly ${f.name}${opt}: readonly (${wrappedType})[];`)
            } else {
                lines.push(`    readonly ${f.name}${opt}: ${wrappedType};`)
            }
        }
        lines.push('  };')
    }

    emitFormChildrenSlot(lines, form, nodeMap, lookupUnion)

    lines.push('}')
    lines.push('')
}

// ---------------------------------------------------------------------------
// Polymorph form children slot emission
// ---------------------------------------------------------------------------

/**
 * Emit the `readonly $children` slot for a polymorph form interface, if the
 * form has any children.
 *
 * @remarks
 * T063 inlining can introduce children slots on polymorph forms that were not
 * present in the original grammar rule. This mirrors the branch emitter so
 * that factories can reach `config?.children` without tripping the type
 * checker.
 *
 * @param lines - Output line buffer to append to.
 * @param form - The polymorph form being emitted.
 * @param nodeMap - The assembled node map, used to look up child type names.
 * @param lookupUnion - Optional union alias resolver.
 */
function emitFormChildrenSlot(
    lines: string[],
    form: AssembledForm,
    nodeMap: NodeMap,
    lookupUnion?: LookupUnion,
): void {
    if (!form.children || form.children.length === 0) return
    const parts = form.children
        .map(c => slotKindNames(c).map(t => {
            const n = nodeMap.nodes.get(t)
            if (!n) return JSON.stringify(t)
            const name = n.typeName
            return /^[A-Za-z_$][\w$]*$/.test(name) ? name : JSON.stringify(t)
        }))
    const flatParts = parts.flat()
    const aliased = lookupUnion?.(flatParts)
    const union = aliased ?? parts.map(p => p.join(' | ')).filter(Boolean).join(' | ')
    if (union) {
        const anyMultiple = form.children.some(c => isMultiple(c))
        if (anyMultiple) {
            lines.push(`  readonly $children: readonly (${union})[];`)
        } else {
            lines.push(`  readonly $children: readonly [${union}];`)
        }
    }
}

/**
 * Format a field's type expression for the types.ts surface — bare
 * identifiers (no `T.` prefix) and missing kinds registered via
 * {@link missingKindTypes} for stub emission.
 *
 * Delegates to the shared {@link fieldTypeComponents} walker so the node-ref /
 * literal / alias-source / hidden-keyword logic lives in one place
 * (factories.ts::fieldElementType is the same walk with a `T.` prefix).
 */
function fieldTypeExpr(
    field: AssembledField,
    nodeMap?: NodeMap,
    lookupUnion?: LookupUnion,
): string {
    const litVals = slotLiteralValues(field)
    const kinds = slotKindNames(field)

    // Pure-literal slot (no node refs) — emit as a string-literal union.
    if (kinds.length === 0 && litVals.length > 0) {
        return [...new Set(litVals.map(v => JSON.stringify(v)))].join(' | ')
    }
    if (kinds.length === 0) return 'string'
    if (!nodeMap) return 'string' // defensive; current callers always pass nodeMap

    const components = fieldTypeComponents(field, nodeMap)
    const parts: string[] = []
    for (const comp of components) {
        if (comp.kind === 'literal') {
            parts.push(JSON.stringify(comp.value))
        } else if (comp.kind === 'nodeKind') {
            parts.push(isValidIdent(comp.value) ? comp.value : JSON.stringify(comp.rawKind))
        } else {
            // missing kind — register for stub emission and use the
            // PascalCase fallback name (bare, no prefix).
            missingKindTypes.set(comp.rawKind, comp.value)
            parts.push(comp.value)
        }
    }
    const deduped = [...new Set(parts)]
    const alias = lookupUnion?.(deduped)
    if (alias) return alias
    return deduped.join(' | ')
}

function toPascal(kind: string): string {
    return kind.replace(/^_/, '').split('_').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join('')
}

/** Quote a type/object key if it is not a plain identifier. */
function quoteKey(key: string): string {
    return /^[A-Za-z_$][\w$]*$/.test(key) ? key : JSON.stringify(key)
}

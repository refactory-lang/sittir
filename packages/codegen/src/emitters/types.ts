/**
 * Emits types.ts — all type aliases derived from the grammar.
 *
 * Uses `interface extends` pattern for compiler caching performance.
 * Every type is derived from NodeData<G,K>, NodeFields<G,K>, or TreeNode<G,K>.
 * The grammar is the single source of truth — no hand-written interfaces.
 *
 *   1. const enum SyntaxKind — all named node kinds
 *   2. Scoped const enums per supertype
 *   3. Node interfaces — interface Foo extends NodeData<G, 'foo'> {}
 *   4. Fields interfaces — interface FooFields extends NodeFields<G, 'foo'> {}
 *   5. Tree interfaces — interface FooTree extends TreeNode<G, 'foo'> {}
 *   6. Supertype unions
 *   7. Discriminated union of all node types
 */

import type { SupertypeInfo } from '../grammar-reader.ts';
import { toTypeName, toGrammarTypeName } from '../naming.ts';

export interface EmitTypesConfig {
	grammar: string;
	nodeKinds: string[];
	leafKinds?: string[];
	supertypes?: SupertypeInfo[];
}

export function emitTypes(config: EmitTypesConfig): string {
	const { grammar, nodeKinds, leafKinds = [], supertypes = [] } = config;
	const grammarTypeName = toGrammarTypeName(grammar);
	const grammarPrefix = grammarTypeName.slice(0, -5);
	const grammarAlias = `${grammarPrefix}Grammar`;

	const allKinds = [...nodeKinds, ...leafKinds];
	const generatedTypes = new Set<string>();

	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('');
	lines.push(`import type { ${grammarAlias} } from './grammar.js';`);
	lines.push("import type { NodeData as BaseNodeData, NodeFields as BaseNodeFields, TreeNode as BaseTreeNode, NodeKind } from '@sittir/types';");
	lines.push('');
	lines.push(`export type { ${grammarAlias} };`);
	lines.push('');
	lines.push('// Grammar-bound type aliases — use NodeData<K> instead of BaseNodeData<G, K>');
	lines.push(`export type NodeData<K extends NodeKind<${grammarAlias}>> = BaseNodeData<${grammarAlias}, K>;`);
	lines.push(`export type NodeFields<K extends NodeKind<${grammarAlias}>> = BaseNodeFields<${grammarAlias}, K>;`);
	lines.push(`export type TreeNode<K extends NodeKind<${grammarAlias}>> = BaseTreeNode<${grammarAlias}, K>;`);
	lines.push('');

	// -----------------------------------------------------------------------
	// 1. const enum SyntaxKind — all named node kinds
	// -----------------------------------------------------------------------
	lines.push('/** All named node kinds in this grammar. */');
	lines.push('export const enum SyntaxKind {');
	for (const kind of allKinds) {
		const enumKey = toTypeName(kind);
		lines.push(`  ${enumKey} = '${kind}',`);
	}
	lines.push('}');
	lines.push('');

	// -----------------------------------------------------------------------
	// 2. Scoped const enums per supertype
	// -----------------------------------------------------------------------
	if (supertypes.length > 0) {
		lines.push('// Scoped enums per supertype');
		for (const st of supertypes) {
			const cleanName = st.name.replace(/^_/, '');
			const enumName = toTypeName(cleanName) + 'Kind';
			lines.push(`export const enum ${enumName} {`);
			for (const sub of st.subtypes) {
				const enumKey = toTypeName(sub);
				lines.push(`  ${enumKey} = '${sub}',`);
			}
			lines.push('}');
			lines.push('');
		}
	}

	// -----------------------------------------------------------------------
	// 3. Node interfaces — interface extends NodeData<G, K>
	//    These are the construction types (factory output).
	// -----------------------------------------------------------------------
	lines.push('// Node types — grammar-derived, interface for compiler caching');
	for (const kind of nodeKinds) {
		const typeName = toTypeName(kind);
		generatedTypes.add(typeName);
		lines.push(`export interface ${typeName} extends NodeData<'${kind}'> {}`);
	}
	lines.push('');

	// Leaf types
	lines.push('// Leaf node types');
	for (const kind of leafKinds) {
		const typeName = toTypeName(kind);
		if (generatedTypes.has(typeName)) continue;
		generatedTypes.add(typeName);
		lines.push(`export interface ${typeName} extends NodeData<'${kind}'> {}`);
	}
	lines.push('');

	// -----------------------------------------------------------------------
	// 4. Fields interfaces — interface extends NodeFields<G, K>
	//    Used as factory config and .from() input base.
	// -----------------------------------------------------------------------
	lines.push('// Fields types — factory config shapes');
	for (const kind of nodeKinds) {
		const typeName = toTypeName(kind) + 'Fields';
		lines.push(`export interface ${typeName} extends NodeFields<'${kind}'> {}`);
	}
	lines.push('');

	// -----------------------------------------------------------------------
	// 5. Tree interfaces — interface extends TreeNode<G, K>
	//    Parsed tree nodes with typed field() access.
	// -----------------------------------------------------------------------
	lines.push('// Tree types — parsed tree nodes with typed navigation');
	for (const kind of nodeKinds) {
		const typeName = toTypeName(kind) + 'Tree';
		lines.push(`export interface ${typeName} extends TreeNode<'${kind}'> {}`);
	}
	lines.push('');

	lines.push('// Leaf tree types');
	for (const kind of leafKinds) {
		const typeName = toTypeName(kind) + 'Tree';
		lines.push(`export interface ${typeName} extends TreeNode<'${kind}'> {}`);
	}
	lines.push('');

	// -----------------------------------------------------------------------
	// 6. Supertype unions
	// -----------------------------------------------------------------------
	if (supertypes.length > 0) {
		lines.push('// Supertype unions (node)');
		for (const st of supertypes) {
			const cleanName = st.name.replace(/^_/, '');
			const typeName = toTypeName(cleanName);
			if (generatedTypes.has(typeName)) continue;
			generatedTypes.add(typeName);

			const members = st.subtypes
				.map(sub => toTypeName(sub))
				.filter(t => generatedTypes.has(t));

			if (members.length > 0) {
				lines.push(`export type ${typeName} =`);
				for (const m of members) lines.push(`  | ${m}`);
				lines.push(';');
				lines.push('');
			}
		}

		lines.push('// Supertype unions (tree)');
		for (const st of supertypes) {
			const cleanName = st.name.replace(/^_/, '');
			const treeName = toTypeName(cleanName) + 'Tree';
			const members = st.subtypes.map(sub => toTypeName(sub) + 'Tree');

			if (members.length > 0) {
				lines.push(`export type ${treeName} =`);
				for (const m of members) lines.push(`  | ${m}`);
				lines.push(';');
				lines.push('');
			}
		}
	}

	// -----------------------------------------------------------------------
	// 7. Discriminated union of all node types
	// -----------------------------------------------------------------------
	lines.push(`export type ${grammarPrefix}Node =`);
	for (const kind of nodeKinds) {
		lines.push(`  | ${toTypeName(kind)}`);
	}
	lines.push(';');
	lines.push('');

	return lines.join('\n');
}

/**
 * Emits types.ts containing:
 *   1. const enum SyntaxKind — all named node kinds
 *   2. Scoped const enums per supertype (ExpressionKind, StatementKind, etc.)
 *   3. Construction types — factory input shapes (no suffix)
 *   4. Navigation types — parse-tree node shapes (*Node suffix)
 *   5. Supertype union types
 *   6. Discriminated union of all node types
 */

import type { SupertypeInfo } from '../grammar-reader.ts';
import { readGrammarKind } from '../grammar-reader.ts';
import { toTypeName, toGrammarTypeName, toFieldName } from '../naming.ts';

export interface EmitTypesConfig {
	grammar: string;
	nodeKinds: string[];
	leafKinds?: string[];
	supertypes?: SupertypeInfo[];
}

export function emitTypes(config: EmitTypesConfig): string {
	const { grammar, nodeKinds, leafKinds = [], supertypes = [] } = config;
	const grammarTypeName = toGrammarTypeName(grammar);
	const grammarPrefix = grammarTypeName.slice(0, -5);
	const grammarAlias = `${grammarPrefix}Grammar`;

	const allKinds = [...nodeKinds, ...leafKinds];
	const generatedTypes = new Set<string>();

	const lines: string[] = [];
	lines.push('// Auto-generated by @sittir/codegen — do not edit');
	lines.push('');
	lines.push(`import type { ${grammarAlias} } from './grammar.js';`);
	lines.push("import type { NodeType } from '@sittir/types';");
	lines.push("import type { NodeData } from '@sittir/types';");
	lines.push('');
	lines.push(`export type { ${grammarAlias} };`);
	lines.push('');

	// -----------------------------------------------------------------------
	// 1. const enum SyntaxKind — all named node kinds
	// -----------------------------------------------------------------------
	lines.push('/** All named node kinds in this grammar. */');
	lines.push('export const enum SyntaxKind {');
	for (const kind of allKinds) {
		const enumKey = toTypeName(kind);
		lines.push(`  ${enumKey} = '${kind}',`);
	}
	lines.push('}');
	lines.push('');

	// -----------------------------------------------------------------------
	// 2. Scoped const enums per supertype
	// -----------------------------------------------------------------------
	if (supertypes.length > 0) {
		lines.push('// Scoped enums per supertype');
		for (const st of supertypes) {
			const cleanName = st.name.replace(/^_/, '');
			const enumName = toTypeName(cleanName) + 'Kind';
			lines.push(`export const enum ${enumName} {`);
			for (const sub of st.subtypes) {
				const enumKey = toTypeName(sub);
				lines.push(`  ${enumKey} = '${sub}',`);
			}
			lines.push('}');
			lines.push('');
		}
	}

	// -----------------------------------------------------------------------
	// 3. Construction types — factory input shapes (no suffix)
	//    These use NodeType<G, K> projections for full type safety.
	// -----------------------------------------------------------------------
	lines.push('// Construction types (factory input shapes)');
	for (const kind of nodeKinds) {
		const typeName = toTypeName(kind);
		generatedTypes.add(typeName);
		lines.push(`export type ${typeName} = NodeType<${grammarAlias}, '${kind}'>;`);
	}
	lines.push('');

	// Leaf construction types — minimal shape
	lines.push('// Leaf construction types');
	for (const kind of leafKinds) {
		const typeName = toTypeName(kind);
		if (generatedTypes.has(typeName)) continue;
		generatedTypes.add(typeName);
		lines.push(`export type ${typeName} = { kind: '${kind}' };`);
	}
	lines.push('');

	// -----------------------------------------------------------------------
	// 4. Navigation types — parse-tree node shapes (*Node suffix)
	//    Typed field accessors for reading parsed trees.
	// -----------------------------------------------------------------------
	// Collect all known type names for forward reference checking
	const allGeneratedNodeTypes = new Set<string>();
	for (const k of nodeKinds) allGeneratedNodeTypes.add(toTypeName(k) + 'Node');
	for (const k of leafKinds) allGeneratedNodeTypes.add(toTypeName(k) + 'Node');
	// Add supertype node names
	for (const st of supertypes) {
		const cleanName = st.name.replace(/^_/, '');
		allGeneratedNodeTypes.add(toTypeName(cleanName) + 'Node');
	}

	lines.push('// Navigation types (parse-tree node shapes — fields nested under readonly fields)');
	for (const kind of nodeKinds) {
		const nodeName = toTypeName(kind) + 'Node';
		const meta = readGrammarKind(grammar, kind);

		lines.push(`export interface ${nodeName} {`);
		lines.push(`  readonly type: SyntaxKind.${toTypeName(kind)};`);
		lines.push(`  readonly text: string;`);
		lines.push(`  readonly isNamed: true;`);
		lines.push(`  readonly fields: {`);

		for (const field of meta.fields) {
			const fieldTypes = field.namedTypes
				.map(t => {
					if (t.startsWith('_')) return 'SyntaxNode';
					const fieldNodeType = toTypeName(t) + 'Node';
					return allGeneratedNodeTypes.has(fieldNodeType) ? fieldNodeType : 'SyntaxNode';
				});

			const typeExpr = [...new Set(fieldTypes)].filter(t => t !== 'undefined').join(' | ') || 'SyntaxNode';
			const opt = !field.required ? '?' : '';

			if (field.multiple) {
				lines.push(`    readonly ${field.name}${opt}: (${typeExpr})[];`);
			} else {
				lines.push(`    readonly ${field.name}${opt}: ${typeExpr};`);
			}
		}

		if (meta.hasChildren && meta.children) {
			const childTypes = meta.children.namedTypes
				.map(t => {
					if (t.startsWith('_')) return 'SyntaxNode';
					const childNodeType = toTypeName(t) + 'Node';
					return allGeneratedNodeTypes.has(childNodeType) ? childNodeType : 'SyntaxNode';
				});
			const childTypeExpr = [...new Set(childTypes)].join(' | ') || 'SyntaxNode';
			const opt = !meta.children.required ? '?' : '';
			lines.push(`    readonly children${opt}: (${childTypeExpr})[];`);
		}

		lines.push(`  };`);
		lines.push(`  range(): { start: { index: number }; end: { index: number } };`);
		lines.push('}');
		lines.push('');
	}

	// Leaf navigation types
	lines.push('// Leaf navigation types');
	for (const kind of leafKinds) {
		const nodeName = toTypeName(kind) + 'Node';
		lines.push(`export interface ${nodeName} {`);
		lines.push(`  readonly type: SyntaxKind.${toTypeName(kind)};`);
		lines.push(`  readonly text: string;`);
		lines.push(`  readonly isNamed: true;`);
		lines.push('}');
		lines.push('');
	}

	// Generic SyntaxNode fallback
	lines.push('/** Generic syntax node — fallback for unresolved types. */');
	lines.push('export interface SyntaxNode {');
	lines.push('  readonly type: string;');
	lines.push('  readonly text: string;');
	lines.push('  readonly isNamed: boolean;');
	lines.push('}');
	lines.push('');

	// -----------------------------------------------------------------------
	// 5. Supertype union types (both construction and navigation)
	// -----------------------------------------------------------------------
	if (supertypes.length > 0) {
		lines.push('// Supertype unions (construction)');
		for (const st of supertypes) {
			const cleanName = st.name.replace(/^_/, '');
			const typeName = toTypeName(cleanName);
			if (generatedTypes.has(typeName)) continue;
			generatedTypes.add(typeName);

			const members = st.subtypes
				.map(sub => toTypeName(sub))
				.filter(t => generatedTypes.has(t));

			if (members.length > 0) {
				lines.push(`export type ${typeName} =`);
				for (const m of members) lines.push(`  | ${m}`);
				lines.push(';');
				lines.push('');
			}
		}

		lines.push('// Supertype unions (navigation)');
		for (const st of supertypes) {
			const cleanName = st.name.replace(/^_/, '');
			const nodeName = toTypeName(cleanName) + 'Node';
			const members = st.subtypes
				.map(sub => toTypeName(sub) + 'Node');

			if (members.length > 0) {
				lines.push(`export type ${nodeName} =`);
				for (const m of members) lines.push(`  | ${m}`);
				lines.push(';');
				lines.push('');
			}
		}
	}

	// -----------------------------------------------------------------------
	// 6. Discriminated union of all node types
	// -----------------------------------------------------------------------
	lines.push(`export type ${grammarPrefix}IrNode =`);
	for (const kind of nodeKinds) {
		lines.push(`  | ${toTypeName(kind)}`);
	}
	lines.push(';');
	lines.push('');

	return lines.join('\n');
}

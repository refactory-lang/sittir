// `fieldsOf` → `nodeFields(node)` (getter on AssembledNodeBase +
// subclass overrides). One source: each class owns the semantics for
// its own interface surface.

type LookupUnion = (parts: readonly string[]) => string | undefined;

function emitInterface(
	lines: string[],
	node: StructuralNode,
	nodeMap: NodeMap,
	lookupUnion?: LookupUnion,
	kindDiscriminant = JSON.stringify(node.kind),
	kindEntries?: readonly KindEnumEntry[]
): void {
	const fields = structuralFieldsOf(node);
	const children = structuralChildrenOf(node);
	lines.push(`export interface ${node.typeName} {`);
	// Canonical-hidden architecture (Option Y): hidden alias-source kinds
	// (`_foo`) keep the leading underscore in the declared `$type`.
	// Factories stamp `_foo`; `wrapNode` canonicalizes parser-output
	// `foo` back to `_foo` before dispatch. The interface's declared
	// `$type` is the single source of truth for both producer paths.
	lines.push(`  readonly $type: ${kindDiscriminant};`);

	// ADR-0018 Phase 2: emit `_<name>: T` storage + `<name>(): T` accessor
	// function types at the top level instead of the old `$fields: { name: T }`
	// nested wrapper. FieldsOf<T> in @sittir/types now extracts _-prefixed keys
	// and strips the underscore prefix for ConfigOf/RuntimeNodeOf derivations.
	if (fields.length > 0) {
		// Storage keys: `readonly _name?: T` (enumerable, serializable)
		for (const f of fields) {
			const typeExpr = fieldTypeExpr(f, nodeMap, lookupUnion);
			const storageInfo = resolveFieldStorageInfo(f, nodeMap);
			const opt = isRequired(f) ? '' : '?';
			const storageType = storageFieldTypeExpr(f, nodeMap, typeExpr);
			if (isMultiple(f) && !storageInfo.collapsesMultiplicity) {
				emitFieldArrayDeclaration(lines, `_${f.name}`, opt, storageType, isNonEmpty(f));
			} else {
				lines.push(`  readonly _${f.name}${opt}: ${storageType};`);
			}
		}
		emitFieldInputHints(lines, fields, node.kind, nodeMap, kindEntries);
		// Accessor function types: `name(): T` (non-enumerable at runtime —
		// declared here for type-safety so consumers can call node.name()).
		for (const f of fields) {
			const typeExpr = fieldTypeExpr(f, nodeMap, lookupUnion);
			const storageInfo = resolveFieldStorageInfo(f, nodeMap);
			const propName = f.propertyName;
			const storageType = storageFieldTypeExpr(f, nodeMap, typeExpr);
			const opt = isRequired(f) ? '' : '?';
			if (isMultiple(f) && !storageInfo.collapsesMultiplicity) {
				// Multiple accessor returns the array type (same as storage type).
				const arrType = isNonEmpty(f) ? `NonEmptyArray<${storageType}>` : `readonly (${storageType})[]`;
				lines.push(`  ${propName}(): ${arrType};`);
			} else {
				lines.push(`  ${propName}(): ${storageType}${opt ? ' | undefined' : ''};`);
			}
		}
	}

	if (children && children.length > 0) {
		const perChildParts = children.map((c) => childContentParts(c, nodeMap));
		const allPartsFlat = perChildParts.flat();
		const aliased = lookupUnion?.(allPartsFlat);
		const childTypes = perChildParts.map((parts) => parts.join(' | ')).filter(Boolean);
		if (childTypes.length > 0) {
			const union = aliased ?? childTypes.join(' | ');
			const anyMultiple = children.some((c) => isMultiple(c));
			const anyNonEmpty = children.some((c) => isNonEmpty(c));
			if (anyMultiple) {
				// Same `repeat1` treatment for children slots — a
				// non-empty children list gets a NonEmptyArray head.
				if (anyNonEmpty) {
					lines.push(`  readonly $children: NonEmptyArray<${union}>;`);
				} else {
					lines.push(`  readonly $children: readonly (${union})[];`);
				}
			} else {
				lines.push(`  readonly $children: readonly [${union}];`);
			}
		}
	}

	lines.push('}');
	lines.push('');
}

// ---------------------------------------------------------------------------
// Field array declaration emission
// ---------------------------------------------------------------------------

/**
 * Emit the `readonly <name><opt>: <arrayType>` declaration for a repeated
 * (`multiple`) field inside a `$fields` block.
 *
 * @remarks
 * `repeat1` fields carry a grammar-enforced `length >= 1` guarantee. They
 * are emitted as `NonEmptyArray<T>` — the alias is inherently `readonly`
 * (TS1354 forbids prefixing a type-alias reference with `readonly`, so the
 * `readonly` lives inside the alias definition). Plain `repeat` fields stay
 * `readonly T[]`.
 *
 * @param lines - Output line buffer to append to.
 * @param name - The raw field name (snake_case).
 * @param opt - Optionality suffix: `""` for required, `"?"` for optional.
 * @param typeExpr - The resolved TypeScript type expression for the element type.
 * @param nonEmpty - Whether the field is `repeat1` (non-empty array guaranteed).
 *   `undefined` is treated as `false`.
 */
function emitFieldArrayDeclaration(
	lines: string[],
	name: string,
	opt: string,
	typeExpr: string,
	nonEmpty: boolean | undefined
): void {
	// ADR-0018 Phase 2: indentation at interface body level (2 spaces) since
	// fields are now declared directly on the interface, not inside $fields: {}.
	if (nonEmpty) {
		lines.push(`  readonly ${name}${opt}: NonEmptyArray<${typeExpr}>;`);
	} else {
		lines.push(`  readonly ${name}${opt}: readonly (${typeExpr})[];`);
	}
}

/**
 * Expand a field's content types into the identifier parts that
 * would form its type union. Used by both the dedup pre-pass and
 * the emission pass. Literal-value enums and empty unions return
 * `[]` — they don't get aliased because they don't produce a
 * multi-type union.
 */
function _fieldTypeParts(field: AssembledNonterminal, nodeMap?: NodeMap): string[] {
	const litVals = slotLiteralValues(field);
	if (litVals.length > 0) return [];
	const kinds = slotKindNames(field);
	if (kinds.length === 0) return [];
	return kinds.map((t) => {
		const node = nodeMap?.nodes.get(t);
		if (!node) return JSON.stringify(t);
		const name = node.typeName;
		return /^[A-Za-z_$][\w$]*$/.test(name) ? name : JSON.stringify(t);
	});
}

function childContentParts(child: AssembledNonterminal, nodeMap: NodeMap): string[] {
	// Inline pure-literal values as string-literal types, mirroring
	// `fieldTypeComponents`. A child slot that carries terminal values
	// alongside node refs (e.g. `children` on a union) would surface the
	// literal text as a string-literal union member.
	const parts: string[] = [];
	for (const v of child.values) {
		if (isTerminalValue(v)) {
			parts.push(JSON.stringify(v.value));
			continue;
		}
		if (!isNodeRef(v)) continue;
		const t = isUnresolvedRef(v.node) ? v.node.name : v.node.kind;
		// Hidden-keyword kinds (`_not_escape_sequence` → `'\\'`) inline
		// their literal text — same rule as `fieldTypeComponents`. Avoids
		// the dangling `NotEscapeSequence` reference that would otherwise
		// require an extra stub alias.
		const lit = resolveHiddenKeywordLiteral(t, nodeMap);
		if (lit !== undefined) {
			parts.push(JSON.stringify(lit));
			continue;
		}
		const n = nodeMap.nodes.get(t);
		if (!n) {
			throw new Error(`types: child references kind '${t}' which is not in NodeMap.`);
		}
		const name = n.typeName;
		parts.push(/^[A-Za-z_$][\w$]*$/.test(name) ? name : JSON.stringify(t));
	}
	return parts;
}

// `childrenOf` → `nodeChildren(node)` (getter on AssembledNodeBase +
// subclass overrides). Each class owns its own children semantics.

function emitFormInterface(
	lines: string[],
	node: AssembledNode,
	form: AssembledGroup,
	typeName: string,
	nodeMap: NodeMap,
	lookupUnion?: LookupUnion,
	kindDiscriminant = JSON.stringify(node.kind),
	kindEntries?: readonly KindEnumEntry[]
): void {
	lines.push(`export interface ${typeName} {`);
	// Canonical-hidden architecture (Option Y): UForm interfaces declare
	// the parent polymorph's kind verbatim — no underscore strip — so
	// factory output (which stamps the parent kind) and `wrapNode`
	// output (which canonicalizes via the alias map) converge on the
	// same `$type` literal that the interface declares.
	lines.push(`  readonly $type: ${kindDiscriminant};`);
	// `$variant` literal discriminant — matches what the form factory stamps
	// on its output (`$variant: '<form.name>' as const`). Makes the parent
	// polymorph union a discriminated union so consumers can narrow via
	// `if (expr.$variant === 'binary') { … }` without structural probing.
	lines.push(`  readonly $variant: '${form.name}';`);

	// ADR-0018 Phase 2: emit `_<name>: T` storage + `<name>(): T` accessor
	// function types at the top level instead of `$fields: { name: T }`.
	if (form.fields.length > 0) {
		// Storage keys: `readonly _name?: T`
		for (const f of form.fields) {
			const typeExpr = fieldTypeExpr(f, nodeMap, lookupUnion);
			const opt = isRequired(f) ? '' : '?';
			const storageInfo = resolveFieldStorageInfo(f, nodeMap);
			const storageType = storageFieldTypeExpr(f, nodeMap, typeExpr);
			if (isMultiple(f) && !storageInfo.collapsesMultiplicity) {
				lines.push(`  readonly _${f.name}${opt}: readonly (${storageType})[];`);
			} else {
				lines.push(`  readonly _${f.name}${opt}: ${storageType};`);
			}
		}
		emitFieldInputHints(lines, form.fields, node.kind, nodeMap, kindEntries);
		// Accessor function types: `name(): T`
		for (const f of form.fields) {
			const typeExpr = fieldTypeExpr(f, nodeMap, lookupUnion);
			const propName = f.propertyName;
			const storageInfo = resolveFieldStorageInfo(f, nodeMap);
			const storageType = storageFieldTypeExpr(f, nodeMap, typeExpr);
			const opt = isRequired(f) ? '' : '?';
			if (isMultiple(f) && !storageInfo.collapsesMultiplicity) {
				lines.push(`  ${propName}(): readonly (${storageType})[];`);
			} else {
				lines.push(`  ${propName}(): ${storageType}${opt ? ' | undefined' : ''};`);
			}
		}
	}

	emitFormChildrenSlot(lines, form, nodeMap, lookupUnion);

	lines.push('}');
	lines.push('');
}

